import 'dart:core';

class FirebaseDatabaseException implements Exception {
  const FirebaseDatabaseException({
    this.code,
    this.message,
    this.stackTrace,
  });

  final String? code;
  final String? message;
  final StackTrace? stackTrace;

  @override
  String toString() => "FirebaseDatabaseException: {$code: $message}";
}
