import 'package:flutter_module/services/api/home_data/payload/gen_project_link_payload.dart';
import 'package:flutter_module/services/api/mfast/payload/mfast_tracking_event_non_user_payload.dart';
import 'package:flutter_module/services/api/mfast/payload/mfast_tracking_event_payload.dart';

import '../../base/base_endpoint.dart';
import '../../base/method_request.dart';

abstract class HomeEndpointProtocol {
  EndpointType getHomePageData();

  EndpointType getCustomerRequirement();

  EndpointType getHomeBanners();

  EndpointType getHomeTips();

  EndpointType getHomeDPD();

  EndpointType getHighlightProjects();

  EndpointType trackingEvent(MFastTrackingEventPayload payload);

  EndpointType trackingEventNonUser(MFastTrackingEventNonUserPayload payload);
  
  EndpointType genProjectLink(GenProjectLinkPayload payload);
}

class HomeEndpoint implements HomeEndpointProtocol {
  @override
  EndpointType getHomePageData() {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/non_user/shop_items_v2",
      httpMethod: HttpMethod.get,
      parameters: {
        'apiVersion': 'v3',
      },
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getCustomerRequirement() {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/non_user/customer_requirements",
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getHomeBanners() {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/non_user/news",
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getHomeTips() {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/Posts/list_tips",
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getHomeDPD() {
    final endpoint = EndpointType(
      path: "/app_api_v1/mfast/show_dpd",
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getHighlightProjects() {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/non_user/highlight_project",
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType trackingEvent(MFastTrackingEventPayload payload) {
    final endpoint = EndpointType(
      path: "/app_api_v1/main/tracking_event",
      httpMethod: HttpMethod.get,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType trackingEventNonUser(MFastTrackingEventNonUserPayload payload) {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/Non_user/moengage_event",
      httpMethod: HttpMethod.post,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType genProjectLink(GenProjectLinkPayload payload) {
    final endpoint = EndpointType(
      path: "/app_api_v1/main/gen_link_referral",
      httpMethod: HttpMethod.get,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }
}
