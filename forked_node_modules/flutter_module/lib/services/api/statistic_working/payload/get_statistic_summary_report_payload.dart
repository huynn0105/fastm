class GetStatisticSummaryReportPayload {
  String? userID;
  String? date;

  GetStatisticSummaryReportPayload({
    this.userID,
    this.date,
  });

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["userID"] = userID;
    map["date"] = date;
    return map;
  }

  GetStatisticSummaryReportPayload copyWith({
    String? userID,
    String? date,
  }) {
    return GetStatisticSummaryReportPayload(
      userID: userID ?? this.userID,
      date: date ?? this.date,
    );
  }
}
