import 'package:flutter_module/services/api/statistic_working/payload/get_statistic_checking_by_filter_payload.dart';
import 'package:flutter_module/services/api/statistic_working/payload/get_statistic_summary_checking_payload.dart';
import 'package:flutter_module/services/api/statistic_working/payload/get_statistic_summary_report_payload.dart';
import 'package:flutter_module/services/base/base_endpoint.dart';
import 'package:flutter_module/services/base/method_request.dart';

import 'payload/get_statistic_report_by_filter_payload.dart';

abstract class StatisticWorkingEndpointProtocol {
  EndpointType getStatisticSummaryChecking(GetStatisticSummaryCheckingPayload payload);

  EndpointType getStatisticCheckingByFilter(GetStatisticCheckingByFilterPayload payload);

  EndpointType getStatisticSummaryReport(GetStatisticSummaryReportPayload payload);

  EndpointType getStatisticReportByFilter(GetStatisticReportByFilterPayload payload);
}

class StatisticWorkingEndpoint implements StatisticWorkingEndpointProtocol {
  @override
  EndpointType getStatisticSummaryChecking(GetStatisticSummaryCheckingPayload payload) {
    final endpoint = EndpointType(
      path: "/app_api_v1/sale_core_report_statistic/user_detail_checking",
      httpMethod: HttpMethod.get,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getStatisticCheckingByFilter(GetStatisticCheckingByFilterPayload payload) {
    final endpoint = EndpointType(
      path: "/app_api_v1/sale_core_report_statistic/checking_filter",
      httpMethod: HttpMethod.post,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getStatisticSummaryReport(GetStatisticSummaryReportPayload payload) {
    final endpoint = EndpointType(
      path: "/app_api_v1/sale_core_report_statistic/user_detail_report",
      httpMethod: HttpMethod.get,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getStatisticReportByFilter(GetStatisticReportByFilterPayload payload) {
    final endpoint = EndpointType(
      path: "/app_api_v1/sale_core_report_statistic/report_filter",
      httpMethod: HttpMethod.post,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }
}
