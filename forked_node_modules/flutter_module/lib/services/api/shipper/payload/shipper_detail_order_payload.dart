
class DeliveryPayload {
  DeliveryPayload({
    required this.orderCode,
  });

  DeliveryPayload.fromJson(dynamic json) {
    orderCode = json['orderCode'];
  }

  String? orderCode;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['orderCode'] = orderCode;
    return map;
  }
}
