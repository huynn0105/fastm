
class WarningPaymentPayload {
  WarningPaymentPayload({
    required this.orderCode,
    required this.gateway,
    required this.systemNotUpdatePaid,
  });

  WarningPaymentPayload.fromJson(dynamic json) {
    orderCode = json['order_code'];
    gateway = json['gateway'];
    systemNotUpdatePaid = json['system_not_update_paid'];
  }

  String? orderCode;
  String? gateway;
  bool? systemNotUpdatePaid;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['order_code'] = orderCode;
    map['gateway'] = gateway;
    map['system_not_update_paid'] = systemNotUpdatePaid;
    return map;
  }
}
