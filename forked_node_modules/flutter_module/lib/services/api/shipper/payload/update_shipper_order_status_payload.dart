class UpdateDeliveryStatusPayload {
  UpdateDeliveryStatusPayload({
    required this.orderCode,
    required this.statusCode,
    required this.note,
    required this.reasonCode,
    required this.longitude,
    required this.latitude,
    required this.notUpdatePaid,
  });

  UpdateDeliveryStatusPayload.fromJson(dynamic json) {
    orderCode = json['orderCode'];
    statusCode = json['statusCode'];
    note = json['note'];
    reasonCode = json['reasonCode'];
    longitude = json['longitude'];
    latitude = json['latitude'];
    notUpdatePaid = json['notUpdatePaid'];
  }

  String? orderCode;
  String? statusCode;
  String? note;
  String? reasonCode;
  String? longitude;
  String? latitude;
  bool? notUpdatePaid;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['orderCode'] = orderCode;
    map['statusCode'] = statusCode;
    map['note'] = note;
    map['reasonCode'] = reasonCode;
    map['longitude'] = longitude;
    map['latitude'] = latitude;
    map['notUpdatePaid'] = notUpdatePaid;
    return map;
  }
}
