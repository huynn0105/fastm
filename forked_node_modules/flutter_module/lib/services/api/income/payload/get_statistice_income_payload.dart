class GetStatisticeIncomePayload {
  GetStatisticeIncomePayload({
    this.month,
    this.year,
  });

  int? month;
  int? year;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['month'] = month?.toString().padLeft(2, '0');
    map['year'] = year;
    return map;
  }

  GetStatisticeIncomePayload copyWith({
    int? month,
    int? year,
  }) {
    return GetStatisticeIncomePayload(
      month: month ?? this.month,
      year: year ?? this.year,
    );
  }
}
