import 'package:flutter_module/app_data.dart';

class SubmitWithdrawPayload {
  String? mobilePhone;
  String? otpCode;
  String? withdrawalMethod ;
  String? bankingId;
  String? amount;
  LocationTrackingModel? location;

  SubmitWithdrawPayload({
    this.mobilePhone,
    this.otpCode,
    this.withdrawalMethod= 'banking',
    this.bankingId,
    this.amount,
    this.location,
  });

  SubmitWithdrawPayload.fromJson(Map<String, dynamic> json) {
    mobilePhone = json["mobilePhone"];
    otpCode = json["otp_code"];
    withdrawalMethod = json["withdrawal_method"];
    bankingId = json["banking_id"];
    amount = json["amount"];
    location = json["location"] == null ? null : LocationTrackingModel.fromJson(json["location"]);
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["mobilePhone"] = mobilePhone;
    data["otp_code"] = otpCode;
    data["withdrawal_method"] = withdrawalMethod;
    data["banking_id"] = bankingId;
    data["amount"] = amount;
    if (location != null) {
      data["location"] = location?.toJson();
    }
    return data;
  }
}

class LocationTrackingModel {
  String? key;
  String? os;
  String? appVersion;
  String? osVersion;
  String? deviceUdid;
  String? deviceModel;
  LocationModel? location;

  LocationTrackingModel({
    this.key,
    this.location,
    this.os,
    this.appVersion,
    this.osVersion,
    this.deviceUdid,
    this.deviceModel,
  });

  LocationTrackingModel.fromJson(Map<String, dynamic> json) {
    key = json["key"];
    os = json["os"];
    appVersion = json["appVersion"];
    osVersion = json["osVersion"];
    deviceUdid = json["deviceUDID"];
    deviceModel = json["deviceModel"];
    location = json["location"] == null ? null : LocationModel.fromJson(json["location"]);
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["key"] = key;
    data["os"] = os;
    data["appVersion"] = appVersion;
    data["osVersion"] = osVersion;
    data["deviceUDID"] = deviceUdid;
    data["deviceModel"] = deviceModel;
    if (location != null) {
      data["location"] = location?.toJson();
    }
    return data..addAll(AppData.instance.getTrackingInfo());
  }
}

class LocationModel {
  double? latitude;
  double? longitude;

  LocationModel({this.latitude, this.longitude});

  LocationModel.fromJson(Map<String, dynamic> json) {
    latitude = json["latitude"];
    longitude = json["longitude"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["latitude"] = latitude;
    data["longitude"] = longitude;
    return data;
  }
}
