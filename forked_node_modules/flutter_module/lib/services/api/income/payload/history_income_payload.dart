class GetHistoryIncomePayload {
  GetHistoryIncomePayload({
    this.page,
    this.month,
    this.year,
    this.filter,
  }) : dateCtr = 'about';

  int? page;
  int? month;
  int? year;
  String? filter;
  String? dateCtr;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['page'] = page;
    map['month'] = month;
    map['year'] = year;
    map['filter'] = filter;
    map['date_ctr'] = dateCtr;
    return map;
  }

  GetHistoryIncomePayload copyWith({
    int? page,
    int? month,
    int? year,
    String? filter,
  }) {
    return GetHistoryIncomePayload(
      page: page ?? this.page,
      month: month ?? this.month,
      year: year ?? this.year,
      filter: filter ?? this.filter,
    );
  }
}
