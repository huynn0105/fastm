class ExportStatementPayload {
  String? email;
  String? dateCtr;
  String? dateFrm;
  String? dateTo;

  ExportStatementPayload({
    this.email,
    this.dateCtr,
    this.dateFrm,
    this.dateTo,
  });

  ExportStatementPayload.fromJson(Map<String, dynamic> json) {
    email = json["email"];
    dateCtr = json["date_ctr"];
    dateFrm = json["date_frm"];
    dateTo = json["date_to"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["email"] = email;
    data["date_ctr"] = dateCtr;
    data["date_frm"] = dateFrm;
    data["date_to"] = dateTo;
    return data;
  }

  ExportStatementPayload copyWith({
    String? email,
    String? dateCtr,
    String? dateFrm,
    String? dateTo,
  }) {
    return ExportStatementPayload(
      email: email ?? this.email,
      dateCtr: dateCtr ?? this.dateCtr,
      dateFrm: dateFrm ?? this.dateFrm,
      dateTo: dateTo ?? this.dateTo,
    );
  }
}
