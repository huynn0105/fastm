class GetWithdrawFeePayload {
  String? amount;
  String? withdrawalMethod;

  GetWithdrawFeePayload({
    this.amount,
    this.withdrawalMethod = 'banking',
  });

  GetWithdrawFeePayload.fromJson(Map<String, dynamic> json) {
    amount = json["amount"];
    withdrawalMethod = json["withdrawal_method"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["amount"] = amount;
    data["withdrawal_method"] = withdrawalMethod;
    return data;
  }

  GetWithdrawFeePayload copyWith({
    String? amount,
    String? withdrawalMethod,
  }) =>
      GetWithdrawFeePayload(
        amount: amount ?? this.amount,
        withdrawalMethod: withdrawalMethod ?? this.withdrawalMethod,
      );
}
