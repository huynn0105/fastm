import 'package:flutter_module/services/api/personal_setting/payload/update_setting_payload.dart';

import '../../base/base_endpoint.dart';
import '../../base/method_request.dart';

abstract class PersonalSettingEndpointProtocol {
  EndpointType getPersonalSetting();

  EndpointType getMentorRating();

  EndpointType getPrivacyVisibleConfigs();

  EndpointType updateVisibleSetting(UpdateSettingPayload payload);

  EndpointType getCoreAgent();
}

class PersonalSettingEndpoint implements PersonalSettingEndpointProtocol {
  @override
  EndpointType getPersonalSetting() {
    final endpoint = EndpointType(
      path: '/mfast_api_v1/Personal/list_setting',
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getMentorRating() {
    final endpoint = EndpointType(
      path: '',
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getPrivacyVisibleConfigs() {
    final endpoint = EndpointType(
      path: '/mfast_api_v1/personal/privacy_configs',
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType updateVisibleSetting(UpdateSettingPayload payload) {
    final endpoint = EndpointType(
      path: '/mfast_api_v1/personal/update_privacy_configs',
      httpMethod: HttpMethod.post,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }
  
  @override
  EndpointType getCoreAgent() {
   final endpoint = EndpointType(
      path: '/mfast_api_v1/personal/core_agent',
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }
}
