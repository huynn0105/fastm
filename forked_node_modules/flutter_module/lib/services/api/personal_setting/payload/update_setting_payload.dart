class UpdateSettingPayload {
  UpdateSettingPayload({
    required this.categoryID,
    required this.status,
  });

  String? categoryID;
  bool? status;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['categoryID'] = categoryID;
    map['status'] = status;
    return map;
  }
}
