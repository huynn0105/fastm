/// listSku : ["aaa","bbb"]
/// provinceCode : "42"
/// districtCode : "589"

class CheckProductStockPayload {
  CheckProductStockPayload({
    this.listSku,
    this.provinceCode,
    this.districtCode,
    this.wardCode,
  });

  CheckProductStockPayload.fromJson(dynamic json) {
    listSku = json['listSku'] != null ? json['listSku'].cast<String>() : [];
    provinceCode = json['provinceCode'];
    districtCode = json['districtCode'];
    wardCode = json['wardCode'];
  }

  List<String>? listSku;
  String? provinceCode;
  String? districtCode;
  String? wardCode;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['listSku'] = listSku;
    map['provinceCode'] = provinceCode;
    map['districtCode'] = districtCode;
    map['wardCode'] = wardCode;
    return map;
  }
}
