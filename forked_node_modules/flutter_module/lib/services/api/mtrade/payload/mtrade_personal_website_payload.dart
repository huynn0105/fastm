class MTradePersonalWebsiteDataPayload {
  MTradePersonalWebsiteDataPayload({
    this.source,
  });

  MTradePersonalWebsiteDataPayload.fromJson(dynamic json) {
    source = json['source'];
  }

  String? source;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['source'] = source;
    return map;
  }
}
