import '../../../../common/utils/text_util.dart';

/// mobilePhone : "0980484549"
/// idNumber : "390845445848"
/// districtID : 42
/// provinceID : 482
/// token : "b6f9c8cad4d0bf491d1fdbef1c91e2a5f18f885e"
/// accessToken : "c7d382d38586f600d1133c601a49af260654fb2d"

class MTradeGenMplTokenPayload {
  MTradeGenMplTokenPayload({
    this.mobilePhone,
    this.idNumber,
    this.districtID,
    this.provinceID,
    this.wardID,
    this.nameHeadOfHousehold,
    this.idNumberHeadOfHousehold,
    this.token,
    this.accessToken,
  });

  MTradeGenMplTokenPayload.fromJson(dynamic json) {
    mobilePhone = json['mobilePhone'];
    idNumber = json['idNumber'];
    districtID = json['districtID'];
    provinceID = json['provinceID'];
    wardID = json['wardID'];
    nameHeadOfHousehold = json['nameHeadOfHousehold'];
    idNumberHeadOfHousehold = json['idNumberHeadOfHousehold'];
    token = json['token'];
    accessToken = json['accessToken'];
  }

  String? mobilePhone;
  String? idNumber;
  int? districtID;
  int? provinceID;
  int? wardID;
  String? nameHeadOfHousehold;
  String? idNumberHeadOfHousehold;
  String? token;
  String? accessToken;

  MTradeGenMplTokenPayload copyWith({
    String? mobilePhone,
    String? idNumber,
    int? districtID,
    int? provinceID,
    int? wardID,
    String? nameHeadOfHousehold,
    String? idNumberHeadOfHousehold,
    String? token,
    String? accessToken,
  }) {
    return MTradeGenMplTokenPayload(
      mobilePhone: mobilePhone ?? this.mobilePhone,
      idNumber: idNumber ?? this.idNumber,
      districtID: districtID ?? this.districtID,
      provinceID: provinceID ?? this.provinceID,
      wardID: wardID ?? this.wardID,
      nameHeadOfHousehold: nameHeadOfHousehold ?? this.nameHeadOfHousehold,
      idNumberHeadOfHousehold: idNumberHeadOfHousehold ?? this.idNumberHeadOfHousehold,
      token: token ?? this.token,
      accessToken: accessToken ?? this.accessToken,
    );
  }

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['mobilePhone'] = mobilePhone;
    map['idNumber'] = idNumber;
    map['wardID'] = wardID;
    map['districtID'] = districtID;
    map['provinceID'] = provinceID;
    map['nameHeadOfHousehold'] = nameHeadOfHousehold;
    map['idNumberHeadOfHousehold'] = idNumberHeadOfHousehold;
    map['token'] = token;
    if (TextUtils.isNotEmpty(accessToken)) {
      map['accessToken'] = accessToken;
    }
    return map;
  }
}
