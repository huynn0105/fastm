class MTradePersonalWebsiteStatisticPayload {
  MTradePersonalWebsiteStatisticPayload({
    this.source,
  });

  MTradePersonalWebsiteStatisticPayload.fromJson(dynamic json) {
    source = json['source'];
  }

  String? source;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['source'] = source;
    return map;
  }
}
