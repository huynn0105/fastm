import 'package:flutter_module/models/mtrade/promotion/data_product_variations_model.dart';

class MTradeCheckPromotionPayload {
  MTradeCheckPromotionPayload({
    this.version,
    this.codes,
    this.productionVariations,
    this.provinceCode,
    this.districtCode,
    this.wardCode,
    this.paymentMethod,
  });

  MTradeCheckPromotionPayload.fromJson(dynamic json) {
    version = json['version'];
    codes = json['codes'] != null ? json['codes'].cast<String>() : [];
    if (json['production_variations'] != null) {
      productionVariations = [];
      json['production_variations'].forEach((v) {
        productionVariations?.add(DataProductVariationsModel.fromJson(v));
      });
    }
    provinceCode = json['province_code'];
    districtCode = json['district_code'];
    wardCode = json['ward_code'];
    paymentMethod = json['payment_method'];
  }

  String? version;
  List<String>? codes;
  List<DataProductVariationsModel>? productionVariations;
  String? provinceCode;
  String? districtCode;
  String? wardCode;
  String? paymentMethod;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['version'] = version;
    map['codes'] = codes;
    if (productionVariations != null) {
      map['production_variations'] = productionVariations?.map((v) => v.toJson()).toList();
    }
    map['province_code'] = provinceCode;
    map['district_code'] = districtCode;
    map['ward_code'] = wardCode;
    map['payment_method'] = paymentMethod;
    return map;
  }
}
