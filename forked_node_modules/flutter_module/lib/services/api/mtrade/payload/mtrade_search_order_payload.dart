class MTradeSearchOrderPayload {
  MTradeSearchOrderPayload({
    this.orderCode,
  });

  MTradeSearchOrderPayload.fromJson(dynamic json) {
    orderCode = json['orderCode'];
  }

  String? orderCode;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['orderCode'] = orderCode;
    return map;
  }

  MTradeSearchOrderPayload copyWith({
    String? orderCode,
  }) {
    return MTradeSearchOrderPayload(orderCode: orderCode);
  }
}
