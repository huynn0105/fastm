class MTradeSkuPayload {
  MTradeSkuPayload({
    this.productID,
    this.provinceCode,
    this.districtCode,
    this.wardCode,
    this.option,
  });

  MTradeSkuPayload.fromJson(dynamic json) {
    productID = json['productID'];
    option = json['option'] != null ? json['option'].cast<String>() : [];
  }

  int? productID;
  String? provinceCode;
  String? districtCode;
  String? wardCode;
  List<List<String>>? option;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['productID'] = productID;
    map['provinceCode'] = provinceCode;
    map['districtCode'] = districtCode;
    map['wardCode'] = wardCode;
    map['option'] = option;
    return map;
  }
}
