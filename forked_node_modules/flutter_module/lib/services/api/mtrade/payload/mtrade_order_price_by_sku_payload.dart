class MTradeOrderPriceBySkuPayload {
  MTradeOrderPriceBySkuPayload({
    this.order,
    this.channel,
    this.paymentMethod,
    this.provinceCode,
    this.districtCode,
    this.wardCode,
  });

  MTradeOrderPriceBySkuPayload.fromJson(dynamic json) {
    if (json['order'] != null) {
      order = [];
      json['order'].forEach((v) {
        order?.add(Order.fromJson(v));
      });
    }
    channel = json['channel'];
    paymentMethod = json['payment_method'];
    provinceCode = json['province_code'];
    districtCode = json['district_code'];
    wardCode = json['ward_code'];
  }

  List<Order>? order;
  String? channel;
  String? paymentMethod;
  String? provinceCode;
  String? districtCode;
  String? wardCode;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (order != null) {
      map['order'] = order?.map((v) => v.toJson()).toList();
    }
    map['channel'] = channel;
    map['payment_method'] = paymentMethod;
    map['province_code'] = provinceCode;
    map['district_code'] = districtCode;
    map['ward_code'] = wardCode;
    return map;
  }
}

class Order {
  Order({
    this.sku,
    this.quantity,
  });

  Order.fromJson(dynamic json) {
    sku = json['sku'];
    quantity = json['quantity'];
  }

  String? sku;
  int? quantity;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['sku'] = sku;
    map['quantity'] = quantity;
    return map;
  }
}
