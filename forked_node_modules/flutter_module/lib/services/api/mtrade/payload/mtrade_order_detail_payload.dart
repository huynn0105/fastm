import '../../../../common/utils/text_util.dart';

class MTradeOrderDetailPayload {
  MTradeOrderDetailPayload({
    this.orderId,
  });

  MTradeOrderDetailPayload.fromJson(dynamic json) {
    orderId = json['orderID'];
  }

  String? orderId;


  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['orderID'] = orderId;
    return map;
  }

  String toParam() {
    return TextUtils.convertUrlParameters(toJson());
  }

  MTradeOrderDetailPayload copyWith({
    String? orderId,
  }) {
    return MTradeOrderDetailPayload(
      orderId: orderId ?? this.orderId,
    );
  }
}
