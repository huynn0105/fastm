import 'mtrade_pay_now_order_payload.dart';

class GetPaymentMethodUrlPayload {
  GetPaymentMethodUrlPayload({
    this.wardCode,
    this.districtCode,
    this.provinceCode,
    this.orderItems,
    this.totalAmount,
    this.action,
  });

  GetPaymentMethodUrlPayload.fromJson(dynamic json) {
    wardCode = json['ward_code'];
    districtCode = json['district_code'];
    provinceCode = json['province_code'];
    if (json['order_items'] != null) {
      orderItems = [];
      json['order_items'].forEach((v) {
        orderItems?.add(OrderItems.fromJson(v));
      });
    }
    totalAmount = json['total_amount'];
    action = json['action'];
  }

  String? wardCode;
  String? districtCode;
  String? provinceCode;
  List<OrderItems>? orderItems;
  double? totalAmount;
  String? action;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['ward_code'] = wardCode;
    map['district_code'] = districtCode;
    map['province_code'] = provinceCode;
    if (orderItems != null) {
      map['order_items'] = orderItems?.map((v) => v.toGetPaymentMethodJson()).toList();
    }
    map['total_amount'] = totalAmount;
    map['action'] = action;
    return map;
  }
}
