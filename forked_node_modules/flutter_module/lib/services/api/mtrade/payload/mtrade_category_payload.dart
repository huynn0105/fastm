class MTradeCategoryPayload {
  MTradeCategoryPayload({
    this.provinceCode,
    this.districtCode,
    this.wardCode,
  });

  MTradeCategoryPayload.fromJson(dynamic json) {
    provinceCode = json['provinceCode'];
    districtCode = json['districtCode'];
    wardCode = json['wardCode'];
  }

  String? provinceCode;
  String? districtCode;
  String? wardCode;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['provinceCode'] = provinceCode ?? "";
    map['districtCode'] = districtCode ?? "";
    map['wardCode'] = wardCode ?? "";
    return map;
  }

  MTradeCategoryPayload copyWith({
    String? provinceCode,
    String? districtCode,
    String? wardCode,
  }) {
    return MTradeCategoryPayload(
      provinceCode: provinceCode ?? this.provinceCode,
      districtCode: districtCode ?? this.districtCode,
      wardCode: wardCode ?? this.wardCode,
    );
  }
}
