import '../../../../common/constants.dart';
import '../../../../common/utils/text_util.dart';

/// month : 3
/// year : 2023
/// filter_text : ""
/// processState : "waiting"
/// collaboratorID : "1240469571"

class MTradeOrderPaymentMethodPayload {
  MTradeOrderPaymentMethodPayload({
    this.month,
    this.year,
    this.processState,
    this.filterText,
    this.collaboratorID,
  });

  MTradeOrderPaymentMethodPayload.fromJson(dynamic json) {
    month = json['month'];
    year = json['year'];
    processState = json['process_state'];
    filterText = json['filter_text'];
    collaboratorID = json['collaboratorID'];
  }

  int? month;
  int? year;
  String? processState;
  String? filterText;
  String? collaboratorID;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['month'] = month;
    map['year'] = year;
    map['process_state'] = processState;
    if (filterText?.isNotEmpty == true) {
      map['filter_text'] = filterText;
    }
    if (collaboratorID?.isNotEmpty == true) {
      map['collaboratorID'] = collaboratorID;
    }
    return map;
  }

  String toParam() {
    return TextUtils.convertUrlParameters(toJson());
  }

  MTradeOrderPaymentMethodPayload copyWith({
    int? month,
    int? year,
    String? processState,
    String? filterText,
    String? collaboratorID,
  }) {
    return MTradeOrderPaymentMethodPayload(
      month: month ?? this.month,
      year: year ?? this.year,
      processState: processState ?? this.processState,
      filterText: filterText ?? this.filterText,
      collaboratorID: collaboratorID ?? this.collaboratorID,
    );
  }
}
