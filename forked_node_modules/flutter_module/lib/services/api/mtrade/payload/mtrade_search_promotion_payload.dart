import '../../../../models/mtrade/promotion/data_product_variations_model.dart';

class MTradeSearchPromotionPayload {
  String? accessToken;
  String? code;
  List<DataProductVariationsModel>? productionVariations;
  List<String>? codes;
  String? provinceCode;
  String? districtCode;
  String? wardCode;
  String? paymentMethod;

  MTradeSearchPromotionPayload({
    this.accessToken,
    this.code,
    this.productionVariations,
    this.codes,
    this.provinceCode,
    this.districtCode,
    this.wardCode,
    this.paymentMethod,
  });

  MTradeSearchPromotionPayload.fromJson(Map<String, dynamic> json) {
    accessToken = json['accessToken'];
    code = json['code'];
    if (json['production_variations'] != null) {
      productionVariations = [];
      json['production_variations'].forEach((v) {
        productionVariations?.add(DataProductVariationsModel.fromJson(v));
      });
    }
    codes = json['codes'].cast<String>();
    provinceCode = json['province_code'];
    districtCode = json['district_code'];
    wardCode = json['ward_code'];
    paymentMethod = json['payment_method'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['accessToken'] = accessToken;
    data['code'] = code;
    data['production_variations'] = productionVariations?.map((v) => v.toJson()).toList();
    data['codes'] = codes;
    data['province_code'] = provinceCode;
    data['district_code'] = districtCode;
    data['ward_code'] = wardCode;
    data['payment_method'] = paymentMethod;
    return data;
  }

  MTradeSearchPromotionPayload copyWith({
    String? accessToken,
    String? code,
    List<DataProductVariationsModel>? productionVariations,
    List<String>? codes,
    String? provinceCode,
    String? districtCode,
    String? wardCode,
    String? paymentMethod,
  }) {
    return MTradeSearchPromotionPayload(
      accessToken: accessToken ?? this.accessToken,
      code: code ?? this.code,
      productionVariations: productionVariations ?? this.productionVariations,
      codes: codes ?? this.codes,
      provinceCode: provinceCode ?? this.provinceCode,
      districtCode: districtCode ?? this.districtCode,
      wardCode: wardCode ?? this.wardCode,
      paymentMethod: paymentMethod ?? this.paymentMethod,
    );
  }
}
