import 'package:flutter_module/common/utils/text_util.dart';

/// socialFacebook : "abas"
/// social : ["socialPhone","socialZalo","socialFacebook"]
/// provinceCode : "42"
/// districtCode : "480"
/// accessToken : "12511cad3182ba0e32d436651353d099"

class MTradePersonalWebsiteSetupPayload {
  MTradePersonalWebsiteSetupPayload({
    this.socialFacebook,
    this.social,
    this.provinceCode,
    this.districtCode,
    this.wardCode,
    this.accessToken,
    this.source,
  });

  MTradePersonalWebsiteSetupPayload.fromJson(dynamic json) {
    socialFacebook = json['socialFacebook'];
    social = json['social'] != null ? json['social'].cast<String>() : [];
    provinceCode = json['provinceCode'];
    districtCode = json['districtCode'];
    wardCode = json['wardCode'];
    accessToken = json['accessToken'];
    source = json['source'];
  }

  String? socialFacebook;
  List<String>? social;
  String? provinceCode;
  String? districtCode;
  String? wardCode;
  String? accessToken;
  String? source;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['socialFacebook'] = socialFacebook;
    map['social'] = social;
    map['provinceCode'] = provinceCode;
    map['districtCode'] = districtCode;
    map['wardCode'] = wardCode;
    if (TextUtils.isNotEmpty(accessToken)) {
      map['accessToken'] = accessToken;
    }
    map['source'] = source;
    return map;
  }
}
