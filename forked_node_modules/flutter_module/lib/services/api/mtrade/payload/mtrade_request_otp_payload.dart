class MTradeRequestOTPPayload {
  String? mobilePhone;
  String? type; // sms voice email
  bool? isRetry;
  String? function; //ekyc profile

  MTradeRequestOTPPayload({this.mobilePhone, String? type, bool? isRetry = false, String? function = 'profile'}) {
    this.type = type ?? 'voice';
    this.isRetry = isRetry ?? false;
    this.function = function ?? "profile";
  }

  MTradeRequestOTPPayload.fromJson(Map<String, dynamic> json) {
    mobilePhone = json['mobilePhone'];
    type = json['type'];
    isRetry = json['isRetry'];
    function = json['function'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['mobilePhone'] = mobilePhone;
    data['type'] = type;
    if (isRetry == true) {
      data['isRetry'] = isRetry;
    }
    data['function'] = function;
    return data;
  }

  MTradeRequestOTPPayload copyWith({
    String? mobilePhone,
    String? type,
    bool? isRetry,
    String? function,
  }) {
    return MTradeRequestOTPPayload(
      mobilePhone: mobilePhone ?? this.mobilePhone,
      type: type ?? this.type,
      isRetry: isRetry ?? this.isRetry,
      function: function ?? this.function,
    );
  }
}
