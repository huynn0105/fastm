import 'package:flutter_contacts/flutter_contacts.dart';

class MTradeTrackingAgentPayload {
  String? os;
  String? osVersion;
  String? appVersion;
  String? deviceUDID;
  String? deviceModel;
  String? event;
  String? mobilephone;
  String? lat;
  String? lon;
  String? ipAddress;
  String? param1;
  String? param2;
  String? param3;
  String? param4;
  String? param5;
  String? extraParams;
  List<Contact>? agentContact;
  List<dynamic>? agentCallHistory;
  List<dynamic>? agentInstallApp;
  String? accessToken;

  MTradeTrackingAgentPayload(
      {this.os,
      this.osVersion,
      this.appVersion,
      this.deviceUDID,
      this.deviceModel,
      this.event,
      this.mobilephone,
      this.lat,
      this.lon,
      this.ipAddress,
      this.param1,
      this.param2,
      this.param3,
      this.param4,
      this.param5,
      this.extraParams,
      this.agentContact,
      this.agentCallHistory,
      this.agentInstallApp,
      this.accessToken});

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['os'] = os;
    data['osVersion'] = osVersion;
    data['appVersion'] = appVersion;
    data['deviceUDID'] = deviceUDID;
    data['deviceModel'] = deviceModel;
    data['event'] = event;
    data['mobilephone'] = mobilephone;
    data['lat'] = lat;
    data['lon'] = lon;
    data['ipAddress'] = ipAddress;
    data['param1'] = param1;
    data['param2'] = param2;
    data['param3'] = param3;
    data['param4'] = param4;
    data['param5'] = param5;
    data['extraParams'] = extraParams;
    if (agentContact != null) {
      data['agent_contact'] = agentContact!.map((v) => v.toJson()).toList();
    }
    if (agentCallHistory != null) {
      data['agent_call_history'] = agentCallHistory!.map((v) => v.toJson()).toList();
    }
    if (agentInstallApp != null) {
      data['agent_install_app'] = agentInstallApp!.map((v) => v.toJson()).toList();
    }
    data['accessToken'] = accessToken;
    return data;
  }
}
