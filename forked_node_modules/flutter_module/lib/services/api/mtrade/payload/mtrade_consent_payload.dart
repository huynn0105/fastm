class MTradeConsentPayload {
  String? channel;
  String? alias;
  String? mobilePhone;
  int? agreementRequest;
  int? logOtpID;

  MTradeConsentPayload({
    this.channel,
    this.alias,
    this.mobilePhone,
    this.agreementRequest,
    this.logOtpID,
  });

  MTradeConsentPayload.fromJson(Map<String, dynamic> json) {
    channel = json['channel'];
    alias = json['alias'];
    mobilePhone = json['mobilePhone'];
    agreementRequest = json['agreement_request'];
    logOtpID = json['logOtpID'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['channel'] = channel;
    data['alias'] = alias;
    data['mobilePhone'] = mobilePhone;
    data['agreement_request'] = agreementRequest;
    data['logOtpID'] = logOtpID;
    return data;
  }

  MTradeConsentPayload copyWith({
    String? channel,
    String? alias,
    String? mobilePhone,
    int? agreementRequest,
    int? logOtpID,
  }) {
    return MTradeConsentPayload(
      channel: channel ?? this.channel,
      alias: alias ?? this.alias,
      mobilePhone: mobilePhone ?? this.mobilePhone,
      agreementRequest: agreementRequest ?? this.agreementRequest,
      logOtpID: logOtpID ?? this.logOtpID,
    );
  }
}
