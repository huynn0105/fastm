class MTradeMyReviewPayload {
  int? page;
  Map<String, String>? filters;

  MTradeMyReviewPayload({
    this.page,
    this.filters,
  });

  MTradeMyReviewPayload.fromJson(Map<String, dynamic> json) {
    page = json['page'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['page'] = page;
    if (filters?.isNotEmpty ?? false) {
      data.addAll(filters!);
    }
    return data;
  }

  MTradeMyReviewPayload copyWith({
    int? page,
    Map<String, String>? filters,
  }) {
    return MTradeMyReviewPayload(
      page: page ?? this.page,
      filters: filters ?? this.filters,
    );
  }
}
