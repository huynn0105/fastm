/// productID : 41
/// sku : "A95.1"
/// quantity : 1

class MTradeCartPayload {
  MTradeCartPayload({
    this.productID,
    this.sku,
    this.quantity,
    this.isUpdate = false,
  });

  MTradeCartPayload.fromJson(dynamic json) {
    productID = json['productID'];
    sku = json['sku'];
    quantity = json['quantity'];
    isUpdate = json['is_update'];
  }

  int? productID;
  String? sku;
  int? quantity;
  bool? isUpdate; // true: update trực tiếp vào DB, false: update theo số lượng truyền lên.

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['productID'] = productID;
    map['sku'] = sku;
    map['quantity'] = quantity;
    map['is_update'] = isUpdate;
    return map;
  }

  Map<String, dynamic> toJsonPreScreen() {
    final map = <String, dynamic>{};
    map['sku'] = sku;
    map['quantity'] = quantity;
    return map;
  }
}
