import 'package:flutter_module/common/utils/text_util.dart';

/// type : "DISTRICT"
/// parentCode : "42"

class MTradeMplLocationPayload {
  MTradeMplLocationPayload({
    this.type,
    this.parentCode,
  });

  MTradeMplLocationPayload.fromJson(dynamic json) {
    type = json['type'];
    parentCode = json['parentCode'];
  }

  String? type;
  String? parentCode;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['type'] = type;
    if (TextUtils.isNotEmpty(parentCode)) {
      map['parentCode'] = parentCode;
    }
    return map;
  }
}
