class MTradePromotionProductPayload {
  MTradePromotionProductPayload({
    this.skus,
    this.provinceCode,
    this.districtCode,
    this.wardCode,
  });

  List<String>? skus;
  String? provinceCode;
  String? districtCode;
  String? wardCode;

  Map<String, dynamic> toJson() {
    var map = <String, dynamic>{};
    map["skus"] = skus;
    map["provinceCode"] = provinceCode;
    map["districtCode"] = districtCode;
    map["wardCode"] = wardCode;
    return map;
  }

  MTradePromotionProductPayload copyWith({
    List<String>? skus,
    String? provinceCode,
    String? districtCode,
    String? wardCode,
  }) {
    return MTradePromotionProductPayload(
      skus: skus ?? this.skus,
      provinceCode: provinceCode ?? this.provinceCode,
      districtCode: districtCode ?? this.districtCode,
      wardCode: wardCode ?? this.wardCode,
    );
  }
}
