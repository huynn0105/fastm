class MTradeVerifyOTPPayload {
  String? mobilePhone;
  String? otpCode;

  MTradeVerifyOTPPayload({this.mobilePhone, this.otpCode});

  MTradeVerifyOTPPayload.fromJson(Map<String, dynamic> json) {
    mobilePhone = json['mobilePhone'];
    otpCode = json['otpCode'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['mobilePhone'] = mobilePhone;
    data['otpCode'] = otpCode;
    return data;
  }

  MTradeVerifyOTPPayload copyWith({
    String? mobilePhone,
    String? otpCode,
  }) {
    return MTradeVerifyOTPPayload(
      mobilePhone: mobilePhone ?? this.mobilePhone,
      otpCode: otpCode ?? this.otpCode,
    );
  }
}
