class MTradeReviewUserPayload {
  String? appID;
  String? userID;
  String? subsID;
  int? rate;
  String? note;

  MTradeReviewUserPayload({this.appID, this.userID, this.subsID, this.rate, this.note});

  MTradeReviewUserPayload.fromJson(Map<String, dynamic> json) {
    appID = json['appID'];
    userID = json['userID'];
    subsID = json['subsID'];
    rate = json['rate'];
    note = json['note'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['appID'] = appID;
    data['userID'] = userID;
    data['subsID'] = subsID;
    data['rate'] = rate;
    data['note'] = note;
    return data;
  }

  MTradeReviewUserPayload copyWith({
    String? appID,
    String? userID,
    String? subsID,
    int? rate,
    String? note,
  }) {
    return MTradeReviewUserPayload(
      appID: appID ?? this.appID,
      userID: userID ?? this.userID,
      subsID: subsID ?? this.subsID,
      rate: rate ?? this.rate,
      note: note ?? this.note,
    );
  }
}
