import '../../../../common/constants.dart';

/// month : 3
/// year : 2023
/// filter_text : ""
/// page : 1
/// per_page : 10
/// processState : "waiting"
/// collaboratorID : "1240469571"

class MTradeOrderPayload {
  MTradeOrderPayload({
    this.page = 1,
    this.perPage = AppConstants.maxPerPage,
    this.processState,
    this.filterText,
    this.collaboratorID,
    this.orderFilter,
  });

  int? page;
  int? perPage;
  String? processState;
  String? filterText;
  String? collaboratorID;
  Map<String, dynamic>? orderFilter;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['page'] = page;
    map['per_page'] = perPage;
    map['process_state'] = processState;
    if (filterText?.isNotEmpty == true) {
      map['filter_text'] = filterText;
    }
    if (collaboratorID?.isNotEmpty == true) {
      map['collaboratorID'] = collaboratorID;
    }
    if (orderFilter?.isNotEmpty == true) {
      map.addAll(orderFilter!.map((key, value) => MapEntry('$key[]', value)));
    }
    return map;
  }

  MTradeOrderPayload copyWith({
    int? page,
    int? perPage,
    String? processState,
    String? filterText,
    String? collaboratorID,
    Map<String, dynamic>? orderFilter,
  }) {
    return MTradeOrderPayload(
      page: page ?? this.page,
      perPage: perPage ?? this.perPage,
      processState: processState ?? this.processState,
      filterText: filterText ?? this.filterText,
      collaboratorID: collaboratorID ?? this.collaboratorID,
      orderFilter: orderFilter ?? this.orderFilter,
    );
  }
}
