
class MTradeBrochureProductPayload {
  MTradeBrochureProductPayload({
    this.provinceCode,
    this.districtCode,
  });

  MTradeBrochureProductPayload.fromJson(dynamic json) {
    provinceCode = json['provinceCode'];
    districtCode = json['districtCode'];
  }

  String? provinceCode;
  String? districtCode;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['provinceCode'] = provinceCode;
    map['districtCode'] = districtCode;
    return map;
  }
}
