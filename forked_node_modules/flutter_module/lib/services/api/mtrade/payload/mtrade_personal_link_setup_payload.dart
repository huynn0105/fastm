import 'package:flutter_module/models/mtrade/personal_link_setup/mtrade_personal_link_setup_model.dart';

class MTradePersonalLinkSetupPayload {
  String? fullName;
  bool? showContact;
  List<String>? social;
  String? socialPhone;
  String? socialZalo;
  String? socialFacebook;
  String? status;

  MTradePersonalLinkSetupPayload({
    this.fullName,
    this.showContact,
    this.social,
    this.socialPhone,
    this.socialZalo,
    this.socialFacebook,
    this.status,
  });

  MTradePersonalLinkSetupPayload.fromJson(Map<String, dynamic> json) {
    fullName = json['full_name'];
    showContact = json['show_contact'];
    social = json['social'].cast<String>();
    socialPhone = json['social_phone'];
    socialZalo = json['social_zalo'];
    socialFacebook = json['social_facebook'];
    status = json['status'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['show_contact'] = showContact;
    data['status'] = status;
    if (showContact ?? false) {
      data['full_name'] = fullName;
      data['social'] = social;
      data['social_phone'] = socialPhone;
      data['social_zalo'] = socialZalo;
      data['social_facebook'] = socialFacebook;
    }
    return data;
  }

  MTradePersonalLinkSetupPayload.fromModel(MTradePersonalLinkSetupModel model) {
    final Map<String, dynamic> data = <String, dynamic>{};
    fullName = model.detail?.fullName;
    showContact = model.detail?.isShowContact();
    social = model.detail?.social;
    socialPhone = model.detail?.socialPhone;
    socialZalo = model.detail?.socialZalo;
    socialFacebook = model.detail?.socialPhone;
    status = model.detail?.active;
  }
}
