class ChatSupportRequestPayload {
  ChatSupportRequestPayload({
    this.userID,
    this.type,
    this.page,
  });

  ChatSupportRequestPayload.fromJson(dynamic json) {
    userID = json['userID'];
    type = json['type'];
    page = json['page'];
  }

  int? userID;
  String? type;
  int? page;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['userID'] = userID;
    map['type'] = type;
    map['page'] = page;
    return map;
  }

  Map<String, dynamic> toJsonPreScreen() {
    final map = <String, dynamic>{};
    map['userID'] = userID;
    map['type'] = type;
    map['page'] = page;
    return map;
  }
}
