import 'package:flutter_module/services/api/chat/payload/thread_messages/thread_payload.dart';

class UpdateReadTimeInThreadPayload extends ThreadPayload {
  UpdateReadTimeInThreadPayload({
    super.threadID,
    this.userID,
  });

  String? userID;

  @override
  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['userID'] = userID;
    return {...super.toJson(), ...map};
  }
}
