class UpdateBackgroundImagePayload {
  const UpdateBackgroundImagePayload({
    this.uid,
    this.title,
    this.photoImage,
    this.backgroundImage,
    this.threadID,
  });

  final String? uid;
  final String? title;
  final String? photoImage;
  final String? backgroundImage;
  final String? threadID;

  UpdateBackgroundImagePayload copyWith({
    String? uid,
    String? title,
    String? photoImage,
    String? backgroundImage,
    String? threadID,
  }) {
    return UpdateBackgroundImagePayload(
      uid: uid ?? this.uid,
      title: title ?? this.title,
      photoImage: photoImage ?? this.photoImage,
      backgroundImage: backgroundImage ?? this.backgroundImage,
      threadID: threadID ?? this.threadID,
    );
  }

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (uid != null) {
      map['uid'] = uid;
    }
    if (title != null) {
      map['title'] = title;
    }
    if (photoImage != null) {
      map['photoImage'] = photoImage;
    }
    if (backgroundImage != null) {
      map['backgroundImage'] = backgroundImage;
    }

    return map;
  }
}
