import 'package:flutter_module/services/api/news/payload/get_news_payload.dart';

import '../../base/base_endpoint.dart';
import '../../base/method_request.dart';

abstract class NewsEndpointProtocol {
  EndpointType getListKnowledges();

  EndpointType getListNews(GetListNewsPayload payload);

  EndpointType getListContests();
}

class NewsEndpoint implements NewsEndpointProtocol {
  @override
  EndpointType getListKnowledges() {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/general/acks_list_v2",
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getListNews(GetListNewsPayload payload) {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/posts/list",
      httpMethod: HttpMethod.get,
      parameters: payload.toJson(),
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }

  @override
  EndpointType getListContests() {
    final endpoint = EndpointType(
      path: "/mfast_api_v1/general/contests",
      httpMethod: HttpMethod.get,
      parameters: {},
      header: DefaultHeader.instance.addDefaultHeader(),
    );
    return endpoint;
  }
}
