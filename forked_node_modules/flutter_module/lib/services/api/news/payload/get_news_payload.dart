import 'package:flutter_module/common/extension/map_extension.dart';

class GetListNewsPayload {
  GetListNewsPayload({
    this.keyword,
  });

  String? keyword;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['keyword'] = keyword;
    return map.removeFalsyValue();
  }

  GetListNewsPayload copyWith({
    String? keyword,
  }) {
    return GetListNewsPayload(
      keyword: keyword ?? this.keyword,
    );
  }
}
