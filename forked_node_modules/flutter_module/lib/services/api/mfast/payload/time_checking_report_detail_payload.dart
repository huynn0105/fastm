class TimeCheckingReportDetailPayload {
  String? reportID;
  String? userID;

  TimeCheckingReportDetailPayload({
    this.reportID,
    this.userID,
  });

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["reportID"] = reportID;
    map["userID"] = userID;
    return map;
  }

  TimeCheckingReportDetailPayload copyWith({
    String? reportID,
    String? userID,
  }) {
    return TimeCheckingReportDetailPayload(
      reportID: reportID ?? this.reportID,
      userID: userID ?? this.userID,
    );
  }
}
