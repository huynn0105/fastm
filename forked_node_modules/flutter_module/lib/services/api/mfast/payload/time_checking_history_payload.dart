import 'package:flutter_module/common/extension/map_extension.dart';

class TimeCheckingHistoryPayload {
  int? year;
  int? month;
  int? day; // if (null) -> get data in month, if (non-null) -> get data in day
  String? userID;

  TimeCheckingHistoryPayload({
    this.year,
    this.month,
    this.day,
    this.userID,
  });

  TimeCheckingHistoryPayload.fromJson(Map<String, dynamic> json) {
    year = json["year"];
    month = json["month"];
    day = json["day"];
    userID = json["userID"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["year"] = year;
    map["month"] = month;
    map["day"] = day;
    map["userID"] = userID;
    return map.removeFalsyValue();
  }

  TimeCheckingHistoryPayload copyWith({
    int? year,
    int? month,
    int? day,
    String? userID,
  }) {
    return TimeCheckingHistoryPayload(
      year: year ?? this.year,
      month: month ?? this.month,
      day: day ?? this.day,
      userID: userID ?? this.userID,
    );
  }

  bool get isHistoryInDay => day != null;
}
