import 'dart:convert';

class MFastTrackingEventPayload {
  MFastTrackingEventPayload({
    this.eventName,
    this.lat,
    this.lon,
    this.param1,
    this.param2,
    this.param3,
    this.param4,
    this.param5,
    this.extraParams,
  });

  String? eventName;
  String? lat;
  String? lon;
  String? param1;
  String? param2;
  String? param3;
  String? param4;
  String? param5;
  Map<String, dynamic>? extraParams;

  MFastTrackingEventPayload.formJson(Map<String, dynamic> json) {
    eventName = json['eventName'];
    lat = json['lat'];
    lon = json['lon'];
    param1 = json['param1'];
    param2 = json['param1'];
    param3 = json['param1'];
    param4 = json['param1'];
    param5 = json['param1'];
    if (json['extraParams'] is String) {
      extraParams = jsonDecode(json['extraParams']);
    }
    if (json['extraParams'] is Map) {
      extraParams = json['extraParams'];
    }
  }

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['eventName'] = eventName;
    if (lat != null) {
      map['lat'] = lat;
    }
    if (lon != null) {
      map['lon'] = lon;
    }
    if (param1 != null) {
      map['param1'] = param1;
    }
    if (param2 != null) {
      map['param2'] = param2;
    }
    if (param3 != null) {
      map['param3'] = param3;
    }
    if (param4 != null) {
      map['param4'] = param4;
    }
    if (param5 != null) {
      map['param5'] = param5;
    }
    if (extraParams != null) {
      map['extraParams'] = jsonEncode(extraParams);
    }
    return map;
  }
}
