class MFastAddBankAccountPayload {
  String? selfiePath;
  String? bankAccountNumber;
  String? bankName;
  String? bankBranch;
  int? isBidvBanking;
  int? correct; // 1: b·ªè qua ocr; 0: ocr
  double? lat;
  double? lon;

  MFastAddBankAccountPayload({
    this.selfiePath,
    this.bankAccountNumber,
    this.bankName,
    this.bankBranch,
    this.isBidvBanking,
    this.correct,
    this.lat,
    this.lon,
  });

  MFastAddBankAccountPayload.fromJson(Map<String, dynamic> json) {
    selfiePath = json['selfie_path'];
    bankAccountNumber = json['bank_accountNumber'];
    bankName = json['bank_name'];
    bankBranch = json['bank_branch'];
    isBidvBanking = json['is_bidv_banking'];
    correct = json['correct'];
    lat = json['lat'];
    lon = json['lon'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['selfie_path'] = selfiePath;
    data['bank_accountNumber'] = bankAccountNumber;
    data['bank_name'] = bankName;
    data['bank_branch'] = bankBranch;
    data['is_bidv_banking'] = isBidvBanking;
    data['correct'] = correct;
    data['lat'] = lat;
    data['lon'] = lon;
    return data;
  }
}
