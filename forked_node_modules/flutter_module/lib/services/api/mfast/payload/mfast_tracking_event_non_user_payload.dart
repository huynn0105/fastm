class MFastTrackingEventNonUserPayload {
  String? action;
  Map<String, dynamic>? attributes;

  MFastTrackingEventNonUserPayload({this.action, this.attributes});

  MFastTrackingEventNonUserPayload.fromJson(Map<String, dynamic> json) {
    action = json['action'];
    attributes = json['attributes'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['action'] = action;
    if (attributes != null) {
      data['attributes'] = attributes;
    }
    return data;
  }
}
