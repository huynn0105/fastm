class GetClientDetailPayload {
  GetClientDetailPayload({
    this.id,
    this.itemID,
  });

  String? id;
  String? itemID;

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['ID'] = id;
    data['itemID'] = itemID;
    return data;
  }

  GetClientDetailPayload copyWith({
    String? id,
    String? itemID,
  }) {
    return GetClientDetailPayload(
      id: id ?? this.id,
      itemID: itemID ?? this.itemID,
    );
  }
}
