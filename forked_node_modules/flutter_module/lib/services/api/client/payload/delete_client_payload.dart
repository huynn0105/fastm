class DeleteClientPayload {
  DeleteClientPayload({
    this.clientID,
    this.permanentlyDeleted,
    this.deleteAll,
  });

  String? clientID;
  bool? permanentlyDeleted;
  bool? deleteAll;

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['ID'] = clientID;
    data['permanentlyDeleted'] = permanentlyDeleted;
    data['deleteAll'] = deleteAll;
    return data;
  }
}
