class PriorityPayload {
  PriorityPayload({
    this.id,
  });

  String? id;

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['ID'] = id;
    return data;
  }

  PriorityPayload copyWith({
    String? id,
  }) {
    return PriorityPayload(
      id: id ?? this.id,
    );
  }
}
