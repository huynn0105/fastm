class GetStatisticByDatePayload {
  GetStatisticByDatePayload({
    this.month,
    this.year,
  });

  String? month;
  String? year;

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['month'] = month;
    data['year'] = year;
    return data;
  }

  GetStatisticByDatePayload copyWith({
    String? month,
    String? year,
  }) {
    return GetStatisticByDatePayload(
      month: month ?? this.month,
      year: year ?? this.year,
    );
  }
}
