import 'package:flutter_module/common/enum/client/ad_link_tab_type.dart';

class GetListAdLinkPayload {
  String? keySearch;
  String? type;
  int? page;

  GetListAdLinkPayload({this.keySearch, this.type, this.page});

  GetListAdLinkPayload.fromJson(Map<String, dynamic> json) {
    keySearch = json['keySearch'];
    type = json['type'];
    page = json['page'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['keySearch'] = keySearch;
    if (type != AdLinkTabType.total.name) {
      data['type'] = type;
    }
    data['page'] = page;
    return data;
  }

  GetListAdLinkPayload copyWith({
    String? keySearch,
    String? type,
    int? page,
  }) {
    return GetListAdLinkPayload(
      keySearch: keySearch ?? this.keySearch,
      type: type ?? this.type,
      page: page ?? this.page,
    );
  }
}
