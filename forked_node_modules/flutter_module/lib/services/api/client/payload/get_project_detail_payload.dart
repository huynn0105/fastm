class GetProjectDetailPayload {
  String? applicationID;
  String? category;

  GetProjectDetailPayload({this.applicationID, this.category});

  GetProjectDetailPayload.fromJson(Map<String, dynamic> json) {
    applicationID = json['applicationID'];
    category = json['category'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['applicationID'] = applicationID;
    data['category'] = category;
    return data;
  }

  GetProjectDetailPayload copyWith({
    String? applicationID,
    String? category,
  }) {
    return GetProjectDetailPayload(
      applicationID: applicationID ?? this.applicationID,
      category: category ?? this.category,
    );
  }
}
