class UpdateClientPayload {
  UpdateClientPayload({
    this.id,
    this.district,
    this.fullName,
    this.idNumber,
    this.mobilePhone,
    this.note,
    this.avatar,
  });

  String? id;
  String? district;
  String? fullName;
  String? idNumber;
  String? mobilePhone;
  String? note;
  String? avatar;

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['ID'] = id;
    data['district'] = district;
    data['fullName'] = fullName;
    data['idNumber'] = idNumber;
    data['mobilePhone'] = mobilePhone;
    data['note'] = note;
    data['avatar'] = avatar;
    return data..removeWhere((key, value) => value == null || value == '');
  }
}
