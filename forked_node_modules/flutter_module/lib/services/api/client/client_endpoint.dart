abstract class ClientEndpointProtocol {
  EndpointType getStatisticByStatus({required String category});

  EndpointType getStatisticByProduct();

  EndpointType getStatisticBySkill();

  EndpointType getFilterClient();

  EndpointType getListCustomer(GetListClientPayload payload);

  EndpointType getCustomerDetail(GetClientDetailPayload payload);

  EndpointType addPriority(PriorityPayload payload);

  EndpointType deletePriority(PriorityPayload payload);

  EndpointType updateClient(UpdateClientPayload payload);

  EndpointType getDistricts();

  EndpointType getDetailProject(GetProjectDetailPayload payload);

  EndpointType deleteClient(DeleteClientPayload payload);

  EndpointType deleteTrash();

  EndpointType getStatisticByDate(GetStatisticByDatePayload payload);

  EndpointType getListAdLink(GetListAdLinkPayload payload);

  EndpointType getTotalAdLink();

  EndpointType getStatisticLink({List? potentialIDs});

  EndpointType getDetailAdLink(String id);

  EndpointType updateAdLink(DetailLinkSetup payload);

  EndpointType createAdLink(DetailLinkSetup payload);

  EndpointType removeAdLink(String id);
}

