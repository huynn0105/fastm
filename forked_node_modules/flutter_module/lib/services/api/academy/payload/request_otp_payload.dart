/// mobilePhone : "0987654321"
/// type : "voice"
/// isRetry : "0"

class AcademyRequestOTPPayload {
  AcademyRequestOTPPayload({
    this.mobilePhone,
    this.type,
    this.isRetry,
  });

  AcademyRequestOTPPayload.fromJson(dynamic json) {
    mobilePhone = json['mobilePhone'];
    type = json['type'];
    isRetry = json['isRetry'] == "1";
  }

  String? mobilePhone;
  String? type;
  bool? isRetry;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['mobilePhone'] = mobilePhone;
    map['type'] = type;
    map['isRetry'] = (isRetry == true ? "1" : "0");
    return map;
  }
}
