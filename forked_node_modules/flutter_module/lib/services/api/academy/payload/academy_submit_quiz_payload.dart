import 'package:flutter_module/common/utils/text_util.dart';

class AcademySubmitQuizPayload {
  int? eventID;
  String? mobilePhone;
  List<SubmitQuizAnswer>? answer;

  AcademySubmitQuizPayload({this.eventID, this.mobilePhone, this.answer});

  AcademySubmitQuizPayload.fromJson(Map<String, dynamic> json) {
    eventID = json['eventID'];
    mobilePhone = json['mobilePhone'];
    if (json['answer'] != null) {
      answer = <SubmitQuizAnswer>[];
      json['answer'].forEach((v) {
        answer!.add(SubmitQuizAnswer.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['eventID'] = eventID;
    data['mobilePhone'] = mobilePhone;
    if (answer != null) {
      data['answer'] = answer!.map((v) => v.toJson()).toList();
    }
    return data;
  }

  AcademySubmitQuizPayload copyWith({
    int? index,
    int? eventID,
    String? mobilePhone,
    List<SubmitQuizAnswer>? answer,
  }) {
    return AcademySubmitQuizPayload(
      eventID: eventID ?? this.eventID,
      mobilePhone: mobilePhone ?? this.mobilePhone,
      answer: answer ?? this.answer,
    );
  }
}

class SubmitQuizAnswer {
  int? index;
  int? questionID;
  String? answerSelect;
  bool? isAnswer;

  SubmitQuizAnswer({this.index, this.questionID, this.answerSelect, this.isAnswer});

  SubmitQuizAnswer.fromJson(Map<String, dynamic> json) {
    index = TextUtils.parseInt(json['index']);
    questionID = TextUtils.parseInt(json['questionID']);
    answerSelect = json['answerSelect'];
    isAnswer = json['is_answer'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['index'] = index;
    data['questionID'] = questionID;
    data['answerSelect'] = answerSelect;
    data['is_answer'] = isAnswer;
    return data;
  }
}
