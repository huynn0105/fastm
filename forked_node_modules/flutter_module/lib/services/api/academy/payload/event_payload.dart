class AcademyEventPayload {
  List<String>? weekTime;
  List<String>? eventType;
  List<String>? eventGroup;
  int? page;

  AcademyEventPayload({
    this.weekTime,
    this.eventType,
    this.eventGroup,
    this.page,
  });

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map['weekTime'] = weekTime;
    map['eventType'] = eventType;
    map['eventGroup'] = eventGroup;
    map['page'] = page;
    return map;
  }

  AcademyEventPayload copyWith({
    List<String>? weekTime,
    List<String>? eventType,
    List<String>? eventGroup,
    int? page,
  }) {
    return AcademyEventPayload(
      weekTime: weekTime ?? this.weekTime,
      eventType: eventType ?? this.eventType,
      eventGroup: eventGroup ?? this.eventGroup,
      page: page ?? this.page,
    );
  }
}
