/// eventID : 3

class AcademyEventDetailPayload {
  AcademyEventDetailPayload({
    this.eventID,
    this.status,
  });

  AcademyEventDetailPayload.fromJson(dynamic json) {
    eventID = json['eventID'];
    status = json['status'];
  }

  String? eventID;
  String? status;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['eventID'] = eventID;
    map['status'] = status;
    return map;
  }
}
