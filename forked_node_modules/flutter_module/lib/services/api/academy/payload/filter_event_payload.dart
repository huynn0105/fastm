class AcademyFilterEventPayload {
  String? type; // if null -> get all

  AcademyFilterEventPayload({
    this.type,
  });

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['type'] = type;
    return data;
  }

  AcademyFilterEventPayload copyWith({
    String? type,
  }) {
    return AcademyFilterEventPayload(
      type: type ?? this.type,
    );
  }
}
