class RegisteredEventPayload {
  RegisteredEventPayload({
    this.type,
    this.page = 1,
  });

  String? type;
  int? page;

  RegisteredEventPayload.fromJson(dynamic json) {
    type = json['type'];
    page = json['page'];
  }

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['type'] = type;
    map['page'] = page;
    return map;
  }

  RegisteredEventPayload copyWith({
    String? type,
    int? page,
  }) {
    return RegisteredEventPayload(
      type: type ?? this.type,
      page: page ?? this.page,
    );
  }
}
