class LogoutPayload {
  LogoutPayload({
    this.deviceUDID,
    this.userID,
    this.accessToken,
    this.logoutAllOfSessions,
  });

  String? deviceUDID;
  String? userID;
  String? accessToken;
  bool? logoutAllOfSessions;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['deviceUDID'] = deviceUDID;
    map['userID'] = userID;
    map['userAccessToken'] = accessToken;
    map['logoutAllOfSessions'] = logoutAllOfSessions?.toString();
    return map;
  }
}
