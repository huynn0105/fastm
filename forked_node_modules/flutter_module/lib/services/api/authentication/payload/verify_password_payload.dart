class VerifyPasswordPayload {
  VerifyPasswordPayload({
    this.mobilePhone,
    this.deviceUDID,
    this.password,
  });

  VerifyPasswordPayload.fromJson(dynamic json) {
    mobilePhone = json['mobilePhone'];
    deviceUDID = json['deviceUDID'];
    password = json['password'];
  }

  String? mobilePhone;
  String? deviceUDID;
  String? password;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['mobilePhone'] = mobilePhone;
    map['deviceUDID'] = deviceUDID;
    map['password'] = password;
    return map;
  }
}
