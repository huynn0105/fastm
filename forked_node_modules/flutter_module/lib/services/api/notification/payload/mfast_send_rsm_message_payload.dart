class MFastSendRSMMessagePayload {
  const MFastSendRSMMessagePayload({
    this.senderID,
    this.receiverID,
    this.text,
  });

  final String? senderID;
  final String? receiverID;
  final String? text;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['senderID'] = senderID;
    map['receiverID'] = receiverID;
    map['text'] = text;
    return map;
  }

  MFastSendRSMMessagePayload copyWith({
    String? senderID,
    String? receiverID,
    String? text,
  }) {
    return MFastSendRSMMessagePayload(
      senderID: senderID ?? this.senderID,
      receiverID: receiverID ?? this.receiverID,
      text: text ?? this.text,
    );
  }
}
