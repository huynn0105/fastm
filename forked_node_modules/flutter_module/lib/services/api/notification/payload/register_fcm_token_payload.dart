class RegisterFcmTokenPayload {
  String? userId;
  String? deviceUdid;
  String? fcmToken;

  RegisterFcmTokenPayload({this.userId, this.deviceUdid, this.fcmToken});

  RegisterFcmTokenPayload.fromJson(Map<String, dynamic> json) {
    userId = json["userID"];
    deviceUdid = json["deviceUDID"];
    fcmToken = json["fcmToken"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["userID"] = userId;
    data["deviceUDID"] = deviceUdid;
    data["fcmToken"] = fcmToken;
    return data;
  }

  RegisterFcmTokenPayload copyWith({
    String? userId,
    String? deviceUdid,
    String? fcmToken,
  }) {
    return RegisterFcmTokenPayload(
      userId: userId ?? this.userId,
      deviceUdid: deviceUdid ?? this.deviceUdid,
      fcmToken: fcmToken ?? this.fcmToken,
    );
  }
}
