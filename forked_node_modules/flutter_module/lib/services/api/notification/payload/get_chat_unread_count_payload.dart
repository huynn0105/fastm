class GetChatUnreadCountPayload {
  String? firebaseToken;
  List<String>? withoutThreadIDs;

  GetChatUnreadCountPayload({
    this.firebaseToken,
    this.withoutThreadIDs,
  });

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["token"] = firebaseToken;
    data["withoutThreadIDs"] = withoutThreadIDs;
    return data;
  }
}
