class MFastNotificationPayload {
  const MFastNotificationPayload({
    this.category,
    this.type,
    this.isUnread,
    this.page = 1,
    this.keyword,
  });

  final String? category;
  final String? type;
  final bool? isUnread;
  final int page;
  final String? keyword;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['category'] = category;
    map['type'] = type;
    map['isUnread'] = isUnread == false ? null : isUnread;
    map['page'] = page;
    map['keyword'] = keyword;
    return map;
  }

  MFastNotificationPayload copyWith({
    String? category,
    String? type,
    bool? isUnread,
    int? page,
    String? keyword,
  }) {
    return MFastNotificationPayload(
      category: category ?? this.category,
      type: type ?? this.type,
      isUnread: isUnread ?? this.isUnread,
      page: page ?? this.page,
      keyword: keyword ?? this.keyword,
    );
  }

  MFastNotificationPayload copyWithIsUnread({
    String? category,
    String? type,
    bool? isUnread,
    int? page,
    String? keyword,
  }) {
    return MFastNotificationPayload(
      category: category ?? this.category,
      type: type ?? this.type,
      isUnread: isUnread,
      page: page ?? this.page,
      keyword: keyword ?? this.keyword,
    );
  }
}
