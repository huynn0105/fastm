import 'package:collection/collection.dart';
import 'package:flutter_module/models/chat/message/user_on_message_model.dart';

class UserChatDataHelper {

  UserChatDataHelper._();

  static final instance = UserChatDataHelper._();

  List<UserOnMessageModel> usersChat = [];

  UserOnMessageModel? getExistUser(String id) {
    return usersChat.firstWhereOrNull((e) => e.id == id);
  }

  addUser(UserOnMessageModel user) {
    usersChat.add(user);
  }
}
