import 'package:flutter_module/common/utils/log_util.dart';

import '../../env_data.dart';

class MFastDeeplinkPath {
  static String _host(String view, {Map<String, String>? params}) {
    final uri = Uri.parse("${EnvData.instance.deepLinkBaseUrl}://open?view=$view");
    final queryParameters = {
      ...uri.queryParameters,
      ...?params,
    };
    final result = uri.replace(queryParameters: queryParameters).toString();
    AppLog.i("MFastDeeplinkPath", result);
    return result;
  }

  static String openLogin({Map<String, String>? params}) {
    return _host("Login", params: params);
  }

  static String openNotification({Map<String, String>? params}) {
    return _host("Notification", params: params);
  }

  static String openOSTicket({Map<String, String>? params}) {
    return _host("OSTicket", params: params);
  }

  static String openAccountInfo({Map<String, String>? params}) {
    return _host("AccountInforScreen", params: params);
  }

  static String openView({required String screenName, Map<String, String>? params}) {
    return _host(screenName, params: params);
  }
}
