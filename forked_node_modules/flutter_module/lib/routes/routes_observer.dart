import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/routes/routes.gr.dart';

import '../common/event_bus/event_bus.dart';
import '../general_config.dart';
import '../services/local/local_data_helper.dart';

class AppRoutesObserver extends AutoRouterObserver {
  @override
  void didPush(Route route, Route? previousRoute) {
    if (previousRoute?.settings.name == '') {}
  }

  @override
  void didPop(Route route, Route? previousRoute) {
    if (LocalDataHelper.instance.getIsUserGuideBanner() && previousRoute?.settings.name == MainTabbarRoute.name) {
      eventBus.fire(StartUserGuideEventBus());
    }
  }
}
