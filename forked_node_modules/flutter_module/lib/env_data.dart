import 'dart:io';

import 'package:flutter_config/flutter_config.dart';

class EnvData {
  EnvData._();

  static final instance = EnvData._();

  late String appName;
  late String baseUrl;
  late String losUrl;
  late String losApiKey;
  late String firebaseFunctionUrl;
  late String apiUserName;
  late String apiPassword;
  late String chatSupportUrl;
  late String deepLinkBaseUrl;
  late String ekycSdkToken;
  late String firebaseAuthToken;
  late String androidGoogleApiKey;
  late String iosGoogleApiKey;
  late bool isProduction;
  late String afDevKey;
  late String appID;
  late String moeAppID;
  late String vnpayTmnCode;
  late String vnpayHashSecret;
  late String vnpPayUrl;
  late String webUrl;

  String? package;
  late bool showLog;

  getEnvData() async {
    appName = FlutterConfig.get('APP_NAME');
    baseUrl = FlutterConfig.get('SERVER_URL');
    apiUserName = FlutterConfig.get('API_USERNAME');
    apiPassword = FlutterConfig.get('API_PASSWORD');
    losUrl = FlutterConfig.get('LOS_URL');
    losApiKey = FlutterConfig.get('LOS_API_KEY');
    showLog = bool.parse(FlutterConfig.get('SHOW_LOG'));
    chatSupportUrl = FlutterConfig.get('API_OS_TICKET');
    deepLinkBaseUrl = FlutterConfig.get('DEEP_LINK_SCHEME');
    ekycSdkToken = FlutterConfig.get('EKYC_SDK_TOKEN');
    androidGoogleApiKey = FlutterConfig.get('ANDROID_GOOGLE_API_KEY');
    iosGoogleApiKey = FlutterConfig.get('IOS_GOOGLE_API_KEY');
    firebaseFunctionUrl = FlutterConfig.get('FIREBASE_FUNCTIONS_BASE_URL');
    isProduction = bool.parse(FlutterConfig.get('IS_PRODUCTION'));
    afDevKey = FlutterConfig.get('AF_DEV_KEY');
    appID = FlutterConfig.get('APP_ID');
    moeAppID = FlutterConfig.get('MOE_API_KEY');
    vnpayTmnCode = FlutterConfig.get('VNP_TMNCODE');
    vnpayHashSecret = FlutterConfig.get('VNP_HASHSECRET');
    vnpPayUrl = FlutterConfig.get('VNP_URL_PAYMENT');
    webUrl = FlutterConfig.get('MTRADE_LANDING_PAGE_URL');
  }

  String get googleApiKey => Platform.isAndroid ? androidGoogleApiKey : iosGoogleApiKey;
}
