abstract class PushNotificationEventBus {}

class RefreshNotificationListEventBus extends PushNotificationEventBus {
  RefreshNotificationListEventBus(this.category);

  final NotificationCategory category;
}

class ForceShowLogoutPopupEventBus extends PushNotificationEventBus {}

class ReadNotificationEventBus extends PushNotificationEventBus {
  ReadNotificationEventBus(this.notificationID);

  final String? notificationID;
}

class ReloadShipperBillLadingEventBus extends PushNotificationEventBus {
  ReloadShipperBillLadingEventBus({
    this.action,
    this.deliveryOrderCode,
  });

  final String? action;
  final String? deliveryOrderCode;
}
