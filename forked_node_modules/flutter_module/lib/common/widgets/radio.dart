import 'package:flutter/material.dart';
import '../colors.dart';
import '../constants.dart';
import '../styles.dart';
import 'buttons.dart';
import 'images.dart';

class AppRadio extends StatelessWidget {
  const AppRadio({
    Key? key,
    required this.title,
    required this.value,
    this.onChanged,
    this.fontSize = 14,
    this.icon,
  }) : super(key: key);

  final String title;
  final bool value;
  final Function(bool)? onChanged;
  final double? fontSize;
  final Widget? icon;

  @override
  Widget build(BuildContext context) {
    return Row(
      children: [
        icon ??
            SizedBox(
              height: 24,
              width: 24,
              child: Transform.scale(
                scale: 1.2,
                child: Radio(
                  value: value,
                  groupValue: true,
                  activeColor: UIColors.primaryColor,
                  onChanged: (bool? value) {
                    onChanged?.call(value == true);
                  },
                ),
              ),
            ),
        const SizedBox(
          width: 8,
        ),
        Text(
          title,
          style: value
              ? UITextStyle.bold.copyWith(
                  color: UIColors.boldText,
                  fontSize: fontSize,
                  height: 1,
                )
              : UITextStyle.regular.copyWith(
                  color: UIColors.grayText,
                  fontSize: fontSize,
                  height: 1,
                ),
        )
      ],
    );
  }
}

class StarMarkRadio extends StatefulWidget {
  const StarMarkRadio({
    Key? key,
    this.value = false,
    this.onChanged,
  }) : super(key: key);

  final bool value;
  final Function(bool)? onChanged;

  @override
  State<StarMarkRadio> createState() => _StarMarkRadioState();
}

class _StarMarkRadioState extends State<StarMarkRadio> {
  late bool value;

  @override
  void initState() {
    super.initState();
    value = widget.value;
  }

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      onTap: () {
        setState(() {
          value = !value;
        });
        widget.onChanged?.call(value);
      },
      child: AnimatedSwitcher(
        duration: AppConstants.duration,
        child: value
            ? AppImage.asset(
                key: ValueKey("marked-${DateTime.now().toIso8601String()}"),
                asset: "ic_star",
                width: 24,
                height: 24,
                color: UIColors.green,
              )
            : AppImage.asset(
                key: ValueKey("unmarked-${DateTime.now().toIso8601String()}"),
                asset: "ic_star_outline",
                width: 24,
                height: 24,
              ),
      ),
    );
  }
}

