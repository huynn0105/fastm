import 'package:flutter/material.dart';
import 'package:flutter_module/common/widgets/collect_data_view.dart';

class CollectDoubleDataView extends StatelessWidget {
  const CollectDoubleDataView({
    Key? key,
    required this.childFirst,
    required this.childSecond,
    this.descriptionFirst,
    this.descriptionSecond,
    this.errorFirstMsg,
    this.errorSecondMsg,
  }) : super(key: key);
  final Widget childFirst;
  final Widget childSecond;
  final Widget? descriptionFirst;
  final Widget? descriptionSecond;
  final String? errorFirstMsg;
  final String? errorSecondMsg;

  @override
  Widget build(BuildContext context) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Expanded(
          child: CollectDataView(
            errorMsg: errorFirstMsg,
            description: descriptionFirst,
            child: childFirst,
          ),
        ),
        const SizedBox(
          width: 19,
        ),
        Expanded(
          child: CollectDataView(
            errorMsg: errorSecondMsg,
            description: descriptionSecond,
            child: childSecond,
          ),
        ),
      ],
    );
  }
}
