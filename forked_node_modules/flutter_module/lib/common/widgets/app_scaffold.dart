import 'package:flutter/foundation.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';

import 'app_background.dart';

class AppScaffold extends StatelessWidget {
  final Widget body;
  final PreferredSizeWidget? appBar;
  final String logo;
  final Color backgroundColor;

  const AppScaffold({
    Key? key,
    required this.body,
    this.appBar,
    this.logo = "mtrade_web_logo",
    this.backgroundColor = UIColors.background,
  }) : super(key: key);

  const AppScaffold.academy({
    Key? key,
    required this.body,
    this.appBar,
    this.logo = "ic_academy_mfast_logo",
    this.backgroundColor = UIColors.background,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return Stack(
      children: [
        if (kIsWeb)
          AppBackground(
            logo: logo,
          ),
        Scaffold(
          appBar: appBar,
          backgroundColor: kIsWeb ? Colors.transparent : backgroundColor,
          body: body,
        ),
      ],
    );
  }
}
