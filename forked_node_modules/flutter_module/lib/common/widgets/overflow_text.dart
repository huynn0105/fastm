import 'package:flutter/material.dart';

class OverflowText extends Text {
  OverflowText(
    String text, {
    Key? key,
    int maxLines = 1,
    TextOverflow overflow = TextOverflow.ellipsis,
    TextStyle? style,
  }) : super(
          maxLines > 1 ? text : text.replaceAll('', '\u{200B}'),
          key: key,
          overflow: overflow,
          maxLines: maxLines,
          softWrap: true,
          style: style,
        );
}
