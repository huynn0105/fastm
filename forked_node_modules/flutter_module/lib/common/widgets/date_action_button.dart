import 'package:flutter/material.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

import '../../../../../../../common/colors.dart';

class DateActionButton extends StatelessWidget {
  const DateActionButton.left({
    Key? key,
    required this.onTap,
    this.asset = "ic_arrow_left",
    this.enabled = true,
  }) : super(key: key);

  const DateActionButton.right({
    Key? key,
    required this.onTap,
    this.asset = "ic_arrow_right",
    this.enabled = true,
  }) : super(key: key);

  final String asset;
  final bool enabled;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    final color = enabled ? UIColors.darkGray : UIColors.lightGray;
    return SplashButton(
      isDisabled: !enabled,
      onTap: onTap,
      borderRadius: BorderRadius.circular(25),
      child: Container(
        width: 25,
        height: 25,
        alignment: Alignment.center,
        margin: const EdgeInsets.fromLTRB(16, 10, 16, 10),
        decoration: BoxDecoration(
          shape: BoxShape.circle,
          border: Border.all(
            color: color,
          ),
        ),
        child: AppImage.asset(
          asset: asset,
          width: 18,
          height: 18,
          color: color,
          fit: BoxFit.contain,
        ),
      ),
    );
  }
}

