import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter/material.dart';

import 'images.dart';

class UpDownNumberInput extends StatelessWidget {
  const UpDownNumberInput({
    Key? key,
    this.value,
    this.min = 0,
    this.max = 100,
    this.unit = '',
    this.readOnly = true,
    this.valueWidth = 80,
    this.onChange,
  }) : super(key: key);
  final int? value;
  final int min;
  final int max;
  final String unit;
  final bool readOnly;
  final double valueWidth;
  final Function(int)? onChange;

  void increment() {
    int current = value ?? min;
    if (onChange != null) {
      onChange!(current + 1);
    }
  }

  void decrement() {
    int current = value ?? min;
    if (onChange != null) {
      onChange!(current - 1);
    }
  }

  @override
  Widget build(BuildContext context) {
    int current = value ?? 0;
    return Wrap(
      alignment: WrapAlignment.start,
      crossAxisAlignment: WrapCrossAlignment.center,
      children: [
        SplashButton(
          onTap: current > min ? decrement : null,
          child: AppImage.asset(
            asset: 'remove_icon',
            width: 24,
            height: 24,
            color: current > min ? UIColors.grayText : UIColors.gray,
            fit: BoxFit.contain,
          ),
        ),
        Padding(
          padding: const EdgeInsets.only(left: 24, right: 24, bottom: 2),
          child: SizedBox(
            width: valueWidth,
            child: Center(
              child: Text(
                '$current $unit',
                style: UITextStyle.semiBold.copyWith(
                  fontSize: 18,
                ),
              ),
            ),
          ),
        ),
        SplashButton(
          onTap: current < max ? increment : null,
          child: AppImage.asset(
            asset: 'add_icon',
            width: 24,
            height: 24,
            color: current < max ? UIColors.grayText : UIColors.gray,
            fit: BoxFit.contain,
          ),
        ),
      ],
    );
  }
}
