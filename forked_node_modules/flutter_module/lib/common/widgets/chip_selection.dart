import 'package:flutter/material.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

import '../colors.dart';
import '../styles.dart';

class ChipSelection extends StatelessWidget {
  const ChipSelection({
    Key? key,
    required this.title,
    required this.onTap,
    this.selected = false,
    this.unselectedTextColor = UIColors.grayText,
    this.unselectedBackgroundColor = UIColors.background,
    this.selectedTextColor = UIColors.white,
    this.selectedBackgroundColor = UIColors.primaryColor,
  }) : super(key: key);

  final String title;
  final bool selected;
  final Color unselectedTextColor;
  final Color unselectedBackgroundColor;
  final Color selectedTextColor;
  final Color selectedBackgroundColor;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    final Color textColor = selected ? selectedTextColor : unselectedTextColor;
    final Color backgroundColor = selected ? selectedBackgroundColor : unselectedBackgroundColor;
    return SplashButton(
      onTap: onTap,
      borderRadius: BorderRadius.circular(20),
      child: Container(
        height: 40,
        alignment: Alignment.center,
        padding: const EdgeInsets.symmetric(horizontal: 12),
        decoration: BoxDecoration(
          color: backgroundColor,
          borderRadius: BorderRadius.circular(20),
        ),
        child: Text(
          title,
          style: UITextStyle.regular.copyWith(
            color: textColor,
          ),
        ),
      ),
    );
  }
}
