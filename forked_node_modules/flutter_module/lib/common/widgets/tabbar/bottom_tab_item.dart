import 'package:flutter/material.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/buttons.dart';
import '../../../../../common/widgets/images.dart';

class BottomTabItem extends StatelessWidget {
  const BottomTabItem({
    super.key,
    required this.image,
    required this.text,
    required this.onTap,
    this.isEnable = false,
    this.highlight = false,
  });

  final String image;
  final String text;
  final VoidCallback onTap;
  final bool isEnable;
  final bool highlight;

  @override
  Widget build(BuildContext context) {
    return Expanded(
      child: SplashButton(
        onTap: onTap,
        child: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            const SizedBox(
              height: 10,
              width: double.infinity,
            ),
            AppImage.asset(
              asset: image + (isEnable ? "_selected" : ""),
              width: 24,
              height: 24,
            ),
            const SizedBox(
              height: 5,
            ),
            Text(
              text,
              style: UITextStyle.regular.copyWith(
                fontSize: 11,
                height: 1.2,
                color: isEnable ? UIColors.primaryColor : UIColors.grayText,
              ),
            ),
            const SizedBox(
              height: 10,
              width: double.infinity,
            ),
          ],
        ),
      ),
    );
  }
}
