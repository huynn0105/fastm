import 'package:flutter/material.dart';

import '../../../models/master_data/master_data_model.dart';
import '../../colors.dart';
import '../../styles.dart';
import '../../utils/vietnamese_util.dart';
import '../textfields.dart';
import 'drop_down_item.dart';

class DropdownItemList extends StatefulWidget {
  const DropdownItemList({
    Key? key,
    this.items = const [],
    this.currentIndex = -1,
    this.maxHeight = 0,
    this.showUnder = true,
    this.onItemTap,
    this.toggleDropdown,
  }) : super(key: key);

  final List<DropdownItem> items;
  final int currentIndex;
  final double maxHeight;
  final bool showUnder;
  final Function(DropdownItem, int)? onItemTap;
  final Function()? toggleDropdown;

  @override
  State<StatefulWidget> createState() {
    return _DropdownItemListState();
  }
}

class _DropdownItemListState extends State<DropdownItemList> {
  final TextEditingController _searchController = TextEditingController();

  @override
  void dispose() {
    _searchController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    List<DropdownItem> data = widget.items.where((element) {
      if (element.value is MasterDataModel) {
        final enTitle =
            VietnameseUtils.toEnglish((element.value as MasterDataModel).name)
                .toLowerCase();
        final enSearch =
            VietnameseUtils.toEnglish(_searchController.text).toLowerCase();
        return enTitle.contains(enSearch);
      }
      return true;
    }).toList();
    return Column(
      mainAxisSize: MainAxisSize.min,
      children: [
        if (widget.showUnder && widget.items.length > 15)
          //size 48
          UITextFieldOutline(
            controller: _searchController,
            contentPadding:
                const EdgeInsets.only(left: 10, right: 10, top: 12, bottom: 12),
            inputBorder: const OutlineInputBorder(
              borderSide: BorderSide(
                width: 1,
                color: UIColors.gray,
              ),
              borderRadius: BorderRadius.all(
                Radius.circular(2),
              ),
            ),
            hintText: 'Nhập để tìm kiếm',
            onChanged: (text) {
              setState(() {});
            },
          ),
        ConstrainedBox(
          constraints: BoxConstraints(
            minHeight: 0,
            maxHeight: widget.maxHeight - 48,//text field with size = 48
          ),
          child: ListView.builder(
            itemCount: data.isEmpty ? 1 : data.length,
            shrinkWrap: true,
            physics: const BouncingScrollPhysics(),
            itemBuilder: (context, index) {
              if (data.isEmpty) {
                return InkWell(
                  onTap: widget.toggleDropdown,
                  child: Container(
                    color: UIColors.white,
                    padding: const EdgeInsets.all(10),
                    child: Text(
                      'Không có dữ liệu',
                      style: UITextStyle.regular,
                    ),
                  ),
                );
              }
              final item = data[index];
              final realIndex = widget.items.indexOf(item);
              return InkWell(
                onTap: () {
                  widget.onItemTap?.call(item, realIndex);
                  widget.toggleDropdown?.call();
                },
                child: Container(
                  color: realIndex == widget.currentIndex
                      ? UIColors.lightGray
                      : UIColors.white,
                  padding: const EdgeInsets.all(10),
                  child: item.child,
                ),
              );
            },
          ),
        ),
        if (!widget.showUnder && widget.items.length > 15)
          UITextFieldOutline(
            controller: _searchController,
            contentPadding:
            const EdgeInsets.only(left: 10, right: 10, top: 12, bottom: 12),
            inputBorder: const OutlineInputBorder(
              borderSide: BorderSide(
                width: 1,
                color: UIColors.gray,
              ),
              borderRadius: BorderRadius.all(
                Radius.circular(2),
              ),
            ),
            hintText: 'Nhập để tìm kiếm',
            //size 48
            onChanged: (text) {
              setState(() {});
            },
          ),
      ],
    );
  }
}
