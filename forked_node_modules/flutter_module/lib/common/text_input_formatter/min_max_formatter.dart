
import 'package:flutter/services.dart';

class MinMaxFormatter extends TextInputFormatter {
  final int min;
  final int max;

  MinMaxFormatter(this.min, this.max);

  @override
  TextEditingValue formatEditUpdate(
      TextEditingValue oldValue, TextEditingValue newValue) {
    if (int.parse(newValue.text) < min) {
      return const TextEditingValue().copyWith(text: '$min');
    }

    if (int.parse(newValue.text) > max) {
      return const TextEditingValue().copyWith(text: '$max');
    }

    return newValue;
  }
}
