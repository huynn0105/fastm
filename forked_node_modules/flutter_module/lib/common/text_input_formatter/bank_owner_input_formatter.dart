import 'package:flutter/services.dart';

import '../utils/vietnamese_util.dart';

class BankOwnerInputFormatter extends TextInputFormatter {
  @override
  TextEditingValue formatEditUpdate(
      TextEditingValue oldValue, TextEditingValue newValue) {
    final reg = RegExp(
        r"[^a-zA-Z _ÀÁÂÃÈÉÊÌÍÒÓÔÕÙÚĂĐĨŨƠàáâãèéêìíòóôõùúăđĩũơƯĂẠẢẤẦẨẪẬẮẰẲẴẶẸẺẼỀỀỂưăạảấầẩẫậắằẳẵặẹẻẽềềểỄỆỈỊỌỎỐỒỔỖỘỚỜỞỠỢỤỦỨỪễếệỉịọỏốồổỗộớờởỡợụủứừỬỮỰỲỴÝỶỸửữựỳỵỷỹ]");
    var text = newValue.text.replaceAll(reg, '');
    int offset = 0;
    if (newValue.selection.baseOffset < text.length) {
      if (text.length == newValue.text.length) {
        offset = newValue.selection.baseOffset;
      } else {
        offset = newValue.selection.baseOffset - 1;
      }
    } else {
      offset = text.length;
    }
    return TextEditingValue(
      text: VietnameseUtils.toEnglish(text.toUpperCase()),
      selection: TextSelection.collapsed(
        offset: offset,
      ),
    );
  }
}
