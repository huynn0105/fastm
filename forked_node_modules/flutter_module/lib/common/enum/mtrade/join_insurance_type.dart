enum JoinInsuranceType {
  required(1), // force user must has join insurance.
  optional(0), // allow user can select join insurance or not.
  hide(null); // not config insurance.

  final int? value;
  const JoinInsuranceType(this.value);
}

extension ExJoinInsuranceType on JoinInsuranceType {
  bool get isRequired => this == JoinInsuranceType.required;
  bool get isOptional => this == JoinInsuranceType.optional;
  bool get isHide => this == JoinInsuranceType.hide;
}