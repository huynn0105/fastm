enum MTradeLandingPageSource {
  mtrade(null),
  daa("daa"),
  tsa("tsa");

  final String? code;

  const MTradeLandingPageSource(this.code);
}

extension ExMTradeLandingPageSource on MTradeLandingPageSource? {
  bool get isMTrade => this == MTradeLandingPageSource.mtrade || this == null;

  bool get isDAA => this == MTradeLandingPageSource.daa;

  bool get isTSA => this == MTradeLandingPageSource.tsa;
}
