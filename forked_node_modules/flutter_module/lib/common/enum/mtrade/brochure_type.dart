enum BrochureType {
  categories(
    type: "Loại sản phẩm",
    title: "Loại sản phẩm",
    subtitle: "Loại sản phẩm cần gới thiệu",
    pdfTitle: "Danh sách sản phẩm",
  ),
  products(
    type: "Sản phẩm",
    title: "Tuỳ chọn sản phẩm",
    subtitle: "Chọn các sản phẩm cần giới thiệu",
    pdfTitle: "Sản phẩm nổi bật",
  ),
  specificProduct(
    type: "Sản phẩm",
    title: "1 sản phẩm",
    subtitle: "Chọn sản phẩm cần giới thiệu",
    pdfTitle: "Sản phẩm nổi bật",
  );

  final String type;
  final String title;
  final String subtitle;
  final String pdfTitle;

  const BrochureType({
    required this.type,
    required this.title,
    required this.subtitle,
    required this.pdfTitle,
  });
}

extension ExBrochureType on BrochureType? {
  bool get isCategories => this == BrochureType.categories;

  bool get isProducts => this == BrochureType.products;

  bool get isSpecificProduct => this == BrochureType.specificProduct;
}
