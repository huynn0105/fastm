enum MTradeProductDetailType {
  product,
  suggestedProductFromDetail,
  suggestedProductFromPayNowOrder,
}

extension ExMTradeProductDetailType on MTradeProductDetailType {
  bool get isProduct {
    return this == MTradeProductDetailType.product;
  }

  bool get isSuggestedProductFromDetail {
    return this == MTradeProductDetailType.suggestedProductFromDetail;
  }

  bool get isSuggestedProductFromPayNowOrder {
    return this == MTradeProductDetailType.suggestedProductFromPayNowOrder;
  }
}
