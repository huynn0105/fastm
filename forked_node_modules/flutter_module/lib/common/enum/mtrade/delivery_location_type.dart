enum LocationType {
  province("Tỉnh/Thành phố", "CITY", "Tất cả tỉnh/thành phố"),
  district("Quận/Huyện", "DISTRICT", "Tất cả quận/huyện"),
  ward("Phường/Xã", "WARD", "Tất cả phường/xã");

  final String title;
  final String mplType;
  final String all;

  const LocationType(this.title, this.mplType, this.all);
}

extension ExDeliveryLocationType on LocationType {
  bool get isProvince => this == LocationType.province;

  bool get isDistrict => this == LocationType.district;

  bool get isWard => this == LocationType.ward;
}
