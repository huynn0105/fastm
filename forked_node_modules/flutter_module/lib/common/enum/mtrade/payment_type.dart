enum MTradePaymentType {
  now('PREPAYMENT'),
  later('INSTALLMENT'),
  onDelivery('PAYMENT_ON_DELIVERY');

  final String code;

  const MTradePaymentType(this.code);
}

extension ExMTradePaymentType on MTradePaymentType {
  bool get isNow => this == MTradePaymentType.now;

  bool get isLater => this == MTradePaymentType.later;
}
