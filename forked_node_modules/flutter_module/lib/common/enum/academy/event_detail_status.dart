enum EventDetailStatus {
  notRegister,
  register,
  checkIn,
  checkOut,
}

extension ExEventDetailStatus on EventDetailStatus? {
  bool get isNotRegister => this == EventDetailStatus.notRegister;

  bool get isRegister => this == EventDetailStatus.register;

  bool get isCheckIn => this == EventDetailStatus.checkIn;

  bool get isCheckOut => this == EventDetailStatus.checkOut;

  bool get showActionEvent => isRegister || isCheckIn || isCheckOut;
}
