import 'dart:ui';

enum AcademyBackgroundGradient {
  blue,
  purple,
  green,
  orange,
}

extension AcademyBackgroundGradientExtension on AcademyBackgroundGradient {
  List<Color> get value {
    switch (this) {
      case AcademyBackgroundGradient.blue:
        return [
          const Color(0xff1f72ff),
          const Color(0xff2822c8),
        ];
      case AcademyBackgroundGradient.purple:
        return [
          const Color(0xff924abf),
          const Color(0xff562277),
        ];
      case AcademyBackgroundGradient.green:
        return [
          const Color(0xff14b88c),
          const Color(0xff0d775b),
        ];
      case AcademyBackgroundGradient.orange:
        return [
          const Color(0xffeb810a),
          const Color(0xff9a5609),
        ];
      default:
        return [
          const Color(0xff1f72ff),
          const Color(0xff2822c8),
        ];
    }
  }
}
