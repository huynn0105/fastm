enum ExportStatementType {
  year(
    '1',
    '1 năm gần nhất',
  ),
  twoYear(
    '2',
    '2 năm gần nhất',
  ),
  threeYear(
    '3',
    '3 năm gần nhất',
  ),
  custom(
    'about',
    'Tự chọn mốc thời gian',
  );

  const ExportStatementType(
    this.id,
    this.title,
  );
  final String id;
  final String title;
}

extension ExExportStatementType on ExportStatementType {
  bool get isYear => this == ExportStatementType.year;

  bool get isTwoYear => this == ExportStatementType.twoYear;

  bool get isThreeYear => this == ExportStatementType.threeYear;
  
  bool get isCustom => this == ExportStatementType.custom;
}
