enum StatisticIncomeType {
  availableBalance,
  taxHolding,
  withdraw,
  payment,
  collection,
}

extension ExStatisticIncomeType on StatisticIncomeType {
  String get id {
    switch (this) {
      case StatisticIncomeType.availableBalance:
        return 'available_balance';
      case StatisticIncomeType.taxHolding:
        return 'tax_holding';
      case StatisticIncomeType.withdraw:
        return 'withdraw';
      case StatisticIncomeType.payment:
        return 'payment';
      case StatisticIncomeType.collection:
        return 'collection';
    }
  }

  String get color {
    switch (this) {
      case StatisticIncomeType.availableBalance:
        return '#005fff';
      case StatisticIncomeType.taxHolding:
        return '#dc41d7';
      case StatisticIncomeType.withdraw:
        return '#ff499c';
      case StatisticIncomeType.payment:
        return '#ff8469';
      case StatisticIncomeType.collection:
        return '#ffc252';
    }
  }

  String get backgroundColor {
    return color; // Assuming background is the same as color in the provided data
  }

  String get name {
    switch (this) {
      case StatisticIncomeType.availableBalance:
        return 'Số dư khả dụng >>';
      case StatisticIncomeType.taxHolding:
        return 'Thuế TNCN tạm tính >>';
      case StatisticIncomeType.withdraw:
        return 'Tiền đã rút >>';
      case StatisticIncomeType.payment:
        return 'Mua sắm >>';
      case StatisticIncomeType.collection:
        return 'Thu hồi >>';
    }
  }

  String get key {
    switch (this) {
      case StatisticIncomeType.availableBalance:
        return 'all';
      case StatisticIncomeType.taxHolding:
        return 'tax_deduct';
      case StatisticIncomeType.withdraw:
        return 'bank_withdrawal';
      case StatisticIncomeType.payment:
        return 'payment';
      case StatisticIncomeType.collection:
        return 'collection';
    }
  }
}
