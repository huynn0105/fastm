enum LivenessSupportStatus {
  pending('PENDING'),
  success('SUCCESS'),
  error('ERROR'),
  failure('FAILURE');

  final String name;

  const LivenessSupportStatus(this.name);
}

extension LivenessSupportStatusExtension on LivenessSupportStatus? {
  bool get isPending => this == LivenessSupportStatus.pending;
  bool get isSuccess => this == LivenessSupportStatus.success;
  bool get isError => this == LivenessSupportStatus.error;
  bool get isFailure => this == LivenessSupportStatus.failure;
}
