enum TimeCheckingStatus {
  fail('Thất bại'),
  pending('Chờ điểm danh'),
  success('Thành công');

  final String value;
  const TimeCheckingStatus(this.value);

  factory TimeCheckingStatus.fromValue(jsonValue) => TimeCheckingStatus.values.singleWhere((i) => jsonValue == i.name);
}

extension ExTimeCheckingStatus on TimeCheckingStatus {
  bool get isFail => this == TimeCheckingStatus.fail;

  bool get isPending => this == TimeCheckingStatus.pending;
  
  bool get isSuccss => this == TimeCheckingStatus.success;
}
