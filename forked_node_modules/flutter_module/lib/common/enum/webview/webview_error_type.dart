enum WebViewErrorType {
  noInternet(
    iosCode: '-1009',
    androidCode: 'ERR_INTERNET_DISCONNECTED',
  ),
  notFound(
    iosCode: '404',
    androidCode: '404',
  );

  final String iosCode;
  final String androidCode;

  const WebViewErrorType({
    required this.iosCode,
    required this.androidCode,
  });
}

extension ExWebViewErrorType on WebViewErrorType {
  bool get isNoInternet => this == WebViewErrorType.noInternet;
  
  bool get isNotFound => this == WebViewErrorType.notFound;
}
