import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/services/local/local_data_helper.dart';

enum ThreadActionType {
  none,
  icon,
  input,
  media,
  audio,
}

extension ExChatType on ThreadActionType {
  bool get isNone => this == ThreadActionType.none;

  bool get isIcon => this == ThreadActionType.icon;

  bool get isInput => this == ThreadActionType.input;

  bool get isMedia => this == ThreadActionType.media;

  bool get isAudio => this == ThreadActionType.audio;

  double get height {
    double kHeight = LocalDataHelper.instance.getKeyboardHeight() ?? AppConstants.keyboardHeight;
    switch (this) {
      case ThreadActionType.none:
        return 0;
      case ThreadActionType.audio:
        return 110;
      case ThreadActionType.input:
      case ThreadActionType.icon:
      case ThreadActionType.media:
      default:
        return kHeight;
    }
  }
}
