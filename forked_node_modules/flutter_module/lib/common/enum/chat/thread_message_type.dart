enum ThreadMessageType {
  text,
  images,
  videos,
  audios,
  sticker,
  location,
  notice,
  mention,
}

extension ExThreadMessageType on ThreadMessageType {
  bool get isText => this == ThreadMessageType.text;

  bool get isImages => this == ThreadMessageType.images;

  bool get isVideos => this == ThreadMessageType.videos;

  bool get isLocation => this == ThreadMessageType.location;

  bool get isAudios => this == ThreadMessageType.audios;

  bool get isSticker => this == ThreadMessageType.sticker;

  bool get isNotice => this == ThreadMessageType.notice;

  bool get isMention => this == ThreadMessageType.mention;

  bool get isMedia => isImages || isVideos;
}
