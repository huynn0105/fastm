enum MediaType {
  /// The asset is not an image, video, or audio file.
  other,

  /// The asset is an image file.
  image,

  /// The asset is a video file.
  video,

  /// The asset is an audio file.
  audio,
}

extension ExMediaType on MediaType? {
  bool get isOther => this == MediaType.other;
  
  bool get isImage => this == MediaType.image;

  bool get isVideo => this == MediaType.video;
  
  bool get isAudio => this == MediaType.audio;
}
