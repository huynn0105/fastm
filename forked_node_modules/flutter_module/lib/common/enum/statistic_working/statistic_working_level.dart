import 'package:collection/collection.dart';

enum StatisticWorkingLevel {
  rsm('RSM'),
  asm('ASM'),
  sup('SUP'),
  agent('Agent');

  const StatisticWorkingLevel(this.code);

  final String code;

  static String getUnderLevel(String? level) {
    if (level == StatisticWorkingLevel.rsm.code) {
      return StatisticWorkingLevel.asm.code;
    }
    if (level == StatisticWorkingLevel.asm.code) {
      return StatisticWorkingLevel.sup.code;
    }
    return StatisticWorkingLevel.agent.code;
  }

  static StatisticWorkingLevel? getLevel(String? level) {
    return StatisticWorkingLevel.values.firstWhereOrNull((e) => e.code == level);
  }
}

extension ExStatisticWorkingLevel on StatisticWorkingLevel? {
  bool get isRSM => this == StatisticWorkingLevel.rsm;

  bool get isASM => this == StatisticWorkingLevel.asm;

  bool get isSUP => this == StatisticWorkingLevel.sup;

  bool get isAgent => this == StatisticWorkingLevel.agent;
}
