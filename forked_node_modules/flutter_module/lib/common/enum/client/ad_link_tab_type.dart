// ignore_for_file: constant_identifier_names

enum AdLinkTabType {
  total("total", "Tất cả"),
  finance("finance", "Tài chính"),
  insurance("insurrance", "Bảo hiểm");

  const AdLinkTabType(this.name, this.title);

  final String name;
  final String title;
}

extension AdLinkTabTypeEx on AdLinkTabType {
  get isFinance => this == AdLinkTabType.finance;
  get isInsurance => this == AdLinkTabType.insurance;
}
