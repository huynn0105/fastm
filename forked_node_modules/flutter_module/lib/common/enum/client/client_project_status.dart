import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';

enum ClientProjectStatus {
  waiting(
    code: '0',
    asset: 'ic_status_waiting',
    color: UIColors.secondaryColor,
  ),
  failure(
    code: '1',
    asset: 'ic_status_success',
    color: UIColors.green,
  ),
  success(
    code: '2',
    asset: 'ic_status_failure',
    color: UIColors.red,
  );

  const ClientProjectStatus({
    required this.code,
    required this.asset,
    required this.color,
  });

  final String code;
  final String asset;
  final Color color;
}
