import 'package:flutter/cupertino.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

class CupertinoDatePickerSheetView extends StatefulWidget {
  const CupertinoDatePickerSheetView({
    super.key,
    required this.title,
    this.initDate,
  });

  final String title;
  final DateTime? initDate;

  @override
  State<CupertinoDatePickerSheetView> createState() => _CupertinoDatePickerSheetViewState();
}

class _CupertinoDatePickerSheetViewState extends State<CupertinoDatePickerSheetView> {
  DateTime? focusDate;
  DateTime? initDate;

  @override
  void initState() {
    super.initState();
    focusDate = initDate = widget.initDate ?? DateTime.now();
  }

  @override
  Widget build(BuildContext context) {
    return Container(
      height: 300,
      width: double.infinity,
      clipBehavior: Clip.hardEdge,
      padding: EdgeInsets.only(
        bottom: MediaQuery.of(context).viewPadding.bottom,
      ),
      decoration: const BoxDecoration(
        color: UIColors.background,
        borderRadius: BorderRadius.only(
          topLeft: Radius.circular(12),
          topRight: Radius.circular(12),
        ),
      ),
      child: Column(
        children: [
          Container(
            height: 40,
            decoration: const BoxDecoration(
              color: UIColors.white,
              borderRadius: BorderRadius.only(
                topLeft: Radius.circular(12),
                topRight: Radius.circular(12),
              ),
            ),
            padding: const EdgeInsets.symmetric(horizontal: 16),
            child: Row(
              children: [
                SplashButton(
                  onTap: () {
                    Navigator.of(context).pop();
                  },
                  child: const SizedBox(
                    width: 48,
                    height: 48,
                    child: Align(
                      alignment: Alignment.centerLeft,
                      child: AppImage.asset(
                        asset: "ic_close",
                        width: 24,
                        height: 24,
                      ),
                    ),
                  ),
                ),
                Expanded(
                  child: Center(
                    child: Text(
                      widget.title,
                      style: UITextStyle.medium.copyWith(
                        fontSize: 16,
                        color: UIColors.grayText,
                      ),
                      maxLines: 1,
                      overflow: TextOverflow.ellipsis,
                    ),
                  ),
                ),
                SplashButton(
                  onTap: () {
                    Navigator.of(context).pop(focusDate);
                  },
                  child: SizedBox(
                    width: 48,
                    height: 48,
                    child: Align(
                      alignment: Alignment.centerRight,
                      child: Text(
                        "Ch·ªçn",
                        style: UITextStyle.bold.copyWith(
                          fontSize: 14,
                          color: UIColors.primaryColor,
                        ),
                      ),
                    ),
                  ),
                )
              ],
            ),
          ),
          Expanded(
            child: CupertinoTheme(
              data: CupertinoThemeData(
                textTheme: CupertinoTextThemeData(
                  dateTimePickerTextStyle: UITextStyle.regular.copyWith(
                    fontSize: 18,
                    color: UIColors.defaultText,
                  ),
                ),
              ),
              child: CupertinoDatePicker(
                initialDateTime: initDate,
                minimumYear: 1900,
                maximumYear: DateTime.now().year,
                mode: CupertinoDatePickerMode.date,
                dateOrder: DatePickerDateOrder.dmy,
                use24hFormat: true,
                showDayOfWeek: false,
                onDateTimeChanged: (DateTime newDate) {
                  focusDate = newDate;
                },
              ),
            ),
          ),
        ],
      ),
    );
  }
}
