import 'package:carousel_slider/carousel_slider.dart';
import 'package:flutter/gestures.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/common/global_function.dart';
import 'package:flutter_module/common/size.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/features/mtrade/cubit/link_brochure/mtrade_link_brochure_cubit.dart';
import 'package:pretty_qr_code/pretty_qr_code.dart';
import 'package:url_launcher/url_launcher_string.dart';

final List<String> imgListBrochureExample = [
  'ex_pl_1',
  'ex_pl_2',
  'ex_pl_3',
  'ex_pl_4',
  'ex_pl_5',
];

class BrochureView extends StatefulWidget {
  const BrochureView({
    super.key,
    this.onSend,
  });

  final Function()? onSend;

  @override
  State<BrochureView> createState() => _BrochureViewState();
}

class _BrochureViewState extends State<BrochureView> {
  final CarouselController _controller = CarouselController();
  int _current = 0;

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<MTradeLinkBrochureCubit, MTradeLinkBrochureState>(
      builder: (context, state) {
        return SizedBox(
          height: AppSize.instance.height * 0.8,
          child: ListView(
            children: [
              Container(
                padding: const EdgeInsets.all(12),
                color: UIColors.white,
                child: Row(
                  children: [
                    Expanded(
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Text(
                            'Gửi tất cả về mail',
                            style: UITextStyle.regular.copyWith(
                              fontSize: 13,
                              color: UIColors.grayText,
                            ),
                          ),
                          Text(
                            state.brochure?.email ?? '',
                            style: UITextStyle.semiBold,
                            maxLines: 1,
                            overflow: TextOverflow.ellipsis,
                          ),
                        ],
                      ),
                    ),
                    PrimaryButton(
                      enabled: state.sendStatus.isInitial,
                      width: 90,
                      height: 40,
                      title: 'Gửi ngay',
                      onPressed: () {
                        widget.onSend?.call();
                      },
                      padding: EdgeInsets.zero,
                      widget: Text(
                        state.sendStatus.isLoading
                            ? 'Đang gửi...'
                            : state.sendStatus.isSuccess
                                ? 'Đã gửi'
                                : 'Gửi ngay',
                        style: UITextStyle.medium.copyWith(
                          color: UIColors.white,
                        ),
                      ),
                    )
                  ],
                ),
              ),
              Padding(
                padding: const EdgeInsets.symmetric(horizontal: 16),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    const SizedBox(
                      height: 20,
                    ),
                    Text(
                      'Bộ nhận diện cho liên kết',
                      style: UITextStyle.regular.copyWith(
                        color: UIColors.grayText,
                      ),
                    ),
                    const SizedBox(
                      height: 16,
                    ),
                    SizedBox(
                      height: 175,
                      child: ListView.separated(
                        scrollDirection: Axis.horizontal,
                        itemBuilder: (context, index) {
                          return Column(
                            children: [
                              if (index == 0) ...{
                                Container(
                                  clipBehavior: Clip.hardEdge,
                                  width: 197,
                                  height: 140,
                                  decoration: BoxDecoration(
                                    borderRadius: BorderRadius.circular(12),
                                    color: UIColors.white,
                                  ),
                                  child: Center(
                                    child: PrettyQr(
                                      data: state.brochure?.link ?? '',
                                      roundEdges: true,
                                    ),
                                  ),
                                ),
                              } else ...{
                                Container(
                                  clipBehavior: Clip.hardEdge,
                                  width: 197,
                                  height: 140,
                                  decoration: BoxDecoration(
                                    borderRadius: BorderRadius.circular(12),
                                    color: UIColors.white,
                                  ),
                                  child: AppImage.network(
                                    url: state.brochure?.poster![index - 1],
                                    fit: BoxFit.contain,
                                  ),
                                ),
                              },
                              const SizedBox(
                                height: 12,
                              ),
                              Text(
                                index == 0 ? 'QRCode dẫn tới trang liên kết' : 'Tờ rời mẫu $index',
                                style: UITextStyle.semiBold.copyWith(
                                  fontSize: 13,
                                ),
                              )
                            ],
                          );
                        },
                        separatorBuilder: (BuildContext context, int index) {
                          return const SizedBox(
                            width: 16,
                          );
                        },
                        itemCount: (state.brochure?.poster?.length ?? 0) + 1,
                      ),
                    ),
                    const SizedBox(
                      height: 24,
                    ),
                    Text(
                      'Sử dụng bộ hình ảnh giúp bạn',
                      style: UITextStyle.regular.copyWith(
                        color: UIColors.grayText,
                      ),
                    ),
                    const CheckText(
                      text: 'Tiếp cận với nhiều khách hàng hơn',
                    ),
                    const SizedBox(
                      height: 12,
                    ),
                    const CheckText(
                      text: 'Đa dạng cách sử dụng: dán lên tường, tủ, mặt kính hoặc đặt lên bàn, phát tờ rơi',
                    ),
                    const SizedBox(
                      height: 12,
                    ),
                    const CheckText(
                      text: 'Kích cỡ chuẩn A3,4,5 dễ dàng in ấn với chi phí rẻ.',
                    ),
                    const SizedBox(
                      height: 12,
                    ),
                    RichText(
                      text: TextSpan(
                        style: UITextStyle.regular.copyWith(
                          color: UIColors.grayText,
                        ),
                        children: [
                          const TextSpan(text: 'Xem hướng dẫn cách sử dụng '),
                          TextSpan(
                            text: 'Tại đây >>',
                            style: UITextStyle.semiBold.copyWith(
                              color: UIColors.primaryColor,
                            ),
                            recognizer: TapGestureRecognizer()
                              ..onTap = () {
                                GlobalFunction.launchScheme(AppConstants.guideUseAdImage);
                              },
                          ),
                        ],
                      ),
                    ),
                  ],
                ),
              ),
              const SizedBox(
                height: 16,
              ),
              CarouselSlider(
                items: imgListBrochureExample.map((e) => ItemSlider(asset: e)).toList(),
                carouselController: _controller,
                options: CarouselOptions(
                    // height: 214,
                    autoPlay: true,
                    aspectRatio: 343 / 214,
                    viewportFraction: 1,
                    autoPlayInterval: const Duration(seconds: 5),
                    onPageChanged: (index, reason) {
                      setState(() {
                        _current = index;
                      });
                    }),
              ),
              const SizedBox(
                height: 16,
              ),
              Row(
                mainAxisAlignment: MainAxisAlignment.center,
                children: imgListBrochureExample.asMap().entries.map((entry) {
                  return GestureDetector(
                    onTap: () => _controller.animateToPage(entry.key),
                    child: Container(
                      width: 6,
                      height: 6,
                      margin: const EdgeInsets.symmetric(horizontal: 4.0),
                      decoration: BoxDecoration(
                        shape: BoxShape.circle,
                        color: (Theme.of(context).brightness == Brightness.dark ? UIColors.border : UIColors.boldText)
                            .withOpacity(_current == entry.key ? 0.9 : 0.4),
                      ),
                    ),
                  );
                }).toList(),
              ),
            ],
          ),
        );
      },
    );
  }
}

class ItemSlider extends StatelessWidget {
  const ItemSlider({
    super.key,
    this.asset,
  });

  final String? asset;

  @override
  Widget build(BuildContext context) {
    return Container(
      width: double.infinity,
      margin: const EdgeInsets.symmetric(horizontal: 16),
      child: ClipRRect(
        borderRadius: BorderRadius.circular(16),
        child: AppImage.asset(asset: asset),
      ),
    );
  }
}

class CheckText extends StatelessWidget {
  const CheckText({
    super.key,
    this.text,
  });

  final String? text;

  @override
  Widget build(BuildContext context) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        const Padding(
          padding: EdgeInsets.only(top: 4),
          child: AppImage.asset(
            asset: 'ic_success',
            width: 16,
            height: 16,
          ),
        ),
        const SizedBox(
          width: 12,
        ),
        Expanded(
          child: Text(
            text ?? '',
            style: UITextStyle.semiBold,
          ),
        )
      ],
    );
  }
}
