import 'dart:convert';

import 'package:flutter_module/common/enum/log_event_key.dart';
import 'package:flutter_module/common/utils/event_util.dart';

class LogEventUtil {
  LogEventUtil._();

  static send(LogEventKey eventName, {Map<String, dynamic>? data}) {
    final payload = {
      "eventName": eventName.value,
      "data": data,
    };
    EventUtil.sendEventToRN('LOG_EVENT', data: jsonEncode(payload));
  }
}
