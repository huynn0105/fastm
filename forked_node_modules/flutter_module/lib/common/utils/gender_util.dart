import 'package:flutter_module/common/enum/mfast/gender_type.dart';

class GenderUtil {
  GenderUtil._();

  static final GenderUtil instance = GenderUtil._();

  String getGender(String? value) {
    String gender = GenderType.other.name;

    switch (value) {
      case 'Male':
      case 'male':
      case 'Nam':
      case 'nam':
        gender = GenderType.male.id;
        break;
      case 'Female':
      case 'female':
      case 'Nữ':
      case 'nữ':
        gender = GenderType.female.id;
        break;
    }

    return gender;
  }

  String getNameGender(String value) {
    String gender = '';

    switch (value) {
      case 'Male':
      case 'male':
      case 'Nam':
      case 'nam':
        gender = GenderType.male.name;
        break;
      case 'Female':
      case 'female':
      case 'Nữ':
      case 'nữ':
        gender = GenderType.female.name;
        break;
    }

    return gender;
  }
}
