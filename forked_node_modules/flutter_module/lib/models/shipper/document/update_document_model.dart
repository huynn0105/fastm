class UpdateDocumentModel {
  UpdateDocumentModel({
    this.id,
    this.mediaUrl,
  });

  UpdateDocumentModel.fromJson(dynamic json) {
    id = json['id'];
    mediaUrl = json['media_url'];
  }

  String? id;
  String? mediaUrl;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['id'] = id;
    map['media_url'] = mediaUrl;
    return map;
  }
}
