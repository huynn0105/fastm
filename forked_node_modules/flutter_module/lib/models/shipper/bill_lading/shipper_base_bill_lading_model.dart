import 'shipper_bill_lading_model.dart';

class ShipperBaseBillLadingModel {
  ShipperBaseBillLadingModel({
    this.orderList,
    this.count,
  });

  List<ShipperBillLadingModel>? orderList;
  int? count;

  ShipperBaseBillLadingModel.fromJson(dynamic json) {
    count = json["count"]?["aggregate"]?["count"];
    if (json['order_list'] != null) {
      orderList = [];
      json['order_list'].forEach((v) {
        orderList?.add(ShipperBillLadingModel.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map["count"] = count;
    if (orderList != null) {
      map['order_list'] = orderList?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}
