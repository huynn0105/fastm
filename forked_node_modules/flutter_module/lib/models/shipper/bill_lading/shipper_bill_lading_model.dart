class ShipperBillLadingModel {
  ShipperBillLadingModel({
    this.id,
    this.orderCode,
    this.orderDeliveryCode,
    this.assignShipper,
    this.updated,
    this.province,
    this.ward,
    this.district,
  });

  ShipperBillLadingModel.fromJson(dynamic json) {
    id = json['id'];
    orderCode = json['code'];
    orderDeliveryCode = json['order_delivery_code'];
    assignShipper = json['assign_shipper'];
    updated = json['updated'];
    province = json['province'] != null ? GeneralDataModel.fromJson(json['province']) : null;
    ward = json['ward'] != null ? GeneralDataModel.fromJson(json['ward']) : null;
    district = json['district'] != null ? GeneralDataModel.fromJson(json['district']) : null;
    customer = json['oms_customer'] != null ? OmsCustomer.fromJson(json['oms_customer']) : null;
    masterDeliveryStatus =
        json['master_delivery_status'] != null ? GeneralObject.fromJson(json['master_delivery_status']) : null;
  }

  String? id;
  String? orderCode;
  String? orderDeliveryCode;
  String? assignShipper;
  String? updated;
  GeneralDataModel? province;
  GeneralDataModel? ward;
  GeneralDataModel? district;
  OmsCustomer? customer;
  GeneralObject? masterDeliveryStatus;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['id'] = id;
    map['code'] = orderCode;
    map['order_delivery_code'] = orderDeliveryCode;
    map['assign_shipper'] = assignShipper;
    map['updated'] = updated;
    if (province != null) {
      map['province'] = province?.toJson();
    }
    if (ward != null) {
      map['ward'] = ward?.toJson();
    }
    if (district != null) {
      map['district'] = district?.toJson();
    }
    if (customer != null) {
      map['oms_customer'] = customer?.toJson();
    }
    if (district != null) {
      map['master_delivery_status'] = masterDeliveryStatus?.toJson();
    }
    return map;
  }
}
