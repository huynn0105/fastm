class PaymentMethodModel {
  PaymentMethodModel({
    this.alias,
    this.title,
  });

  PaymentMethodModel.fromJson(dynamic json) {
    alias = json['alias'];
    title = json['title'];
  }

  String? alias;
  String? title;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['alias'] = alias;
    map['title'] = title;
    return map;
  }
}
