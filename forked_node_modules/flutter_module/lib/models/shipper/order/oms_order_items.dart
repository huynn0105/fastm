import 'oms_product_variation.dart';

class OmsOrderItems {
  OmsOrderItems({
    this.price,
    this.discount,
    this.quantity,
    this.omsProductVariation,
  });

  OmsOrderItems.fromJson(dynamic json) {
    price = json['price'];
    discount = json['discount'];
    quantity = json['quantity'];
    omsProductVariation =
        json['oms_product_variation'] != null ? OmsProductVariation.fromJson(json['oms_product_variation']) : null;
  }

  int? price;
  int? discount;
  int? quantity;
  OmsProductVariation? omsProductVariation;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['price'] = price;
    map['discount'] = discount;
    map['quantity'] = quantity;
    if (omsProductVariation != null) {
      map['oms_product_variation'] = omsProductVariation?.toJson();
    }
    return map;
  }
}
