import 'package:flutter_module/models/general_object.dart';
import 'package:flutter_module/models/genernal_message.dart';

class OmsOrderHistory {
  OmsOrderHistory({
    this.id,
    this.orderDeliveryCode,
    this.masterOrderReasonByReasonDelivery,
    this.masterDeliveryStatus,
    this.address,
    this.code,
    this.deliveryStatus,
    this.note,
    this.operationDate,
    this.assignShipper,
    this.deleted,
    this.created,
    this.updated,
  });

  OmsOrderHistory.fromJson(dynamic json) {
    id = json['id'];
    orderDeliveryCode = json['order_delivery_code'];
    masterOrderReasonByReasonDelivery = json['masterOrderReasonByReasonDelivery'] != null ? GeneralMessage.fromJson(json['masterOrderReasonByReasonDelivery']) : null;
    masterDeliveryStatus = json['master_delivery_status'] != null ? GeneralObject.fromJson(json['master_delivery_status']) : null;
    address = json['address'];
    code = json['code'];
    deliveryStatus = json['delivery_status'];
    note = json['note'];
    operationDate = json['operation_date'];
    assignShipper = json['assign_shipper'];
    deleted = json['deleted'];
    created = json['created'];
    updated = json['updated'];
  }

  String? id;
  String? orderDeliveryCode;
  GeneralMessage? masterOrderReasonByReasonDelivery;
  GeneralObject? masterDeliveryStatus;
  dynamic address;
  String? code;
  int? deliveryStatus;
  String? note;
  String? operationDate;
  dynamic assignShipper;
  bool? deleted;
  String? created;
  String? updated;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['id'] = id;
    map['order_delivery_code'] = orderDeliveryCode;
    map['masterOrderReasonByReasonDelivery'] = masterOrderReasonByReasonDelivery?.toJson();
    map['master_delivery_status'] = masterDeliveryStatus?.toJson();
    map['address'] = address;
    map['code'] = code;
    map['delivery_status'] = deliveryStatus;
    map['note'] = note;
    map['operation_date'] = operationDate;
    map['assign_shipper'] = assignShipper;
    map['deleted'] = deleted;
    map['created'] = created;
    map['updated'] = updated;
    return map;
  }
}
