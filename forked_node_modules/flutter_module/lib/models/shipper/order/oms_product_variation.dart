import '../../general_object.dart';
import 'oms_product.dart';

class OmsProductVariation {
  OmsProductVariation({
    this.variationValue,
    this.sku,
    this.omsProductVariationMedia,
    this.masterVariationType,
    this.omsProduct,
  });

  OmsProductVariation.fromJson(dynamic json) {
    variationValue = json['variation_value'];
    sku = json['sku'];
    if (json['oms_product_variation_media'] != null) {
      omsProductVariationMedia = [];
      json['oms_product_variation_media'].forEach((v) {
        omsProductVariationMedia?.add(v);
      });
    }
    masterVariationType =
        json['master_variation_type'] != null ? GeneralObject.fromJson(json['master_variation_type']) : null;
    omsProduct = json['oms_product'] != null ? OmsProduct.fromJson(json['oms_product']) : null;
  }

  String? variationValue;
  String? sku;
  List<dynamic>? omsProductVariationMedia;
  GeneralObject? masterVariationType;
  OmsProduct? omsProduct;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['variation_value'] = variationValue;
    map['sku'] = sku;
    if (omsProductVariationMedia != null) {
      map['oms_product_variation_media'] = omsProductVariationMedia?.map((v) => v.toJson()).toList();
    }
    if (masterVariationType != null) {
      map['master_variation_type'] = masterVariationType?.toJson();
    }
    if (omsProduct != null) {
      map['oms_product'] = omsProduct?.toJson();
    }
    return map;
  }
}
