import 'package:flutter_module/models/genernal_message.dart';
import 'package:flutter_module/models/shipper/order/sale_info_model.dart';

import '../../general_object.dart';
import 'order_detail.dart';
import 'process.dart';

class DeliveryModel {
  DeliveryModel({
    this.orderDetail,
    this.mediaTypes,
    this.reasonDelay,
    this.reasonFailure,
    this.process,
    this.saleInfo,
  });

  DeliveryModel.fromJson(dynamic json) {
    orderDetail = json['orderDetail'] != null ? OrderDetail.fromJson(json['orderDetail']) : null;
    if (json['mediaTypes'] != null) {
      mediaTypes = [];
      json['mediaTypes'].forEach((v) {
        mediaTypes?.add(GeneralObject.fromJson(v));
      });
    }
    if (json['reason_delay'] != null) {
      reasonDelay = [];
      json['reason_delay'].forEach((v) {
        reasonDelay?.add(GeneralMessage.fromJson(v));
      });
    }
    if (json['reason_failure'] != null) {
      reasonFailure = [];
      json['reason_failure'].forEach((v) {
        reasonFailure?.add(GeneralMessage.fromJson(v));
      });
    }
    if (json['process'] != null) {
      process = [];
      json['process'].forEach((v) {
        process?.add(Process.fromJson(v));
      });
    }
    saleInfo = json['sale_info'] != null ? SaleInfoModel.fromJson(json['sale_info']) : null;
  }

  OrderDetail? orderDetail;
  SaleInfoModel? saleInfo;
  List<GeneralObject>? mediaTypes;
  List<GeneralMessage>? reasonDelay;
  List<GeneralMessage>? reasonFailure;
  List<Process>? process;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (orderDetail != null) {
      map['orderDetail'] = orderDetail?.toJson();
    }
    if (mediaTypes != null) {
      map['mediaTypes'] = mediaTypes?.map((v) => v.toJson()).toList();
    }
    if (reasonDelay != null) {
      map['reason_delay'] = reasonDelay?.map((v) => v.toJson()).toList();
    }
    if (reasonFailure != null) {
      map['reason_failure'] = reasonFailure?.map((v) => v.toJson()).toList();
    }
    if (process != null) {
      map['process'] = process?.map((v) => v.toJson()).toList();
    }
    if (saleInfo != null) {
      map['sale_info'] = saleInfo?.toJson();
    }
    return map;
  }
}
