import 'package:flutter_module/models/general_object.dart';

class OmsOrderDocuments {
  OmsOrderDocuments({
    this.latitude,
    this.longitude,
    this.created,
    this.mediaUrl,
    this.id,
    this.masterDocumentType,
  });

  OmsOrderDocuments.fromJson(dynamic json) {
    latitude = json['latitude'];
    longitude = json['longitude'];
    created = json['created'];
    mediaUrl = json['media_url'];
    id = json['id'];
    masterDocumentType =
        json['master_document_type'] != null ? GeneralObject.fromJson(json['master_document_type']) : null;
  }

  String? latitude;
  String? longitude;
  String? created;
  String? mediaUrl;
  String? id;
  GeneralObject? masterDocumentType;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['latitude'] = latitude;
    map['longitude'] = longitude;
    map['created'] = created;
    map['media_url'] = mediaUrl;
    map['id'] = id;
    if (masterDocumentType != null) {
      map['master_document_type'] = masterDocumentType?.toJson();
    }
    return map;
  }
}
