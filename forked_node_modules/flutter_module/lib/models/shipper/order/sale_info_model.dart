/// full_name_sale : "NGUYỄN MINH HIẾU"
/// mobile_phone_sale : "0938058692"

class SaleInfoModel {
  SaleInfoModel({
    this.fullName,
    this.mobilePhone,
  });

  SaleInfoModel.fromJson(dynamic json) {
    fullName = json['full_name_sale'];
    mobilePhone = json['mobile_phone_sale'];
  }

  String? fullName;
  String? mobilePhone;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['full_name_sale'] = fullName;
    map['mobile_phone_sale'] = mobilePhone;
    return map;
  }
}
