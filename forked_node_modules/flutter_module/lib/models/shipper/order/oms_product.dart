import '../../general_object.dart';

class OmsProduct {
  OmsProduct({
    this.code,
    this.name,
    this.price,
    this.riskLevel,
    this.masterProductType,
    this.omsMerchant,
  });

  OmsProduct.fromJson(dynamic json) {
    code = json['code'];
    name = json['name'];
    price = json['price'];
    riskLevel = json['master_risk_level_type'] != null ? GeneralObject.fromJson(json['master_risk_level_type']) : null;
    masterProductType = json['master_product_type'];
    omsMerchant = json['oms_merchant'];
  }

  String? code;
  String? name;
  GeneralObject? riskLevel;
  dynamic price;
  dynamic masterProductType;
  dynamic omsMerchant;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['code'] = code;
    map['name'] = name;
    map['price'] = price;
    map['risk_level'] = riskLevel?.toJson();
    map['master_product_type'] = masterProductType;
    map['oms_merchant'] = omsMerchant;
    return map;
  }
}
