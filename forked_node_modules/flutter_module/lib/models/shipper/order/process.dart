class Process {
  Process({
    this.processName,
    this.extraType,
  });

  Process.fromJson(dynamic json) {
    processName = json['processName'];
    extraType = json['extraType'];
  }

  String? processName;
  String? extraType;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['processName'] = processName;
    map['extraType'] = extraType;
    return map;
  }
}
