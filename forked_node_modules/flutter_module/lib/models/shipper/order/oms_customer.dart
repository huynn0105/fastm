class OmsCustomer {
  OmsCustomer({
    this.name,
    this.phone,
    this.addressDelivery,
    this.distance,
    this.longitude,
    this.latitude,
    this.estimatedTime,
  });

  OmsCustomer.fromJson(dynamic json) {
    name = json['name'];
    phone = json['phone'];
    addressDelivery = json['address_delivery'];
    distance = json['distance']?.toDouble();
    longitude = json['longitude'];
    latitude = json['latitude'];
    estimatedTime = json['estimated_time'];
  }

  String? name;
  String? phone;
  String? addressDelivery;
  double? distance;
  String? longitude;
  String? latitude;
  int? estimatedTime; // minutes

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['name'] = name;
    map['phone'] = phone;
    map['address_delivery'] = addressDelivery;
    map['distance'] = distance;
    map['longitude'] = longitude;
    map['latitude'] = latitude;
    map['estimated_time'] = estimatedTime;
    return map;
  }
}
