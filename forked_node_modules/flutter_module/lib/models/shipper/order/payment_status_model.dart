class PaymentStatusModel {
  PaymentStatusModel({
    this.status,
    this.recheckTime,
  });

  PaymentStatusModel.fromJson(dynamic json) {
    status = json['status'];
    recheckTime = json['recheckTime'];
  }

  bool? status;
  int? recheckTime;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['status'] = status;
    map['recheckTime'] = recheckTime;
    return map;
  }
}
