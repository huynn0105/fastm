class ClientDocumentModel {
  ClientDocumentModel({
    this.date,
    this.image,
  });

  ClientDocumentModel.fromJson(dynamic json) {
    date = json['date'];
    image = json['image'] != null ? json['image'].cast<String>() : [];
  }

  String? date;
  List<String>? image;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['date'] = date;
    map['image'] = image;
    return map;
  }
}
