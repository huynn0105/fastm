class ClientDistrictModel {
  ClientDistrictModel({
    this.id,
    this.value,
  });

  ClientDistrictModel.fromJson(dynamic json) {
    id = json['id'];
    value = json['value'];
  }

  String? id;
  String? value;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['id'] = id;
    map['value'] = value;
    return map;
  }
}
