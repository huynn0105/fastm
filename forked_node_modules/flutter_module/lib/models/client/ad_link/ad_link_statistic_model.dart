import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/models/client_data/filter_client_model.dart';

class AdLinkStatisticModel {
  int? total;
  List<AdLinkStatisticData>? data;
  List<FilterProjects>? links;
  List<UserGuide>? userGuide;

  AdLinkStatisticModel({this.total, this.data, this.links, this.userGuide});

  AdLinkStatisticModel.fromJson(Map<String, dynamic> json) {
    total = json['total'];
    if (json['data'] != null) {
      data = <AdLinkStatisticData>[];
      json['data'].forEach((v) {
        data!.add(AdLinkStatisticData.fromJson(v));
      });
    }
    if (json['links'] != null) {
      links = <FilterProjects>[];
      json['links'].forEach((v) {
        links!.add(FilterProjects.fromJson(v));
      });
    }
    if (json['user_guide'] != null) {
      userGuide = <UserGuide>[];
      json['user_guide'].forEach((v) {
        userGuide!.add(UserGuide.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['total'] = total;
    if (this.data != null) {
      data['data'] = this.data!.map((v) => v.toJson()).toList();
    }
    if (links != null) {
      data['links'] = links!.map((v) => v.toJson()).toList();
    }
    if (userGuide != null) {
      data['user_guide'] = userGuide!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

class AdLinkStatisticData {
  String? title;
  int? count;
  double? percent;

  AdLinkStatisticData({this.title, this.count, this.percent});

  AdLinkStatisticData.fromJson(Map<String, dynamic> json) {
    title = json['title'];
    count = json['count'];
    percent = TextUtils.parseDouble(json['percent']);
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['title'] = title;
    data['count'] = count;
    data['percent'] = percent;
    return data;
  }
}

class UserGuide {
  String? id;
  String? title;
  String? url;
  String? icon;

  UserGuide({this.id, this.title, this.url, this.icon});

  UserGuide.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    title = json['title'];
    url = json['url'];
    icon = json['icon'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['id'] = id;
    data['title'] = title;
    data['url'] = url;
    data['icon'] = icon;
    return data;
  }
}
