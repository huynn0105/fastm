class TotalAdLinkModel {
  int? total;
  int? finance;
  int? insurrance;
  int? pageQc;

  TotalAdLinkModel({this.total, this.finance, this.insurrance, this.pageQc});

  TotalAdLinkModel.fromJson(Map<String, dynamic> json) {
    total = json['total'];
    finance = json['finance'];
    insurrance = json['insurrance'];
    pageQc = json['page_qc'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['total'] = total;
    data['finance'] = finance;
    data['insurrance'] = insurrance;
    data['page_qc'] = pageQc;
    return data;
  }
}
