class CustomerRequirementDataModel {
  CustomerRequirementDataModel({
    this.id,
    this.projectName,
    this.linkShare,
    this.projectDescription,
    this.iconURL,
    this.webviewURL,
    this.linkWebView,
  });

  CustomerRequirementDataModel.fromJson(dynamic json) {
    id = json['ID'];
    projectName = json['projectName'];
    linkShare = json['link_share'];
    projectDescription = json['projectDescription'];
    iconURL = json['iconURL'];
    webviewURL = json['webviewURL'];
    linkWebView = json['linkWebView'];
  }

  String? id;
  String? projectName;
  String? linkShare;
  String? projectDescription;
  String? iconURL;
  String? webviewURL;
  String? linkWebView;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['ID'] = id;
    map['projectName'] = projectName;
    map['link_share'] = linkShare;
    map['projectDescription'] = projectDescription;
    map['iconURL'] = iconURL;
    map['webviewURL'] = webviewURL;
    map['linkWebView'] = linkWebView;
    return map;
  }
}
