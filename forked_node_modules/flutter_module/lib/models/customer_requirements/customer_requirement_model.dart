import 'dart:convert';

import 'package:flutter_module/models/customer_requirements/customer_requirement_data_model.dart';

/// requirementID : "3"
/// requirementTitle : "Mở thẻ tín dụng"
/// requirementKey : "credit"
/// requirementURL : "https://appay-rc.cloudcms.vn/finance/home/vib_oto?projectID=449"
/// requirementImage : "https://appay-rc.cloudcms.vn/assets/img/project_non_pl/Other3.png"

class CustomerRequirementModel {
  CustomerRequirementModel({
    this.requirementID,
    this.requirementTitle,
    this.requirementKey,
    this.requirementURL,
    this.requirementImage,
    this.data,
  });

  CustomerRequirementModel.fromJson(dynamic json) {
    requirementID = json['requirementID'];
    requirementTitle = json['requirementTitle'];
    requirementKey = json['requirementKey'];
    requirementURL = json['requirementURL'];
    requirementImage = json['requirementImage'];
    if (json['data'] != null && json['data'] is List) {
      data = [];
      (json['data'] as List?)?.forEach((v) {
        data?.add(CustomerRequirementDataModel.fromJson(v));
      });
    }
  }

  String? requirementID;
  String? requirementTitle;
  String? requirementKey;
  String? requirementURL;
  String? requirementImage;
  List<CustomerRequirementDataModel>? data;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['requirementID'] = requirementID;
    map['requirementTitle'] = requirementTitle;
    map['requirementKey'] = requirementKey;
    map['requirementURL'] = requirementURL;
    map['requirementImage'] = requirementImage;
    if (data != null) {
      map['data'] = data!.map((v) => v.toJson()).toList();
    }
    return map;
  }
}
