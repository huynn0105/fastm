import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/models/income/bank_model.dart';

class WithdrawInfoModel {
  double? availabilityMoney;
  List<BankModel>? bankingPicked;

  WithdrawInfoModel({
    this.bankingPicked,
    this.availabilityMoney,
  });

  WithdrawInfoModel.fromJson(Map<String, dynamic> json) {
    availabilityMoney = TextUtils.parseDouble(json["availability_money"]);
    bankingPicked = json["banking_picked"] == null
        ? null
        : (json["banking_picked"] as List).map((e) => BankModel.fromJson(e)).toList();
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["availability_money"] = availabilityMoney;
    if (bankingPicked != null) {
      data["banking_picked"] = bankingPicked?.map((e) => e.toJson()).toList();
    }
    return data;
  }
}
