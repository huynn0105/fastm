class CheckWithdrawInfoModel {
  bool? allowWithdrawal;
  List<CheckWithdrawErrorModel>? errors;

  CheckWithdrawInfoModel({
    this.allowWithdrawal,
    this.errors,
  });

  CheckWithdrawInfoModel.fromJson(Map<String, dynamic> json) {
    allowWithdrawal = json["allow_withdrawal"];
    errors = json["errors"] == null
        ? null
        : (json["errors"] as List).map((e) => CheckWithdrawErrorModel.fromJson(e)).toList();
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["allow_withdrawal"] = allowWithdrawal;
    if (errors != null) {
      data["errors"] = errors?.map((e) => e.toJson()).toList();
    }
    return data;
  }
}

class CheckWithdrawErrorModel {
  String? type;
  String? html;
  String? text;

  CheckWithdrawErrorModel({this.type, this.html});

  CheckWithdrawErrorModel.fromJson(Map<String, dynamic> json) {
    type = json["type"];
    html = json["html"];
    text = json["text"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["type"] = type;
    data["html"] = html;
    data["text"] = text;
    return data;
  }
}
