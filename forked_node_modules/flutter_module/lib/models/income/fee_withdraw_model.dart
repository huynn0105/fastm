import 'package:flutter_module/common/utils/text_util.dart';

class FeeWithdrawModel {
  double? withdrawalFee;

  FeeWithdrawModel({this.withdrawalFee});

  FeeWithdrawModel.fromJson(Map<String, dynamic> json) {
    withdrawalFee = TextUtils.parseDouble(json["withdrawal_fee"]?.toString().replaceAll('.', ''));
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["withdrawal_fee"] = withdrawalFee;
    return data;
  }
}
