class WalletModel {
  String? active;
  String? fullName;
  String? mobiPhone;

  WalletModel({this.active, this.fullName, this.mobiPhone});

  WalletModel.fromJson(Map<String, dynamic> json) {
    active = json["active"];
    fullName = json["fullName"];
    mobiPhone = json["mobiPhone"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["active"] = active;
    data["fullName"] = fullName;
    data["mobiPhone"] = mobiPhone;
    return data;
  }
}
