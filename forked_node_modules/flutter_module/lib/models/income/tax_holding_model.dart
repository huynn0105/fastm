import 'package:flutter_module/common/utils/text_util.dart';

class TaxHoldingModel {
  double? totalAmount;
  double? totalTaxHolding;
  String? lastUpdate;
  String? title;

  TaxHoldingModel({
    this.totalAmount,
    this.totalTaxHolding,
    this.lastUpdate,
    this.title,
  });

  TaxHoldingModel.fromJson(Map<String, dynamic> json) {
    totalAmount = TextUtils.parseDouble(json["total_amount"]);
    totalTaxHolding = TextUtils.parseDouble(json["total_tax_holding"]);
    lastUpdate = json["lastUpdate"];
    title = json["title"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["total_amount"] = totalAmount;
    map["total_tax_holding"] = totalTaxHolding;
    map["lastUpdate"] = lastUpdate;
    map["title"] = title;
    return map;
  }
}
