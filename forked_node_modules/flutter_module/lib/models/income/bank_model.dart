class BankModel {
  String? id;
  String? icon;
  String? bankName;
  String? bankFullName;
  String? bankAccountNumber;
  bool? disabled;

  BankModel({this.id, this.bankName, this.bankFullName, this.bankAccountNumber});

  BankModel.fromJson(Map<String, dynamic> json) {
    id = json["ID"];
    icon = json["icon"];
    bankName = json["bank_name"];
    bankFullName = json["bank_fullName"];
    bankAccountNumber = json["bank_accountNumber"];
    disabled = json["disable"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["ID"] = id;
    data["icon"] = icon;
    data["bank_name"] = bankName;
    data["bank_fullName"] = bankFullName;
    data["bank_accountNumber"] = bankAccountNumber;
    return data;
  }
}
