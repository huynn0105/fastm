import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/models/income/history_income_item_model.dart';
import 'package:flutter_module/models/income/tax_holding_model.dart';

class HistoryIncomeModel {
  List<HistoryIncomeItemModel>? list;
  TaxHoldingModel? taxHolding;
  int? page;
  bool? loadMore;

  HistoryIncomeModel({
    this.list,
    this.taxHolding,
    this.page,
    this.loadMore,
  });

  HistoryIncomeModel.fromJson(Map<String, dynamic> json) {
    list = json["list"] == null ? null : (json["list"] as List).map((e) => HistoryIncomeItemModel.fromJson(e)).toList();
    taxHolding = json["tax_holding"] == null || json["tax_holding"] is List ? null :  TaxHoldingModel.fromJson(json["tax_holding"]);
    page = TextUtils.parseInt(json["page"]);
    loadMore = json["load_more"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (list != null) {
      data["list"] = list?.map((e) => e.toJson()).toList();
    }
    if (taxHolding != null) {
      data["tax_holding"] = taxHolding?.toJson();
    }
    data["page"] = page;
    data["load_more"] = loadMore;
    return data;
  }
}
