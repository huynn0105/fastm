class OtpOptionModel {
  OtpOptionModel({
    this.id,
    this.icon,
    this.label,
  });

  OtpOptionModel.fromJson(dynamic json) {
    id = json['id'];
    icon = json['icon'];
    label = json['label'];
  }

  String? id;
  String? icon;
  String? label;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['id'] = id;
    map['icon'] = icon;
    map['label'] = label;
    return map;
  }
}
