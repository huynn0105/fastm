import 'package:flutter_module/common/utils/text_util.dart';

/// resendTime : 30
/// otpCode : "8651"

class RequestEmailOTPModel {
  RequestEmailOTPModel({
    this.resendTime,
    this.otpCode,
  });

  RequestEmailOTPModel.fromJson(dynamic json) {
    resendTime = TextUtils.parseInt(json['resendTime']);
    otpCode = json['otpCode'];
  }

  int? resendTime;
  String? otpCode;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['resendTime'] = resendTime;
    map['otpCode'] = otpCode;
    return map;
  }
}
