class GeneralDataModel {
  GeneralDataModel({
    this.id,
    this.code,
    this.name,
    this.type,
    this.value,
  });

  GeneralDataModel.fromJson(dynamic json) {
    id = json['id'];
    code = json['code'];
    name = json['name'];
    name = json['type'];
    value = json['value'];
  }

  int? id;
  String? code;
  String? name;
  String? type;
  String? value;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['id'] = id;
    map['code'] = code;
    map['name'] = name;
    map['type'] = type;
    map['value'] = value;
    return map;
  }
}
