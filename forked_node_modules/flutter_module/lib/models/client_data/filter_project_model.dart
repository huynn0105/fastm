/// ID : "2"
/// projectName : "MCredit"

class ProjectsModel {
  ProjectsModel({
    this.id,
    this.projectName,
  });

  ProjectsModel.fromJson(dynamic json) {
    id = json['ID'];
    projectName = json['projectName'];
  }

  String? id;
  String? projectName;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['ID'] = id;
    map['projectName'] = projectName;
    return map;
  }
}