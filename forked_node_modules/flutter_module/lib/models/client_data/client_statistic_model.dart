import 'package:flutter_module/common/utils/text_util.dart';

class ClientStatisticModel {
  List<StatisticData>? data;
  int? total;

  ClientStatisticModel({this.data, this.total});

  ClientStatisticModel.fromJson(Map<String, dynamic> json) {
    if (json['data'] != null) {
      data = <StatisticData>[];
      json['data'].forEach((v) {
        data!.add(StatisticData.fromJson(v));
      });
    }
    total = json['total'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (this.data != null) {
      data['data'] = this.data!.map((v) => v.toJson()).toList();
    }
    data['total'] = total;
    return data;
  }
}

class StatisticData {
  String? title;
  int? count;
  double? percent;
  List<StatisticFiltersData>? filters;

  StatisticData({this.title, this.count, this.percent, this.filters});

  StatisticData.fromJson(Map<String, dynamic> json) {
    title = json['title'];
    count = json['count'];
    percent = TextUtils.parseDouble(json['percent']);
    if (json['filters'] != null) {
      filters = <StatisticFiltersData>[];
      json['filters'].forEach((v) {
        filters!.add(StatisticFiltersData.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['title'] = title;
    data['count'] = count;
    data['percent'] = percent;
    if (filters != null) {
      data['filters'] = filters!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

class StatisticFiltersData {
  String? key;
  String? value;

  StatisticFiltersData({this.key, this.value});

  StatisticFiltersData.fromJson(Map<String, dynamic> json) {
    key = json['key'];
    value = json['value'].toString();
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['key'] = key;
    data['value'] = value;
    return data;
  }
}
