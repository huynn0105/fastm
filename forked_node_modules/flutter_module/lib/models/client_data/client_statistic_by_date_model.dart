import 'package:flutter_module/common/utils/text_util.dart';

class ClientStatisticByDateModel {
  List<ClientStatisticByDateDataModel>? data;
  int? total;
  double? percent;

  ClientStatisticByDateModel({this.data, this.total, this.percent});

  ClientStatisticByDateModel.fromJson(Map<String, dynamic> json) {
    data = json["data"] == null
        ? null
        : (json["data"] as List).map((e) => ClientStatisticByDateDataModel.fromJson(e)).toList();
    total = TextUtils.parseInt(json["total"]);
    percent = TextUtils.parseDouble(json["percent"]);
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (this.data != null) {
      data["data"] = this.data?.map((e) => e.toJson()).toList();
    }
    data["total"] = total;
    data["percent"] = percent;
    return data;
  }

  ClientStatisticByDateModel copyWith({
    List<ClientStatisticByDateDataModel>? data,
    int? total,
    double? percent,
  }) =>
      ClientStatisticByDateModel(
        data: data ?? this.data,
        total: total ?? this.total,
        percent: percent ?? this.percent,
      );
}

class ClientStatisticByDateDataModel {
  int? month;
  String? monthYear;
  int? count;
  String? day;

  ClientStatisticByDateDataModel({this.month, this.monthYear, this.count});

  ClientStatisticByDateDataModel.fromJson(Map<String, dynamic> json) {
    month = json["month"];
    monthYear = json["month_year"];
    count = json["count"];
    day = json["day"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["month"] = month;
    data["month_year"] = monthYear;
    data["count"] = count;
    data["day"] = day;
    return data;
  }
}
