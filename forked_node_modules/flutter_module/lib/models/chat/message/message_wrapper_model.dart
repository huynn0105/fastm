import 'package:flutter/cupertino.dart';
import 'package:flutter_module/models/chat/message/message_model.dart';

class MessageWrapperModel {
  MessageWrapperModel({
    this.header,
    this.message,
    this.showReceiverAvatar = false,
    this.showMessageCreateTime = false,
    this.showMessageStatus = false,
    this.isFirstMessageInThread = false,
    this.isLastMessageInThread = false,
    this.isFirstMessageInGroup = false,
    this.isLastMessageInGroup = false,
  });

  Widget? header;
  MessageModel? message;

  bool showReceiverAvatar;
  bool showMessageCreateTime;
  bool showMessageStatus;
  bool isFirstMessageInThread;
  bool isLastMessageInThread;
  bool isFirstMessageInGroup;
  bool isLastMessageInGroup;

  MessageWrapperModel copyWith({
    Widget? header,
    MessageModel? message,
    bool? showReceiverAvatar,
    bool? showMessageCreateTime,
    bool? showMessageStatus,
    bool? isFirstMessageInThread,
    bool? isLastMessageInThread,
    bool? isFirstMessageInGroup,
    bool? isLastMessageInGroup,
  }) {
    return MessageWrapperModel(
      header: header ?? this.header,
      message: message ?? this.message,
      showReceiverAvatar: showReceiverAvatar ?? this.showReceiverAvatar,
      showMessageCreateTime: showMessageCreateTime ?? this.showMessageCreateTime,
      showMessageStatus: showMessageStatus ?? this.showMessageStatus,
      isFirstMessageInThread: isFirstMessageInThread ?? this.isFirstMessageInThread,
      isLastMessageInThread: isLastMessageInThread ?? this.isLastMessageInThread,
      isFirstMessageInGroup: isFirstMessageInGroup ?? this.isFirstMessageInGroup,
      isLastMessageInGroup: isLastMessageInGroup ?? this.isLastMessageInGroup,
    );
  }
}
