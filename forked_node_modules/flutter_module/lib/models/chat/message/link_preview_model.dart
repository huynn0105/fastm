class LinkPreviewModel {
  LinkPreviewModel({
    this.description,
    this.image,
    this.link,
    this.title,
  });

  LinkPreviewModel.fromJson(dynamic json) {
    description = json['description'];
    image = json['image'] != null ? MediaModel.fromJson(json['image']) : null;
    link = json['link'];
    title = json['title'];
  }

  String? description;
  MediaModel? image;
  String? link;
  String? title;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['description'] = description;
    if (image != null) {
      map['image'] = image?.toJson();
    }
    map['link'] = link;
    map['title'] = title;
    return map;
  }
}
