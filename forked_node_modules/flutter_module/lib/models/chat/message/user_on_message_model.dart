class UserOnMessageModel {
  UserOnMessageModel({
    this.id,
    this.fullName,
    this.avatar,
  });

  UserOnMessageModel.fromJson(dynamic json) {
    id = json['uid'];
    fullName = json["fullName"];
    avatar = json["avatarImage"];
  }

  String? id;
  String? fullName;
  String? avatar;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['uid'] = id;
    map['fullName'] = fullName;
    map['avatarImage'] = avatar;
    return map;
  }

  UserOnMessageModel copyWith({
    String? id,
    String? fullName,
    String? avatar,
  }) {
    return UserOnMessageModel(
      id: id ?? this.id,
      fullName: fullName ?? this.fullName,
      avatar: avatar ?? this.avatar,
    );
  }
}
