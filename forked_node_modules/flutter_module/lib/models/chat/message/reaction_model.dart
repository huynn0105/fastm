/// reaction : "like"
/// count : 10
/// userIDs: ["user_2049610769"]

class ReactionModel {
  ReactionModel({
    this.code,
    this.userIDs,
  });

  ReactionModel.fromJson(dynamic json) {
    code = json['code'];
    userIDs = (json['userIDs'] as List?)?.cast<String>();
  }

  String? code;
  List<String>? userIDs;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['reaction'] = code;
    map['userIDs'] = userIDs;
    return map;
  }

  int get count => userIDs?.length ?? 0;
}
