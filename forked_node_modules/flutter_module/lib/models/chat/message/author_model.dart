import 'package:flutter_module/features/chat/hive_database/entities/message/message_entity.dart';

/// fullName : "BabyShark"
/// avatar : ""

class AuthorModel {
  AuthorModel({
    this.id,
    this.fullName,
    this.avatar,
  });

  factory AuthorModel.fromEntity(AuthorEntity entity) {
    return AuthorModel(
      id: entity.uid,
      avatar: entity.avatar,
      fullName: entity.fullName,
    );
  }

  AuthorModel.fromJson(dynamic json) {
    id = json['authorID'];
    fullName = json['authorFullName'];
    avatar = json['authorAvatarImage'];
  }

  AuthorModel.fromConfigJson(dynamic json) {
    id = json['userId'];
    fullName = json['nickname'] ?? json['fullName'];
    avatar = json['avatar'];
  }

  AuthorModel.fromUserLoginJson(dynamic json) {
    id = json['uid'] ?? json['ID'];
    fullName = json['fullName'];
    avatar = json['avatarImage'];
  }

  String? id;
  String? fullName;
  String? avatar;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['authorID'] = id;
    map['authorFullName'] = fullName;
    map['authorAvatarImage'] = avatar;
    return map;
  }

  AuthorModel copyWith({
    String? id,
    String? fullName,
    String? avatar,
  }) {
    return AuthorModel(
      id: id ?? this.id,
      fullName: fullName ?? this.fullName,
      avatar: avatar ?? this.avatar,
    );
  }
}
