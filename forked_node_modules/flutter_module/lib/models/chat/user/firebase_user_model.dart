import 'package:flutter_module/models/user/user_info_model.dart';

class FirebaseUserModel {
  String? avatarImage;
  String? email;
  String? fullName;
  String? mPhoneNumber;
  String? standardPhoneNumber;
  String? uid;
  String? wallImage;

  FirebaseUserModel({
    this.avatarImage,
    this.email,
    this.fullName,
    this.mPhoneNumber,
    this.standardPhoneNumber,
    this.uid,
    this.wallImage,
  });

  FirebaseUserModel.fromJson(Map<String, dynamic> json) {
    avatarImage = json["avatarImage"];
    email = json["email"];
    fullName = json["fullName"];
    mPhoneNumber = json["mPhoneNumber"];
    standardPhoneNumber = json["standardPhoneNumber"];
    uid = json["uid"];
    wallImage = json["wallImage"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["avatarImage"] = avatarImage;
    map["email"] = email;
    map["fullName"] = fullName;
    map["mPhoneNumber"] = mPhoneNumber;
    map["standardPhoneNumber"] = standardPhoneNumber;
    map["uid"] = uid;
    map["wallImage"] = wallImage;
    return map;
  }

  FirebaseUserModel.fromUserInfo(UserInfoModel user) {
    avatarImage = user.avatarImage ?? '';
    email = user.email ?? '';
    fullName = user.fullName ?? '';
    mPhoneNumber = user.getPhoneNumber() ?? '';
    standardPhoneNumber = user.getStandardPhoneNumber() ?? '';
    uid = user.id ?? '';
    wallImage = user.wallImage ?? '';
  }

  Map<String, dynamic> toFirebasePhoneJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["uid"] = uid;
    map["phoneNumber"] = mPhoneNumber;
    return map;
  }
}
