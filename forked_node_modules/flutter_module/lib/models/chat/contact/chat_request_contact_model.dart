class ChatRequestContactModel {
  String? message;
  String? fullName;
  String? avatar;
  int? sendingTime;
  String? receiverUID;
  String? senderID;
  String? uid;
  String? phoneNumber;

  ChatRequestContactModel({
    this.message,
    this.fullName,
    this.avatar,
    this.sendingTime,
    this.receiverUID,
    this.senderID,
    this.uid,
    this.phoneNumber,
  });

  ChatRequestContactModel.fromJson(Map<String, dynamic> json) {
    message = json['message'];
    fullName = json['fullName'] ?? json['nickname'];
    avatar = json['avatar'];
    sendingTime = json['sendingTime'] ?? json['sendingTime'];
    receiverUID = json['receiverUID'];
    senderID = json['senderID'];
    uid = json['uid'];
    phoneNumber = json['phoneNumber'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['message'] = message;
    data['fullName'] = fullName;
    data['avatar'] = avatar;
    data['sendingTime'] = sendingTime;
    data['receiverUID'] = receiverUID;
    data['senderID'] = senderID;
    data['uid'] = uid;
    data['phoneNumber'] = phoneNumber;
    return data;
  }

  ChatRequestContactModel copyWith({
    String? message,
    String? fullName,
    String? avatar,
    int? sendingTime,
    String? receiverUID,
    String? senderID,
    String? uid,
    String? phoneNumber,
  }) {
    return ChatRequestContactModel(
      message: message ?? this.message,
      fullName: fullName ?? this.fullName,
      avatar: avatar ?? this.avatar,
      sendingTime: sendingTime ?? this.sendingTime,
      receiverUID: receiverUID ?? this.receiverUID,
      senderID: senderID ?? this.senderID,
      uid: uid ?? this.uid,
      phoneNumber: phoneNumber ?? this.phoneNumber,
    );
  }
}
