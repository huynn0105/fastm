class ChatContactModel {
  String? uid;
  String? fullName;
  String? avatar;
  String? phone;

  ChatContactModel({this.uid, this.fullName, this.avatar, this.phone});

  ChatContactModel.fromJson(Map<String, dynamic> json) {
    uid = json['uid'];
    fullName = json['fullName'];
    avatar = json['avatar'];
    phone = json['phone'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['uid'] = uid;
    data['fullName'] = fullName;
    data['avatar'] = avatar;
    data['phone'] = phone;
    return data;
  }
}
