import 'package:flutter_module/models/chat/contact/chat_contact_model.dart';
import 'package:flutter_module/models/chat/thread/chat_thread_model.dart';

class ForwardContactModel {
  String? uid;
  String? title;
  String? avatar;
  String? type;

  ForwardContactModel({
    this.uid,
    this.title,
    this.avatar,
    this.type,
  });

  ForwardContactModel.fromJson(dynamic json) {
    uid = json['uid'];
    title = json['title'];
    avatar = json['avatar'];
    type = json['type'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['uid'] = uid;
    data['title'] = title;
    data['avatar'] = avatar;
    data['type'] = type;
    return data;
  }

  ForwardContactModel.fromThread(ChatThreadModel? obj) {
    uid = obj?.uid;
    title = obj?.getDefaultThreadTitle();
    avatar = obj?.photoImageURI();
  }

  ForwardContactModel.fromContact(ChatContactModel? obj) {
    uid = obj?.uid;
    title = obj?.fullName;
    avatar = obj?.avatar;
  }

  ForwardContactModel copyWith({
    String? uid,
    String? title,
    String? avatar,
    String? type,
  }) {
    return ForwardContactModel(
      uid: uid ?? this.uid,
      title: title ?? this.title,
      avatar: avatar ?? this.avatar,
      type: type ?? this.type,
    );
  }
}
