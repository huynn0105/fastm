class ChatSearchContactModel {
  String? avatar;
  String? nickname;
  String? referralCode;
  String? phoneNumber;

  ChatSearchContactModel({this.avatar, this.nickname, this.referralCode, this.phoneNumber});

  ChatSearchContactModel.fromJson(Map<String, dynamic> json) {
    avatar = json['avatar'];
    nickname = json['nickname'];
    referralCode = json['referralCode'];
    phoneNumber = json['phoneNumber'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['avatar'] = avatar;
    data['nickname'] = nickname;
    data['referralCode'] = referralCode;
    data['phoneNumber'] = phoneNumber;
    return data;
  }
}
