class ChatSupportRequestModel {
  int? total;
  List<Tickets>? tickets;

  ChatSupportRequestModel({this.total, this.tickets});

  ChatSupportRequestModel.fromJson(Map<String, dynamic> json) {
    total = json['total'];
    if (json['tickets'] != null) {
      tickets = <Tickets>[];
      json['tickets'].forEach((v) {
        tickets!.add(Tickets.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['total'] = total;
    if (tickets != null) {
      data['tickets'] = tickets!.map((v) => v.toJson()).toList();
    }
    return data;
  }

  ChatSupportRequestModel copyWith({
    int? total,
    List<Tickets>? tickets,
  }) {
    return ChatSupportRequestModel(
      total: total ?? this.total,
      tickets: tickets ?? this.tickets,
    );
  }
}

class Tickets {
  String? ticketNumber;
  String? ticketId;
  String? ticketObject;
  String? isAnswered;
  int? numberOfReplies;
  String? lastMessage;
  int? lastMessageDate;
  String? status;
  String? poster;
  int? created;
  String? selectedFeedbackDetails;

  Tickets(
      {this.ticketNumber,
      this.ticketId,
      this.ticketObject,
      this.isAnswered,
      this.numberOfReplies,
      this.lastMessage,
      this.lastMessageDate,
      this.status,
      this.poster,
      this.created,
      this.selectedFeedbackDetails});

  Tickets.fromJson(Map<String, dynamic> json) {
    ticketNumber = json['ticket_number'];
    ticketId = json['ticket_id'];
    ticketObject = json['ticket_object'];
    isAnswered = json['isAnswered'];
    numberOfReplies = json['numberOfReplies'];
    lastMessage = json['lastMessage'];
    lastMessageDate = json['lastMessage_date'];
    status = json['status'];
    poster = json['poster'];
    created = json['created'];
    selectedFeedbackDetails = json['selectedFeedbackDetails'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['ticket_number'] = ticketNumber;
    data['ticket_id'] = ticketId;
    data['ticket_object'] = ticketObject;
    data['isAnswered'] = isAnswered;
    data['numberOfReplies'] = numberOfReplies;
    data['lastMessage'] = lastMessage;
    data['lastMessage_date'] = lastMessageDate;
    data['status'] = status;
    data['poster'] = poster;
    data['created'] = created;
    data['selectedFeedbackDetails'] = selectedFeedbackDetails;
    return data;
  }
}
