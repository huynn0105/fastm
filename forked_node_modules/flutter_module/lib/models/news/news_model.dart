import 'package:flutter_module/common/utils/text_util.dart';

class NewsModel {
  NewsModel({
    this.postID,
    this.categories,
    this.hashtag,
    this.postTitle,
    this.webURL,
    this.postImage,
    this.createdDate,
    this.totalViews,
    this.sortPosition,
    this.isHighlight,
    this.subHashtag,
  });

  NewsModel.fromJson(dynamic json) {
    postID = json['postID'];
    categories = (json['categories'] as List?)?.map((e) => e.toString()).toList().cast<String>();
    hashtag = (json['hashtag'] as List?)?.map((e) => e.toString()).toList().cast<String>();
    postTitle = json['postTitle'];
    webURL = json['webURL'];
    postImage = json['postImage'];
    createdDate = json['createdDate'];
    totalViews = TextUtils.parseInt(json['totalViews']);
    sortPosition = json['sortPosition'];
    isHighlight = json['isHighlight'] == 1;
    subHashtag = (json["subHashtag"] as List?)?.map((e) => e.toString()).toList().cast<String>();
  }

  int? postID;
  List<String>? categories;
  List<String>? hashtag;
  String? postTitle;
  String? webURL;
  String? postImage;
  int? createdDate;
  int? totalViews;
  int? sortPosition;
  bool? isHighlight;
  List<String>? subHashtag;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['postID'] = postID;
    map['categories'] = categories;
    map['hashtag'] = hashtag;
    map['postTitle'] = postTitle;
    map['webURL'] = webURL;
    map['postImage'] = postImage;
    map['createdDate'] = createdDate;
    map['totalViews'] = totalViews;
    map['sortPosition'] = sortPosition;
    map['isHighlight'] = isHighlight! ? 1 : 0;
    map['subHashtag'] = subHashtag;
    return map;
  }
}
