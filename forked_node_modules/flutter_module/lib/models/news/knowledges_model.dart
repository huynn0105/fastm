class KnowledgesModel {
  KnowledgesModel({
    this.title,
    this.ackUrl,
    this.imageUrl,
    this.extend,
  });

  KnowledgesModel.fromJson(dynamic json) {
    title = json['title'];
    ackUrl = json['ackUrl'];
    imageUrl = json['imageUrl'];
    extend = (json['extend'] as List?)
        ?.map((e) => KnowledgesModel.fromJson(e))
        .toList();
  }

  String? title;
  String? ackUrl;
  String? imageUrl;
  List<KnowledgesModel>? extend;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['title'] = title;
    map['ackUrl'] = ackUrl;
    map['imageUrl'] = imageUrl;
    map['extend'] = extend;
    return map;
  }
}
