class ContestsModel {
  List<ContestItemModel> processing;
  List<ContestItemModel> end;

  ContestsModel({required this.processing, required this.end});

  factory ContestsModel.fromJson(Map<String, dynamic> json) {
    json['end'] = [
      {
        "cat_title": "Các chương trình đã kết thúc",
        "items": json['end'],
      }
    ];
    return ContestsModel(
      processing: (json['processing'] as List).map((contestItem) => ContestItemModel.fromJson(contestItem)).toList(),
      end: (json['end'] as List).map((contestItem) => ContestItemModel.fromJson(contestItem)).toList(),
    );
  }

  List<ContestItemModel> getData(String key) {
    switch (key) {
      case 'all':
        return [...processing, ...end];
      case 'processing':
        return processing;
      case 'end':
        return end;
      default:
        return [];
    }
  }
}

class ContestItemModel {
  String? catTitle;
  List<ContestBannerModel>? items;

  ContestItemModel({this.catTitle, this.items});

  ContestItemModel.fromJson(Map<String, dynamic> json) {
    catTitle = json["cat_title"];
    items = json["items"] == null ? null : (json["items"] as List).map((e) => ContestBannerModel.fromJson(e)).toList();
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["cat_title"] = catTitle;
    if (items != null) {
      data["items"] = items?.map((e) => e.toJson()).toList();
    }
    return data;
  }
}

class ContestBannerModel {
  String img;
  String urlTitle;
  String url;
  dynamic moengageEvent;

  ContestBannerModel({
    required this.img,
    required this.urlTitle,
    required this.url,
    required this.moengageEvent,
  });

  factory ContestBannerModel.fromJson(Map<String, dynamic> json) {
    return ContestBannerModel(
      img: json['img'] ?? '',
      urlTitle: json['url_title'] ?? '',
      url: json['url'] ?? '',
      moengageEvent: json['moengage_event'] ?? '',
    );
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["img"] = img;
    data["url_title"] = urlTitle;
    data["url"] = url;
    data["moengage_event"] = moengageEvent;
    return data;
  }
}
