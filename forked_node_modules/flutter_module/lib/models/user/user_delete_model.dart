class UserDeleteModel {
  bool? status;
  bool? requestDelete;
  String? requestDeleteDate;
  String? requestReason;

  UserDeleteModel({this.status, this.requestDelete, this.requestDeleteDate, this.requestReason});

  UserDeleteModel.fromJson(Map<String, dynamic> json) {
    status = json['status'];
    requestDelete = json['requestDelete'];
    requestDeleteDate = json['requestDeleteDate'];
    requestReason = json['requestReason'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['status'] = status;
    data['requestDelete'] = requestDelete;
    data['requestDeleteDate'] = requestDeleteDate;
    data['requestReason'] = requestReason;
    return data;
  }
}
