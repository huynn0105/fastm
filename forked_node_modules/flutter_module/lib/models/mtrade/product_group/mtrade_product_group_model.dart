/// keyword : "productGroup"
/// groups : [{"name":"Tất cả","code":"all","icon":"https://appay-rc.cloudcms.vn/mfast_assets/img/icons/category_all.png"}]

class MTradeProductGroupModel {
  MTradeProductGroupModel({
    this.keyword,
    this.list,
  });

  MTradeProductGroupModel.fromJson(dynamic json) {
    keyword = json['keyword'];
    if (json['list'] != null) {
      list = [];
      json['list'].forEach((v) {
        list?.add(ProductGroupsModel.fromJson(v));
      });
    }
  }

  String? keyword;
  List<ProductGroupsModel>? list;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['keyword'] = keyword;
    if (list != null) {
      map['groups'] = list?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}

