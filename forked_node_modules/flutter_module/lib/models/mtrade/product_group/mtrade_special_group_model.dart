class MTradeSpecialGroupModel {
  MTradeSpecialGroupModel({
    this.title,
    this.keyword,
    this.code,
    this.image,
  });

  MTradeSpecialGroupModel.fromJson(dynamic json) {
    title = json['title'];
    keyword = json['keyword'];
    code = json['code'];
    image = json['image'];
  }

  String? title;
  String? keyword;
  String? code;
  String? image;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['title'] = title;
    map['keyword'] = keyword;
    map['code'] = code;
    map['image'] = image;
    return map;
  }
}
