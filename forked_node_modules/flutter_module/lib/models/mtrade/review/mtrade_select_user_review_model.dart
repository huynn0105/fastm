class MTradeSelectUserReviewModel {
  String? userID;
  String? subsID;
  String? fullName;
  String? avatarImage;
  String? roleName;
  String? roleAlias;

  MTradeSelectUserReviewModel(
      {this.userID, this.subsID, this.fullName, this.avatarImage, this.roleName, this.roleAlias});

  MTradeSelectUserReviewModel.fromJson(Map<String, dynamic> json) {
    userID = json['userID'];
    subsID = json['subsID'];
    fullName = json['fullName'];
    avatarImage = json['avatarImage'];
    roleName = json['roleName'];
    roleAlias = json['roleAlias'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['userID'] = userID;
    data['subsID'] = subsID;
    data['fullName'] = fullName;
    data['avatarImage'] = avatarImage;
    data['roleName'] = roleName;
    data['roleAlias'] = roleAlias;
    return data;
  }
}
