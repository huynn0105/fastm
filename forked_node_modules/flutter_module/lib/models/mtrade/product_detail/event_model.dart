class EventModel {
  EventModel({
    this.content,
    this.icon,
    this.title,
  });

  EventModel.fromJson(dynamic json) {
    content = json['content'];
    icon = json['icon'];
    title = json['title'];
  }

  String? content;
  String? icon;
  String? title;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['content'] = content;
    map['icon'] = icon;
    map['title'] = title;
    return map;
  }
}
