import 'comm_model.dart';

/// tenorMin : "1"
/// tenorMax : "36"
/// comm : "5.5"
/// interestRate : "0.00"

class PayLaterModel {
  PayLaterModel({
    this.tenorMin,
    this.tenorMax,
    this.comm,
    this.interestRate,
  });

  PayLaterModel.fromJson(dynamic json) {
    tenorMin = json['tenorMin']?.toString();
    tenorMax = json['tenorMax']?.toString();
    comm = json['comm'] != null ? CommModel.fromJson(json['comm']) : null;
    interestRate = json['interestRate']?.toString();
  }

  String? tenorMin;
  String? tenorMax;
  CommModel? comm;
  String? interestRate;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['tenorMin'] = tenorMin;
    map['tenorMax'] = tenorMax;
    if (comm != null) {
      map['comm'] = comm?.toJson();
    }
    map['interestRate'] = interestRate;
    return map;
  }
}
