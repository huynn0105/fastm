import '../../../common/utils/text_util.dart';

/// insurance_duration : "15 th√°ng"
/// size : "2 x 3 x 4 cm"
/// mass : "5.3kg"

class DetailInfoModel {
  DetailInfoModel({
    this.insuranceDuration,
    this.size,
    this.mass,
  });

  DetailInfoModel.fromJson(dynamic json) {
    insuranceDuration = json['insurance_duration'];
    size = json['size'];
    mass = json['mass'];
  }

  String? insuranceDuration;
  String? size;
  String? mass;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['insurance_duration'] = insuranceDuration;
    map['size'] = size;
    map['mass'] = mass;
    return map;
  }

  bool isEnabled() {
    return TextUtils.isNotEmpty(insuranceDuration) || TextUtils.isNotEmpty(size) || TextUtils.isNotEmpty(mass);
  }
}
