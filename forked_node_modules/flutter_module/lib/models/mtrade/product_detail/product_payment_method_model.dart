
class ProductPaymentMethodModel {
  ProductPaymentMethodModel({
    this.productInvalid,
    this.payNow,
    this.payLater,
  });

  ProductPaymentMethodModel.fromJson(dynamic json) {
    productInvalid = json['product_invalid'];
    payNow = json['pay_now'];
    payLater = json['pay_later'];
  }

  bool? productInvalid;
  bool? payNow;
  bool? payLater;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['product_invalid'] = productInvalid;
    map['pay_now'] = payNow;
    map['pay_later'] = payLater;
    return map;
  }
}
