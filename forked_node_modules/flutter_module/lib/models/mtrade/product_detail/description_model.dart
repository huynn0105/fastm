
/// title : "Mô tả sản phẩm"
/// content : ""

class DescriptionModel {
  DescriptionModel({
    this.title,
    this.content,
  });

  DescriptionModel.fromJson(dynamic json) {
    title = json['title'];
    content = json['content'];
  }
  String? title;
  String? content;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['title'] = title;
    map['content'] = content;
    return map;
  }
}