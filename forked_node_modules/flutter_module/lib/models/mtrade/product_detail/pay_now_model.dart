import 'comm_model.dart';

/// comm : "7.6"

class PayNowModel {
  PayNowModel({
    this.comm,
  });

  PayNowModel.fromJson(dynamic json) {
    comm = json['comm'] != null ? CommModel.fromJson(json['comm']) : null;
  }

  CommModel? comm;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (comm != null) {
      map['comm'] = comm?.toJson();
    }
    return map;
  }
}
