import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/common/utils/text_util.dart';

/// ticket_size : {"value":4.5,"currency":"%"}
/// scheme : {"value":22500000,"currency":"Ä‘"}

class CommModel {
  CommModel({
    this.ticketSize,
    this.scheme,
  });

  CommModel.fromJson(dynamic json) {
    ticketSize = json['ticket_size'] != null ? ValueModel.fromJson(json['ticket_size']) : null;
    scheme = json['scheme'] != null ? ValueModel.fromJson(json['scheme']) : null;
  }

  ValueModel? ticketSize;
  ValueModel? scheme;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (ticketSize != null) {
      map['ticket_size'] = ticketSize?.toJson();
    }
    if (scheme != null) {
      map['scheme'] = scheme?.toJson();
    }
    return map;
  }
}

/// value : 55
/// currency : "%"

class ValueModel {
  ValueModel({
    this.value,
    this.currency,
  });

  ValueModel.fromJson(dynamic json) {
    value = TextUtils.parseDouble(json['value']);
    currency = json['currency'];
  }

  double? value;
  String? currency;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['value'] = value;
    map['currency'] = currency;
    return map;
  }

  String getValue() {
    return [FormatUtil.doubleFormat(value), currency ?? ""].join("");
  }
}
