/// value : "Xanh Dương"
/// disable : false

class OptionModel {
  OptionModel({
    this.value,
    this.disabled,
  });

  OptionModel.fromJson(dynamic json) {
    value = json['value'];
    disabled = json['disable'] == true || json['disable'] == "true";
  }

  String? value;
  bool? disabled;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['value'] = value;
    map['disable'] = disabled;
    return map;
  }
}
