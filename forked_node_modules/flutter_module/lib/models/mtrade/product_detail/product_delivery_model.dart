import 'package:flutter_module/common/enum/mtrade/product_delivery_type.dart';
import 'package:flutter_module/models/mtrade/product_detail/event_model.dart';

/// time : "1-3 ng√†y"
/// type : "IN_HOUSE"

class ProductDeliveryModel {
  ProductDeliveryModel({
    this.time,
    this.type,
    this.event,
  });

  ProductDeliveryModel.fromJson(dynamic json) {
    time = json['time'];
    type = json['type'];
    event = json['event'] != null ? EventModel.fromJson(json['event']) : null;
  }

  String? time;
  String? type;
  EventModel? event;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['time'] = time;
    map['type'] = type;
    map['event'] = event?.toJson();
    return map;
  }

  bool isPreOrder() {
    return type == ProductDeliveryType.preOrder.code;
  }
}
