import 'package:flutter_module/models/mtrade/product_detail/product_delivery_model.dart';

import 'product_payment_method_model.dart';

/// is_support : true
/// product_delivery_time : {"time":"2 ng√†y","type":"PRE_ORDER"}

class ProductLocationSupportModel {
  ProductLocationSupportModel({
    this.isSupport,
    this.delivery,
    this.productPaymentMethod,
  });

  ProductLocationSupportModel.fromJson(dynamic json) {
    isSupport = json['is_support'];
    delivery = json['product_delivery_time'] != null ? ProductDeliveryModel.fromJson(json['product_delivery_time']) : null;
    productPaymentMethod = json['product_payment_method'] != null ? ProductPaymentMethodModel.fromJson(json['product_payment_method']) : null;
  }

  bool? isSupport;
  ProductDeliveryModel? delivery;
  ProductPaymentMethodModel? productPaymentMethod;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['is_support'] = isSupport;
    if (delivery != null) {
      map['product_delivery_time'] = delivery?.toJson();
    }
    if (productPaymentMethod != null) {
      map['product_payment_method'] = productPaymentMethod?.toJson();
    }
    return map;
  }
}