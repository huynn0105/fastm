import 'option_model.dart';

/// name : "Màu sắc"
/// value : "sku"
/// option : [{"name":"Xanh Dương","sku":"98712THNVM98123.","price":"11000000.00"}]

class AttributeModel {
  AttributeModel({
    this.name,
    this.value,
    this.options,
  });

  AttributeModel.fromJson(dynamic json) {
    name = json['name'];
    value = json['value'];
    if (json['option'] != null) {
      options = [];
      json['option'].forEach((v) {
        options?.add(OptionModel.fromJson(v));
      });
    }
  }

  String? name;
  String? value;
  List<OptionModel>? options;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['name'] = name;
    map['keyword'] = value;
    if (options != null) {
      map['option'] = options?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}
