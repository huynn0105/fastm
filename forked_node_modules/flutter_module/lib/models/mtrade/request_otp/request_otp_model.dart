class RequestOTPModel {
  bool? status;
  String? otpCode;
  String? channel;
  int? logOtpID;
  bool? allowRetryEmail;
  int? resendTime;

  RequestOTPModel({this.status, this.otpCode, this.channel, this.logOtpID, this.allowRetryEmail, this.resendTime});

  RequestOTPModel.fromJson(Map<String, dynamic> json) {
    status = json['status'];
    otpCode = json['otp_code'];
    channel = json['channel'];
    logOtpID = json['logOtpID'];
    allowRetryEmail = json['allow_retry_email'];
    resendTime = json['resendTime'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['status'] = status;
    data['otp_code'] = otpCode;
    data['channel'] = channel;
    data['logOtpID'] = logOtpID;
    data['allow_retry_email'] = allowRetryEmail;
    data['resendTime'] = resendTime;
    return data;
  }
}
