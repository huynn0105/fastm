class LandingPageUserGuideModel {
  LandingPageUserGuideModel({
    this.link,
    this.content,
  });

  LandingPageUserGuideModel.fromJson(dynamic json) {
    link = json['link'];
    content = json['content'];
  }

  String? link;
  String? content;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['link'] = link;
    map['content'] = content;
    return map;
  }
}
