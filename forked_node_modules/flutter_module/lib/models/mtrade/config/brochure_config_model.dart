class BrochureConfigModel {
  BrochureConfigModel({
    this.pageImg,
    this.banner,
    this.categories,
    this.email,
    this.phone,
  });

  BrochureConfigModel.fromJson(dynamic json) {
    pageImg = json['page_img'] != null ? json['page_img'].cast<String>() : [];
    banner = json['banner'] != null ? json['banner'].cast<String>() : [];
    categories = json['categories'] != null ? json['categories'].cast<String>() : [];
    email = json['email'];
    phone = json['phone'];
  }

  List<String>? pageImg;
  List<String>? banner;
  List<String>? categories;
  String? email;
  String? phone;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['page_img'] = pageImg;
    map['banner'] = banner;
    map['categories'] = categories;
    map['email'] = email;
    map['phone'] = phone;
    return map;
  }
}
