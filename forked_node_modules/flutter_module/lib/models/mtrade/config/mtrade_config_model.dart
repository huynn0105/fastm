import 'package:flutter_module/models/mtrade/config/product_config_model.dart';

/// configs : {"product_limit":20,"quantity_limit":99}

class MTradeConfigModel {
  MTradeConfigModel({
    this.product,
  });

  MTradeConfigModel.fromJson(dynamic json) {
    product = json['configs'] != null ? ProductConfigModel.fromJson(json['configs']) : null;
  }

  ProductConfigModel? product;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (product != null) {
      map['configs'] = product?.toJson();
    }
    return map;
  }
}