import 'mtrade_promotion_model.dart';

class MTradeSelectedPromotionModel extends MTradePromotionModel {
  List<String>? tickets;
  List<SelectedPromotionModel>? selection;

  MTradeSelectedPromotionModel({
    this.tickets,
    this.selection,
  });

  MTradeSelectedPromotionModel.fromJson(Map<String, dynamic> json) : super.fromJson(json);

  MTradeSelectedPromotionModel.fromBase(MTradePromotionModel? base) : super.fromJson(base?.toJson() ?? {});
}

class SelectedPromotionModel {
  String? code;
  String? sku;
  double? promotionPrice;
  double? promotionValue;
  String? currency;
  String? ticketValue;

  SelectedPromotionModel({
    this.code,
    this.sku,
    this.promotionPrice,
    this.promotionValue,
    this.currency,
    this.ticketValue,
  });
}
