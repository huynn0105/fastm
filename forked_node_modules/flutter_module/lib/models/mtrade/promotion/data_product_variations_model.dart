import 'mtrade_promotion_model.dart';

class DataProductVariationsModel {
  DataProductVariationsModel({
    this.codes,
    this.quantity,
    this.sku,
  });

  DataProductVariationsModel.fromJson(dynamic json) {
    if (json['codes'] != null) {
      codes = <PromotionCode>[];
      json['codes'].forEach((v) {
        codes!.add(PromotionCode.fromJson(v));
      });
    }
    quantity = json['quantity'];
    sku = json['sku'];
  }

  List<PromotionCode>? codes;
  int? quantity;
  String? sku;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['codes'] = codes?.map((e) => e.code).toList();
    map['quantity'] = quantity;
    map['sku'] = sku;
    return map;
  }

  DataProductVariationsModel copyWith({
    List<PromotionCode>? codes,
    int? quantity,
    String? sku,
  }) {
    return DataProductVariationsModel(
      codes: codes ?? this.codes,
      quantity: quantity ?? this.quantity,
      sku: sku ?? this.sku,
    );
  }
}

class PromotionCode {
  PromotionCode({
    this.code,
    this.promotion,
  });

  PromotionCode.fromJson(dynamic json) {
    code = json['code'];
    promotion = json['promotion'] != null ? MTradePromotionModel.fromJson(json['promotion']) : null;
  }

  MTradePromotionModel? promotion;
  String? code;
  double? discount;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['code'] = code;
    return map;
  }
}

