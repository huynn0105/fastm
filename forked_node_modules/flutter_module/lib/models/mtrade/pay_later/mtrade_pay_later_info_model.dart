import 'package:flutter_module/common/utils/text_util.dart';

/// emiExpected : 1500000

class MTradePayLaterInfoModel {
  MTradePayLaterInfoModel({
    this.emiExpected,
  });

  MTradePayLaterInfoModel.fromJson(dynamic json) {
    emiExpected = TextUtils.parseDouble(json['emiExpected']);
  }

  double? emiExpected;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['emiExpected'] = emiExpected;
    return map;
  }
}
