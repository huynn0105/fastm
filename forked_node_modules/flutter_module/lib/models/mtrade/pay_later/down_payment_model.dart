import 'package:flutter_module/models/general_object.dart';

class DownPaymentModel extends GeneralObject {
  DownPaymentModel({
    super.id,
    super.code,
    super.name,
    this.percent,
    this.amount,
  });

  double? percent;
  double? amount;
}

class TenorModel extends GeneralObject {
  TenorModel({
    super.id,
    super.code,
    super.name,
    this.tenor,
  });

  int? tenor;
}
