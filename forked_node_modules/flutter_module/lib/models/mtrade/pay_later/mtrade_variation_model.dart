class MTradeVariationModel {
  MTradeVariationModel({
    this.id,
    this.sku,
    this.price,
    this.scheme,
    this.quantity,
    this.createdDate,
    this.variationType,
    this.variationValue,
    this.partnerProductID,
    this.status,
  });

  MTradeVariationModel.fromJson(dynamic json) {
    id = TextUtils.parseInt(json['ID']);
    sku = json['sku'];
    price = TextUtils.parseInt(json['price']);
    scheme = json['scheme'];
    quantity = TextUtils.parseInt(json['quantity']);
    createdDate = json['createdDate'];
    variationType = json['variationType'];
    variationValue = json['variationValue'];
    partnerProductID = json['partnerProductID'];
    status = json['status'];
  }

  int? id;
  String? sku;
  int? price;
  String? scheme;
  int? quantity;
  String? createdDate;
  String? variationType;
  String? variationValue;
  int? partnerProductID;
  String? status;


  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['ID'] = id;
    map['sku'] = sku;
    map['price'] = price;
    map['scheme'] = scheme;
    map['quantity'] = quantity;
    map['createdDate'] = createdDate;
    map['variationType'] = variationType;
    map['variationValue'] = variationValue;
    map['partnerProductID'] = partnerProductID;
    map['status'] = status;
    return map;
  }
}