import 'package:flutter_module/common/utils/text_util.dart';

/// conversation_fee : 5.0
/// tenor : 3

class TenorConversionFeeSchemeModel {
  TenorConversionFeeSchemeModel({
    this.conversionFee,
    this.tenor,
    this.name,
  });

  TenorConversionFeeSchemeModel.fromJson(dynamic json) {
    conversionFee = TextUtils.parseDouble(json['conversation_fee']);
    tenor = TextUtils.parseInt(json['tenor']);
    name = tenor != null ? "${tenor ?? 0} th√°ng" : null;
  }

  double? conversionFee;
  int? tenor;
  String? name;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['conversation_fee'] = conversionFee;
    map['tenor'] = tenor;
    return map;
  }
}
