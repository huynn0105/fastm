import '../../../common/utils/text_util.dart';

/// ID : "2"
/// stageName : "Đang duyệt"
/// status : "processing"
/// description : null
/// createdDate : "2023-02-22 10:47:40"
/// updatedDate : "2023-02-27 10:18:53"
/// icon : "https://appay-rc.cloudcms.vn/mfast_assets/img/icons/mtrade-order-1.png"
/// position : "1"
/// processState : "waiting"
/// count : 0

class ProcessModel {
  ProcessModel({
    this.id,
    this.stageName,
    this.status,
    this.description,
    this.createdDate,
    this.updatedDate,
    this.icon,
    this.position,
    this.processState,
    this.count,
  });

  ProcessModel.fromJson(dynamic json) {
    id = json['ID'];
    stageName = json['stageName'];
    status = json['status'];
    description = json['description'];
    createdDate = json['createdDate'];
    updatedDate = json['updatedDate'];
    icon = json['icon'];
    position = json['position'];
    processState = json['processState'];
    count = TextUtils.parseInt(json['count']);
  }

  String? id;
  String? stageName;
  String? status;
  String? description;
  String? createdDate;
  String? updatedDate;
  String? icon;
  String? position;
  String? processState;
  int? count;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['ID'] = id;
    map['stageName'] = stageName;
    map['status'] = status;
    map['description'] = description;
    map['createdDate'] = createdDate;
    map['updatedDate'] = updatedDate;
    map['icon'] = icon;
    map['position'] = position;
    map['processState'] = processState;
    map['count'] = count;
    return map;
  }
}
