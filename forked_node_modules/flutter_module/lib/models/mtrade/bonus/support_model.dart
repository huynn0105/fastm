/// icon : "https://appay-rc.cloudcms.vn/mfast_assets/img/personal/email_icon_app.png"
/// label : "Email"
/// action : "mailto:hotro-mpl@mfast.vn?subject=MFast: Yêu cầu hỗ trợ"
/// rightLabel : "hotro-mpl@mfast.vn"

class SupportModel {
  SupportModel({
    this.icon,
    this.label,
    this.action,
    this.rightLabel,
  });

  SupportModel.fromJson(dynamic json) {
    icon = json['icon'];
    label = json['label'];
    action = json['action'];
    rightLabel = json['rightLabel'];
  }

  String? icon;
  String? label;
  String? action;
  String? rightLabel;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['icon'] = icon;
    map['label'] = label;
    map['action'] = action;
    map['rightLabel'] = rightLabel;
    return map;
  }
}
