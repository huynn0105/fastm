class MTradeBonusModel {
  MTradeBonusModel({
    this.directBonusSum,
    this.indirectBonusSum,
    this.process,
    this.support,
  });

  MTradeBonusModel.fromJson(dynamic json) {
    directBonusSum = TextUtils.parseInt(json['direct_bonus_sum']);
    indirectBonusSum = TextUtils.parseInt(json['indirect_bonus_sum']);
    if (json['list_process'] != null) {
      process = [];
      json['list_process'].forEach((v) {
        process?.add(ProcessModel.fromJson(v));
      });
    }
    if (json['support'] != null) {
      support = [];
      json['support'].forEach((v) {
        support?.add(SupportModel.fromJson(v));
      });
    }
  }

  int? directBonusSum;
  int? indirectBonusSum;
  List<ProcessModel>? process;
  List<SupportModel>? support;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['direct_bonus_sum'] = directBonusSum;
    map['indirect_bonus_sum'] = indirectBonusSum;
    if (process != null) {
      map['list_process'] = process?.map((v) => v.toJson()).toList();
    }
    if (support != null) {
      map['support'] = support?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}
