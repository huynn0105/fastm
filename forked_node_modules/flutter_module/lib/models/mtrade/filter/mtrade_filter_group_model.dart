import 'mtrade_filter_model.dart';
import 'mtrade_input_price_model.dart';

class MTradeFilterGroupModel {
  MTradeFilterGroupModel({
    this.title,
    this.keyword,
    this.multiSelect,
    this.defaultValue,
    this.showLastItemFullScreen,
    this.list,
  });

  MTradeFilterGroupModel.fromJson(dynamic json) {
    title = json['title'];
    keyword = json['keyword'];
    multiSelect = json['multiSelect'];
    defaultValue = json['defaultValue'];
    showLastItemFullScreen = json['show_last_item_full_screen'];
    if (json['list'] != null) {
      list = [];
      json['list'].forEach((v) {
        list?.add(MTradeFilterModel.fromJson(v));
      });
    }
    if (json['inputs'] != null) {
      inputs = [];
      json['inputs'].forEach((v) {
        inputs?.add(MTradeInputPriceModel.fromJson(v));
      });
    }
  }

  String? title;
  String? keyword;
  bool? multiSelect;
  String? defaultValue;
  bool? showLastItemFullScreen;
  List<MTradeInputPriceModel>? inputs;
  List<MTradeFilterModel>? list;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['title'] = title;
    map['keyword'] = keyword;
    map['multiSelect'] = multiSelect;
    map['defaultValue'] = defaultValue;
    map['show_last_item_full_screen'] = showLastItemFullScreen;
    if (list != null) {
      map['list'] = list?.map((v) => v.toJson()).toList();
    }
    if (inputs != null) {
      map['inputs'] = inputs?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}
