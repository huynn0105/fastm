class FilterObject {
  FilterObject({
    this.id,
    this.name,
    this.code,
    this.listCode,
    this.index,
    this.parentCode,
    this.hasSubGroup,
  });

  String? id;
  String? name;
  String? code;
  List<String>? listCode;
  int? index;
  String? parentCode;
  bool? hasSubGroup;

  FilterObject copyWith({
    String? id,
    String? name,
    String? code,
    List<String>? listCode,
    int? index,
    String? parentCode,
    bool? hasSubGroup,
  }) {
    return FilterObject(
      id: id ?? this.id,
      name: name ?? this.name,
      code: code ?? this.code,
      listCode: listCode ?? this.listCode,
      index: index ?? this.index,
      parentCode: parentCode ?? this.parentCode,
      hasSubGroup: hasSubGroup ?? this.hasSubGroup,
    );
  }

  Map<String, dynamic> toJson() {
    return {
      'id': id,
      'name': name,
      'code': code,
      'listCode': listCode,
      'index': index,
      'parentCode': parentCode,
      'hasSubGroup': hasSubGroup,
    };
  }
}
