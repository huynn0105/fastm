import 'mtrade_filter_group_model.dart';

class MTradeFilterModel {
  MTradeFilterModel({
    this.name,
    this.code,
    this.subGroup,
    this.price,
  });

  MTradeFilterModel.fromJson(dynamic json) {
    name = json['name'];
    code = json['code'];
    subGroup = json['subGroup'] != null ? MTradeFilterGroupModel.fromJson(json['subGroup']) : null;
    price = json['price'] != null ? (json['price'] as List?)?.map((e) => (e ?? 0) > 0 ? "$e" : "").toList() : [];
  }

  String? name;
  String? code;
  MTradeFilterGroupModel? subGroup;
  List<String>? price;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['name'] = name;
    map['code'] = code;
    if (subGroup != null) {
      map['subGroup'] = subGroup?.toJson();
    }
    map['price'] = price;
    return map;
  }
}
