import 'package:flutter_module/models/mtrade/filter/filter_object.dart';

class ParentFilterObject {
  ParentFilterObject({
    this.code,
    this.filters = const [],
    this.subFilters = const [],
  });

  String? code;
  List<FilterObject> filters;
  List<FilterObject> subFilters;

  String getName() {
    return filters.map((e) => "${e.name ?? ""}${e.hasSubGroup == true ? "{sub}" : ""}").toList().join(", ");
  }
}
