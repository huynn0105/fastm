class SelectedFilterModel {
  SelectedFilterModel({
    this.code,
    this.name,
    this.keyword,
    this.parentKeyword,
    this.index,
  });

  String? code;
  String? name;
  String? keyword;
  String? parentKeyword;
  int? index;

  SelectedFilterModel copyWith({
    String? code,
    String? name,
    String? keyword,
    String? parentKeyword,
    int? index,
  }) {
    return SelectedFilterModel(
      code: code ?? this.code,
      name: name ?? this.name,
      keyword: keyword ?? this.keyword,
      parentKeyword: parentKeyword ?? this.parentKeyword,
      index: index ?? this.index,
    );
  }
}
