class MTradeLinkBrochureModel {
  String? email;
  List<String>? poster;
  String? link;

  MTradeLinkBrochureModel({this.email, this.poster, this.link});

  MTradeLinkBrochureModel.fromJson(Map<String, dynamic> json) {
    email = json['email'];
    poster = json['poster'].cast<String>();
    link = json['link'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['email'] = email;
    data['poster'] = poster;
    data['link'] = link;
    return data;
  }
}
