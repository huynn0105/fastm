class OrderItemModel {
  OrderItemModel({
    this.productName,
    this.quantity,
    this.amount,
    this.createdDate,
    this.updatedDate,
    this.itemID,
    this.processID,
    this.processText,
    this.addressFull,
    this.image,
    this.price,
    this.promotionPrice,
  });

  OrderItemModel.fromJson(dynamic json) {
    productName = json['productName'];
    quantity = TextUtils.parseInt(json['quantity']);
    amount = TextUtils.parseInt(json['amount']);
    createdDate = json['createdDate'];
    updatedDate = json['updatedDate'];
    itemID = json['itemID'];
    processID = json['processID'];
    processText = json['processText'];
    addressFull = json['addressFull'];
    image = json['image'];
    price = TextUtils.parseDouble(json['price']);
    promotionPrice = TextUtils.parseDouble(json['promotionPrice']);
  }

  String? productName;
  int? quantity;
  int? amount;
  String? createdDate;
  String? updatedDate;
  String? itemID;
  String? processID;
  String? processText;
  String? addressFull;
  String? image;
  double? price;
  double? promotionPrice;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['productName'] = productName;
    map['quantity'] = quantity;
    map['amount'] = amount;
    map['createdDate'] = createdDate;
    map['updatedDate'] = updatedDate;
    map['itemID'] = itemID;
    map['processID'] = processID;
    map['processText'] = processText;
    map['addressFull'] = addressFull;
    map['image'] = image;
    map['price'] = price;
    map['promotionPrice'] = promotionPrice;
    return map;
  }
}
