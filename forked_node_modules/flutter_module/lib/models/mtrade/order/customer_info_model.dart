class CustomerInfoModel {
  CustomerInfoModel({
    this.name,
    this.phone,
    this.address,
  });

  CustomerInfoModel.fromJson(dynamic json) {
    name = json['name'];
    phone = json['phone'];
    address = json['address'];
  }

  String? name;
  String? phone;
  String? address;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['name'] = name;
    map['phone'] = phone;
    map['address'] = address;
    return map;
  }
}
