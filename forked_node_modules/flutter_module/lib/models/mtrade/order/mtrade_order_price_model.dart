class MTradeOrderPriceModel {
  MTradeOrderPriceModel({
    this.listPrice,
  });

  MTradeOrderPriceModel.fromJson(dynamic json) {
    listPrice = [];
    for (var element in (json['list_price'] as Map).values) {
      listPrice?.add(OrderPrice.fromJson(element));
    }
  }

  List<OrderPrice>? listPrice;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['list_price'] = listPrice;
    return map;
  }
}

class OrderPrice {
  OrderPrice({
    this.newPrice,
    this.originalPrice,
    this.productPriceId,
    this.productPriceItemId,
    this.public,
    this.sku,
  });

  OrderPrice.fromJson(dynamic json) {
    newPrice = json['new_price'];
    originalPrice = json['original_price'];
    productPriceId = json['product_price_id'];
    productPriceItemId = json['product_price_item_id'];
    public = json['public'];
    sku = json['sku'];
  }

  int? newPrice;
  int? originalPrice;
  String? productPriceId;
  String? productPriceItemId;
  bool? public;
  String? sku;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['new_price'] = newPrice;
    map['original_price'] = originalPrice;
    map['product_price_id'] = productPriceId;
    map['product_price_item_id'] = productPriceItemId;
    map['public'] = public;
    map['sku'] = sku;
    return map;
  }

  bool existWholesalePrice() {
    return newPrice != null;
  }
}
