class OrderHistoryModel {
  OrderHistoryModel({
    this.stateName,
    this.icon,
    this.processText,
    this.processNote,
    this.updatedDate,
  });

  OrderHistoryModel.fromJson(dynamic json) {
    stateName = json['stateName'];
    icon = json['icon'];
    processText = json['processText'];
    processNote = json['processNote'];
    updatedDate = json['updatedDate'];
  }

  String? stateName;
  String? icon;
  String? processText;
  String? processNote;
  String? updatedDate;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['stateName'] = stateName;
    map['icon'] = icon;
    map['processText'] = processText;
    map['processNote'] = processNote;
    map['updatedDate'] = updatedDate;
    return map;
  }
}
