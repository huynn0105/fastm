import 'package:flutter_module/models/mtrade/order/order_payment_method_model.dart';

class MTradeOrderPaymentMethodModel {
  MTradeOrderPaymentMethodModel({
    this.filterPaymentMethod,
  });

  MTradeOrderPaymentMethodModel.fromJson(dynamic json) {
    if (json['filter_payment_method'] != null) {
      filterPaymentMethod = [];
      json['filter_payment_method'].forEach((v) {
        filterPaymentMethod?.add(OrderPaymentMethodModel.fromJson(v));
      });
    }
  }

  List<OrderPaymentMethodModel>? filterPaymentMethod;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (filterPaymentMethod != null) {
      map['filter_payment_method'] =
          filterPaymentMethod?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}
