class OrderKnowledgeModel {
  OrderKnowledgeModel({
    this.link,
    this.content,
    this.icon,
  });

  OrderKnowledgeModel.fromJson(dynamic json) {
    link = json['link'];
    content = json['content'];
    icon = json['icon'];
  }

  String? link;
  String? content;
  String? icon;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['link'] = link;
    map['content'] = content;
    map['icon'] = icon;
    return map;
  }
}
