import 'customer_info_model.dart';

class PaymentDetailModel {
  PaymentDetailModel({
    this.paymentMethod,
    this.paymentTotal,
    this.customerInfo,
    this.listOrders,
  });

  PaymentDetailModel.fromJson(dynamic json) {
    paymentMethod = json['payment_method'];
    paymentTotal = json['payment_total'];
    customerInfo = json['customer_info'] != null ? CustomerInfoModel.fromJson(json['customer_info']) : null;
    if (json['list_orders'] != null) {
      listOrders = [];
      json['list_orders'].forEach((v) {
        listOrders?.add(ListOrders.fromJson(v));
      });
    }
  }

  String? paymentMethod;
  int? paymentTotal;
  CustomerInfoModel? customerInfo;
  List<ListOrders>? listOrders;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['payment_method'] = paymentMethod;
    map['payment_total'] = paymentTotal;
    if (customerInfo != null) {
      map['customer_info'] = customerInfo?.toJson();
    }
    if (listOrders != null) {
      map['list_orders'] = listOrders?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}

class ListOrders {
  ListOrders({
    this.id,
    this.orderID,
    this.earliestDeliveryDate,
    this.lastestDeliveryDate,
    this.orderItems,
  });

  ListOrders.fromJson(dynamic json) {
    id = json['ID'];
    orderID = json['orderID'];
    earliestDeliveryDate = json['earliest_delivery_date'];
    lastestDeliveryDate = json['lastest_delivery_date'];
    if (json['order_items'] != null) {
      orderItems = [];
      json['order_items'].forEach((v) {
        orderItems?.add(OrderItems.fromJson(v));
      });
    }
  }

  int? id;
  int? orderID;
  String? earliestDeliveryDate;
  String? lastestDeliveryDate;
  List<OrderItems>? orderItems;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['ID'] = id;
    map['orderID'] = orderID;
    map['earliest_delivery_date'] = earliestDeliveryDate;
    map['lastest_delivery_date'] = lastestDeliveryDate;
    if (orderItems != null) {
      map['order_items'] = orderItems?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}

class OrderItems {
  OrderItems({
    this.name,
    this.quantity,
  });

  OrderItems.fromJson(dynamic json) {
    name = json['name'];
    quantity = json['quantity'];
  }

  String? name;
  int? quantity;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['name'] = name;
    map['quantity'] = quantity;
    return map;
  }
}
