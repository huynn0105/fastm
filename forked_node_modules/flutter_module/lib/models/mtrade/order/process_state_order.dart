/// name : "Hạn cập nhật"
/// expired : "2023-04-10 10:15:07"

class ProcessStateOrder {
  ProcessStateOrder({
    this.name,
    this.expired,
  });

  ProcessStateOrder.fromJson(dynamic json) {
    name = json['name'];
    expired = json['expired'];
  }

  String? name;
  String? expired;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['name'] = name;
    map['expired'] = expired;
    return map;
  }
}
