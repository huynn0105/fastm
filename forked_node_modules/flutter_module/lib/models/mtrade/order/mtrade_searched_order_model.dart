import 'order_history_model.dart';
import 'order_model.dart';

class MTradeSearchOrderModel {
  MTradeSearchOrderModel({
    this.histories,
    this.orderDetail,
  });

  MTradeSearchOrderModel.fromJson(dynamic json) {
    if (json['history'] != null) {
      histories = [];
      json['history'].forEach((v) {
        histories?.add(OrderHistoryModel.fromJson(v));
      });
    }
    orderDetail = json['orderDetail'] != null ? OrderModel.fromJson(json['orderDetail']) : null;
  }

  List<OrderHistoryModel>? histories;
  OrderModel? orderDetail;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (histories != null) {
      map['data'] = histories?.map((v) => v.toJson()).toList();
    }
    if (orderDetail != null) {
      map['orderDetail'] = orderDetail?.toJson();
    }
    return map;
  }
}
