class PaymentMethodModel {
  PaymentMethodModel({
    this.label,
    this.desc,
    this.paymentFee,
    this.paymentTotal,
    this.icon,
    this.paymentMethod,
  });

  PaymentMethodModel.fromJson(dynamic json) {
    label = json['label'];
    desc = json['desc'];
    paymentFee = json['payment_fee'];
    paymentTotal = json['payment_total'];
    icon = json['icon'];
    paymentMethod = json['payment_method'];
  }

  String? label;
  String? desc;
  String? paymentFee;
  String? paymentTotal;
  String? icon;
  String? paymentMethod;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['label'] = label;
    map['desc'] = desc;
    map['payment_fee'] = paymentFee;
    map['payment_total'] = paymentTotal;
    map['icon'] = icon;
    map['payment_method'] = paymentMethod;
    return map;
  }
}
