import 'package:flutter_module/models/mtrade/order/payment_detai_model.dart';

/// orderID : "02032023AYFG5W"
/// payment_url : "https://appay-rc.cloudcms.vn/payment?accessToken=fa95b1064bf85fb3e57c8d3734d304287a391545&orderID=02032023AYFG5W"

class MTradePayNowOrderModel {
  MTradePayNowOrderModel({
    this.orderID,
    this.paymentUrl,
    this.sendPaymentUrl,
    this.paymentStatus,
    this.paymentNote,
    this.invalidSku,
    this.paymentDetail,
  });

  MTradePayNowOrderModel.fromJson(dynamic json) {
    orderID = json['orderID'];
    paymentUrl = json['payment_url'];
    sendPaymentUrl = json['send_payment_url'];
    paymentStatus = json['payment_status'];
    paymentNote = json['payment_note'];
    invalidSku = json['invalid_sku'] != null ? json['invalid_sku'].cast<String>() : [];
    paymentDetail = json['payment_detail'] != null ? PaymentDetailModel.fromJson(json['payment_detail']) : null;
  }

  String? orderID;
  String? paymentUrl;
  String? sendPaymentUrl;
  String? paymentStatus;
  String? paymentNote;
  List<String>? invalidSku;
  PaymentDetailModel? paymentDetail;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['orderID'] = orderID;
    map['payment_url'] = paymentUrl;
    map['send_payment_url'] = sendPaymentUrl;
    map['payment_status'] = paymentStatus;
    map['payment_note'] = paymentNote;
    map['invalid_sku'] = invalidSku;
    if (paymentDetail != null) {
      map['paymentDetail'] = paymentDetail?.toJson();
    }
    return map;
  }
}
