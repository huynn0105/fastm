class OrderPaymentMethodModel {
  OrderPaymentMethodModel({
    this.alias,
    this.label,
    this.value,
  });

  OrderPaymentMethodModel.fromJson(dynamic json) {
    alias = json['alias'];
    label = json['label'];
    value = json['value'];
  }

  String? alias;
  String? label;
  String? value;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['alias'] = alias;
    map['label'] = label;
    map['value'] = value;
    return map;
  }
}

