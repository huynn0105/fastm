import 'package:flutter_module/common/utils/text_util.dart';

/// color : "#221db0"
/// count : 3
/// label : "<b style=\"color: #6b6b81\">Chỉ vào xem</b>, không để lại thông tin"
/// unit : "người"
/// percent : 100

class StatisticModel {
  StatisticModel({
    this.icon,
    this.color,
    this.count,
    this.label,
    this.unit,
    this.percent,
  });

  StatisticModel.fromJson(dynamic json) {
    icon = json['icon'];
    color = json['color'];
    count = TextUtils.parseInt(json['count']);
    label = json['label'];
    unit = json['unit'];
    percent = TextUtils.parseDouble(json['percent']);
  }

  String? icon;
  String? color;
  int? count;
  String? label;
  String? unit;
  double? percent;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['icon'] = icon;
    map['color'] = color;
    map['count'] = count;
    map['label'] = label;
    map['unit'] = unit;
    map['percent'] = percent;
    return map;
  }
}
