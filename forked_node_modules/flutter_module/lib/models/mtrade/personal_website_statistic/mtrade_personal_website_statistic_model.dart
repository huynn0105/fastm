class MTradePersonalWebsiteStatisticModel {
  MTradePersonalWebsiteStatisticModel({
    this.statistics,
    this.details,
    this.total,
  });

  MTradePersonalWebsiteStatisticModel.fromJson(dynamic json) {
    if (json['statistics'] != null) {
      statistics = [];
      json['statistics'].forEach((v) {
        statistics?.add(StatisticModel.fromJson(v));
      });
    }
    if (json['details'] != null) {
      details = [];
      json['details'].forEach((v) {
        details?.add(StatisticModel.fromJson(v));
      });
    }
    total = json['total'] != null ? StatisticModel.fromJson(json['total']) : null;
  }

  List<StatisticModel>? statistics;
  List<StatisticModel>? details;
  StatisticModel? total;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (statistics != null) {
      map['statistics'] = statistics?.map((v) => v.toJson()).toList();
    }
    if (details != null) {
      map['details'] = details?.map((v) => v.toJson()).toList();
    }
    if (total != null) {
      map['total'] = total?.toJson();
    }
    return map;
  }
}
