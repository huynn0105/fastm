class ToolsModel {
  ToolsModel({
    this.action,
    this.keyword,
    this.contents,
  });

  ToolsModel.fromJson(dynamic json) {
    action = json['action'];
    keyword = json['keyword'];
    if (json['contents'] != null) {
      contents = [];
      json['contents'].forEach((v) {
        contents?.add(ContentModel.fromJson(v));
      });
    }
  }

  String? action;
  String? keyword;
  List<ContentModel>? contents;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['action'] = action;
    map['keyword'] = keyword;
    if (contents != null) {
      map['contents'] = contents?.map((v) => v.toJson()).toList();
    }
    return map;
  }
