class MTradeToolsModel {
  MTradeToolsModel({
    this.tools,
    this.banners,
  });

  MTradeToolsModel.fromJson(dynamic json) {
    if (json['tools'] != null) {
      tools = [];
      json['tools'].forEach((v) {
        tools?.add(ToolsModel.fromJson(v));
      });
    }
    banners = json['banners'] != null ? json['banners'].cast<String>() : [];
  }

  List<ToolsModel>? tools;
  List<String>? banners;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (tools != null) {
      map['tools'] = tools?.map((v) => v.toJson()).toList();
    }
    map['banners'] = banners;
    return map;
  }
}
