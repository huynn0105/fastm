class CampaignModel {
  CampaignModel({
    this.action,
    this.content,
    this.created,
    this.end,
    this.icon,
    this.id,
    this.start,
    this.title,
    this.type,
  });

  CampaignModel.fromJson(dynamic json) {
    action = json['action'];
    content = json['content'];
    created = json['created'];
    end = json['end'];
    icon = json['icon'];
    id = json['id'];
    start = json['start'];
    title = json['title'];
    type = json['type'];
  }

  String? action;
  String? content;
  String? created;
  String? end;
  String? icon;
  int? id;
  String? start;
  String? title;
  int? type;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['action'] = action;
    map['content'] = content;
    map['created'] = created;
    map['end'] = end;
    map['icon'] = icon;
    map['id'] = id;
    map['start'] = start;
    map['title'] = title;
    map['type'] = type;
    return map;
  }
}
