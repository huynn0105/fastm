class MTradeMyReviewModel {
  int? total;
  List<MyReviewData>? list;

  MTradeMyReviewModel({this.total, this.list});

  MTradeMyReviewModel.fromJson(Map<String, dynamic> json) {
    total = json['total'];
    if (json['list'] != null) {
      list = <MyReviewData>[];
      json['list'].forEach((v) {
        list!.add(MyReviewData.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['total'] = total;
    if (list != null) {
      data['list'] = list!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

class MyReviewData {
  String? title;
  int? rate;
  String? note;
  String? createdDate;
  String? fullName;

  MyReviewData({this.title, this.rate, this.note, this.createdDate, this.fullName});

  MyReviewData.fromJson(Map<String, dynamic> json) {
    title = json['title'];
    rate = json['rate'];
    note = json['note'];
    createdDate = json['createdDate'];
    fullName = json['fullName'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['title'] = title;
    data['rate'] = rate;
    data['note'] = note;
    data['createdDate'] = createdDate;
    data['fullName'] = fullName;
    return data;
  }
}
