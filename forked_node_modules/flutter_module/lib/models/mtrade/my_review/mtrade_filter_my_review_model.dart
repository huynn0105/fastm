class MTradeFilterMyReviewModel {
  SumaryMyReview? sumaryData;
  List<FilterMyReview>? filters;

  MTradeFilterMyReviewModel({this.sumaryData, this.filters});

  MTradeFilterMyReviewModel.fromJson(Map<String, dynamic> json) {
    sumaryData = json['sumary_data'] != null ? SumaryMyReview.fromJson(json['sumary_data']) : null;
    if (json['filters'] != null) {
      filters = <FilterMyReview>[];
      json['filters'].forEach((v) {
        filters!.add(FilterMyReview.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (sumaryData != null) {
      data['sumary_data'] = sumaryData!.toJson();
    }
    if (filters != null) {
      data['filters'] = filters!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

class SumaryMyReview {
  String? averageRate;
  String? rateCount;
  RateList? rateList;

  SumaryMyReview({this.averageRate, this.rateCount, this.rateList});

  SumaryMyReview.fromJson(Map<String, dynamic> json) {
    averageRate = json['average_rate'];
    rateCount = json['rate_count'];
    rateList = json['rate_list'] != null ? RateList.fromJson(json['rate_list']) : null;
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['average_rate'] = averageRate;
    data['rate_count'] = rateCount;
    if (rateList != null) {
      data['rate_list'] = rateList!.toJson();
    }
    return data;
  }
}

class RateList {
  String? s1;
  String? s2;
  String? s3;
  String? s4;
  String? s5;

  RateList({this.s1, this.s2, this.s3, this.s4, this.s5});

  RateList.fromJson(Map<String, dynamic> json) {
    s1 = json['1'];
    s2 = json['2'];
    s3 = json['3'];
    s4 = json['4'];
    s5 = json['5'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['1'] = s1;
    data['2'] = s2;
    data['3'] = s3;
    data['4'] = s4;
    data['5'] = s5;
    return data;
  }
}

class FilterMyReview {
  String? name;
  Options? options;

  FilterMyReview({this.name, this.options});

  FilterMyReview.fromJson(Map<String, dynamic> json) {
    name = json['name'];
    options = json['options'] != null ? Options.fromJson(json['options']) : null;
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['name'] = name;
    if (options != null) {
      data['options'] = options!.toJson();
    }
    return data;
  }
}

class Options {
  String? all;
  String? predsa;
  String? deWash;
  String? tsa;
  String? s1;
  String? s2;
  String? s3;
  String? s4;
  String? s5;

  Options({this.all, this.predsa, this.deWash, this.tsa, this.s1, this.s2, this.s3, this.s4, this.s5});

  Options.fromJson(Map<String, dynamic> json) {
    all = json['all'];
    predsa = json['predsa'];
    deWash = json['de_wash'];
    tsa = json['tsa'];
    s1 = json['1'];
    s2 = json['2'];
    s3 = json['3'];
    s4 = json['4'];
    s5 = json['5'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['all'] = all;
    if (predsa?.isNotEmpty ?? false) {
      data['predsa'] = predsa;
    }
    if (deWash?.isNotEmpty ?? false) {
      data['de_wash'] = deWash;
    }
    if (tsa?.isNotEmpty ?? false) {
      data['tsa'] = tsa;
    }
    if (s1?.isNotEmpty ?? false) {
      data['1'] = s1;
    }
    if (s2?.isNotEmpty ?? false) {
      data['2'] = s2;
    }
    if (s3?.isNotEmpty ?? false) {
      data['3'] = s3;
    }
    if (s4?.isNotEmpty ?? false) {
      data['4'] = s4;
    }
    if (s5?.isNotEmpty ?? false) {
      data['5'] = s5;
    }
    return data;
  }
}
