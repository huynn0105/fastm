class DPDItemModel {
  String? icon;
  String? value;
  String? color;
  String? unit;
  String? title;
  String? path;
  List<dynamic>? contest;

  DPDItemModel({
    this.icon,
    this.value,
    this.color,
    this.unit,
    this.title,
    this.path,
    this.contest,
  });

  DPDItemModel.fromJson(Map<String, dynamic> json) {
    icon = json["icon"];
    value = json["value"];
    color = json["color"];
    unit = json["unit"];
    title = json["title"];
    path = json["path"];
    contest = json["contest"] ?? [];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["icon"] = icon;
    data["value"] = value;
    data["color"] = color;
    data["unit"] = unit;
    data["title"] = title;
    data["path"] = path;
    if (contest != null) {
      data["contest"] = contest;
    }
    return data;
  }
}
