/// alias : "tip"
/// name : "Mẹo cho bạn"
/// iconUrl : "https://appay-rc.cloudcms.vn/assets/img/main_group/tip.png"

class MainGroupModel {
  MainGroupModel({
    this.alias,
    this.name,
    this.iconUrl,
  });

  MainGroupModel.fromJson(dynamic json) {
    alias = json['alias'];
    name = json['name'];
    iconUrl = json['iconUrl'];
  }

  String? alias;
  String? name;
  String? iconUrl;

  MainGroupModel copyWith({
    String? alias,
    String? name,
    String? iconUrl,
  }) {
    return MainGroupModel(
      alias: alias ?? this.alias,
      name: name ?? this.name,
      iconUrl: iconUrl ?? this.iconUrl,
    );
  }

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['alias'] = alias;
    map['name'] = name;
    map['iconUrl'] = iconUrl;
    return map;
  }
}

class PositionMainGroupModel {
  PositionMainGroupModel({
    required this.alias,
    this.sizeH = 0.0,
    this.startPosition = 0.0,
  });

  String alias;
  double sizeH;
  double startPosition;

  @override
  String toString() {
    return '{alias: $alias, position: $startPosition, sizeH: $sizeH}';
  }
}
