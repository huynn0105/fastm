import 'package:flutter_module/models/home_data/dpd_data_model.dart';

class DPDModel {
  DPDDataModel? data;
  DPDDataModel? dataNew;

  DPDModel({this.data, this.dataNew});

  DPDModel.fromJson(Map<String, dynamic> json) {
    data = json["data"] == null ? null : DPDDataModel.fromJson(json["data"]);
    dataNew = json["data_new"] == null ? null : DPDDataModel.fromJson(json["data_new"]);
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (this.data != null) {
      data["data"] = this.data?.toJson();
    }
    if (dataNew != null) {
      data["data_new"] = dataNew?.toJson();
    }
    return data;
  }
}
