import 'package:flutter_module/models/home_data/home_data_model.dart';
import 'package:flutter_module/models/home_data/main_group_model.dart';

class HomePageLayoutModel {
  HomePageLayoutModel({
    this.home,
    this.group,
  });

  HomePageLayoutModel.fromJson(dynamic json) {
    if (json['data'] != null) {
      home = [];
      json['data'].forEach((v) {
        home?.add(HomeDataModel.fromJson(v));
      });
    }
    if (json['main_group'] != null) {
      group = [];
      json['main_group'].forEach((v) {
        group?.add(MainGroupModel.fromJson(v));
      });
    }
  }

  List<HomeDataModel>? home;
  List<MainGroupModel>? group;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    if (home != null) {
      map['data'] = home?.map((v) => v.toJson()).toList();
    }
    if (group != null) {
      map['main_group'] = group?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}
