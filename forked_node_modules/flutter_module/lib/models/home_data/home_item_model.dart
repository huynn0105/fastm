import 'package:flutter_module/common/utils/text_util.dart';

import 'extra_config_model.dart';

/// url_title : "MCredit"
/// title : "MCredit"
/// icon : "https://appay-rc.cloudcms.vn/mfast_assets/img/project_logo/icons/mc.png"
/// url : "https://appay-rc.cloudcms.vn/finance/Mcredit?non_pl_id=376"
/// disable : false
/// sup_group_id : "1"
/// badge : ""
/// badge_color : ""
/// icon_opacity : ""
/// highlight : false
/// star : false
/// tag_name : "MCREDIT"
/// extra_config : null

class HomeItemModel {
  HomeItemModel({
    this.id,
    this.urlTitle,
    this.title,
    this.icon,
    this.url,
    this.disable,
    this.supGroupId,
    this.badge,
    this.badgeColor,
    this.iconOpacity,
    this.highlight,
    this.star,
    this.tagName,
    this.extraConfig,
  });

  HomeItemModel.fromJson(dynamic json) {
    id = json['ID']?.toString();
    urlTitle = json['url_title'];
    title = json['title'];
    icon = json['icon'];
    url = json['url'];
    disable = json["disable"]?.toString() == 'true';
    supGroupId = json['sup_group_id'];
    badge = json['badge'];
    badgeColor = json['badge_color'];
    iconOpacity = TextUtils.parseDouble(json['icon_opacity']);
    highlight = json['highlight']?.toString() == 'true' || json['highlight']?.toString() == '1';
    star = json['star'];
    tagName = json['tag_name'];
    extraConfig = (json['extra_config'] != null && json['extra_config'] is Map)
        ? ExtraConfigModel.fromJson(json['extra_config'])
        : null;
  }

  String? id;
  String? urlTitle;
  String? title;
  String? icon;
  String? url;
  bool? disable;
  String? supGroupId;
  String? badge;
  String? badgeColor;
  double? iconOpacity;
  bool? highlight;
  bool? star;
  String? tagName;
  ExtraConfigModel? extraConfig;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['ID'] = id;
    map['url_title'] = urlTitle;
    map['title'] = title;
    map['icon'] = icon;
    map['url'] = url;
    map['disable'] = disable;
    map['sup_group_id'] = supGroupId;
    map['badge'] = badge;
    map['badge_color'] = badgeColor;
    map['icon_opacity'] = iconOpacity;
    map['highlight'] = highlight;
    map['star'] = star;
    map['tag_name'] = tagName;
    map['extra_config'] = extraConfig;
    return map;
  }
}
