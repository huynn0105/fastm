/// ID : "1"
/// name : "Tiền mặt"

class SupGroupModel {
  SupGroupModel({
    this.id,
    this.name,
  });

  SupGroupModel.fromJson(dynamic json) {
    id = json['ID'];
    name = json['name'];
  }

  String? id;
  String? name;
  SupGroupModel copyWith({
    String? id,
    String? name,
  }) {
    return SupGroupModel(
      id: id ?? this.id,
      name: name ?? this.name,
    );
  }

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['ID'] = id;
    map['name'] = name;
    return map;
  }
}
