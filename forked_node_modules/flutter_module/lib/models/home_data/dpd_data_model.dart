import 'package:flutter_module/models/home_data/dpd_group_model.dart';

class DPDDataModel {
  DPDGroupModel? finance;
  DPDGroupModel? bank;

  DPDDataModel({this.finance, this.bank});

  DPDDataModel.fromJson(Map<String, dynamic> json) {
    finance = json["finance"] == null ? null : DPDGroupModel.fromJson(json["finance"]);
    bank = json["bank"] == null ? null : DPDGroupModel.fromJson(json["bank"]);
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (finance != null) {
      data["finance"] = finance?.toJson();
    }
    if (bank != null) {
      data["bank"] = bank?.toJson();
    }
    return data;
  }
}
