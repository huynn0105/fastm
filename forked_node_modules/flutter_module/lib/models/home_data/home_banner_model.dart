class HomeBannerModel {
  String? newsId;
  String? newsTitle;
  String? newsDescription;
  String? newsUrl;
  String? newsImage;
  String? eventStart;

  HomeBannerModel({
    this.newsId,
    this.newsTitle,
    this.newsDescription,
    this.newsUrl,
    this.newsImage,
    this.eventStart,
  });

  HomeBannerModel.fromJson(Map<String, dynamic> json) {
    newsId = json["newsID"];
    newsTitle = json["newsTitle"];
    newsDescription = json["newsDescription"];
    newsUrl = json["newsURL"];
    newsImage = json["newsImage"];
    eventStart = json["eventStart"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["newsID"] = newsId;
    data["newsTitle"] = newsTitle;
    data["newsDescription"] = newsDescription;
    data["newsURL"] = newsUrl;
    data["newsImage"] = newsImage;
    data["eventStart"] = eventStart;
    return data;
  }
}
