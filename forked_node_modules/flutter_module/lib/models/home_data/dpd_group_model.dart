import 'package:flutter_module/models/home_data/dpd_item_model.dart';

class DPDGroupModel {
  String? title;
  List<DPDItemModel>? items;

  DPDGroupModel({this.title, this.items});

  DPDGroupModel.fromJson(Map<String, dynamic> json) {
    title = json["title"];
    items = json["items"] == null ? null : (json["items"] as List).map((e) => DPDItemModel.fromJson(e)).toList();
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data["title"] = title;
    if (items != null) {
      data["items"] = items?.map((e) => e.toJson()).toList();
    }
    return data;
  }
}
