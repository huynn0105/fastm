class MFastNotificationSettingModel {
  String? iD;
  String? name;
  String? description;
  bool? subsStatus;

  MFastNotificationSettingModel({this.iD, this.name, this.description, this.subsStatus});

  MFastNotificationSettingModel.fromJson(Map<String, dynamic> json) {
    iD = json['ID'];
    name = json['name'];
    description = json['description'];
    subsStatus = json['subs_status'] == '1';
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['ID'] = iD;
    data['name'] = name;
    data['description'] = description;
    data['subs_status'] = subsStatus == true ? "1" : '0';
    return data;
  }

  MFastNotificationSettingModel copyWith({
    String? iD,
    String? name,
    String? description,
    bool? subsStatus,
  }) {
    return MFastNotificationSettingModel(
      iD: iD ?? this.iD,
      name: name ?? this.name,
      description: description ?? this.description,
      subsStatus: subsStatus ?? this.subsStatus,
    );
  }
}
