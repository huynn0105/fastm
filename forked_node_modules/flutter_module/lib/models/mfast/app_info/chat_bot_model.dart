class ChatBotModel {
  String? id;
  String? title;
  String? image;
  bool? showChatBot;
  String? type;

  ChatBotModel({
    this.id,
    this.title,
    this.image,
    this.showChatBot,
    this.type,
  });

  ChatBotModel.fromJson(Map<String, dynamic> json) {
    id = json["id"];
    title = json["title"];
    image = json["image"];
    showChatBot = json["showChatBot"];
    type = json["type"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["id"] = id;
    map["title"] = title;
    map["image"] = image;
    map["showChatBot"] = showChatBot;
    map["type"] = type;
    return map;
  }
}
