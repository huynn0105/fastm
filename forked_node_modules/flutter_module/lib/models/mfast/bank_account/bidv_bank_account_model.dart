class MFastBIDVBankAccountModel {
  String? fullName;
  String? bankName;
  String? bankBranch;
  String? image;
  BankData? bankData;

  MFastBIDVBankAccountModel({this.fullName, this.bankName, this.bankBranch, this.image, this.bankData});

  MFastBIDVBankAccountModel.fromJson(Map<String, dynamic> json) {
    fullName = json['fullName'];
    bankName = json['bank_name'];
    bankBranch = json['bank_branch'];
    image = json['image'];
    bankData = json['bank_data'] != null ? BankData.fromJson(json['bank_data']) : null;
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['fullName'] = fullName;
    data['bank_name'] = bankName;
    data['bank_branch'] = bankBranch;
    data['image'] = image;
    if (bankData != null) {
      data['bank_data'] = bankData!.toJson();
    }
    return data;
  }
}

class BankData {
  String? iD;
  String? branchName;
  String? branchCode;
  String? bankName;
  String? govBIN;
  String? status;

  BankData({this.iD, this.branchName, this.branchCode, this.bankName, this.govBIN, this.status});

  BankData.fromJson(Map<String, dynamic> json) {
    iD = json['ID'];
    branchName = json['branchName'];
    branchCode = json['branchCode'];
    bankName = json['bankName'];
    govBIN = json['govBIN'];
    status = json['status'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['ID'] = iD;
    data['branchName'] = branchName;
    data['branchCode'] = branchCode;
    data['bankName'] = bankName;
    data['govBIN'] = govBIN;
    data['status'] = status;
    return data;
  }
}
