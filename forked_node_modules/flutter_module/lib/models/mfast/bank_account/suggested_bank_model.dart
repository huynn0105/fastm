/// commission : {"phase_1":"55k","phase_2":"130k"}

class MFastSuggestedBankModel {
  MFastSuggestedBankModel({
    this.title,
    this.desc,
    this.url,
    this.urlIntro,
    this.urlBonus,
    this.descHtml,
    this.image,
    this.commission,
  });

  MFastSuggestedBankModel.fromJson(dynamic json) {
    title = json['title'];
    desc = json['desc'];
    url = json['url'];
    urlIntro = json['url_intro'];
    urlBonus = json['url_bonus'];
    descHtml = json['desc_html'];
    image = json['image'];
    commission = json['commission'] != null ? CommissionModel.fromJson(json['commission']) : null;
  }

  String? title;
  String? desc;
  String? url;
  String? urlIntro;
  String? urlBonus;
  String? descHtml;
  String? image;
  CommissionModel? commission;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['title'] = title;
    map['desc'] = desc;
    map['url'] = url;
    map['url_intro'] = urlIntro;
    map['url_bonus'] = urlBonus;
    map['desc_html'] = descHtml;
    map['image'] = image;
    if (commission != null) {
      map['commission'] = commission?.toJson();
    }
    return map;
  }
}

/// phase_1 : "55k"
/// phase_2 : "130k"

class CommissionModel {
  CommissionModel({
    this.phase1,
    this.phase2,
  });

  CommissionModel.fromJson(dynamic json) {
    phase1 = json['phase_1'];
    phase2 = json['phase_2'];
  }

  String? phase1;
  String? phase2;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['phase_1'] = phase1;
    map['phase_2'] = phase2;
    return map;
  }
}
