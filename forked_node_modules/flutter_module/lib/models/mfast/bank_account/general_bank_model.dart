/// bankName : "BANK OF CHINA"

class MFastGeneralBankModel {
  MFastGeneralBankModel({
    this.bankName,
  });

  MFastGeneralBankModel.fromJson(dynamic json) {
    bankName = json['bankName'];
  }

  String? bankName;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['bankName'] = bankName;
    return map;
  }
}
