class TimeCheckingSubmitPayload {
  String? image;
  String? lat;
  String? lon;
  String? type;

  TimeCheckingSubmitPayload.fromJson({
    this.image,
    this.lat,
    this.lon,
    this.type,
  });

  Map<String, dynamic> toJson() {
    return {
      'image': image,
      'lat': lat,
      'lon': lon,
      'type': type,
    };
  }
}
