class TimeCheckingConfigModel {
  int? totalReport;
  CheckConfigDataModel? checkConfig;
  bool? isRequired;

  TimeCheckingConfigModel({
    this.totalReport,
    this.checkConfig,
    this.isRequired,
  });

  TimeCheckingConfigModel.fromJson(Map<String, dynamic> json) {
    totalReport = json["totalReport"];
    checkConfig = json["checkConfig"] == null ? null : CheckConfigDataModel.fromJson(json["checkConfig"]);
    isRequired = json["isRequired"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["totalReport"] = totalReport;
    if (checkConfig != null) {
      map["checkConfig"] = checkConfig?.toJson();
    }
    map["isRequired"] = isRequired;
    return map;
  }
}

class CheckConfigDataModel {
  String? startTime;
  String? endTime;
  String? desc;
  bool? checkStatus;
  String? type;
  String? title;
  int? duration;

  CheckConfigDataModel({
    this.startTime,
    this.endTime,
    this.desc,
    this.checkStatus,
    this.type,
    this.title,
    this.duration,
  });

  CheckConfigDataModel.fromJson(Map<String, dynamic> json) {
    startTime = json["startTime"];
    endTime = json["endTime"];
    desc = json["desc"];
    checkStatus = json["checkStatus"];
    type = json["type"];
    title = json["title"];
    duration = json["duration"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["startTime"] = startTime;
    map["endTime"] = endTime;
    map["desc"] = desc;
    map["checkStatus"] = checkStatus;
    map["type"] = type;
    map["title"] = title;
    map["duration"] = duration;
    return map;
  }
}
