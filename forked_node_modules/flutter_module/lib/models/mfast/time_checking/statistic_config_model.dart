class StatisticConfigModel {
  String? userId;
  String? nickName;
  String? fullName;
  String? image;
  String? level;
  String? mobilePhone;

  StatisticConfigModel({
    this.userId,
    this.nickName,
    this.fullName,
    this.image,
    this.level,
    this.mobilePhone,
  });

  StatisticConfigModel.fromJson(Map<String, dynamic> json) {
    userId = json["userID"];
    nickName = json["nickName"];
    fullName = json["fullName"];
    image = json["image"];
    level = json["level"];
    mobilePhone = json["mobilePhone"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["userID"] = userId;
    map["nickName"] = nickName;
    map["fullName"] = fullName;
    map["image"] = image;
    map["level"] = level;
    map["mobilePhone"] = mobilePhone;
    return map;
  }
}
