import 'package:flutter_module/common/utils/text_util.dart';

class ReportSubmitResponseModel {
  final double totalIncome;
  final double estKpi;
  final String? note;

  ReportSubmitResponseModel({
    required this.totalIncome,
    required this.estKpi,
    this.note,
  });

  Map<String, dynamic> toJson() {
    return {
      'totalIncome': totalIncome,
      'estKpi': estKpi,
      'note': note,
    };
  }

  factory ReportSubmitResponseModel.fromJson(Map<String, dynamic> map) {
    return ReportSubmitResponseModel(
      totalIncome: TextUtils.parseDouble(map['totalIncome']) ?? 0,
      estKpi: TextUtils.parseDouble(map['estKpi']) ?? 0,
      note: map['note'],
    );
  }
}
