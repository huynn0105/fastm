
class ReportSubmitModel {
  final bool isFinalSubmit;
  final List<ReportDataModel> reportData;
  final String? reportID;

  ReportSubmitModel({
    required this.isFinalSubmit,
    required this.reportData,
    this.reportID,

  });

  Map<String, dynamic> toJson() {
    return {
      'isFinalSubmit': isFinalSubmit,
      'reportID': reportID,
      'reportData': reportData.map((x) => x.toJson()).toList(),

    };
  }

  factory ReportSubmitModel.fromMap(Map<String, dynamic> map) {
    return ReportSubmitModel(
      isFinalSubmit: map['isFinalSubmit'] ?? false,
      reportID: map['reportID'],
      reportData: List<ReportDataModel>.from(map['reportData']?.map((x) => ReportDataModel.fromJson(x))),

    );
  }
}

class ReportDataModel {
  final String metaID;
  final String projectID;
  final String groupID;
  final String value;
  final String? logID;

  ReportDataModel({
    required this.metaID,
    required this.projectID,
    required this.groupID,
    required this.value,
    this.logID,
  });

  Map<String, dynamic> toJson() {
    return {
      'metaID': metaID,
      'projectID': projectID,
      'groupID': groupID,
      'value': value,
      'logID': logID,
    };
  }

  factory ReportDataModel.fromJson(Map<String, dynamic> map) {
    return ReportDataModel(
      metaID: map['metaID'] ?? '',
      projectID: map['projectID']?? '',
      groupID: map['groupID'] ?? '',
      value: map['value'] ?? '',
      logID: map['logID'],
    );
  }
}
