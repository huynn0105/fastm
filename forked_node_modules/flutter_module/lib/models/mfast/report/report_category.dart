
import 'package:flutter_module/models/mfast/report/report_model.dart';

import 'filter_report_model.dart';

class ReportCategory {
  final List<ReportModel> reports;
  final List<FilterReportModel>? filterReportModel;

  ReportCategory({
    required this.reports,
    required this.filterReportModel,
  });

  factory ReportCategory.fromJson(Map<String, dynamic> map) {
    return ReportCategory(
      reports: List<ReportModel>.from(map['projects']?.map((x) => ReportModel.fromJson(x))),
      filterReportModel: map['filterTwoLevel'] != null
          ? List<FilterReportModel>.from(map['filterTwoLevel']?.map((x) => FilterReportModel.fromJson(x)))
          : null,
    );
  }
}
