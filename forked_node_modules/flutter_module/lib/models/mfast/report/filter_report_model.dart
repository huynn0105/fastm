import 'package:flutter_module/common/bottom_sheet/wrapper/data_wrapper.dart';

class FilterReportModel {
  String? id;
  String? title;
  List<FilterReportModel>? items;

  FilterReportModel({
    this.id,
    this.title,
    this.items,
  });

  FilterReportModel.fromJson(Map<String, dynamic> json) {
    id = json["id"];
    title = json["title"];
    items = json["sub"] == null ? null : (json["sub"] as List).map((e) => FilterReportModel.fromJson(e)).toList();
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["id"] = id;
    map["title"] = title;
    if (items != null) {
      map["items"] = items?.map((e) => e.toJson()).toList();
    }
    return map;
  }

  DataWrapper toDataWrapper() {
    return DataWrapper(
      id: id,
      value: title,
    );
  }
}
