/// description : "Desc modify"
/// avatar : "https://appay-rc.cloudcms.vn/uploads/academy/speaker/1685004661/avatar_1685004661.png"

class SpeakerModel {
  SpeakerModel({
    this.name,
    this.description,
    this.avatar,
  });

  SpeakerModel.fromJson(dynamic json) {
    name = json['name'];
    description = json['description'];
    avatar = json['avatar'];
  }

  String? name;
  String? description;
  String? avatar;

  SpeakerModel copyWith({
    String? name,
    String? description,
    String? avatar,
  }) =>
      SpeakerModel(
        name: name ?? this.name,
        description: description ?? this.description,
        avatar: avatar ?? this.avatar,
      );

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['name'] = name;
    map['description'] = description;
    map['avatar'] = avatar;
    return map;
  }
}
