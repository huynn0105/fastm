class AcademyRegisteredEventModel {
  String? eventName;
  String? timeLine;
  String? location;
  String? link;
  String? eventID;
  String? eventStart;
  bool? register;
  bool? checkin;
  bool? checkout;

  AcademyRegisteredEventModel(
      {this.eventName, this.timeLine, this.location, this.link, this.eventID, this.checkin, this.checkout});

  AcademyRegisteredEventModel.fromJson(Map<String, dynamic> json) {
    eventName = json['eventName'];
    timeLine = json['timeLine'];
    location = json['location'];
    link = json['link'];
    eventID = json['eventID'];
    eventStart = json['eventStart'];
    register = json['register'];
    checkin = json['checkin'];
    checkout = json['checkout'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['eventName'] = eventName;
    data['timeLine'] = timeLine;
    data['location'] = location;
    data['link'] = link;
    data['eventID'] = eventID;
    data['eventStart'] = eventStart;
    data['register'] = register;
    data['checkin'] = checkin;
    data['checkout'] = checkout;
    return data;
  }
}
