class AcademyCountRegisteredEventModel {
  String? title;
  int? total;
  String? keyword;
  String? value;

  AcademyCountRegisteredEventModel({this.title, this.total, this.keyword, this.value});

  AcademyCountRegisteredEventModel.fromJson(Map<String, dynamic> json) {
    title = json['title'];
    total = json['total'];
    keyword = json['keyword'];
    value = json['value'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['title'] = title;
    data['total'] = total;
    data['keyword'] = keyword;
    data['value'] = value;
    return data;
  }
}
