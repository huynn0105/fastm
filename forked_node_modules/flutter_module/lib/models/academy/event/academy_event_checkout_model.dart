import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/services/api/academy/payload/academy_submit_quiz_payload.dart';

class AcademyEventCheckOutModel {
  List<EventQuiz>? listQuestion;
  List<EventSupport>? listSupport;
  List<SubmitQuizAnswer>? userAnswer;
  String? questionType;
  String? questionLink;

  AcademyEventCheckOutModel({this.listQuestion, this.listSupport, this.userAnswer});

  AcademyEventCheckOutModel.fromJson(Map<String, dynamic> json) {
    if (json['listQuestion'] != null) {
      listQuestion = <EventQuiz>[];
      json['listQuestion'].forEach((v) {
        listQuestion!.add(EventQuiz.fromJson(v));
      });
    }
    if (json['listSupport'] != null) {
      listSupport = <EventSupport>[];
      json['listSupport'].forEach((v) {
        listSupport!.add(EventSupport.fromJson(v));
      });
    }
    if (json['userAnswer'] != null) {
      userAnswer = <SubmitQuizAnswer>[];
      json['userAnswer'].forEach((v) {
        userAnswer!.add(SubmitQuizAnswer.fromJson(v));
      });
    }
    questionType = json["questionType"];
    questionLink = json["questionLink"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (listQuestion != null) {
      data['listQuestion'] = listQuestion!.map((v) => v.toJson()).toList();
    }
    if (listSupport != null) {
      data['listSupport'] = listSupport!.map((v) => v.toJson()).toList();
    }
    if (userAnswer != null) {
      data['userAnswer'] = userAnswer!.map((v) => v.toJson()).toList();
    }
    return data;
  }
}

class EventQuiz {
  int? index;
  int? questionID;
  List<EventQuizAnswer>? answer;
  String? question;
  String? explain;

  EventQuiz({this.questionID, this.answer, this.question, this.explain});

  EventQuiz.fromJson(Map<String, dynamic> json) {
    index = json['index'];
    questionID = TextUtils.parseInt(json['questionID']);
    if (json['answer'] != null) {
      answer = <EventQuizAnswer>[];
      json['answer'].forEach((v) {
        answer!.add(EventQuizAnswer.fromJson(v));
      });
    }
    question = json['question'];
    explain = json['explain'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['index'] = index;
    data['questionID'] = questionID;
    if (answer != null) {
      data['answer'] = answer!.map((v) => v.toJson()).toList();
    }
    data['question'] = question;
    data['explain'] = explain;
    return data;
  }
}

class EventQuizAnswer {
  String? desc;
  bool? isAnswer;

  EventQuizAnswer({this.desc, this.isAnswer});

  EventQuizAnswer.fromJson(Map<String, dynamic> json) {
    desc = json['desc'];
    isAnswer = json['is_answer'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['desc'] = desc;
    data['is_answer'] = isAnswer;
    return data;
  }
}

class EventSupport {
  String? name;
  String? link;

  EventSupport({this.name, this.link});

  EventSupport.fromJson(Map<String, dynamic> json) {
    name = json['name'];
    link = json['link'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['name'] = name;
    data['link'] = link;
    return data;
  }
}
