import 'package:flutter_module/models/academy/event/academy_filter_event_model.dart';

class AcademyFilterEventTypeModel {
  String? title;
  String? keyword;
  List<AcademyFilterEventModel>? listFilter;
  bool? multiSelect;

  AcademyFilterEventTypeModel({
    this.title,
    this.keyword,
    this.listFilter,
    this.multiSelect,
  });

  AcademyFilterEventTypeModel.fromJson(dynamic json) {
    title = json["title"];
    keyword = json["keyword"];
    listFilter = json["list_filter"] == null
        ? null
        : (json["list_filter"] as List).map((e) => AcademyFilterEventModel.fromJson(e)).toList();
    multiSelect = json["multiSelect"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["title"] = title;
    map["keyword"] = keyword;
    if (listFilter != null) {
      map["list_filter"] = listFilter?.map((e) => e.toJson()).toList();
    }
    map["multiSelect"] = multiSelect;
    return map;
  }
}
