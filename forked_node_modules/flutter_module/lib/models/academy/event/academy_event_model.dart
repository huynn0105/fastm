class AcademyEventModel {
  String? eventName;
  String? participantAmount;
  String? timeLine;
  String? banner;
  String? location;
  String? eventID;
  bool? eventRegisterEnd;
  bool? eventInvalid;
  String? invalidMessage;
  int? validAmount;
  String? duration;
  bool? register;
  bool? checkin;
  bool? checkout;

  AcademyEventModel(
      {this.eventName,
      this.participantAmount,
      this.timeLine,
      this.banner,
      this.location,
      this.eventID,
      this.eventRegisterEnd,
      this.eventInvalid,
      this.invalidMessage,
      this.validAmount,
      this.duration,
      this.register,
      this.checkin,
      this.checkout});

  AcademyEventModel.fromJson(Map<String, dynamic> json) {
    eventName = json['eventName'];
    participantAmount = json['participantAmount'];
    timeLine = json['timeLine'];
    banner = json['banner'];
    location = json['location'];
    eventID = json['eventID'];
    eventRegisterEnd = json['eventRegisterEnd'];
    eventInvalid = json['eventInvalid'];
    invalidMessage = json['invalidMessage'];
    validAmount = json['validAmount'];
    duration = json['duration'];
    register = json['register'];
    checkin = json['checkin'];
    checkout = json['checkout'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['eventName'] = eventName;
    data['participantAmount'] = participantAmount;
    data['timeLine'] = timeLine;
    data['banner'] = banner;
    data['location'] = location;
    data['eventID'] = eventID;
    data['eventRegisterEnd'] = eventRegisterEnd;
    data['eventInvalid'] = eventInvalid;
    data['invalidMessage'] = invalidMessage;
    data['validAmount'] = validAmount;
    data['duration'] = duration;
    data['register'] = register;
    data['checkin'] = checkin;
    data['checkout'] = checkout;
    return data;
  }
}
