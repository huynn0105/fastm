class AcademyFilterEventModel {
  List<String>? value;
  String? text;
  int? count;
  bool? isDefault;

  AcademyFilterEventModel({this.value, this.text, this.count, this.isDefault});

  AcademyFilterEventModel.fromJson(Map<String, dynamic> json) {
    text = json['text'];
    count = json['count'];
    isDefault = json['isDefault'];

    if (json['value'] is String) {
      value = [json['value']];
    } else if (json['value'] is List) {
      value = json['value'].cast<String>();
    }
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    data['value'] = value;
    data['text'] = text;
    data['count'] = count;
    data['isDefault'] = isDefault;
    return data;
  }
}
