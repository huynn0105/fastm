import 'package:flutter_module/common/utils/text_util.dart';

class StatisticCheckingModel {
  String? date;
  String? userID;
  String? nickName;
  String? fullName;
  String? image;
  String? level;
  String? mobilePhone;
  String? lastCheckingTime;
  int? totalChecking;
  CheckingDetailModel? checkingDetail;

  StatisticCheckingModel({
    this.date,
    this.userID,
    this.nickName,
    this.fullName,
    this.image,
    this.level,
    this.mobilePhone,
    this.lastCheckingTime,
    this.totalChecking,
    this.checkingDetail,
  });

  StatisticCheckingModel.fromJson(Map<String, dynamic> json) {
    date = json["date"];
    userID = json["userID"]?.toString();
    nickName = json["nickName"];
    fullName = json["fullName"];
    image = json["image"];
    level = json["level"];
    mobilePhone = json["mobilePhone"];
    lastCheckingTime = json["lastChekingTime"];
    totalChecking = json["totalChecking"];
    checkingDetail = json["checkingDetail"] == null ? null : CheckingDetailModel.fromJson(json["checkingDetail"]);
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["date"] = date;
    map["userID"] = userID;
    map["nickName"] = nickName;
    map["fullName"] = fullName;
    map["image"] = image;
    map["level"] = level;
    map["mobilePhone"] = mobilePhone;
    map["lastChekingTime"] = lastCheckingTime;
    map["totalChecking"] = totalChecking;
    if (checkingDetail != null) {
      map["checkingDetail"] = checkingDetail?.toJson();
    }
    return map;
  }
}

class CheckingDetailModel {
  int? checkingTotal;
  double? avg;
  int? notCheckingAgent;
  int? checkingAgent;

  CheckingDetailModel({
    this.checkingTotal,
    this.avg,
    this.notCheckingAgent,
    this.checkingAgent,
  });

  CheckingDetailModel.fromJson(Map<String, dynamic> json) {
    checkingTotal = json["checkingTotal"];
    avg = TextUtils.parseDouble(json["avg"]);
    notCheckingAgent = json["notCheckingAgent"];
    checkingAgent = json["checkingAgent"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["checkingTotal"] = checkingTotal;
    map["avg"] = avg;
    map["notCheckingAgent"] = notCheckingAgent;
    map["checkingAgent"] = checkingAgent;
    return map;
  }
}
