import 'package:flutter_module/models/statistic_working/statistic_checking_model.dart';
import 'package:flutter_module/models/statistic_working/statistic_report_model.dart';

class SaleInfoModel {
  String? userID;
  String? fullName;
  String? level;
  String? date;

  SaleInfoModel({
    this.userID,
    this.fullName,
    this.level,
    this.date,
  });

  SaleInfoModel.fromJson(Map<String, dynamic> json) {
    userID = json["userID"];
    fullName = json["fullName"];
    level = json["level"];
    date = json["date"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["userID"] = userID;
    map["fullName"] = fullName;
    map["level"] = level;
    map["date"] = date;
    return map;
  }

  SaleInfoModel.fromStatisticChecking(StatisticCheckingModel data) {
    userID = data.userID;
    fullName = data.fullName;
    level = data.level;
    date = data.date;
  }

  SaleInfoModel.fromStatisticReport(StatisticReportModel data) {
    userID = data.userID;
    fullName = data.fullName;
    level = data.level;
    date = data.date;
  }
}
