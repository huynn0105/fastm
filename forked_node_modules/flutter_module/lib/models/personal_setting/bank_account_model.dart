import 'package:flutter/foundation.dart';

class BankAccountModel {
  String? bankingId;
  String? status;
  String? statusText;
  String? bankAccountNumber;
  String? bankAccountNumberCensor;
  String? bankName;
  String? bankBranch;
  String? idNumberIssueDate;
  String? idNumberIssueBy;
  String? bankIdNumber;
  String? bankFullName;
  String? taxNumber;
  String? selfiePath;
  String? errorMessage;
  String? createdDate;
  String? note;
  String? correct;
  bool? isDefault;
  bool? disabled;
  String? disabledMessage;
  String? icon;
  bool? bankLink;

  BankAccountModel({
    this.bankingId,
    this.status,
    this.statusText,
    this.bankAccountNumber,
    this.bankAccountNumberCensor,
    this.bankName,
    this.bankBranch,
    this.idNumberIssueDate,
    this.idNumberIssueBy,
    this.bankIdNumber,
    this.bankFullName,
    this.taxNumber,
    this.selfiePath,
    this.errorMessage,
    this.createdDate,
    this.note,
    this.correct,
    this.isDefault,
    this.disabled,
    this.disabledMessage,
    this.icon,
    this.bankLink,
  });

  BankAccountModel.fromJson(Map<String, dynamic> json) {
    bankingId = json['banking_id'];
    status = json['status'];
    statusText = json['status_text'];
    bankAccountNumber = json['bank_accountNumber'];
    bankAccountNumberCensor = json['bank_accountNumber_censor'];
    bankName = json['bank_name'];
    bankBranch = json['bank_branch'];
    idNumberIssueDate = json['idNumberIssueDate'];
    idNumberIssueBy = json['idNumberIssueBy'];
    bankIdNumber = json['bank_idNumber'];
    bankFullName = json['bank_fullName'];
    taxNumber = json['tax_number'];
    selfiePath = json['selfie_path'];
    errorMessage = json['error_message'];
    createdDate = json['created_date'];
    note = json['note'];
    correct = json['correct'];
    isDefault = json['default'] == '1';
    disabled = json['disabled'];
    disabledMessage = json['disabled_message'];
    icon = json['icon'];
    bankLink = json['bank_link'];
  }
}
