class PersonalSettingModel {
  PersonalSettingModel({
    this.title,
    this.id,
    this.note,
    this.horizontal,
    this.data,
    this.headerData,
  });

  PersonalSettingModel.fromJson(dynamic json) {
    title = json['title'];
    id = json['id'];
    note = json['note'];
    horizontal = json['horizontal'];
    if (json['data'] != null) {
      data = [];
      json['data'].forEach((v) {
        data?.add(PersonalItemModel.fromJson(v));
      });
    }
    if (json['headerData'] != null) {
      headerData = [];
      json['headerData'].forEach((v) {
        headerData?.add(PersonalItemModel.fromJson(v));
      });
    }
  }

  String? title;
  String? id;
  String? note;
  bool? horizontal;
  List<PersonalItemModel>? data;
  List<PersonalItemModel>? headerData;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['title'] = title;
    map['id'] = id;
    map['note'] = note;
    map['horizontal'] = horizontal;
    if (data != null) {
      map['data'] = data?.map((v) => v.toJson()).toList();
    }
    if (headerData != null) {
      map['headerData'] = headerData?.map((v) => v.toJson()).toList();
    }
    return map;
  }
}

