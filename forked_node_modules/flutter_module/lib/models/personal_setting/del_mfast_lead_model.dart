class DelMFastLeadModel {
  FPDModel? fpd5;
  FPDModel? fpd10;
  String? url;

  DelMFastLeadModel({
    this.fpd5,
    this.fpd10,
  });

  DelMFastLeadModel.fromJson(Map<String, dynamic> json, {required Map<String, dynamic>? raw}) {
    fpd5 = json["fpd5"] == null ? null : FPDModel.fromJson(json["fpd5"]);
    fpd10 = json["fpd10"] == null ? null : FPDModel.fromJson(json["fpd10"]);
    url = raw?["url"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    if (fpd5 != null) {
      map["fpd5"] = fpd5?.toJson();
    }
    if (fpd10 != null) {
      map["fpd10"] = fpd10?.toJson();
    }
    return map;
  }
}

class FPDModel {
  int? count;
  int? day;

  FPDModel({
    this.count,
    this.day,
  });

  FPDModel.fromJson(Map<String, dynamic> json) {
    count = json["count"];
    day = json["day"];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> map = <String, dynamic>{};
    map["count"] = count;
    map["day"] = day;
    return map;
  }
}
