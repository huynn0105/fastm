/// status : "1"

class VisibleSettingModel {
  VisibleSettingModel({
    this.userID,
    this.categoryID,
    this.name,
    this.description,
    this.status,
  });

  VisibleSettingModel.fromJson(dynamic json) {
    userID = json['userID'];
    categoryID = json['categoryID'];
    name = json['name'];
    description = json['description'];
    status = json['status']?.toString();
  }

  String? userID;
  String? categoryID;
  String? name;
  String? description;
  String? status;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['userID'] = userID;
    map['categoryID'] = categoryID;
    map['name'] = name;
    map['description'] = description;
    map['status'] = status;
    return map;
  }

  VisibleSettingModel copyWith({
    String? userID,
    String? categoryID,
    String? name,
    String? description,
    String? status,
  }) {
    return VisibleSettingModel(
      userID: userID ?? this.userID,
      categoryID: categoryID ?? this.categoryID,
      name: name ?? this.name,
      description: description ?? this.description,
      status: status ?? this.status,
    );
  }
}
