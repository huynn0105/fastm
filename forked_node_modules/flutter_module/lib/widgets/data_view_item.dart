import 'package:flutter/material.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';

class DataViewItem extends StatelessWidget {
  const DataViewItem({
    Key? key,
    required this.title,
    required this.value,
    this.titleTextStyle,
    this.valueTextStyle,
  }) : super(key: key);

  DataViewItem.highlight({
    Key? key,
    required this.title,
    required this.value,
  })  : titleTextStyle = UITextStyle.regular.copyWith(
          fontSize: 13,
          color: UIColors.lightBlue.withOpacity(0.7),
        ),
        valueTextStyle = UITextStyle.medium.copyWith(
          fontSize: 16,
          color: UIColors.secondaryColor,
        ),
        super(key: key);

  DataViewItem.normal({
    Key? key,
    required this.title,
    required this.value,
  })  : titleTextStyle = UITextStyle.regular.copyWith(
          fontSize: 13,
          color: UIColors.lightBlue.withOpacity(0.7),
        ),
        valueTextStyle = UITextStyle.medium.copyWith(
          fontSize: 16,
          color: UIColors.lightBlue,
        ),
        super(key: key);

  final String title;
  final String value;
  final TextStyle? titleTextStyle;
  final TextStyle? valueTextStyle;

  @override
  Widget build(BuildContext context) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          title,
          style: titleTextStyle ?? UITextStyle.regular.copyWith(
            color: UIColors.grayText,
          ),
        ),
        Text(
          value,
          style: valueTextStyle ?? UITextStyle.medium.copyWith(
            fontSize: 16,
          ),
        ),
      ],
    );
  }
}
