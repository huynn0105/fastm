part of 'update_client_info_cubit.dart';

class UpdateClientInfoState extends Equatable {
  const UpdateClientInfoState({
    this.status = BlocStatus.initial,
    this.errorFields = const {},
    this.errorMessage,
    this.enabledSubmit = true,
    this.districts = const [],
    this.selectedDistrictCode,
  });

  final BlocStatus status;
  final Map<String, String> errorFields;
  final String? errorMessage;
  final bool enabledSubmit;
  final List<ClientDistrictModel> districts;
  final String? selectedDistrictCode;

  @override
  List<Object?> get props => [
        status,
        errorFields,
        errorMessage,
        enabledSubmit,
        districts,
        selectedDistrictCode,
      ];

  UpdateClientInfoState copyWith({
    BlocStatus? status,
    Map<String, String>? errorFields,
    String? errorMessage,
    bool? enabledSubmit,
    List<ClientDistrictModel>? districts,
    String? selectedDistrictCode,
  }) {
    return UpdateClientInfoState(
      status: status ?? this.status,
      errorFields: errorFields ?? this.errorFields,
      errorMessage: errorMessage,
      enabledSubmit: enabledSubmit ?? this.enabledSubmit,
      districts: districts ?? this.districts,
      selectedDistrictCode: selectedDistrictCode ?? this.selectedDistrictCode,
    );
  }
}

enum UpdateClientType {
  info,
  note,
}

enum UpdateClientInfoFields {
  fullName,
  idNumber,
  phone,
  address,
  note,
}
