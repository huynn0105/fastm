part of 'client_list_cubit.dart';

class ClientListState extends Equatable {
  const ClientListState({
    this.status = BlocStatus.initial,
    this.priorityStatus = BlocStatus.initial,
    this.data = const [],
    this.errorMessage,
    this.enabledLoadMore = true,
    this.forceRefresh = false,
  });

  final BlocStatus status;
  final BlocStatus priorityStatus;
  final List<ClientDetailModel> data;
  final String? errorMessage;
  final bool enabledLoadMore;
  final bool forceRefresh;

  @override
  List<Object?> get props => [
        status,
        data,
        priorityStatus,
        errorMessage,
        enabledLoadMore,
        forceRefresh,
      ];

  ClientListState copyWith({
    BlocStatus? status,
    BlocStatus? priorityStatus,
    List<ClientDetailModel>? data,
    String? errorMessage,
    bool? enabledLoadMore,
    bool? forceRefresh,
  }) {
    return ClientListState(
      status: status ?? this.status,
      priorityStatus: priorityStatus ?? this.priorityStatus,
      data: data ?? this.data,
      enabledLoadMore: enabledLoadMore ?? this.enabledLoadMore,
      forceRefresh: forceRefresh ?? this.forceRefresh,
    );
  }
}
