part of 'ad_link_statistic_cubit.dart';

class AdLinkStatisticState extends Equatable {
  const AdLinkStatisticState({
    this.status = BlocStatus.initial,
    this.guideStatus = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final BlocStatus guideStatus;
  final AdLinkStatisticModel? data;

  @override
  List<Object?> get props => [
        status,
        guideStatus,
        data,
      ];

  AdLinkStatisticState copyWith({
    BlocStatus? status,
    BlocStatus? guideStatus,
    AdLinkStatisticModel? data,
  }) {
    return AdLinkStatisticState(
      status: status ?? this.status,
      guideStatus: guideStatus ?? this.guideStatus,
      data: data ?? this.data,
    );
  }
}
