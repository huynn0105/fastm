part of 'statistic_by_product_cubit.dart';

class StatisticByProductState extends Equatable {
  const StatisticByProductState({
    this.status = BlocStatus.initial,
    this.statistic,
  });

  final BlocStatus status;
  final ClientStatisticModel? statistic;

  @override
  List<Object?> get props => [
        status,
        statistic,
      ];

  StatisticByProductState copyWith({
    BlocStatus? status,
    String? category,
    ClientStatisticModel? statistic,
  }) {
    return StatisticByProductState(
      status: status ?? this.status,
      statistic: statistic ?? this.statistic,
    );
  }
}
