part of 'detail_project_cubit.dart';

class DetailProjectState extends Equatable {
  const DetailProjectState({
    this.status = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final ProjectDetailModel? data;

  @override
  List<Object?> get props => [
        status,
        data,
      ];

  DetailProjectState copyWith({
    BlocStatus? status,
    ProjectDetailModel? data,
  }) {
    return DetailProjectState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
