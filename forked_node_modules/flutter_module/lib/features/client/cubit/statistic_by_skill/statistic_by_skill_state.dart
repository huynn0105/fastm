part of 'statistic_by_skill_cubit.dart';

class StatisticBySkillState extends Equatable {
  const StatisticBySkillState({
    this.status = BlocStatus.initial,
    this.statistic,
  });

  final BlocStatus status;
  final ClientStatisticModel? statistic;

  @override
  List<Object?> get props => [
        status,
        statistic,
      ];

  StatisticBySkillState copyWith({
    BlocStatus? status,
    String? category,
    ClientStatisticModel? statistic,
  }) {
    return StatisticBySkillState(
      status: status ?? this.status,
      statistic: statistic ?? this.statistic,
    );
  }
}
