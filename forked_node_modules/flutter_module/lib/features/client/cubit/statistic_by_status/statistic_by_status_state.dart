part of 'statistic_by_status_cubit.dart';

class StatisticByStatusState extends Equatable {
  const StatisticByStatusState({
    this.status = BlocStatus.initial,
    this.category = '',
    this.statistic,
  });

  final BlocStatus status;
  final String category;
  final ClientStatisticModel? statistic;

  @override
  List<Object?> get props => [
        status,
        category,
        statistic,
      ];

  StatisticByStatusState copyWith({
    BlocStatus? status,
    String? category,
    ClientStatisticModel? statistic,
  }) {
    return StatisticByStatusState(
      status: status ?? this.status,
      category: category ?? this.category,
      statistic: statistic ?? this.statistic,
    );
  }
}
