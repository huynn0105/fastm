part of 'client_detail_cubit.dart';

class ClientDetailState extends Equatable {
  const ClientDetailState({
    this.status = BlocStatus.initial,
    this.priorityStatus = BlocStatus.initial,
    this.updateStatus = BlocStatus.initial,
    this.deleteStatus = BlocStatus.initial,
    this.data,
    this.errorMessage,
    this.isUserGuideClientDetail = false,
    this.isUserGuideClientTrash = false,
    this.isUserGuideClientUnclassified = false,
  });

  final BlocStatus status;
  final BlocStatus priorityStatus;
  final BlocStatus updateStatus;
  final BlocStatus deleteStatus;
  final ClientDetailModel? data;
  final String? errorMessage;
  final bool isUserGuideClientDetail;
  final bool isUserGuideClientTrash;
  final bool isUserGuideClientUnclassified;

  @override
  List<Object?> get props => [
        status,
        priorityStatus,
        updateStatus,
        deleteStatus,
        data,
        errorMessage,
        isUserGuideClientDetail,
        isUserGuideClientUnclassified,
        isUserGuideClientTrash,
      ];

  ClientDetailState copyWith({
    BlocStatus? status,
    BlocStatus? priorityStatus,
    BlocStatus? updateStatus,
    BlocStatus? deleteStatus,
    ClientDetailModel? data,
    String? errorMessage,
    bool? clearClient,
    bool? isUserGuideClientDetail,
    bool? isUserGuideClientUnclassified,
    bool? isUserGuideClientTrash,
  }) {
    return ClientDetailState(
      status: status ?? this.status,
      priorityStatus: priorityStatus ?? this.priorityStatus,
      updateStatus: updateStatus ?? this.updateStatus,
      deleteStatus: deleteStatus ?? this.deleteStatus,
      isUserGuideClientDetail: isUserGuideClientDetail ?? this.isUserGuideClientDetail,
      isUserGuideClientTrash: isUserGuideClientTrash ?? this.isUserGuideClientTrash,
      isUserGuideClientUnclassified: isUserGuideClientUnclassified ?? this.isUserGuideClientUnclassified,
      data: clearClient == true ? null : data ?? this.data,
      errorMessage: errorMessage,
    );
  }
}
