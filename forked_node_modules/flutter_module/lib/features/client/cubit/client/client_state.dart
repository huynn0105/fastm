part of 'client_cubit.dart';

class ClientState extends Equatable {
  const ClientState({
    this.status = BlocStatus.initial,
    this.filterClient,
    this.filterSelected = const {},
    this.filterSelecting = const {},
    this.bottomSheetSelecting = const [],
  });

  final BlocStatus status;
  final FilterClientModel? filterClient;
  final Map<String, dynamic> filterSelected;
  final Map<String, dynamic> filterSelecting;
  final List bottomSheetSelecting;

  @override
  List<Object?> get props => [
        status,
        filterClient,
        filterSelected,
        filterSelecting,
        bottomSheetSelecting,
      ];

  ClientState copyWith({
    BlocStatus? status,
    FilterClientModel? filterClient,
    Map<String, dynamic>? filterSelected,
    Map<String, dynamic>? filterSelecting,
    List? bottomSheetSelecting,
  }) {
    return ClientState(
      status: status ?? this.status,
      filterClient: filterClient ?? this.filterClient,
      filterSelected: filterSelected ?? this.filterSelected,
      filterSelecting: filterSelecting ?? this.filterSelecting,
      bottomSheetSelecting: bottomSheetSelecting ?? this.bottomSheetSelecting,
    );
  }
}
