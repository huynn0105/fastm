part of 'ad_link_list_cubit.dart';

class AdLinkListState extends Equatable {
  const AdLinkListState({
    this.status = BlocStatus.initial,
    this.data = const [],
    this.tabSelected = '',
    this.total,
  });

  final BlocStatus status;
  final List<AdLinkModel> data;
  final String tabSelected;
  final TotalAdLinkModel? total;

  @override
  List<Object?> get props => [
        status,
        data,
        tabSelected,
        total,
      ];

  AdLinkListState copyWith({
    BlocStatus? status,
    List<AdLinkModel>? data,
    String? tabSelected,
    TotalAdLinkModel? total,
  }) {
    return AdLinkListState(
      status: status ?? this.status,
      data: data ?? this.data,
      tabSelected: tabSelected ?? this.tabSelected,
      total: total ?? this.total,
    );
  }
}
