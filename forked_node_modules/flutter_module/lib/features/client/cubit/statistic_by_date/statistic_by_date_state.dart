part of 'statistic_by_date_cubit.dart';

class StatisticByDateState extends Equatable {
  const StatisticByDateState({
    this.status = BlocStatus.initial,
    this.selectedType = ClientStatisticChartFilterType.year,
    this.selectingType = ClientStatisticChartFilterType.year,
    this.statistic,
  });

  final BlocStatus status;
  final ClientStatisticChartFilterType selectedType;
  final ClientStatisticChartFilterType selectingType;
  final ClientStatisticByDateModel? statistic;

  @override
  List<Object?> get props => [
        status,
        selectedType,
        selectingType,
        statistic,
      ];

  StatisticByDateState copyWith({
    BlocStatus? status,
    ClientStatisticChartFilterType? selectedType,
    ClientStatisticChartFilterType? selectingType,
    ClientStatisticByDateModel? statistic,
  }) {
    return StatisticByDateState(
      status: status ?? this.status,
      selectedType: selectedType ?? this.selectedType,
      selectingType: selectingType ?? this.selectingType,
      statistic: statistic ?? this.statistic,
    );
  }
}
