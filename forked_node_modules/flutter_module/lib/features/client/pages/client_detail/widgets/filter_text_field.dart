import 'package:flutter/material.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

import '../../../../../../../common/colors.dart';

class FilterTextField extends StatelessWidget {
  const FilterTextField({
    Key? key,
    this.controller,
    this.hintText,
    this.onTap,
    this.enabled = true,
  }) : super(key: key);

  final TextEditingController? controller;
  final String? hintText;
  final bool enabled;
  final VoidCallback? onTap;

  @override
  Widget build(BuildContext context) {
    final outlineInputBorder = OutlineInputBorder(
      borderRadius: BorderRadius.circular(8),
      borderSide: const BorderSide(
        color: UIColors.lightGray,
      ),
    );

    return SizedBox(
      height: 40,
      child: SplashButton(
        isDisabled: !enabled,
        onTap: onTap,
        borderRadius: BorderRadius.circular(8),
        child: TextField(
          readOnly: true,
          enabled: false,
          controller: controller,
          cursorColor: UIColors.primaryColor,
          cursorHeight: 25,
          cursorRadius: const Radius.circular(8),
          style: UITextStyle.semiBold.copyWith(
            fontSize: 14,
            color: enabled ? UIColors.defaultText : UIColors.grayText,
          ),
          decoration: InputDecoration(
            hintText: hintText,
            hintStyle: UITextStyle.semiBold.copyWith(
              fontSize: 14,
              color: UIColors.darkGray,
            ),
            fillColor: UIColors.background,
            filled: true,
            border: outlineInputBorder,
            enabledBorder: outlineInputBorder,
            focusedBorder: outlineInputBorder,
            suffixIconConstraints: const BoxConstraints(
              maxWidth: 40,
              maxHeight: 40,
            ),
            contentPadding: const EdgeInsets.fromLTRB(16, 8, 0, 8),
            suffixIcon: Center(
              child: AppImage.asset(
                asset: "ic_dropdown",
                width: 20,
                height: 20,
                color: enabled ? UIColors.grayText : UIColors.gray,
              ),
            ),
          ),
        ),
      ),
    );
  }
}
