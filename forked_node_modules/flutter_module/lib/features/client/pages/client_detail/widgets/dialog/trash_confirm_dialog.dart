import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/checkbox.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/features/client/pages/client_detail/widgets/dialog/confirm_dialog.dart';

class TrashConfirmDialog extends StatelessWidget {
  const TrashConfirmDialog({
    super.key,
    required this.isAlwaysConfirm,
    required this.onAlwaysConfirmToggle,
  });

  final bool isAlwaysConfirm;
  final Function(bool) onAlwaysConfirmToggle;

  @override
  Widget build(BuildContext context) {
    return ClientConfirmDialog(
      onNegative: () {
        Navigator.of(context).pop(false);
      },
      onPositive: () {
        Navigator.of(context).pop(true);
      },
      child: Container(
        padding: const EdgeInsets.all(16),
        child: Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            const AppImage.asset(
              asset: 'ic_trash_outline',
              width: 48,
              height: 48,
            ),
            const SizedBox(
              height: 16,
            ),
            RichText(
              textAlign: TextAlign.center,
              text: TextSpan(
                style: UITextStyle.regular.copyWith(
                  fontSize: 16,
                  color: UIColors.grayText,
                ),
                children: [
                  const TextSpan(
                    text: 'Khách hàng sẽ được đưa vào ',
                  ),
                  TextSpan(
                    text: 'Danh sách\nbỏ qua ',
                    style: UITextStyle.bold.copyWith(
                      fontSize: 16,
                      color: UIColors.red,
                    ),
                  ),
                  const TextSpan(
                    text: 'và tự động xoá sau 30 ngày',
                  ),
                ],
              ),
            ),
            const SizedBox(
              height: 6,
            ),
            SplashButton(
              onTap: () => onAlwaysConfirmToggle(!isAlwaysConfirm),
              child: Padding(
                padding: const EdgeInsets.symmetric(vertical: 10),
                child: IgnorePointer(
                  child: AppCheckbox.rectangle(
                    fit: FlexFit.loose,
                    mainAxisSize: MainAxisSize.min,
                    value: isAlwaysConfirm,
                    title: 'Đã hiểu và không nhắc lại',
                    onChanged: (_) {},
                  ),
                ),
              ),
            ),
            const SizedBox(
              height: 14,
            ),
          ],
        ),
      ),
    );
  }
}
