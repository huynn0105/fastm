import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

class SaleActionTag extends StatelessWidget {
  const SaleActionTag({
    super.key,
    required this.asset,
    required this.title,
    required this.color,
    required this.backgroundColor,
    this.onTap,
  });

  const SaleActionTag.trash({
    super.key,
    this.asset = 'ic_trash',
    this.title = 'BỎ QUA',
    this.color = UIColors.red,
    this.backgroundColor = UIColors.lightRed,
    this.onTap,
  });

  const SaleActionTag.priority({
    super.key,
    this.asset = 'ic_star',
    this.title = 'TIỀM NĂNG',
    this.color = UIColors.green,
    this.backgroundColor = UIColors.lightGreen,
    this.onTap,
  });

  final String asset;
  final String title;
  final Color color;
  final Color backgroundColor;
  final Function()? onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: onTap,
      borderRadius: BorderRadius.circular(22),
      child: Container(
        height: 30,
        padding: const EdgeInsets.symmetric(horizontal: 8),
        decoration: BoxDecoration(
          color: backgroundColor.withOpacity(0.5),
          borderRadius: BorderRadius.circular(8),
          border: Border.all(
            color: color,
          ),
        ),
        child: Row(
          mainAxisSize: MainAxisSize.min,
          children: [
            AppImage.asset(
              asset: asset,
              width: 20,
              height: 20,
              color: color,
              fit: BoxFit.contain,
            ),
            const SizedBox(
              width: 2,
            ),
            Text(
              title,
              style: UITextStyle.medium.copyWith(
                fontSize: 14,
                color: color,
              ),
            ),
          ],
        ),
      ),
    );
  }
}
