import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

class ClientConfirmDialog extends StatelessWidget {
  const ClientConfirmDialog({
    super.key,
    required this.child,
    this.positiveTitle = 'Xác nhận',
    this.negativeTitle = 'Xem lại',
    this.positiveColor = UIColors.primaryColor,
    this.negativeColor = UIColors.grayText,
    this.onPositive,
    this.onNegative,
    this.hideNegative = false,
  });

  final Widget child;
  final String positiveTitle;
  final String negativeTitle;
  final Color positiveColor;
  final Color negativeColor;
  final Function()? onPositive;
  final Function()? onNegative;
  final bool hideNegative;

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: BoxDecoration(
        color: UIColors.white,
        borderRadius: BorderRadius.circular(16),
      ),
      child: Column(
        mainAxisSize: MainAxisSize.min,
        children: [
          child,
          SizedBox(
            height: 50,
            child: Row(
              children: [
                if (!hideNegative)
                  Expanded(
                    child: SplashButton(
                      onTap: onNegative,
                      borderRadius: const BorderRadius.only(
                        bottomLeft: Radius.circular(16),
                      ),
                      child: Container(
                        alignment: Alignment.center,
                        decoration: const BoxDecoration(
                          color: UIColors.lightBlue,
                          borderRadius: BorderRadius.only(
                            bottomLeft: Radius.circular(16),
                          ),
                        ),
                        child: Text(
                          negativeTitle,
                          style: UITextStyle.medium.copyWith(
                            fontSize: 14,
                            color: negativeColor,
                          ),
                        ),
                      ),
                    ),
                  ),
                Expanded(
                  child: SplashButton(
                    onTap: onPositive,
                    borderRadius: BorderRadius.only(
                      bottomLeft: hideNegative ? const Radius.circular(16) : Radius.zero,
                      bottomRight: const Radius.circular(16),
                    ),
                    child: Container(
                      alignment: Alignment.center,
                      decoration: BoxDecoration(
                        color: UIColors.lightBlue,
                        borderRadius: BorderRadius.only(
                          bottomLeft: hideNegative ? const Radius.circular(16) : Radius.zero,
                          bottomRight: const Radius.circular(16),
                        ),
                      ),
                      child: Text(
                        positiveTitle,
                        style: UITextStyle.medium.copyWith(
                          fontSize: 14,
                          color: positiveColor,
                        ),
                      ),
                    ),
                  ),
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }
}
