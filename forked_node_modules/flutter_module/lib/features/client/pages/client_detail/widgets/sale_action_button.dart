import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

class SaleActionButton extends StatelessWidget {
  const SaleActionButton({
    super.key,
    required this.asset,
    required this.title,
    required this.color,
    this.showTitle = true,
    this.onTap,
  });

  const SaleActionButton.create({
    super.key,
    this.asset = 'ic_create_sale',
    this.title = 'Khởi tạo bán hàng',
    this.color = UIColors.primaryColor,
    this.showTitle = true,
    this.onTap,
  });

  const SaleActionButton.trash({
    super.key,
    this.asset = 'ic_trash',
    this.title = '',
    this.color = UIColors.red,
    this.showTitle = true,
    this.onTap,
  });

  const SaleActionButton.priority({
    super.key,
    this.asset = 'ic_star',
    this.title = '',
    this.color = UIColors.green,
    this.showTitle = true,
    this.onTap,
  });

  final String asset;
  final String title;
  final bool showTitle;
  final Color color;
  final Function()? onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: onTap,
      borderRadius: BorderRadius.circular(22),
      child: Row(
        mainAxisSize: MainAxisSize.min,
        children: [
          Container(
            height: 44,
            width: 44,
            alignment: Alignment.center,
            decoration: BoxDecoration(
              shape: BoxShape.circle,
              color: UIColors.white,
              border: Border.all(
                color: color,
              ),
            ),
            child: AppImage.asset(
              asset: asset,
              width: 28,
              height: 28,
              color: color,
              fit: BoxFit.contain,
            ),
          ),
          if (showTitle) ...[
            const SizedBox(
              width: 10,
            ),
            Text(
              title,
              style: UITextStyle.medium.copyWith(
                fontSize: 16,
                color: color,
              ),
            ),
          ],
        ],
      ),
    );
  }
}
