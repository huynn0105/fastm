import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/enum/client/client_trash_type.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/common/widgets/radio.dart';
import 'package:flutter_module/common/widgets/separated_widget.dart';
import 'package:flutter_module/features/client/pages/client_detail/widgets/dialog/confirm_dialog.dart';

class TrashAllConfirmDialog extends StatelessWidget {
  const TrashAllConfirmDialog({
    super.key,
    required this.type,
    required this.onTypeSelected,
  });

  final ClientTrashAllType type;
  final Function(ClientTrashAllType) onTypeSelected;

  @override
  Widget build(BuildContext context) {
    return ClientConfirmDialog(
      positiveTitle: 'Quay lại',
      negativeTitle: 'Xoá ngay',
      negativeColor: UIColors.red,
      positiveColor: UIColors.primaryColor,
      onNegative: () {
        Navigator.of(context).pop(true);
      },
      onPositive: () {
        Navigator.of(context).pop(false);
      },
      child: Container(
        padding: const EdgeInsets.all(16),
        child: Column(
          mainAxisSize: MainAxisSize.min,
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Center(
              child: Column(
                mainAxisSize: MainAxisSize.min,
                children: [
                  const AppImage.asset(
                    asset: 'ic_trash_star_outline',
                    width: 48,
                    height: 48,
                  ),
                  const SizedBox(
                    height: 12,
                  ),
                  Text(
                    'Làm trống tệp chờ xử lý',
                    style: UITextStyle.semiBold.copyWith(
                      fontSize: 16,
                      color: UIColors.red,
                    ),
                  ),
                ],
              ),
            ),
            const SizedBox(
              height: 16,
            ),
            Text(
              'Chọn phương thức làm trống',
              style: UITextStyle.regular.copyWith(
                fontSize: 14,
                color: UIColors.defaultText,
              ),
            ),
            const SizedBox(
              height: 8,
            ),
            SeparatedColumn(
              mainAxisSize: MainAxisSize.min,
              separatorBuilder: (_, __) => const SizedBox(
                height: 8,
              ),
              children: List.generate(
                ClientTrashAllType.values.length,
                (index) {
                  final item = ClientTrashAllType.values[index];
                  return SplashButton(
                    onTap: () => onTypeSelected(item),
                    child: Row(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        SizedBox(
                          height: 24,
                          child: IgnorePointer(
                            child: AppRadio(
                              title: '',
                              value: item == type,
                            ),
                          ),
                        ),
                        const SizedBox(
                          width: 4,
                        ),
                        Flexible(
                          child: Column(
                            mainAxisAlignment: MainAxisAlignment.start,
                            crossAxisAlignment: CrossAxisAlignment.start,
                            children: [
                              Container(
                                height: 24,
                                alignment: Alignment.centerLeft,
                                child: Text(
                                  item.title,
                                  style: UITextStyle.medium.copyWith(
                                    fontSize: 16,
                                    color: UIColors.defaultText,
                                  ),
                                ),
                              ),
                              Text(
                                item.description,
                                style: UITextStyle.regular.copyWith(
                                  fontSize: 14,
                                  fontStyle: FontStyle.italic,
                                  color: UIColors.secondaryColor,
                                ),
                              ),
                            ],
                          ),
                        ),
                      ],
                    ),
                  );
                },
              ),
            ),
            const SizedBox(
              height: 16,
            ),
            RichText(
              text: TextSpan(
                style: UITextStyle.regular.copyWith(
                  fontSize: 14,
                  color: UIColors.grayText,
                ),
                children: [
                  const TextSpan(
                    text: 'Lưu ý: các khách hàng đã được ',
                  ),
                  TextSpan(
                    text: 'phân loại là tiềm năng ',
                    style: UITextStyle.semiBold.copyWith(
                      fontSize: 14,
                      color: UIColors.defaultText,
                    ),
                  ),
                  const TextSpan(
                    text: 'sẽ không bị ảnh hưởng',
                  ),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}
