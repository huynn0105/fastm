class DAAProjectDetailItem extends StatelessWidget {
  const DAAProjectDetailItem({
    super.key,
    this.item,
    this.initItem,
  });

  final DAAProjectDetailModel? item;
  final ClientProjectModel? initItem;

  @override
  Widget build(BuildContext context) {
    final status = ClientProjectStatus.values.firstWhereOrNull((e) => e.code == initItem?.status);

    return ListView(
      padding: EdgeInsets.zero,
      physics: const NeverScrollableScrollPhysics(),
      shrinkWrap: true,
      children: [
        Padding(
          padding: const EdgeInsets.all(16),
          child: Container(
            decoration: BoxDecoration(
              color: UIColors.white,
              borderRadius: BorderRadius.circular(8),
            ),
            padding: const EdgeInsets.all(16),
            child: Column(
              children: [
                Row(
                  children: [
                    AppImage.network(
                      url: initItem?.iconURL ?? "",
                      width: 64,
                      height: 40,
                      fit: BoxFit.contain,
                    ),
                    const SizedBox(
                      width: 16,
                    ),
                    Expanded(
                      child: Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Text(
                            "Sản phẩm",
                            style: UITextStyle.regular.copyWith(
                              color: UIColors.grayText,
                            ),
                          ),
                          Text(
                            initItem?.projectName ?? "",
                            style: UITextStyle.regular,
                          ),
                        ],
                      ),
                    )
                  ],
                ),
                const DottedDivider(
                  height: 12,
                  color: UIColors.lightGray,
                ),
                ItemValueRow(
                  title: "Trạng thái",
                  value: initItem?.statusText ?? "",
                  isBold: true,
                  valueColor: status?.color ?? UIColors.boolText,
                ),
                const SizedBox(
                  height: 6,
                ),
                ItemValueRow(
                  title: "Ghi chú",
                  value: item?.lastProcessText ?? "---",
                ),
                const SizedBox(
                  height: 6,
                ),
                ItemValueRow(
                  title: "Vào lúc",
                  value: DateTimeUtil.getNumberDayBetween(
                    initItem?.updatedDate ?? '',
                    forceShowDateFormat: (duration) {
                      if (duration.inDays > 3) {
                        return DateTimeFormat.ddMMyyyy;
                      }
                      return null;
                    },
                  ),
                ),
              ],
            ),
          ),
        ),
        if (TextUtils.isNotEmpty(item?.webviewURL))
          Center(
            child: SplashButton(
              onTap: () {
                _onTapMore(context, url: item?.webviewURL);
              },
              child: Row(
                mainAxisSize: MainAxisSize.min,
                children: [
                  Text(
                    "Xem chi tiết",
                    style: UITextStyle.medium.copyWith(
                      fontSize: 16,
                      color: UIColors.primaryColor,
                    ),
                  ),
                  const SizedBox(
                    width: 4,
                  ),
                  const AppImage.asset(
                    asset: "ic_arrow_long_right",
                    width: 28,
                    height: 28,
                  ),
                ],
              ),
            ),
          ),
        const SizedBox(
          height: 12,
        ),
      ],
    );
  }

  _onTapMore(BuildContext context, {String? url}) {
    if (url == null) return;
    context.pushRoute(WebViewRoute(
      title: "Chi tiết dự án",
      url: url,
    ));
  }
}

class ItemValue extends StatelessWidget {
  const ItemValue({
    super.key,
    this.title = "",
    this.value = "",
    this.subValue,
    this.isBold = false,
    this.valueColor = UIColors.boolText,
  });

  final String title;
  final String value;
  final String? subValue;
  final bool isBold;
  final Color valueColor;

  @override
  Widget build(BuildContext context) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          title,
          style: UITextStyle.regular.copyWith(
            fontSize: 13,
            color: UIColors.grayText,
          ),
        ),
        const SizedBox(
          height: 2,
        ),
        RichText(
          text: TextSpan(
            style: (isBold ? UITextStyle.bold : UITextStyle.regular).copyWith(color: valueColor),
            children: [
              TextSpan(text: value),
              if (subValue != null)
                TextSpan(
                  text: "\n$subValue",
                  style: UITextStyle.regular,
                ),
            ],
          ),
        )
      ],
    );
  }
}

class ItemValueRow extends StatelessWidget {
  const ItemValueRow({
    super.key,
    this.title = "",
    this.value = "",
    this.subValue,
    this.isBold = false,
    this.valueColor = UIColors.boolText,
  });

  final String title;
  final String value;
  final String? subValue;
  final bool isBold;
  final Color valueColor;

  @override
  Widget build(BuildContext context) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Expanded(
          flex: 35,
          child: Text(
            title,
            style: UITextStyle.regular.copyWith(
              fontSize: 13,
              color: UIColors.grayText,
            ),
          ),
        ),
        const SizedBox(
          height: 2,
        ),
        Expanded(
          flex: 65,
          child: RichText(
            text: TextSpan(
              style: (isBold ? UITextStyle.bold : UITextStyle.regular).copyWith(color: valueColor),
              children: [
                TextSpan(text: value),
                if (subValue != null)
                  TextSpan(
                    text: "\n$subValue",
                    style: UITextStyle.regular,
                  ),
              ],
            ),
          ),
        )
      ],
    );
  }
}
