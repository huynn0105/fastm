import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

import '../../../../../../../common/widgets/images.dart';

class AttachmentListItem extends StatelessWidget {
  const AttachmentListItem({
    Key? key,
    required this.data,
    this.onShowMore,
  }) : super(key: key);

  final List<String> data;
  final Function({String? url})? onShowMore;

  @override
  Widget build(BuildContext context) {
    if (data.isEmpty) {
      return const SizedBox();
    }
    int maxLength = 4;
    return SizedBox(
      height: 75,
      child: ListView.separated(
        padding: EdgeInsets.zero,
        physics: const ClampingScrollPhysics(),
        scrollDirection: Axis.horizontal,
        itemBuilder: (context, index) {
          return SizedBox(
            width: 75,
            height: 75,
            child: Stack(
              children: [
                Positioned.fill(
                  child: AppSplashButton(
                    onTap: () => onShowMore?.call(url: data[index]),
                    child: AppImage.network(
                      url: data[index],
                      width: 75,
                      height: 75,
                      borderRadius: BorderRadius.circular(8),
                    ),
                  ),
                ),
                Visibility(
                  visible: data.length > maxLength && index == maxLength - 1,
                  child: Positioned.fill(
                    child: AppSplashButton(
                      onTap: () => onShowMore?.call(),
                      child: Container(
                        height: 75,
                        width: 75,
                        alignment: Alignment.center,
                        decoration: BoxDecoration(
                          color: UIColors.blurBackground.withOpacity(0.7),
                          borderRadius: BorderRadius.circular(8),
                        ),
                        child: const AppImage.asset(
                          asset: "ic_arrow_right",
                          width: 24,
                          height: 24,
                          color: UIColors.primaryColor,
                        ),
                      ),
                    ),
                  ),
                ),
              ],
            ),
          );
        },
        separatorBuilder: (_, __) => const SizedBox(width: 4),
        itemCount: data.length.clamp(0, maxLength),
      ),
    );
  }
}
