import 'package:collection/collection.dart';
import 'package:ekyc/common/widgets/buttons.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/enum/client/client_project_status.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/datetime_util.dart';
import 'package:flutter_module/common/widgets/avatar_widget.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/client/client_detail/client_project_model.dart';

class ProjectHistoryItem extends StatelessWidget {
  const ProjectHistoryItem({
    super.key,
    required this.data,
    this.onTap,
  });

  final ClientProjectModel data;
  final VoidCallback? onTap;

  @override
  Widget build(BuildContext context) {
    final status = ClientProjectStatus.values.firstWhereOrNull((e) => e.code == data.status);
    return SplashButton(
      onTap: onTap,
      child: Row(
        crossAxisAlignment: CrossAxisAlignment.center,
        children: [
          SizedBox(
            width: 44,
            height: 44,
            child: Stack(
              fit: StackFit.expand,
              clipBehavior: Clip.none,
              children: [
                AvatarWidget(
                  url: data.iconURL ?? '',
                  size: 44,
                  fit: BoxFit.contain,
                  backgroundColor: UIColors.extraLightGray,
                ),
                if (status != null)
                  Positioned(
                    bottom: -2,
                    right: -2,
                    child: AppImage.asset(
                      asset: status.asset,
                      width: 20,
                      height: 20,
                    ),
                  ),
              ],
            ),
          ),
          const SizedBox(
            width: 12,
          ),
          Expanded(
            child: Row(
              children: [
                Expanded(
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Text(
                        data.projectName ?? '',
                        style: UITextStyle.regular.copyWith(
                          fontSize: 13,
                        ),
                      ),
                      Text(
                        data.statusText ?? '',
                        style: UITextStyle.semiBold.copyWith(
                          fontSize: 13,
                          color: status?.color,
                        ),
                      ),
                      Text(
                        DateTimeUtil.getNumberDayBetween(data.updatedDate ?? ''),
                        style: UITextStyle.semiBold.copyWith(
                          fontSize: 13,
                          color: UIColors.grayText,
                        ),
                      ),
                    ],
                  ),
                ),
                const SizedBox(
                  width: 10,
                ),
                const AppImage.asset(
                  asset: "ic_arrow_right",
                  width: 20,
                  height: 20,
                  color: UIColors.grayText,
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }
}
