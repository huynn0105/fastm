import 'package:flutter_module/features/client/cubit/statistic_by_status/statistic_by_status_cubit.dart';
import 'package:flutter_module/features/mtrade/page/order/order_process_state/components/list_selecotor.dart';

class ClientStatisticByStatusComponent extends StatelessWidget {
  const ClientStatisticByStatusComponent({super.key});

  @override
  Widget build(BuildContext context) {
    final selectorData = ClientTabType.values.map((e) => DataWrapper(id: e.name, value: e.title)).toList().sublist(2);

    return BlocConsumer<StatisticByStatusCubit, StatisticByStatusState>(
      listener: (context, state) {
        _onListenStateChange(context);
      },
      builder: (context, state) {
        final cubit = context.read<StatisticByStatusCubit>();
        if (cubit.state.status.isInitial) {
          WidgetsBinding.instance.addPostFrameCallback((timeStamp) {
            cubit.fetchData();
          });
        }
        return Padding(
          padding: const EdgeInsets.symmetric(
            horizontal: 16,
          ),
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                "Khách hàng theo trạng thái",
                style: UITextStyle.semiBold.copyWith(
                  fontSize: 18,
                ),
              ),
              const SizedBox(
                height: 8,
              ),
              ListSelector(
                data: selectorData,
                fontSize: 14,
                indexSelected: selectorData.indexWhere((element) => element.id == state.category),
                onTabItem: (item, index) {
                  _onChangeCategory(context, item: item as DataWrapper);
                },
              ),
              const SizedBox(
                height: 12,
              ),
              Container(
                padding: const EdgeInsets.all(16),
                decoration: BoxDecoration(
                  color: UIColors.white,
                  borderRadius: BorderRadius.circular(12),
                ),
                child: Column(
                  mainAxisSize: MainAxisSize.min,
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Row(
                      children: [
                        AnimatedDonutChart(
                          controller: cubit.controller,
                          size: 108,
                          totalValue: TextUtils.parseDouble(state.statistic?.total),
                          enableDisplayTitle: true,
                          onDisplayTitle: (value) {
                            return FormatUtil.formatNumberByThousandSeparator(value, digits: 0).split(" ").valueAt(0) ?? "";
                          },
                          subtitle: FormatUtil.formatNumberByThousandSeparator(
                                      TextUtils.parseDouble(state.statistic?.total) ?? 0)
                                  .split(" ")
                                  .valueAt(1) ??
                              'người',
                        ),
                        const SizedBox(
                          width: 16,
                        ),
                        Expanded(
                          child: Column(
                            mainAxisSize: MainAxisSize.min,
                            mainAxisAlignment: MainAxisAlignment.center,
                            crossAxisAlignment: CrossAxisAlignment.start,
                            children: [
                              Text(
                                'Tổng khách hàng',
                                style: UITextStyle.regular.copyWith(
                                  fontSize: 14,
                                  color: UIColors.grayText,
                                ),
                              ),
                              Text(
                                "${FormatUtil.doubleFormat(TextUtils.parseDouble(state.statistic?.total ?? 0))} người",
                                style: UITextStyle.semiBold.copyWith(
                                  fontSize: 18,
                                ),
                              ),
                            ],
                          ),
                        ),
                      ],
                    ),
                    const SizedBox(
                      height: 12,
                    ),
                    Text(
                      "Phân loại theo trạng thái tham gia",
                      style: UITextStyle.regular.copyWith(
                        fontSize: 14,
                        color: UIColors.grayText,
                      ),
                    ),
                    DonutChartLegend(
                      controller: cubit.controller,
                      enableShowLoading: false,
                      padding: const EdgeInsets.only(top: 12),
                      onDisplayLabel: (section) {
                        return "${FormatUtil.doubleFormat(section.value)} ${section.unit}";
                      },
                      onTapLabel: (index) {
                        _onFilterClient(context, index: index);
                      },
                    ),
                    // if (data?.urlPost?.getFirst() != null) ...[
                    //   const SizedBox(
                    //     height: 12,
                    //   ),
                    //   LegendaryKnowledgeWidget(
                    //     title: data?.urlPost?.getFirst()?.title ?? '',
                    //     url: data?.urlPost?.getFirst()?.url ?? '',
                    //   ),
                    // ],
                  ],
                ),
              ),
            ],
          ),
        );
      },
    );
  }

  _onListenStateChange(BuildContext context) {
    final cubit = context.read<StatisticByStatusCubit>();
    if (cubit.state.status.isLoading) {
      cubit.controller.load();
      return;
    }
    if (cubit.state.status.isSuccess) {
      cubit.controller.show(data: cubit.getDonutChartData(), forceDelay: true);
      return;
    }
    if (cubit.state.status.isFailure) {
      cubit.controller.empty();
      return;
    }
  }

  _onChangeCategory(BuildContext context, {required DataWrapper item}) {
    final cubit = context.read<StatisticByStatusCubit>();
    cubit.changeCategory(item.id ?? "");
    cubit.fetchData();
  }

  _onFilterClient(BuildContext context, {required int index}) {
    final clientCubit = context.read<ClientGlobalCubit>();
    final cubit = context.read<StatisticByStatusCubit>();
    final item = cubit.state.statistic?.data.valueAt(index);
   clientCubit.selectStatisticItem(item);
  }
}
