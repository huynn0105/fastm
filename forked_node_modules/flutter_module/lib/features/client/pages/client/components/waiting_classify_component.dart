import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/enum/client/client_tab_type.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/features/client/cubit/client/client_cubit.dart';
import 'package:flutter_module/routes/routes.gr.dart';

import '../../../../../common/styles.dart';
import '../../../../../common/widgets/images.dart';

class WaitingClassifyComponent extends StatelessWidget {
  const WaitingClassifyComponent({
    Key? key,
    this.margin = EdgeInsets.zero,
    this.isHideWhenNodata = false,
  }) : super(key: key);

  final EdgeInsets margin;
  final bool isHideWhenNodata;

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<ClientGlobalCubit, ClientState>(
      buildWhen: (previous, current) => previous.filterClient?.data != current.filterClient?.data,
      builder: (context, state) {
        final total = state.filterClient?.total?.pageQc ?? 0;
        final hasNoData = total == 0;

        if (hasNoData && isHideWhenNodata) {
          return const SizedBox();
        }

        final label = hasNoData
            ? 'Sử dụng công cụ liên kết tiếp thị để tiếp cận không giới hạn khách hàng tiềm năng'
            : 'khách hàng có nhu cầu chờ phân loại';

        return Container(
          margin: margin,
          constraints: const BoxConstraints(minHeight: 50),
          width: double.infinity,
          padding: const EdgeInsets.symmetric(vertical: 12),
          decoration: BoxDecoration(
            color: UIColors.darkBlue,
            borderRadius: BorderRadius.circular(8),
          ),
          child: AppSplashButton(
            onTap: () {
              if (hasNoData) {
                context.router.push(const AdLinkRoute());
                return;
              }
              context.router.push(ClientDetailMiddleRoute(type: ClientDetailType.PAGE_QC));
            },
            borderRadius: BorderRadius.circular(8),
            child: Container(
              alignment: Alignment.center,
              padding: const EdgeInsets.symmetric(horizontal: 16),
              child: Row(
                crossAxisAlignment: CrossAxisAlignment.center,
                children: [
                  const AppImage.asset(
                    asset: "ic_client_classify",
                    width: 32,
                    height: 32,
                    fit: BoxFit.contain,
                  ),
                  const SizedBox(
                    width: 10,
                  ),
                  Expanded(
                    child: RichText(
                      maxLines: hasNoData ? 3 : 1,
                      overflow: TextOverflow.ellipsis,
                      text: TextSpan(
                        children: [
                          if (!hasNoData) ...[
                            TextSpan(
                              text: "$total ",
                              style: UITextStyle.semiBold.copyWith(
                                fontSize: 16,
                                color: UIColors.green,
                              ),
                            ),
                          ],
                          TextSpan(
                            text: label,
                            style: UITextStyle.regular.copyWith(
                              fontSize: 13,
                              color: UIColors.white.withOpacity(0.7),
                            ),
                          ),
                        ],
                      ),
                    ),
                  ),
                  SizedBox(
                    width: 16,
                    height: 16,
                    child: Stack(
                      clipBehavior: Clip.none,
                      children: [
                        Positioned(
                          top: -10,
                          bottom: -10,
                          right: 30,
                          child: AppImage.asset(
                            asset: "ic_white_logo_mfast",
                            width: 32,
                            height: 32,
                            color: UIColors.white.withOpacity(0.2),
                            fit: BoxFit.contain,
                          ),
                        ),
                        Positioned.fill(
                          child: AppImage.asset(
                            asset: "ic_arrow_right",
                            width: 16,
                            height: 16,
                            color: UIColors.white.withOpacity(0.7),
                          ),
                        ),
                      ],
                    ),
                  ),
                ],
              ),
            ),
          ),
        );
      },
    );
  }
}
