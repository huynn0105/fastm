import 'package:flutter/material.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';

class SelectedChipItem extends StatelessWidget {
  const SelectedChipItem({
    Key? key,
    required this.title,
    this.amount = 0,
    this.isSelected = false,
    this.onTap,
    this.bgActiveColor,
    this.bgColor,
    required this.showAmount,
  }) : super(key: key);

  final String title;
  final int amount;
  final bool isSelected;
  final VoidCallback? onTap;
  final Color? bgActiveColor;
  final Color? bgColor;
  final bool showAmount;

  @override
  Widget build(BuildContext context) {
    final textColor = isSelected ? UIColors.white : UIColors.grayText;
    final backgroundColor = isSelected ? (bgActiveColor ?? UIColors.primaryColor) : (bgColor ?? UIColors.background);
    final amount = showAmount ? ' (${this.amount})' : '';

    ///
    return SplashButton(
      onTap: onTap,
      child: Container(
        height: 32,
        padding: const EdgeInsets.symmetric(
          vertical: 6,
          horizontal: 12,
        ),
        decoration: BoxDecoration(
          color: backgroundColor,
          borderRadius: BorderRadius.circular(25),
        ),
        child: Text(
          "$title$amount",
          style: UITextStyle.regular.copyWith(
            color: textColor,
          ),
        ),
      ),
    );
  }
}
