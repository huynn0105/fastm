import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/checkbox.dart';

class CheckboxSheetItem extends StatelessWidget {
  const CheckboxSheetItem({
    super.key,
    this.title,
    this.value = false,
    this.onChange,
  });

  final String? title;
  final bool value;
  final Function(bool)? onChange;

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      onTap: () => onChange?.call(!value),
      child: SizedBox(
        height: 40,
        child: Row(
          children: [
            IgnorePointer(
              child: AppCheckbox.rectangle(
                value: value,
                onChanged: (_) {},
              ),
            ),
            const SizedBox(
              width: 12,
            ),
            Expanded(
              child: Text(
                maxLines: 1,
                overflow: TextOverflow.ellipsis,
                title ?? "",
                style: UITextStyle.regular.copyWith(
                  fontSize: 16,
                  color: UIColors.grayText,
                ),
              ),
            ),
            const SizedBox(
              width: 2,
            ),
          ],
        ),
      ),
    );
  }
}
