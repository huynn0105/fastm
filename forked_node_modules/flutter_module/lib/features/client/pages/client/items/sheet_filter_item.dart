import 'package:flutter/material.dart';
import 'package:flutter_module/common/utils/text_util.dart';

import '../../../../../common/bottom_sheet/bottom_sheet_provider.dart';
import '../../../../../common/colors.dart';
import '../../../../../common/size.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/block_widget.dart';
import '../../../../../common/widgets/buttons.dart';
import '../../../../../common/widgets/images.dart';

class SheetFilterItem extends StatelessWidget {
  const SheetFilterItem({
    Key? key,
    required this.title,
    required this.hintText,
    required this.children,
    this.onDone,
    this.onStartSelect,
    this.onEndSelect,
    this.text,
  }) : super(key: key);

  final String title;
  final String hintText;
  final List<Widget> children;
  final VoidCallback? onDone;
  final VoidCallback? onStartSelect;
  final Function(bool submitted)? onEndSelect;
  final String? text;

  @override
  Widget build(BuildContext context) {
    return BlockWidget(
      title: title,
      child: Container(
        height: 40,
        width: double.infinity,
        padding: const EdgeInsets.symmetric(horizontal: 16),
        decoration: BoxDecoration(
          color: UIColors.background,
          borderRadius: BorderRadius.circular(8),
        ),
        child: AppSplashButton(
          onTap: () {
            onStartSelect?.call();
            BottomSheetProvider.instance
                .show(
              context,
              enabledOnDone: true,
              onDone: onDone,
              closeOnRight: false,
              title: "Lọc ${title.replaceAll("từng tham gia", "").toLowerCase()}",
              child: ConstrainedBox(
                constraints: BoxConstraints(
                  maxHeight: AppSize.instance.height * 0.5,
                ),
                child: ListView(
                  padding: const EdgeInsets.all(16),
                  shrinkWrap: true,
                  children: children,
                ),
              ),
            )
                .then((value) {
              onEndSelect?.call(value == true);
            });
          },
          child: Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: [
              Expanded(
                child: Text(
                  text ?? hintText,
                  style: TextUtils.isNotEmpty(text)
                      ? UITextStyle.semiBold
                      : UITextStyle.regular.copyWith(
                          color: UIColors.grayText,
                        ),
                ),
              ),
              const AppImage.asset(
                asset: "ic_dropdown",
                width: 20,
                height: 20,
              ),
            ],
          ),
        ),
      ),
    );
  }
}
