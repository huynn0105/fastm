import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/common/widgets/loading.dart';
import 'package:flutter_module/features/client/cubit/ad_link_statistic/ad_link_statistic_cubit.dart';
import 'package:flutter_module/routes/routes.gr.dart';

class AdLinkGuideComponent extends StatelessWidget {
  const AdLinkGuideComponent({super.key});

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<AdLinkStatisticCubit, AdLinkStatisticState>(
      builder: (context, state) {
        if (state.guideStatus.isInitial) {
          WidgetsBinding.instance.addPostFrameCallback((timeStamp) {
            final cubit = context.read<AdLinkStatisticCubit>();
            cubit.getAdLinkGuide();
          });
        }

        if (state.guideStatus.isLoading) {
          return const SizedBox(
            height: 200,
            child: LoadingWidget.withoutText(),
          );
        }

        return ListView.separated(
          padding: const EdgeInsets.symmetric(horizontal: 16),
          shrinkWrap: true,
          itemBuilder: (BuildContext context, int index) {
            final item = state.data?.userGuide?[index];
            return SplashButton(
              onTap: () {
                context.pushRoute(WebViewRoute(url: item?.url, title: item?.title));
              },
              child: Padding(
                padding: const EdgeInsets.symmetric(vertical: 16),
                child: Row(
                  children: [
                    AppImage.network(
                      url: item?.icon ?? "",
                      width: 24,
                      height: 24,
                    ),
                    const SizedBox(
                      width: 12,
                    ),
                    Expanded(
                      child: Text(
                        item?.title ?? "",
                        style: UITextStyle.medium.copyWith(
                          fontSize: 16,
                          color: UIColors.grayText,
                        ),
                      ),
                    ),
                    const AppImage.asset(
                      asset: 'ic_arrow_right',
                      width: 20,
                      height: 20,
                    )
                  ],
                ),
              ),
            );
          },
          separatorBuilder: (BuildContext context, int index) {
            return const Divider(
              thickness: 1,
              height: 1,
              indent: 36,
            );
          },
          itemCount: state.data?.userGuide?.length ?? 0,
        );
      },
    );
  }
}
