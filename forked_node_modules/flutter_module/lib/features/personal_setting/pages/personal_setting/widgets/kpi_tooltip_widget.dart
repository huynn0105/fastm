class _Tooltips extends StatelessWidget {
  const _Tooltips({
    super.key,
  });

  @override
  Widget build(BuildContext context) {
    return Builder(builder: (context) {
      return SplashButton(
        onTap: () {
          showPopover(
            context: context,
            width: AppSize.instance.width * 0.75,
            arrowWidth: 10,
            arrowHeight: 10,
            bodyBuilder: (context) {
              return Container(
                color: Colors.white,
                child: Container(
                  color: Colors.white,
                  margin: const EdgeInsets.all(12),
                  child: const Column(
                    mainAxisSize: MainAxisSize.min,
                    children: [
                      _TooltipItem(
                        title: '%Chỉ tiêu KPI: ',
                        content: 'chỉ tiêu doanh số KPI tháng theo quy định.',
                      ),
                      SizedBox(height: 4),
                      _TooltipItem(
                        title: 'Thực đạt: ',
                        content: 'doanh số KPI thực đạt tính đến thời điểm hiện tại.',
                      ),
                      SizedBox(height: 4),
                      _TooltipItem(
                        title: '%KPI thực đạt: ',
                        content: 'tỉ lệ hoàn thành KPI tính đến thời điểm hiện tại.',
                      ),
                      SizedBox(height: 4),
                      _TooltipItem(
                        title: '%KPI dự kiến:',
                        content: 'tỉ lệ hoàn thành KPI dự kiến tính đến hết tháng',
                      ),
                    ],
                  ),
                ),
              );
            },
          );
        },
        child: Padding(
          padding: const EdgeInsets.symmetric(horizontal: 8, vertical: 2),
          child: Row(
            children: [
              Text(
                'Ghi chú',
                style: UITextStyle.regular.copyWith(
                  color: UIColors.grayText,
                  fontSize: 13,
                ),
              ),
              const SizedBox(width: 4),
              const AppImage.asset(
                asset: 'ic_info_outline',
                width: 20,
                height: 20,
              ),
            ],
          ),
        ),
      );
    });
  }
}

class _TooltipItem extends StatelessWidget {
  const _TooltipItem({
    super.key,
    required this.title,
    required this.content,
  });
  final String title;
  final String content;

  @override
  Widget build(BuildContext context) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          ' •',
          style: UITextStyle.semiBold.copyWith(
            color: UIColors.grayBackground,
            fontSize: 14,
          ),
        ),
        const SizedBox(width: 3),
        Expanded(
          child: RichText(
            text: TextSpan(
                text: title,
                style: UITextStyle.semiBold.copyWith(
                  fontSize: 14,
                  color: UIColors.dartBlueText,
                ),
                children: [
                  TextSpan(
                    text: content,
                    style: UITextStyle.regular.copyWith(
                      fontSize: 14,
                      color: UIColors.dartBlueText,
                    ),
                  )
                ]),
          ),
        ),
      ],
    );
  }
}
