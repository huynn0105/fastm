import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/count_down_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/common/widgets/tracking_event/tracking_app_life_cycle_widget.dart';
import 'package:flutter_module/features/mfast/page/time_checking_history/components/time_checking_info_component.dart';
import 'package:flutter_module/features/personal_setting/cubit/time_checking_config/time_checking_config_cubit.dart';
import 'package:flutter_module/routes/routes.gr.dart';

class TimeCheckingConfigComponent extends StatelessWidget {
  const TimeCheckingConfigComponent({super.key});

  @override
  Widget build(BuildContext context) {
    final cubit = context.read<TimeCheckingConfigCubit>();
    return BlocBuilder<TimeCheckingConfigCubit, TimeCheckingConfigState>(
      builder: (context, state) {
        if (state.timeChecking == null) {
          return const SizedBox();
        }

        ///
        final data = state.timeChecking;

        ///
        return TrackingAppLifeCycleWidget(
          onBackground: cubit.pauseCountDown,
          onForeground: cubit.resumeCountDown,
          child: Padding(
            padding: const EdgeInsets.symmetric(horizontal: 16),
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  'Báo cáo, điểm danh hôm nay',
                  style: UITextStyle.semiBold.copyWith(
                    fontSize: 14,
                    color: UIColors.grayText,
                  ),
                ),
                const SizedBox(
                  height: 8,
                ),
                Container(
                  padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 12),
                  decoration: BoxDecoration(
                    color: UIColors.white,
                    borderRadius: BorderRadius.circular(8),
                  ),
                  child: Column(
                    children: [
                      if (state.showTimeCheckingCountDown) ...[
                        SizedBox(
                          height: 45,
                          child: Row(
                            mainAxisAlignment: MainAxisAlignment.start,
                            children: [
                              Expanded(
                                child: Column(
                                  crossAxisAlignment: CrossAxisAlignment.start,
                                  children: [
                                    Text(
                                      data?.checkConfig?.title ?? '',
                                      style: UITextStyle.regular.copyWith(
                                        fontSize: 14,
                                        color: UIColors.grayText,
                                      ),
                                    ),
                                    Text(
                                      data?.checkConfig?.desc ?? '',
                                      style: UITextStyle.semiBold.copyWith(
                                        fontSize: 16,
                                        color: UIColors.defaultText,
                                      ),
                                    ),
                                  ],
                                ),
                              ),
                              PrimaryButton(
                                onPressed: () => onChecking(context, data?.checkConfig?.type ?? ''),
                                height: 45,
                                padding: const EdgeInsets.symmetric(horizontal: 16),
                                title: 'Điểm danh',
                              ),
                            ],
                          ),
                        ),
                        const SizedBox(
                          height: 3,
                        ),
                        ValueListenableBuilder(
                          valueListenable: cubit.countdownUtil!.result,
                          builder: (context, value, child) {
                            if (value <= 0) {
                              return const SizedBox();
                            }
                            return Row(
                              children: [
                                const AppImage.asset(
                                  asset: 'ic_count_down',
                                  width: 24,
                                  height: 24,
                                  fit: BoxFit.contain,
                                  color: UIColors.accentOrange,
                                ),
                                const SizedBox(
                                  width: 2,
                                ),
                                Expanded(
                                  child: Text(
                                    'Hết hạn sau ${CountDownUtil.formatTimeChecking(value)}',
                                    style: UITextStyle.semiBold.copyWith(
                                      fontSize: 14,
                                      color: UIColors.accentOrange,
                                    ),
                                  ),
                                ),
                              ],
                            );
                          },
                        ),
                        const Divider(
                          color: UIColors.border,
                        ),
                      ],
                      AppSplashButton(
                        onTap: () => _onReport(context),
                        child: SizedBox(
                          height: 35,
                          child: Row(
                            children: [
                              const AppImage.asset(
                                asset: 'ic_money_target',
                                width: 24,
                                height: 24,
                                fit: BoxFit.contain,
                              ),
                              const SizedBox(
                                width: 12,
                              ),
                              Text(
                                'Báo cáo hằng ngày',
                                style: UITextStyle.semiBold.copyWith(
                                  fontSize: 14,
                                  color: UIColors.primaryColor,
                                ),
                              ),
                              const Spacer(),
                              Visibility(
                                visible: (data?.totalReport ?? 0) > 0,
                                child: Text(
                                  '${data?.totalReport}',
                                  style: UITextStyle.semiBold.copyWith(
                                    fontSize: 14,
                                    color: UIColors.primaryColor,
                                  ),
                                ),
                              ),
                              const SizedBox(
                                width: 4,
                              ),
                              const AppImage.asset(
                                asset: 'ic_arrow_right',
                                width: 18,
                                height: 18,
                                fit: BoxFit.contain,
                                color: UIColors.primaryColor,
                              ),
                            ],
                          ),
                        ),
                      ),
                    ],
                  ),
                ),
                const SizedBox(
                  height: 24,
                ),
              ],
            ),
          ),
        );
      },
    );
  }

  _onReport(BuildContext context) {
    ///
    context.router.push(const TimeCheckingHistoryRoute());
  }
}
