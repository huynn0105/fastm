import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/features/account/cubit/user/user_cubit.dart';
import 'package:flutter_module/features/legendary/cubit/legendary_supporter/legendary_supporter_cubit.dart';

class PersonalSettingLayoutComponent extends StatelessWidget {
  const PersonalSettingLayoutComponent({
    super.key,
    required this.builder,
  });

  final Widget Function(
    UserState userState,
    LegendarySupporterState supporterState,
  ) builder;

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<UserCubit, UserState>(
      builder: (context, userState) {
        return BlocBuilder<LegendarySupporterCubit, LegendarySupporterState>(
          builder: (context, supporterState) {
            return builder(userState, supporterState);
          },
        );
      },
    );
  }
}
