import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/features/personal_setting/cubit/personal_setting/personal_setting_cubit.dart';
import 'package:flutter_module/routes/routes.gr.dart';

class DelMFastLeadComponent extends StatelessWidget {
  const DelMFastLeadComponent({super.key});

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<PersonalSettingCubit, PersonalSettingState>(
      builder: (context, state) {
        if (state.delMFastLead == null) {
          return const SizedBox();
        }

        ///
        const title = 'Thống kê DEL MFast Lead';
        final fpd5 = state.delMFastLead?.fpd5;
        final fpd10 = state.delMFastLead?.fpd10;
        final url = state.delMFastLead?.url;

        ///
        return Padding(
          padding: const EdgeInsets.symmetric(horizontal: 16),
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                'Thống kê DEL MFast Lead',
                style: UITextStyle.semiBold.copyWith(
                  fontSize: 14,
                  color: UIColors.grayText,
                ),
              ),
              const SizedBox(
                height: 8,
              ),
              Container(
                padding: const EdgeInsets.all(12),
                decoration: BoxDecoration(
                  color: UIColors.white,
                  borderRadius: BorderRadius.circular(8),
                ),
                child: IntrinsicHeight(
                  child: Row(
                    crossAxisAlignment: CrossAxisAlignment.stretch,
                    children: [
                      Expanded(
                        child: AppSplashButton(
                          onTap: () => _onDetail(context, title, url),
                          child: Column(
                            children: [
                              Text(
                                '${fpd5?.count ?? 0} Khách hàng',
                                style: UITextStyle.bold.copyWith(
                                  fontSize: 16,
                                  color: UIColors.red,
                                ),
                              ),
                              const SizedBox(
                                height: 2,
                              ),
                              Text(
                                'đến kì FPD trong ${fpd5?.day ?? 0} ngày',
                                style: UITextStyle.regular.copyWith(
                                  fontSize: 13,
                                  color: UIColors.grayText,
                                ),
                              ),
                            ],
                          ),
                        ),
                      ),
                      const VerticalDivider(
                        color: UIColors.border,
                      ),
                      Expanded(
                        child: AppSplashButton(
                          onTap: () => _onDetail(context, title, url),
                          child: Column(
                            children: [
                              Text(
                                '${fpd10?.count ?? 0} Khách hàng',
                                style: UITextStyle.bold.copyWith(
                                  fontSize: 16,
                                  color: UIColors.orange,
                                ),
                              ),
                              const SizedBox(
                                height: 2,
                              ),
                              Text(
                                'đến kì FPD trong ${fpd10?.day ?? 0} ngày',
                                style: UITextStyle.regular.copyWith(
                                  fontSize: 13,
                                  color: UIColors.grayText,
                                ),
                              ),
                            ],
                          ),
                        ),
                      ),
                    ],
                  ),
                ),
              ),
              const SizedBox(
                height: 24,
              ),
            ],
          ),
        );
      },
    );
  }

  _onDetail(
    BuildContext context,
    String? title,
    String? url,
  ) {
    context.router.push(WebViewRoute(title: title, url: url));
  }
}
