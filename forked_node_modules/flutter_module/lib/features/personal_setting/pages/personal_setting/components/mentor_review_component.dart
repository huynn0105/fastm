import 'package:flutter/material.dart';
import 'package:flutter_rating_bar/flutter_rating_bar.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/size.dart';

class MentorReviewComponent extends StatefulWidget {
  const MentorReviewComponent({
    Key? key,
    required this.name,
    required this.url,
  }) : super(key: key);

  final String name;
  final String url;

  @override
  State<MentorReviewComponent> createState() => _MentorReviewComponentState();
}

class _MentorReviewComponentState extends State<MentorReviewComponent> {
  int rating = 0;

  late FocusNode focusNode;
  late TextEditingController controller;

  @override
  void initState() {
    super.initState();
    focusNode = FocusNode();
    controller = TextEditingController();
  }

  @override
  void dispose() {
    focusNode.dispose();
    controller.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return LimitedBox(
      maxHeight: AppSize.instance.height * 0.75,
      child: SingleChildScrollView(
        child: Padding(
          padding: const EdgeInsets.symmetric(vertical: 21, horizontal: 16),
          child: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              AppImage.network(
                url: widget.url,
                width: 80,
                height: 80,
                borderRadius: BorderRadius.circular(40),
              ),
              const SizedBox(
                height: 10,
              ),
              Text(
                widget.name,
                style: UITextStyle.semiBold.copyWith(
                  fontSize: 18,
                ),
              ),
              const SizedBox(
                height: 10,
              ),
              RatingBar(
                glow: false,
                minRating: 0,
                initialRating: 0,
                allowHalfRating: false,
                direction: Axis.horizontal,
                itemCount: 5,
                itemPadding: const EdgeInsets.symmetric(horizontal: 4),
                ratingWidget: RatingWidget(
                  full: const AppImage.asset(
                    asset: 'ic_star',
                    width: 24,
                    height: 24,
                    color: UIColors.green,
                  ),
                  half: const SizedBox(),
                  empty: const AppImage.asset(
                    asset: 'ic_star_outline',
                    width: 24,
                    height: 24,
                  ),
                ),
                onRatingUpdate: (rating) {
                  setState(() {
                    this.rating = rating.toInt();
                  });
                },
              ),
              const SizedBox(
                height: 16,
              ),
              Text(
                "Chạm vào dấu sao để đánh giá",
                style: UITextStyle.regular.copyWith(
                  color: UIColors.grayText,
                ),
              ),
              const SizedBox(
                height: 16,
              ),
              Container(
                clipBehavior: Clip.hardEdge,
                constraints: const BoxConstraints(
                  minHeight: 48,
                ),
                decoration: BoxDecoration(
                  borderRadius: BorderRadius.circular(8),
                ),
                child: TextField(
                  controller: controller,
                  focusNode: focusNode,
                  cursorWidth: 2,
                  cursorHeight: 23,
                  cursorColor: UIColors.primaryColor,
                  cursorRadius: const Radius.circular(8),
                  style: UITextStyle.regular,
                  onChanged: (value) {},
                  minLines: 3,
                  maxLines: 3,
                  decoration: InputDecoration(
                    filled: true,
                    fillColor: UIColors.white,
                    hintText: "Nhập nhận xét (nếu có)",
                    hintStyle: UITextStyle.medium.copyWith(
                      fontSize: 16,
                      height: 1.5,
                      color: UIColors.grayText,
                    ),
                    border: InputBorder.none,
                    focusedBorder: OutlineInputBorder(
                      borderSide: const BorderSide(
                        color: UIColors.primaryColor,
                      ),
                      borderRadius: BorderRadius.circular(8),
                    ),
                    contentPadding: const EdgeInsets.symmetric(
                      vertical: 8,
                      horizontal: 16,
                    ),
                  ),
                ),
              ),
              const SizedBox(
                height: 20,
              ),
              PrimaryButton(
                onPressed: () {
                  Navigator.of(context).pop();
                },
                padding: const EdgeInsets.symmetric(horizontal: 48),
                title: "Hoàn tất",
              ),
              const SizedBox(
                height: 20,
              ),
              Container(
                padding: const EdgeInsets.all(16),
                decoration: BoxDecoration(
                  color: UIColors.lightBlue,
                  borderRadius: BorderRadius.circular(8),
                ),
                child: Row(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    const AppImage.asset(
                      asset: "ic_info_fill",
                      width: 24,
                      height: 24,
                    ),
                    const SizedBox(
                      width: 16,
                    ),
                    Expanded(
                      child: RichText(
                        text: TextSpan(
                          text: "Đánh giá của bạn rất quan trọng "
                              "trong việc nâng cao chất lượng dẫn dắt. Vui lòng phản ánh ",
                          style: UITextStyle.regular.copyWith(),
                          children: [
                            TextSpan(
                              text: "trung thực ",
                              style: UITextStyle.bold.copyWith(
                                color: UIColors.darkBlue,
                              ),
                            ),
                            const TextSpan(
                              text: "và ",
                            ),
                            TextSpan(
                              text: "chi tiết",
                              style: UITextStyle.bold.copyWith(
                                color: UIColors.darkBlue,
                              ),
                            ),
                            const TextSpan(
                              text: ".",
                            ),
                          ],
                        ),
                      ),
                    ),
                  ],
                ),
              ),
              const SizedBox(
                height: 100,
              ),
            ],
          ),
        ),
      ),
    );
  }
}
