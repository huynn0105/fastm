import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/features/personal_setting/pages/login_history/items/login_history_item.dart';
import 'package:flutter_module/models/personal_setting/login_history_model.dart';

class GroupLoginHistoryItem extends StatelessWidget {
  const GroupLoginHistoryItem({
    super.key,
    required this.title,
    required this.data,
  });

  final String title;
  final List<LoginHistoryModel> data;

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 12),
      decoration: const BoxDecoration(
        color: UIColors.white,
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: UITextStyle.medium.copyWith(
              fontSize: 16,
              color: UIColors.darkBlue,
            ),
          ),
          const SizedBox(
            height: 8,
          ),
          ListView.separated(
            shrinkWrap: true,
            padding: EdgeInsets.zero,
            physics: const NeverScrollableScrollPhysics(),
            itemBuilder: (context, index) {
              return LoginHistoryItem(
                data: data[index],
              );
            },
            separatorBuilder: (_, __) => const Divider(
              height: 24,
              color: UIColors.border,
            ),
            itemCount: data.length,
          ),
        ],
      ),
    );
  }
}
