part of 'mentor_rating_cubit.dart';

class MentorRatingState extends Equatable {
  const MentorRatingState({
    this.status = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final MentorRatingModel? data;

  MentorRatingState copyWith({
    BlocStatus? status,
    MentorRatingModel? data,
  }) {
    return MentorRatingState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }

  @override
  List<Object?> get props => [status, data];
}
