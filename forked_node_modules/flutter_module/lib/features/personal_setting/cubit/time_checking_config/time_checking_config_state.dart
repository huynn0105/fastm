part of 'time_checking_config_cubit.dart';

class TimeCheckingConfigState extends Equatable {
  const TimeCheckingConfigState({
    this.status = BlocStatus.initial,
    this.timeChecking,
    this.statistic,
    this.showTimeCheckingCountDown = false,
  });

  final BlocStatus status;
  final TimeCheckingConfigModel? timeChecking;
  final StatisticConfigModel? statistic;
  final bool showTimeCheckingCountDown;

  @override
  List<Object?> get props => [
        status,
        timeChecking,
        statistic,
        showTimeCheckingCountDown,
      ];

  TimeCheckingConfigState copyWith({
    BlocStatus? status,
    TimeCheckingConfigModel? timeChecking,
    StatisticConfigModel? statistic,
    bool? clearTimeChecking,
    bool? clearStatistic,
    bool? showTimeCheckingCountDown,
  }) {
    return TimeCheckingConfigState(
      status: status ?? this.status,
      timeChecking: clearTimeChecking == true ? null : (timeChecking ?? this.timeChecking),
      statistic: clearStatistic == true ? null : (statistic ?? this.statistic),
      showTimeCheckingCountDown: showTimeCheckingCountDown ?? this.showTimeCheckingCountDown,
    );
  }
}
