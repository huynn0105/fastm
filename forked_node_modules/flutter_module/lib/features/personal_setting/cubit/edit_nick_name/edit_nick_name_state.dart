part of 'edit_nick_name_cubit.dart';

class EditNickNameState extends Equatable {
  const EditNickNameState({
    this.status = BlocStatus.initial,
    this.errorFields = const {},
    this.errorMessage,
    this.enabledNextStep = true,
  });

  final BlocStatus status;
  final Map<String, String> errorFields;
  final String? errorMessage;
  final bool enabledNextStep;

  @override
  List<Object?> get props => [
        status,
        errorFields,
        errorMessage,
        enabledNextStep,
      ];

  EditNickNameState copyWith({
    BlocStatus? status,
    Map<String, String>? errorFields,
    String? errorMessage,
    bool? enabledNextStep,
  }) {
    return EditNickNameState(
      status: status ?? this.status,
      errorFields: errorFields ?? this.errorFields,
      errorMessage: errorMessage ?? this.errorMessage,
      enabledNextStep: enabledNextStep ?? this.enabledNextStep,
    );
  }
}

enum EditNickNameFields {
  nickName,
}
