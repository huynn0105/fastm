part of 'security_cubit.dart';

class SecurityState extends Equatable {
  const SecurityState({
    this.status = BlocStatus.initial,
    this.loginByPassword = false,
    this.useBiometric = false,
  });

  final BlocStatus status;
  final bool loginByPassword;
  final bool useBiometric;

  @override
  List<Object> get props => [status, loginByPassword, useBiometric];

  SecurityState copyWith({
    BlocStatus? status,
    bool? loginByPassword,
    bool? useBiometric,
  }) {
    return SecurityState(
      status: status ?? this.status,
      loginByPassword: loginByPassword ?? this.loginByPassword,
      useBiometric: useBiometric ?? this.useBiometric,
    );
  }
}
