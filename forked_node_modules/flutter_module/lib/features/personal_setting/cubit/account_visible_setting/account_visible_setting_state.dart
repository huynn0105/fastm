part of 'account_visible_setting_cubit.dart';

class AccountVisibleSettingState extends Equatable {
  const AccountVisibleSettingState({
    this.status = BlocStatus.initial,
    this.updateStatus = BlocStatus.initial,
    this.settings = const [],
    this.errorMessage,
  });

  final BlocStatus status;
  final BlocStatus updateStatus;
  final List<VisibleSettingModel> settings;
  final String? errorMessage;

  @override
  List<Object> get props => [
        status,
        updateStatus,
        settings,
      ];

  AccountVisibleSettingState copyWith({
    BlocStatus? status,
    BlocStatus? updateStatus,
    List<VisibleSettingModel>? settings,
    String? errorMessage,
  }) {
    return AccountVisibleSettingState(
      status: status ?? this.status,
      updateStatus: updateStatus ?? this.updateStatus,
      settings: settings ?? this.settings,
      errorMessage: errorMessage,
    );
  }
}
