part of 'personal_setting_cubit.dart';

class PersonalSettingState extends Equatable {
  const PersonalSettingState({
    this.status = BlocStatus.initial,
    this.user,
    this.data = const [],
    this.coreAgent,
  });

  final BlocStatus status;
  final UserInfoModel? user;
  final List<PersonalSettingModel> data;
  final CoreAgent? coreAgent;

  PersonalSettingState copyWith({
    BlocStatus? status,
    UserInfoModel? user,
    CoreAgent? coreAgent,
    List<PersonalSettingModel>? data,
  }) {
    return PersonalSettingState(
      status: status ?? this.status,
      user: user ?? this.user,
      data: data ?? this.data,
      coreAgent: coreAgent ?? this.coreAgent,
    );
  }

  @override
  List<Object?> get props => [status, user, data, coreAgent];
}
