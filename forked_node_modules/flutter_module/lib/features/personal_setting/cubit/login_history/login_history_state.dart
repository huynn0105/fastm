part of 'login_history_cubit.dart';

class LoginHistoryState extends Equatable {
  const LoginHistoryState({
    this.status = BlocStatus.initial,
    this.histories = const [],
    this.groupHistories = const {},
  });

  final BlocStatus status;
  final List<LoginHistoryModel> histories;
  final Map<String, List<LoginHistoryModel>> groupHistories;

  @override
  List<Object> get props => [
        status,
        histories,
        groupHistories,
      ];

  LoginHistoryState copyWith({
    BlocStatus? status,
    List<LoginHistoryModel>? histories,
    Map<String, List<LoginHistoryModel>>? groupHistories,
  }) {
    return LoginHistoryState(
      status: status ?? this.status,
      histories: histories ?? this.histories,
      groupHistories: groupHistories ?? this.groupHistories,
    );
  }
}
