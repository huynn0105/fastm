part of 'home_search_cubit.dart';

class HomeSearchState extends Equatable {
  const HomeSearchState({
    this.status = BlocStatus.initial,
    this.products = const [],
    this.searchedProducts = const [],
    this.highlightProducts = const [],
    this.recentSearchedProducts = const [],
  });

  final BlocStatus status;
  final List<HomeItemModel> products;
  final List<HomeItemModel> searchedProducts;
  final List<HomeItemModel> highlightProducts;
  final List<HomeItemModel> recentSearchedProducts;

  @override
  List<Object> get props => [
        status,
        products,
        searchedProducts,
        highlightProducts,
        recentSearchedProducts,
      ];

  HomeSearchState copyWith({
    BlocStatus? status,
    List<HomeItemModel>? products,
    List<HomeItemModel>? searchedProducts,
    List<HomeItemModel>? highlightProducts,
    List<HomeItemModel>? recentSearchedProducts,
  }) {
    return HomeSearchState(
      status: status ?? this.status,
      products: products ?? this.products,
      searchedProducts: searchedProducts ?? this.searchedProducts,
      highlightProducts: highlightProducts ?? this.highlightProducts,
      recentSearchedProducts: recentSearchedProducts ?? this.recentSearchedProducts,
    );
  }
}
