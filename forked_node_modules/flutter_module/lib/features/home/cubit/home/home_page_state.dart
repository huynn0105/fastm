part of 'home_page_cubit.dart';

class HomePageState extends Equatable {
  const HomePageState({
    this.status = BlocStatus.initial,
    this.layout = const [],
    this.group = const [],
    this.banners = const [],
    this.tips = const [],
    this.news = const [],
    this.contests = const [],
    this.dpd,
    this.isUserGuide = false,
  });

  final BlocStatus status;
  final List<HomeDataModel> layout;
  final List<MainGroupModel> group;
  final List<HomeBannerModel> banners;
  final List<NewsModel> tips;
  final List<NewsModel> news;
  final List<ContestItemModel> contests;
  final DPDModel? dpd;
  final bool isUserGuide;

  @override
  List<Object?> get props => [
        status,
        layout,
        group,
        banners,
        tips,
        news,
        contests,
        dpd,
        isUserGuide,
      ];

  HomePageState copyWith({
    BlocStatus? status,
    List<HomeDataModel>? layout,
    List<MainGroupModel>? group,
    List<HomeBannerModel>? banners,
    List<NewsModel>? tips,
    List<NewsModel>? news,
    List<ContestItemModel>? contests,
    DPDModel? dpd,
    bool? isUserGuide,
    bool? clearDPD,
  }) {
    return HomePageState(
      status: status ?? this.status,
      layout: layout ?? this.layout,
      group: group ?? this.group,
      banners: banners ?? this.banners,
      tips: tips ?? this.tips,
      news: news ?? this.news,
      contests: contests ?? this.contests,
      dpd: clearDPD == true ? null : dpd ?? this.dpd,
      isUserGuide: isUserGuide ?? this.isUserGuide,
    );
  }
}
