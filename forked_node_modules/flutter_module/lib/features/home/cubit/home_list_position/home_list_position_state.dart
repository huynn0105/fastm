part of 'home_list_position_cubit.dart';

class HomeListPositionState extends Equatable {
  const HomeListPositionState({
    this.data = const [],
    this.index = 0,
  });

  final int index;
  final List<PositionMainGroupModel> data;

  HomeListPositionState copyWith({
    int? index,
    List<PositionMainGroupModel>? data,
  }) {
    return HomeListPositionState(
      index: index ?? this.index,
      data: data ?? this.data,
    );
  }

  @override
  List<Object> get props => [index, data];

  @override
  String toString() {
    return "{data: ${data.toList()}, alias: ${data.valueAt(index)?.alias}";
  }
}
