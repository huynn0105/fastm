part of 'filter_cubit.dart';

class FilterState extends Equatable {
  const FilterState({
    this.status = BlocStatus.initial,
  });

  final BlocStatus status;

  FilterState copyWith({
    BlocStatus? status,
  }) {
    return FilterState(
      status: status ?? this.status,
    );
  }

  @override
  List<Object> get props => [status];
}
