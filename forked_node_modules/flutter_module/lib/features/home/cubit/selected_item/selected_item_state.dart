part of 'selected_item_cubit.dart';

class SelectedItemState extends Equatable {
  const SelectedItemState({
    this.id = "",
  });

  final String id;

  SelectedItemState copyWith({
    String? id,
  }) {
    return SelectedItemState(
      id: id ?? this.id,
    );
  }

  @override
  List<Object> get props => [id];
}
