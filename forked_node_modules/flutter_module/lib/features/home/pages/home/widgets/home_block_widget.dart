import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/measure_size_widget.dart';
import 'package:flutter_module/features/home/pages/home/widgets/home_divider.dart';


class HomeBlockWidget extends StatefulWidget {
  const HomeBlockWidget({
    super.key,
    required this.alias,
    required this.title,
    required this.onSizeChanged,
    this.subtitle,
    this.icon,
    this.child,
    this.showSpaceBetween = true,
    this.showBottomPadding = true,
    this.onTapSubTitle,
    this.showEmptySizedBox = false,
  });

  final String alias;
  final String title;
  final String? subtitle;
  final Widget? icon;
  final Widget? child;
  final bool showSpaceBetween;
  final bool showBottomPadding;
  final Function()? onTapSubTitle;
  final bool showEmptySizedBox;
  final Function(String, Size) onSizeChanged;

  @override
  State<HomeBlockWidget> createState() => _HomeBlockWidgetState();
}

class _HomeBlockWidgetState extends State<HomeBlockWidget> {
  @override
  Widget build(BuildContext context) {
    return MeasureSizeWidget(
      onSizeChanged: (size) {
        widget.onSizeChanged(widget.alias, size);
      },
      child: widget.showEmptySizedBox
          ? const SizedBox()
          : Column(
              children: [
                Padding(
                  padding: const EdgeInsets.symmetric(horizontal: 16),
                  child: Row(
                    mainAxisAlignment: MainAxisAlignment.spaceBetween,
                    crossAxisAlignment: CrossAxisAlignment.baseline,
                    textBaseline: TextBaseline.alphabetic,
                    children: [
                      RichText(
                        maxLines: 1,
                        overflow: TextOverflow.ellipsis,
                        text: TextSpan(
                          text: widget.title,
                          style: UITextStyle.semiBold.copyWith(
                            fontSize: 18,
                            height: 1,
                          ),
                          children: [
                            if (widget.icon != null)
                              WidgetSpan(
                                child: Builder(builder: (context) {
                                  return Padding(
                                    padding: const EdgeInsets.only(left: 5),
                                    child: widget.icon!,
                                  );
                                }),
                              ),
                          ],
                        ),
                      ),
                      if (widget.subtitle != null)
                        AppSplashButton(
                          onTap: () {
                            widget.onTapSubTitle?.call();
                          },
                          child: Text(
                            widget.subtitle!,
                            style: UITextStyle.regular.copyWith(
                              fontSize: 14,
                              color: UIColors.primaryColor,
                            ),
                          ),
                        ),
                    ],
                  ),
                ),
                if (widget.showSpaceBetween)
                  const SizedBox(
                    height: 10,
                  ),
                if (widget.child != null) widget.child!,
                if (widget.showBottomPadding) const HomeDivider(),
              ],
            ),
    );
  }
}
