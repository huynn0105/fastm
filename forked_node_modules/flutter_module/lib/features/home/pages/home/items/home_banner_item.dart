import 'package:flutter/material.dart';
import 'package:flutter_module/common/utils/deeplink_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/home_data/home_banner_model.dart';

class HomeBannerItem extends StatelessWidget {
  const HomeBannerItem({
    super.key,
    required this.data,
  });

  final HomeBannerModel data;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: () => _onTap(context),
      borderRadius: BorderRadius.circular(16),
      child: AppImage.network(
        url: data.newsImage,
        height: 200,
        width: double.infinity,
        fit: BoxFit.fill,
        borderRadius: BorderRadius.circular(16),
      ),
    );
  }

  _onTap(BuildContext context) {
    DeepLinkUtil.open(
      data.newsUrl ?? "",
      requiredLogin: true,
    );
  }
}
