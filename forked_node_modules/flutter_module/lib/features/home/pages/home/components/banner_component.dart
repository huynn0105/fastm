import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/size.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/features/home/cubit/home/home_page_cubit.dart';
import 'package:flutter_module/features/home/pages/home/items/home_banner_item.dart';

class HomeBannerComponent extends StatelessWidget {
  final SwiperController controller;
  const HomeBannerComponent({
    Key? key,
    required this.controller,
  }) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<HomePageCubit, HomePageState>(
      builder: (context, state) {
        final data = state.banners;
        return Swiper(
          itemBuilder: (BuildContext context, int index) {
            return Stack(
              children: [
                Padding(
                  padding: const EdgeInsets.only(bottom: 10, top: 10, left: 10),
                  child: HomeBannerItem(
                    data: data[index],
                  ),
                ),
                _UserGuideCard(
                  isUserGuide: index == 0 && state.isUserGuide,
                  onTap: () {
                    context.read<HomePageCubit>().disableGuide();
                  },
                )
              ],
            );
          },
          itemCount: data.length,
          itemHeight: 200,
          index: 0,
          loop: true,
          controller: controller,
          autoplay: true,
          duration: 500,
          autoplayDelay: 5000,
          itemWidth: AppSize.instance.width * 0.94,
          layout: SwiperLayout.STACK,
          axisDirection: AxisDirection.right,
          scrollDirection: Axis.horizontal,
        );
      },
    );
  }
}

class _UserGuideCard extends StatelessWidget {
  const _UserGuideCard({
    required this.isUserGuide,
    required this.onTap,
  });

  final bool isUserGuide;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    return Visibility(
      visible: isUserGuide,
      child: Padding(
        padding: const EdgeInsets.only(right: 10),
        child: GestureDetector(
          onPanStart: (_) {
            onTap();
          },
          onTap: onTap,
          child: Container(
            decoration: BoxDecoration(color: Colors.black.withOpacity(0.9), borderRadius: BorderRadius.circular(20)),
            height: 200,
            width: AppSize.instance.width * 0.94,
            alignment: Alignment.center,
            child: Column(
              mainAxisSize: MainAxisSize.min,
              children: [
                const AppImage.asset(
                  asset: 'ic_swipe',
                  color: UIColors.white,
                  width: 50,
                ),
                const SizedBox(height: 16),
                Text(
                  'Vuốt để xem các sự kiện tiếp theo',
                  style: UITextStyle.medium.copyWith(
                    color: UIColors.white,
                    fontSize: 16,
                  ),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}
