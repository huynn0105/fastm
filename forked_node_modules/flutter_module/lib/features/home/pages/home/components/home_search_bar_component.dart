class HomeSearchBarComponent extends StatelessWidget {
  const HomeSearchBarComponent({Key? key, required this.width}) : super(key: key);

  final double width;

  @override
  Widget build(BuildContext context) {
    return Align(
      alignment: Alignment.center,
      child: Row(
        children: [
          Expanded(
            child: SplashButton(
              onTap: () {
                final cubit = context.read<HomePageCubit>();
                final homeGroups = cubit.state.layout;
                context.router.push(HomeSearchRoute(
                  homeGroups: homeGroups,
                ));
              },
              borderRadius: BorderRadius.circular(20),
              child: Container(
                decoration: const BoxDecoration(
                  borderRadius: BorderRadius.only(
                    topLeft: Radius.circular(20),
                    bottomLeft: Radius.circular(20),
                  ),
                ),
                child: Container(
                  height: 40,
                  padding: const EdgeInsets.symmetric(
                    horizontal: 16,
                  ),
                  decoration: BoxDecoration(
                    color: const Color(0xFFd6e5ff).withOpacity(0.3),
                    borderRadius: BorderRadius.circular(30),
                  ),
                  child: Row(
                    children: [
                      Icon(
                        Icons.search_rounded,
                        size: 25,
                        color: UIColors.white.withOpacity(0.5),
                      ),
                      const SizedBox(
                        width: 10,
                      ),
                      Text(
                        "Tìm kiếm",
                        style: UITextStyle.regular.copyWith(
                          color: UIColors.white.withOpacity(0.5),
                        ),
                      ),
                    ],
                  ),
                ),
              ),
            ),
          ),
          const SizedBox(
            width: 10,
          ),
          BlocBuilder<AuthCubit, AuthState>(
            builder: (context, state) {
              if (!state.hasLoggedIn) {
                return Container(
                  height: 40,
                  width: 40,
                  alignment: Alignment.center,
                  child: const AppImage.asset(
                    asset: 'ic_mfast_logo',
                    width: 24,
                    height: 23,
                  ),
                );
              }

              ///
              final highlightChatBot = AppData.instance.appInfo.highlightChatBot;
              return Row(
                mainAxisSize: MainAxisSize.min,
                children: [
                  if (highlightChatBot?.showChatBot == true) ...[
                    AppSplashButton(
                      onTap: () {
                        context.pushRoute(ChatRoute(chatBotID: highlightChatBot?.id));
                      },
                      child: Container(
                        height: 40,
                        width: 40,
                        alignment: Alignment.center,
                        child: Lottie.asset(
                          'assets/jsons/mascot_chat_bot_head.json',
                          width: 32,
                          height: 32,
                          repeat: false,
                          package: EnvData.instance.package,
                        ),
                      ),
                    ),
                  ],
                  BlocBuilder<PushNotificationCubit, PushNotificationState>(
                    builder: (context, state) {
                      return Row(
                        mainAxisSize: MainAxisSize.min,
                        children: [
                          AppSplashButton(
                            onTap: () {
                              context.pushRoute(ChatRoute());
                            },
                            child: Container(
                              height: 40,
                              width: 40,
                              alignment: Alignment.center,
                              child: BadgeWidget(
                                count: state.chatUnreadCount,
                                child: const AppImage.asset(
                                  asset: "ic_chat",
                                  height: 24,
                                  width: 24,
                                ),
                              ),
                            ),
                          ),
                          AppSplashButton(
                            onTap: () {
                              context.pushRoute(const MFastNotificationRoute());
                            },
                            child: Container(
                              height: 40,
                              width: 40,
                              alignment: Alignment.center,
                              child: BadgeWidget(
                                count: state.notificationUnreadCount,
                                child: const AppImage.asset(
                                  asset: "ic_notification",
                                  height: 24,
                                  width: 24,
                                  color: UIColors.white,
                                ),
                              ),
                            ),
                          ),
                        ],
                      );
                    },
                  ),
                ],
              );
            },
          ),
        ],
      ),
    );
  }
}
