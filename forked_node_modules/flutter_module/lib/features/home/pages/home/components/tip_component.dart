import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/models/news/news_model.dart';
import 'package:flutter_module/routes/routes.gr.dart';

import '../../../../../common/widgets/images.dart';
import '../widgets/home_block_widget.dart';

class TipComponent extends StatelessWidget {
  const TipComponent({
    super.key,
    required this.alias,
    required this.data,
    required this.onSizeChanged,
  });

  final String alias;
  final List<NewsModel> data;
  final Function(String, Size) onSizeChanged;

  @override
  Widget build(BuildContext context) {
    return HomeBlockWidget(
      showEmptySizedBox: data.isEmpty,
      onSizeChanged: onSizeChanged,
      alias: alias,
      title: "Mẹo dành cho bạn",
      subtitle: "Tất cả",
      onTapSubTitle: () {
        context.pushRoute(NewsRoute(focusedTabIndex: '1', newsTabIndex: '5'));
      },
      child: SizedBox(
        height: 90,
        child: ListView.separated(
          scrollDirection: Axis.horizontal,
          padding: const EdgeInsets.symmetric(horizontal: 16),
          itemBuilder: (context, index) {
            final item = data[index];
            return SplashButton(
              onTap: () {
                context.router.push(WebViewRoute(
                  title: item.postTitle ?? "",
                  url: item.webURL ?? "",
                ));
              },
              borderRadius: BorderRadius.circular(10),
              child: AppImage.network(
                url: item.postImage ?? '',
                height: 90,
                width: 150,
                fit: BoxFit.fill,
                borderRadius: BorderRadius.circular(10),
              ),
            );
          },
          itemCount: data.length,
          separatorBuilder: (_, __) => const SizedBox(
            width: 12,
          ),
        ),
      ),
    );
  }
}
