import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/features/home/pages/home/items/home_product_item.dart';
import 'package:flutter_module/models/home_data/home_item_model.dart';
import 'package:flutter_staggered_grid_view/flutter_staggered_grid_view.dart';

class HighlightProductComponent extends StatelessWidget {
  const HighlightProductComponent({
    super.key,
    required this.data,
    required this.onTap,
  });

  final List<HomeItemModel> data;
  final Function(HomeItemModel) onTap;

  @override
  Widget build(BuildContext context) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Padding(
          padding: const EdgeInsets.fromLTRB(16, 16, 16, 0),
          child: Text(
            'Sản phẩm nổi bật',
            style: UITextStyle.regular.copyWith(
              fontSize: 14,
              color: UIColors.grayText,
            ),
          ),
        ),
        AlignedGridView.count(
          crossAxisCount: 4,
          shrinkWrap: true,
          padding: EdgeInsets.zero,
          physics: const NeverScrollableScrollPhysics(),
          itemBuilder: (context, index) {
            final item = data[index];
            return Center(
              child: HomeProductItem(
                alias: '',
                item: item,
                onTap: () => onTap(item),
                showBadge: false,
                showHighlight: false,
                maxTitleLine: 3,
              ),
            );
          },
          itemCount: data.length,
        ),
      ],
    );
  }
}
