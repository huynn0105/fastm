import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/home_data/home_item_model.dart';

class SearchedProductComponent extends StatelessWidget {
  const SearchedProductComponent({
    super.key,
    required this.data,
    required this.onTap,
  });

  final List<HomeItemModel> data;
  final Function(HomeItemModel) onTap;

  @override
  Widget build(BuildContext context) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Padding(
          padding: const EdgeInsets.fromLTRB(16, 16, 16, 0),
          child: Text(
            'Kết quả tìm kiếm',
            style: UITextStyle.regular.copyWith(
              fontSize: 14,
              color: UIColors.grayText,
            ),
          ),
        ),
        Visibility(
          visible: data.isEmpty,
          child: Center(
            child: Column(
              mainAxisSize: MainAxisSize.min,
              mainAxisAlignment: MainAxisAlignment.center,
              crossAxisAlignment: CrossAxisAlignment.center,
              children: [
                const SizedBox(
                  height: 24,
                ),
                const AppImage.asset(
                  asset: 'ic_null',
                  width: 22,
                  height: 22,
                ),
                const SizedBox(
                  height: 8,
                ),
                Text(
                  'Không tìm thấy sản phẩm liên quan',
                  style: UITextStyle.regular.copyWith(fontSize: 14, color: UIColors.grayText),
                )
              ],
            ),
          ),
        ),
        Visibility(
          visible: data.isNotEmpty,
          child: ListView.separated(
            shrinkWrap: true,
            padding: EdgeInsets.zero,
            physics: const NeverScrollableScrollPhysics(),
            itemBuilder: (context, index) {
              final item = data[index];
              return AppSplashButton(
                onTap: () => onTap(item),
                child: Container(
                  height: 48,
                  padding: const EdgeInsets.symmetric(horizontal: 12),
                  child: Row(
                    children: [
                      Container(
                        height: 48,
                        width: 48,
                        alignment: Alignment.center,
                        child: AppImage.network(
                          url: item.icon ?? '',
                          width: 32,
                          height: 32,
                          fit: BoxFit.contain,
                        ),
                      ),
                      const SizedBox(
                        width: 12,
                      ),
                      Expanded(
                        child: Text(
                          item.title ?? '',
                          style: UITextStyle.regular.copyWith(
                            fontSize: 15,
                            color: UIColors.defaultText,
                          ),
                        ),
                      ),
                      const SizedBox(
                        width: 12,
                      ),
                      const AppImage.asset(
                        asset: 'ic_arrow_right',
                        width: 24,
                        height: 24,
                      ),
                    ],
                  ),
                ),
              );
            },
            separatorBuilder: (_, __) => const SizedBox(
              height: 12,
            ),
            itemCount: data.length,
          ),
        ),
      ],
    );
  }
}
