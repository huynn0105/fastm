import '../../../models/base_model.dart';

class HomeRepository {
  Future<BaseModel<HomePageLayoutModel>> getHomePageData() {
    return ApiProvider.instance.home.getHomePageData();
  }

  Future<BaseModel<MFastAppInfoModel>> getAppInfo() {
    return ApiProvider.instance.app.getAppInfo();
  }

  Future<BaseModel<List<HomeBannerModel>>> getHomeBanners() {
    return ApiProvider.instance.home.getHomeBanners();
  }

  Future<BaseModel<List<NewsModel>>> getHomeTips() {
    return ApiProvider.instance.home.getHomeTips();
  }

  Future<BaseModel<DPDModel>> getHomeDPD() {
    return ApiProvider.instance.home.getHomeDPD();
  }

  Future<BaseModel<List<HomeItemModel>>> getHighlightProjects() {
    return ApiProvider.instance.home.getHighlightProjects();
  }

  Future<BaseModel<bool>> trackingEvent(MFastTrackingEventPayload payload) {
    return ApiProvider.instance.home.trackingEvent(payload);
  }

  Future<BaseModel<bool>> trackingEventNonUser(MFastTrackingEventNonUserPayload payload) {
    return ApiProvider.instance.home.trackingEventNonUser(payload);
  }

  Future<BaseModel<String>> genProjectLink(GenProjectLinkPayload payload) {
    return ApiProvider.instance.home.genProjectLink(payload);
  }
}
