part of 'mfast_add_bank_account_cubit.dart';

class MFastAddBankAccountState extends Equatable {
  const MFastAddBankAccountState({
    this.status = BlocStatus.initial,
    this.submitStatus = BlocStatus.initial,
    this.banks = const [],
    this.bankBranches = const [],
    this.groupBankBranches = const {},
    this.selectedGeneralBank,
    this.selectedGeneralBankBranch,
    this.enabledGeneralBank = false,
    this.enabledGeneralBankBranch = false,
    this.enabledSubmit = true,
    this.errorFields = const {},
    this.errorMessage,
    this.bidvMetaData,
    this.bidvBankAccount,
  });

  final BlocStatus status;
  final BlocStatus submitStatus;
  final List<MFastGeneralBankModel> banks;
  final List<MFastGeneralBankBranchModel> bankBranches;
  final Map<String, List<MFastGeneralBankBranchModel>> groupBankBranches;
  final String? selectedGeneralBank;
  final String? selectedGeneralBankBranch;
  final bool enabledGeneralBank;
  final bool enabledGeneralBankBranch;
  final bool enabledSubmit;
  final Map<String, String> errorFields;
  final String? errorMessage;
  final MFastSuggestedBankModel? bidvMetaData;
  final MFastBIDVBankAccountModel? bidvBankAccount;

  @override
  List<Object?> get props => [
        status,
        submitStatus,
        banks,
        bankBranches,
        groupBankBranches,
        selectedGeneralBank,
        selectedGeneralBankBranch,
        enabledGeneralBank,
        enabledGeneralBankBranch,
        enabledSubmit,
        errorFields,
        errorMessage,
        bidvMetaData,
        bidvBankAccount,
      ];

  MFastAddBankAccountState copyWith({
    BlocStatus? status,
    BlocStatus? submitStatus,
    List<MFastGeneralBankModel>? banks,
    List<MFastGeneralBankBranchModel>? bankBranches,
    Map<String, List<MFastGeneralBankBranchModel>>? groupBankBranches,
    String? selectedGeneralBank,
    String? selectedGeneralBankBranch,
    bool? enabledGeneralBank,
    bool? enabledGeneralBankBranch,
    bool? enabledSubmit,
    Map<String, String>? errorFields,
    String? errorMessage,
    MFastSuggestedBankModel? bidvMetaData,
    MFastBIDVBankAccountModel? bidvBankAccount,
    bool? clearSelectedBankBranch,
  }) {
    return MFastAddBankAccountState(
      status: status ?? this.status,
      submitStatus: submitStatus ?? this.submitStatus,
      banks: banks ?? this.banks,
      bankBranches: bankBranches ?? this.bankBranches,
      groupBankBranches: groupBankBranches ?? this.groupBankBranches,
      selectedGeneralBank: selectedGeneralBank ?? this.selectedGeneralBank,
      selectedGeneralBankBranch:
          clearSelectedBankBranch == true ? null : selectedGeneralBankBranch ?? this.selectedGeneralBankBranch,
      enabledGeneralBank: enabledGeneralBank ?? this.enabledGeneralBank,
      enabledGeneralBankBranch: enabledGeneralBankBranch ?? this.enabledGeneralBankBranch,
      enabledSubmit: enabledSubmit ?? this.enabledSubmit,
      errorFields: errorFields ?? this.errorFields,
      errorMessage: errorMessage ?? this.errorMessage,
      bidvMetaData: bidvMetaData ?? this.bidvMetaData,
      bidvBankAccount: bidvBankAccount ?? this.bidvBankAccount,
    );
  }
}
