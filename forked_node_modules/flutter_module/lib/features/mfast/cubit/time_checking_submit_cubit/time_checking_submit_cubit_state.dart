part of 'time_checking_submit_cubit_cubit.dart';

class TimeCheckingSubmitState extends Equatable {
  final BlocStatus status;
  final String? statusCode;
  final String? errorMessage;
  final BlocStatus takePictureStatus;
  final File? fileImage;

  const TimeCheckingSubmitState({
    this.status = BlocStatus.initial,
    this.takePictureStatus = BlocStatus.initial,
    this.statusCode,
    this.fileImage,
    this.errorMessage,
  });

  TimeCheckingSubmitState copyWith({
    BlocStatus status = BlocStatus.initial,
    BlocStatus? takePictureStatus,
    String? statusCode,
    String? errorMessage,
    File? fileImage,
    bool? isClearPicture,
  }) {
    return TimeCheckingSubmitState(
      statusCode: statusCode,
      errorMessage: errorMessage,
      status: status,
      fileImage: isClearPicture == true ? null : fileImage ?? this.fileImage,
      takePictureStatus: takePictureStatus ?? this.takePictureStatus,
    );
  }

  @override
  List<Object?> get props => [
        status,
        statusCode,
        takePictureStatus,
        fileImage,
        errorMessage,
      ];
}
