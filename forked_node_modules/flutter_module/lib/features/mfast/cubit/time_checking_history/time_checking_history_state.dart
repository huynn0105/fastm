part of 'time_checking_history_cubit.dart';

class TimeCheckingHistoryState extends Equatable {
  const TimeCheckingHistoryState({
    this.status = BlocStatus.initial,
    this.selectedDay,
    required this.dateTime,
    this.errorMessage = '',
    this.eventsList = const {},
    this.dataInDay,
    this.dataInMonth = const [],
    this.isCalendarClick = false,
  });
  final BlocStatus status;
  final DateTime? selectedDay;
  final DateTime dateTime;
  final Map<DateTime, List<String>> eventsList;
  final String errorMessage;
  final TimeCheckingInfoModel? dataInDay;
  final List<TimeCheckingInfoModel> dataInMonth;
  final bool isCalendarClick;

  @override
  List<Object?> get props => [
        status,
        selectedDay,
        eventsList,
        dataInDay,
        dataInMonth,
        errorMessage,
        dateTime,
        isCalendarClick,
      ];

  TimeCheckingHistoryState copyWith({
    BlocStatus? status,
    DateTime? selectedDay,
    DateTime? dateTime,
    String? errorMessage,
    Map<DateTime, List<String>>? eventsList,
    TimeCheckingInfoModel? dataInDay,
    List<TimeCheckingInfoModel>? dataInMonth,
    bool isClearData = false,
    bool? isCalendarClick,
  }) {
    return TimeCheckingHistoryState(
      status: status ?? this.status,
      selectedDay: selectedDay ?? this.selectedDay,
      dateTime: dateTime ?? this.dateTime,
      errorMessage: errorMessage ?? this.errorMessage,
      eventsList: eventsList ?? this.eventsList,
      dataInDay: isClearData ? null : dataInDay ?? this.dataInDay,
      dataInMonth: isClearData ? [] : dataInMonth ?? this.dataInMonth,
      isCalendarClick: isCalendarClick ?? this.isCalendarClick,
    );
  }
}
