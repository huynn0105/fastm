part of 'personal_information_cubit.dart';

class PersonalInformationState extends Equatable {
  const PersonalInformationState({
    this.status = BlocStatus.initial,
    this.districtStatus = BlocStatus.initial,
    this.lockStatus = BlocStatus.initial,
    this.checkStatus = BlocStatus.initial,
    this.submitStatus = BlocStatus.initial,
    this.userMetaData,
    this.districts = const [],
    this.updateErrorMessage = '',
  });

  final BlocStatus status;
  final BlocStatus districtStatus;
  final BlocStatus lockStatus;
  final BlocStatus checkStatus;
  final BlocStatus submitStatus;
  final MFastUserMetaDataModel? userMetaData;
  final List<DataWrapper> districts;
  final String updateErrorMessage;

  @override
  List<Object?> get props => [
        status,
        districtStatus,
        checkStatus,
        lockStatus,
        submitStatus,
        userMetaData,
        districts,
        updateErrorMessage,
      ];

  PersonalInformationState copyWith({
    BlocStatus? status,
    BlocStatus? districtStatus,
    BlocStatus? lockStatus,
    BlocStatus? checkStatus,
    BlocStatus? submitStatus,
    MFastUserMetaDataModel? userMetaData,
    List<DataWrapper>? districts,
    String? updateErrorMessage,
  }) {
    return PersonalInformationState(
      status: status ?? this.status,
      districtStatus: districtStatus ?? this.districtStatus,
      lockStatus: lockStatus ?? this.lockStatus,
      checkStatus: checkStatus ?? this.checkStatus,
      submitStatus: submitStatus ?? this.submitStatus,
      userMetaData: userMetaData ?? this.userMetaData,
      districts: districts ?? this.districts,
      updateErrorMessage: updateErrorMessage ?? this.updateErrorMessage,
    );
  }
}
