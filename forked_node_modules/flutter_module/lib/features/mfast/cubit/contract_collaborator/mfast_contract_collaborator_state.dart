part of 'mfast_contract_collaborator_cubit.dart';

class MFastContractCollaboratorState extends Equatable {
  const MFastContractCollaboratorState({
    this.status = BlocStatus.initial,
    this.submitStatus = BlocStatus.initial,
    this.acceptedPolicy = false,
    this.enabledSubmit = true,
    this.errorFields = const {},
    this.errorMessage,
    this.userMetaData,
  });

  final BlocStatus status;
  final BlocStatus submitStatus;
  final bool acceptedPolicy;
  final bool enabledSubmit;
  final Map<String, String> errorFields;
  final String? errorMessage;
  final MFastUserMetaDataModel? userMetaData;

  @override
  List<Object?> get props => [
        status,
        submitStatus,
        acceptedPolicy,
        enabledSubmit,
        errorFields,
        errorMessage,
        userMetaData,
      ];

  MFastContractCollaboratorState copyWith({
    BlocStatus? status,
    BlocStatus? submitStatus,
    bool? acceptedPolicy,
    bool? enabledSubmit,
    Map<String, String>? errorFields,
    String? errorMessage,
    MFastUserMetaDataModel? userMetaData,
  }) {
    return MFastContractCollaboratorState(
      status: status ?? this.status,
      submitStatus: submitStatus ?? this.submitStatus,
      acceptedPolicy: acceptedPolicy ?? this.acceptedPolicy,
      enabledSubmit: enabledSubmit ?? this.enabledSubmit,
      errorFields: errorFields ?? this.errorFields,
      errorMessage: errorMessage ?? this.errorMessage,
      userMetaData: userMetaData ?? this.userMetaData,
    );
  }
}
