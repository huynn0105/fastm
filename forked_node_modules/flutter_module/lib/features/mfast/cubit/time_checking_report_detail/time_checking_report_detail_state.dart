part of 'time_checking_report_detail_cubit.dart';

class TimeCheckingReportDetailState extends Equatable {
  const TimeCheckingReportDetailState({
    this.status = BlocStatus.initial,
    this.submitStatus = BlocStatus.initial,
    this.isFinalSubmit = false,
    this.submitReportData,
    this.reportDetailData,
    this.errorMessage,
    this.isEditMode = false,
  });

  final BlocStatus status;
  final BlocStatus submitStatus;
  final bool isFinalSubmit;
  final SubmitReportData? submitReportData;
  final TimeCheckingReportDetailModel? reportDetailData;
  final String? errorMessage;
  final bool isEditMode;

  @override
  List<Object?> get props {
    return [
      status,
      submitStatus,
      isFinalSubmit,
      submitReportData,
      reportDetailData,
      errorMessage,
      isEditMode,
    ];
  }

  TimeCheckingReportDetailState copyWith({
    BlocStatus? status,
    BlocStatus? submitStatus,
    bool? isFinalSubmit,
    bool? isEditMode,
    SubmitReportData? submitReportData,
    TimeCheckingReportDetailModel? reportDetailData,
    String? errorMessage,
  }) {
    return TimeCheckingReportDetailState(
      status: status ?? this.status,
      submitStatus: submitStatus ?? BlocStatus.initial,
      isFinalSubmit: isFinalSubmit ?? this.isFinalSubmit,
      isEditMode: isEditMode ?? this.isEditMode,
      submitReportData: submitReportData ?? this.submitReportData,
      reportDetailData: reportDetailData ?? this.reportDetailData,
      errorMessage: errorMessage,
    );
  }
}
