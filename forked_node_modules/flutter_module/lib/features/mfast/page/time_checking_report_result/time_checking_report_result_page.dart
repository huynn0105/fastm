@RoutePage()
class TimeCheckingReportResultPage extends StatefulWidget {
  const TimeCheckingReportResultPage({
    super.key,
    this.data,
  });

  final ReportSubmitResponseModel? data;

  @override
  State<TimeCheckingReportResultPage> createState() => _TimeCheckingReportResultPageState();
}

class _TimeCheckingReportResultPageState extends State<TimeCheckingReportResultPage> {
  bool forceRouterPop = false;

