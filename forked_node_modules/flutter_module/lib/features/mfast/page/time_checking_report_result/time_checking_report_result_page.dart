  void initState() {
    super.initState();
    WidgetsBinding.instance.addPostFrameCallback((timeStamp) {
      EventUtil.sendEventToRNv2(EventKey.refreshTimeChecking.value);
      eventBus.fire(RefreshHistoryInDayEventBus());
      eventBus.fire(RefresshTimeCheckingEventBus());
    });
  }

