                                left: 0,
                                right: 0,
                                child: AppImage.asset(
                                  asset: 'ic_mtrade_mascot_success',
                                  width: 140,
                                  height: 140,
                                ),
                              ),
                            ],
                          ),
                        ),
                        const SizedBox(
                          height: 12,
                        ),
                        Text(
                          'Hoàn tất báo cáo!',
                          style: UITextStyle.semiBold.copyWith(
                            fontSize: 18,
                            color: UIColors.accentGreen,
                          ),
                          textAlign: TextAlign.center,
                          maxLines: 2,
                          overflow: TextOverflow.ellipsis,
                        ),
                        const SizedBox(
                          height: 8,
                        ),
                        Text(
                          'Kết quả hoạt động ước tính của bạn sau báo cáo này là:',
                          style: UITextStyle.regular.copyWith(
