import 'package:assorted_layout_widgets/assorted_layout_widgets.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/global_function.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/features/mfast/page/time_checking_history/widgets/circular_percent_widget.dart';

class KPIWidget extends StatelessWidget {
  const KPIWidget({
    super.key,
    required this.title,
    required this.content,
    this.width,
    this.titleColor = UIColors.grayText,
    this.contentColor = UIColors.defaultText,
    this.backgroundColor = UIColors.white,
    this.headerBuilder,
  });

  final double? width;
  final String title;
  final String content;
  final Color titleColor;
  final Color contentColor;
  final Color backgroundColor;
  final Widget Function(Size)? headerBuilder;

  factory KPIWidget.salesAmount({
    required String content,
    double? width,
    double? height,
    Color titleColor = UIColors.grayText,
    Color contentColor = UIColors.defaultText,
    Color backgroundColor = UIColors.white,
    String title = 'Doanh số KPI\ntự khai ước tính',
  }) {
    return KPIWidget(
      width: width,
      title: title,
      content: content,
      titleColor: titleColor,
      contentColor: contentColor,
      backgroundColor: backgroundColor,
      headerBuilder: (size) {
        return AppImage.asset(
          asset: 'ic_target',
          width: size.width,
          height: size.height,
          fit: BoxFit.contain,
        );
      },
    );
  }

  factory KPIWidget.salesPercent({
    required String content,
    required double percent,
    double? width,
    double? height,
    Color titleColor = UIColors.grayText,
    Color backgroundColor = UIColors.white,
    String title = '%KPI\ntự khai ước tính',
  }) {
    final progressColor = GlobalFunction.getProgressColor(percent);
    return KPIWidget(
      width: width,
      title: title,
      content: content,
      titleColor: titleColor,
      contentColor: progressColor,
      backgroundColor: backgroundColor,
      headerBuilder: (size) {
        return CircularPercentWidget(
          title: 'KPI',
          percent: percent,
          size: size.width,
          progressColor: progressColor,
        );
      },
    );
  }

  @override
  Widget build(BuildContext context) {
    double headerSize = 48;
    double innerDistance = -(headerSize / 2);
    Widget? header;

    if (headerBuilder != null) {
      header = headerBuilder!(Size(headerSize, headerSize));
    } else {
      header = Container(
        height: headerSize,
        width: headerSize,
        color: UIColors.background,
      );
    }

    return ColumnSuper(
      invert: true,
      innerDistance: innerDistance,
      children: [
        Center(
          child: Container(
            width: headerSize,
            height: headerSize,
            decoration: const BoxDecoration(
              shape: BoxShape.circle,
              color: UIColors.white,
            ),
            child: header,
          ),
        ),
        Container(
          width: width,
          alignment: Alignment.center,
          padding: const EdgeInsets.fromLTRB(12, 36, 12, 12),
          decoration: BoxDecoration(
            color: backgroundColor,
            borderRadius: BorderRadius.circular(12),
          ),
          child: Column(
            mainAxisSize: MainAxisSize.min,
            children: [
              Container(
                alignment: Alignment.center,
                constraints: const BoxConstraints(
                  minHeight: 25,
                ),
                child: Text(
                  title,
                  style: UITextStyle.medium.copyWith(
                    fontSize: 14,
                    color: titleColor,
                  ),
                  textAlign: TextAlign.center,
                  maxLines: 2,
                  overflow: TextOverflow.ellipsis,
                ),
              ),
              const SizedBox(
                height: 3,
              ),
              Text(
                content,
                style: UITextStyle.semiBold.copyWith(
                  fontSize: 18,
                  color: contentColor,
                ),
                textAlign: TextAlign.center,
                maxLines: 2,
                overflow: TextOverflow.ellipsis,
              ),
            ],
          ),
        ),
      ],
    );
  }
}
