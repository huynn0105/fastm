                  height: 24,
                  color: UIColors.white,
                ),
              ),
            ),
            const SizedBox(
              width: 12,
            ),
            Expanded(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    title ?? '',
                    style: UITextStyle.medium.copyWith(
                      fontSize: 16,
