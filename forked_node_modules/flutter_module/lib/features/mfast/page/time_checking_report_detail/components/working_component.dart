import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/features/mfast/page/time_checking_report_detail/widgets/report_info_widget.dart';
import 'package:flutter_module/models/mfast/report/report_model.dart';

class WorkingReportComponent extends StatelessWidget {
  const WorkingReportComponent({
    super.key,
    required this.data,
  });

  final List<ReportModel> data;

  @override
  Widget build(BuildContext context) {
    if (data.isEmpty) {
      return const SizedBox();
    }
    return Padding(
      padding: const EdgeInsets.symmetric(horizontal: 16),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            'Báo cáo hoạt động',
            style: UITextStyle.semiBold.copyWith(
              fontSize: 16,
              color: UIColors.darkBlue,
            ),
          ),
          const SizedBox(
            height: 12,
          ),
          ListView.separated(
            shrinkWrap: true,
            padding: EdgeInsets.zero,
            physics: const NeverScrollableScrollPhysics(),
            itemBuilder: (context, index) {
              final item = data[index];
              return ReportInfoWidget(
                imageURL: item.image,
                title: item.title,
                items: item.items,
              );
            },
            separatorBuilder: (_, __) => const SizedBox(
              height: 16,
            ),
            itemCount: data.length,
          ),
        ],
      ),
    );
  }
}
