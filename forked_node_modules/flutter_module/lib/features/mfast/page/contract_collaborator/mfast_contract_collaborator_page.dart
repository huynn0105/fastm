import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_inappwebview/flutter_inappwebview.dart';
import 'package:flutter_module/app_data.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/redirect_util.dart';
import 'package:flutter_module/common/widgets/appbar.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

@RoutePage()
class MFastContractCollaboratorPage extends StatelessWidget {
  const MFastContractCollaboratorPage({super.key});

  @override
  Widget build(BuildContext context) {
    final webviewUrl = AppData.instance.appInfo.ctvUserMFastUrl ?? "";

    return WillPopScope(
      onWillPop: () async {
        if (!context.router.canPop()) {
          RedirectUtil.backToMFast();
          return false;
        }
        return true;
      },
      child: Scaffold(
        appBar: MFastSimpleAppBar(
          context: context,
          title: "Hợp đồng dịch vụ MFast",
          onBack: context.popRoute,
        ),
        body: Container(
          color: UIColors.background,
          child: Column(
            children: [
              const SizedBox(height: 10),
              Container(
                padding: const EdgeInsets.symmetric(vertical: 12),
                decoration: const BoxDecoration(
                  color: UIColors.white,
                ),
                child: Row(
                  crossAxisAlignment: CrossAxisAlignment.center,
                  children: [
                    const SizedBox(
                      width: 16,
                    ),
                    const AppImage.asset(
                      asset: "ic_shield",
                      width: 24,
                      height: 24,
                    ),
                    const SizedBox(
                      width: 8,
                    ),
                    Expanded(
                      child: Text(
                        "Ký hợp đồng dịch vụ với MFast thành công",
                        style: UITextStyle.regular.copyWith(
                          fontSize: 16,
                          color: UIColors.darkBlue,
                        ),
                      ),
                    ),
                  ],
                ),
              ),
              const SizedBox(height: 16),
              Expanded(
                child: Container(
                  clipBehavior: Clip.hardEdge,
                  margin: const EdgeInsets.symmetric(horizontal: 16),
                  decoration: BoxDecoration(
                    borderRadius: BorderRadius.circular(8),
                  ),
                  child: InAppWebView(
                    initialUrlRequest: URLRequest(
                      url: Uri.parse(webviewUrl).replace(queryParameters: AppData.instance.defaultParam.toJson()),
                    ),
                  ),
                ),
              ),
              Padding(
                padding: const EdgeInsets.symmetric(
                  horizontal: 16,
                  vertical: 16,
                ),
                child: PrimaryButton(
                  onPressed: context.popRoute,
                  width: double.infinity,
                  title: 'Quay lại',
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }
}
