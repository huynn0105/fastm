import 'package:flutter/material.dart';
import 'package:flutter_module/common/widgets/separated_widget.dart';
import 'package:flutter_module/features/mfast/page/account_identification/page/add_banking_account/widgets/info_confirm_widget.dart';

class BankConfirmComponent extends StatelessWidget {
  const BankConfirmComponent({
    super.key,
    required this.bankAccountName,
    required this.bankAccountNumber,
    required this.bankName,
    required this.bankBranchName,
  });

  final String bankAccountName;
  final String bankAccountNumber;
  final String bankName;
  final String bankBranchName;

  @override
  Widget build(BuildContext context) {
    return Column(
      mainAxisSize: MainAxisSize.min,
      children: [
        SeparatedColumn(
          mainAxisSize: MainAxisSize.min,
          separatorBuilder: (_, __) => const SizedBox(height: 5),
          children: List.generate(
            data.entries.length,
            (index) {
              final item = data.entries.toList()[index];
              return InfoConfirmWidget(
                title: item.key,
                content: item.value,
              );
            },
          ),
        ),
      ],
    );
  }

  Map<String, String> get data {
    return {
      'Chủ tài khoản': bankAccountName,
      'Số tài khoản': bankAccountNumber,
      'Tên ngân hàng': bankName,
      'Tên chi nhánh': bankBranchName,
    };
  }
}
