import 'package:ekyc/common/colors.dart';
import 'package:ekyc/common/styles.dart';
import 'package:flutter/material.dart';

class InfoConfirmWidget extends StatelessWidget {
  const InfoConfirmWidget({
    super.key,
    required this.title,
    required this.content,
    this.titleFlex = 3,
    this.contentFlex = 6,
  });

  final int titleFlex;
  final int contentFlex;
  final String title;
  final String content;

  @override
  Widget build(BuildContext context) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Expanded(
          flex: titleFlex,
          child: Text(
            '$title:',
            style: UITextStyle.regular.copyWith(
              fontSize: 13,
              color: UIColors.grayText,
            ),
          ),
        ),
        const SizedBox(
          width: 5,
        ),
        Expanded(
          flex: contentFlex,
          child: Text(
            content,
            style: UITextStyle.medium.copyWith(
              fontSize: 13,
              color: UIColors.defaultText,
            ),
            maxLines: 3,
            overflow: TextOverflow.ellipsis,
          ),
        ),
      ],
    );
  }
}
