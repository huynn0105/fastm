import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/extension/list_extension.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

class LinerStepIndicatorComponent extends StatelessWidget {
  const LinerStepIndicatorComponent({
    super.key,
    this.step = 1,
    this.width = 300,
    this.index = 0,
    this.onChangeIndex,
    this.labels = const [],
  });

  final int step;
  final double width;
  final int index;
  final Function(int index)? onChangeIndex;
  final List<String> labels;

  @override
  Widget build(BuildContext context) {
    final itemWidth = width / step;
    final process = index / (step - 1);

    return Center(
      child: SizedBox(
        width: width,
        child: Stack(
          children: [
            Positioned(
              top: 10,
              left: 0,
              right: 0,
              child: Container(
                margin: EdgeInsets.symmetric(horizontal: itemWidth / 2),
                alignment: Alignment.centerLeft,
                color: UIColors.lightGray,
                width: double.infinity,
                height: 4,
                child: AnimatedFractionallySizedBox(
                  duration: const Duration(milliseconds: 500),
                  heightFactor: 1,
                  widthFactor: process,
                  child: Container(
                    color: UIColors.blue,
                  ),
                ),
              ),
            ),
            Row(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: List.generate(step, (idx) {
                final isPassed = index > idx;
                final isCurrent = index == idx;
                final label = labels.valueAt(idx) ?? '';

                return SplashButton(
                  onTap: () {
                    onChangeIndex?.call(idx);
                  },
                  child: SizedBox(
                    width: itemWidth,
                    child: Column(
                      children: [
                        Container(
                          alignment: Alignment.center,
                          width: 24,
                          height: 24,
                          decoration: BoxDecoration(
                            gradient: LinearGradient(
                              colors: isPassed || isCurrent
                                  ? UIColors.stepGradient
                                  : [UIColors.gray, UIColors.gray],
                              begin: const Alignment(0.5, -0.5),
                              end: const Alignment(-0.5, 0.5),
                            ),
                            borderRadius: BorderRadius.circular(24 / 2),
                            border: Border.all(
                              color: UIColors.white,
                              width: isCurrent ? 2 : 0,
                            ),
                          ),
                          child: isPassed
                              ? const AppImage.asset(
                                  asset: 'ic_success',
                                  width: 12,
                                  height: 12,
                                  color: UIColors.white,
                                )
                              : Text(
                                  (idx + 1).toString(),
                                  style: UITextStyle.bold.copyWith(
                                    fontSize: 12,
                                    color: isCurrent ? UIColors.white : UIColors.grayBackground,
                                  ),
                                ),
                        ),
                        const SizedBox(
                          height: 8,
                        ),
                        Text(
                          label,
                          style: isCurrent
                              ? UITextStyle.semiBold.copyWith(
                                  fontSize: 12,
                                )
                              : UITextStyle.regular.copyWith(
                                  fontSize: 12,
                                  color: UIColors.grayText,
                                ),
                          textAlign: TextAlign.center,
                        ),
                      ],
                    ),
                  ),
                );
              }),
            ),
          ],
        ),
      ),
    );
  }
}
