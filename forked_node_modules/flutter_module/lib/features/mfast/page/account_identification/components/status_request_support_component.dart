import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/enum/mfast/liveness_support_status.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/images.dart';

class StatusRequestSupportComponent extends StatelessWidget {
  const StatusRequestSupportComponent({
    super.key,
    required this.status,
    this.title,
    this.note,
    this.margin = const EdgeInsets.only(top: 16),
  });

  final LivenessSupportStatus status;
  final String? title;
  final String? note;
  final EdgeInsets margin;

  @override
  Widget build(BuildContext context) {
    final Color backgroundColor = status.isPending
        ? UIColors.lightOrange
        : status.isSuccess
            ? UIColors.backgroundGreen
            : UIColors.backgroundRed;
    final Color borderColor = status.isPending
        ? UIColors.darkOrange
        : status.isSuccess
            ? UIColors.green
            : UIColors.red;
    final Color textColor = status.isPending
        ? UIColors.orangeText
        : status.isSuccess
            ? UIColors.green
            : UIColors.red;
    final String icon = status.isPending
        ? 'ic_pending_time_outline'
        : status.isSuccess
            ? 'ic_success_outline'
            : 'ic_error_outline';
    final String textStatus = status.isPending
        ? 'Chờ duyệt'
        : status.isSuccess
            ? "Hoàn tất"
            : 'Thất bại';

    return Container(
      margin: margin,
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(
        color: backgroundColor,
        borderRadius: BorderRadius.circular(8),
        border: Border.all(
          width: 1,
          color: borderColor,
        ),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            children: [
              Expanded(
                child: Text(
                  'Kết quả duyệt mã số thuế',
                  style: UITextStyle.regular.copyWith(
                    fontSize: 13,
                    color: UIColors.grayText,
                  ),
                ),
              ),
              AppImage.asset(
                asset: icon,
                width: 24,
                height: 24,
              ),
              const SizedBox(
                width: 8,
              ),
              Text(
                title ?? textStatus,
                style: UITextStyle.semiBold.copyWith(
                  color: textColor,
                  fontSize: 13,
                ),
              ),
            ],
          ),
          const SizedBox(
            height: 8,
          ),
          Text(
            note ?? '',
            style: UITextStyle.regular.copyWith(
              color: textColor,
              fontSize: 13,
            ),
          )
        ],
      ),
    );
  }
}
