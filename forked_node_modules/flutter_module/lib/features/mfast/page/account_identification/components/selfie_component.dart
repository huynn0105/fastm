import 'package:dotted_border/dotted_border.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

class SelfieComponent extends StatelessWidget {
  const SelfieComponent({
    super.key,
    this.url,
    this.onTap,
    this.onRemove,
    this.isLoading = false,
    this.hideRemove = false,
  });

  final String? url;
  final VoidCallback? onTap;
  final VoidCallback? onRemove;
  final bool isLoading;
  final bool hideRemove;

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      isDisable: isLoading,
      onTap: onTap,
      child: DottedBorder(
        padding: EdgeInsets.zero,
        dashPattern: const [6],
        strokeWidth: 2,
        radius: const Radius.circular(10),
        color: UIColors.lightGray,
        borderType: BorderType.Circle,
        child: Center(
          child: Container(
            width: 180,
            height: 180,
            decoration: BoxDecoration(
              color: UIColors.white,
              borderRadius: BorderRadius.circular(180 / 2),
            ),
            child: Stack(
              children: [
                if (TextUtils.isNotEmpty(url)) ...{
                  Container(
                    width: double.infinity,
                    height: double.infinity,
                    decoration: const BoxDecoration(
                      shape: BoxShape.circle,
                    ),
                    clipBehavior: Clip.hardEdge,
                    child: AppImage.network(
                      url: url,
                    ),
                  ),
                } else ...{
                  SizedBox(
                    width: double.infinity,
                    child: Column(
                      mainAxisAlignment: MainAxisAlignment.center,
                      children: [
                        const AppImage.asset(
                          asset: 'ic_selfie',
                          width: 34,
                          height: 40,
                          fit: BoxFit.contain,
                        ),
                        const SizedBox(
                          height: 8,
                        ),
                        Text(
                          'Ch√¢n dung',
                          style: UITextStyle.medium.copyWith(
                            color: UIColors.grayText,
                          ),
                        ),
                      ],
                    ),
                  ),
                },
                if (TextUtils.isNotEmpty(url) && !hideRemove)
                  Positioned(
                    top: 4,
                    right: 4,
                    child: SplashButton(
                      onTap: () {
                        onRemove?.call();
                      },
                      child: const AppImage.asset(
                        asset: 'ic_remove',
                        width: 24,
                        height: 24,
                      ),
                    ),
                  ),
                Visibility(
                  visible: isLoading,
                  child: const Center(
                    child: SizedBox(
                      height: 24,
                      width: 24,
                      child: CircularProgressIndicator(
                        strokeWidth: 1,
                        color: UIColors.primaryColor,
                      ),
                    ),
                  ),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}
