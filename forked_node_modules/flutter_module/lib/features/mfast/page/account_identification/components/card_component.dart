import 'package:dotted_border/dotted_border.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/enum/mfast/card_type.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

class CardComponent extends StatelessWidget {
  const CardComponent({
    super.key,
    required this.type,
    this.url,
    this.onTap,
    this.onRemove,
    this.isLoading = false,
    this.hideRemove = false,
  });

  final String? url;
  final CardType type;
  final VoidCallback? onTap;
  final VoidCallback? onRemove;
  final bool isLoading;
  final bool hideRemove;

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      isDisable: isLoading,
      onTap: onTap,
      child: DottedBorder(
        padding: EdgeInsets.zero,
        dashPattern: const [6],
        strokeWidth: 2,
        radius: const Radius.circular(10),
        color: UIColors.lightGray,
        borderType: BorderType.RRect,
        child: Container(
          decoration: BoxDecoration(
            color: UIColors.white,
            borderRadius: BorderRadius.circular(10),
          ),
          child: AspectRatio(
            aspectRatio: 150 / 100,
            child: Stack(
              clipBehavior: Clip.none,
              children: [
                SizedBox(
                  width: double.infinity,
                  child: TextUtils.isNotEmpty(url)
                      ? Container(
                          width: double.infinity,
                          height: double.infinity,
                          clipBehavior: Clip.hardEdge,
                          decoration: BoxDecoration(
                            color: UIColors.white,
                            borderRadius: BorderRadius.circular(10),
                          ),
                          child: AppImage.network(
                            url: url,
                          ),
                        )
                      : Column(
                          mainAxisAlignment: MainAxisAlignment.center,
                          children: [
                            AppImage.asset(
                              asset: type.isFront ? 'ic_card_front' : 'ic_card_back',
                              width: 40,
                              height: 29,
                              fit: BoxFit.contain,
                            ),
                            const SizedBox(
                              height: 8,
                            ),
                            Text(
                              type.isFront ? "Mặt trước" : 'Mặt sau',
                              style: UITextStyle.medium.copyWith(
                                color: UIColors.grayText,
                              ),
                            ),
                          ],
                        ),
                ),
                if (TextUtils.isNotEmpty(url) && !hideRemove)
                  Positioned(
                    top: -4,
                    right: -4,
                    child: SplashButton(
                      onTap: () {
                        onRemove?.call();
                      },
                      child: const AppImage.asset(
                        asset: 'ic_remove',
                        width: 24,
                        height: 24,
                      ),
                    ),
                  ),
                Visibility(
                  visible: isLoading,
                  child: const Center(
                    child: SizedBox(
                      height: 24,
                      width: 24,
                      child: CircularProgressIndicator(
                        strokeWidth: 1,
                        color: UIColors.primaryColor,
                      ),
                    ),
                  ),
                ),
              ],
            ),
          ),
        ),
      ),
    );
  }
}
