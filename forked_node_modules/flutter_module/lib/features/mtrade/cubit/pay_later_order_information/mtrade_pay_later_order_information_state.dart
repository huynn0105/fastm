part of 'mtrade_pay_later_order_information_cubit.dart';

class MTradePayLaterOrderInformationState extends Equatable {
  const MTradePayLaterOrderInformationState({
    this.curIndex = 0,
    this.preIndex = 0,
  });

  final int curIndex;
  final int preIndex;

  @override
  List<Object?> get props => [curIndex, preIndex];

  MTradePayLaterOrderInformationState copyWith({
    int? curIndex,
    int? preIndex,
  }) {
    return MTradePayLaterOrderInformationState(
      curIndex: curIndex ?? this.curIndex,
      preIndex: preIndex ?? this.preIndex,
    );
  }
}
