part of 'mtrade_campaign_cubit.dart';

class MTradeCampaignState extends Equatable {
  const MTradeCampaignState({
    this.status = BlocStatus.initial,
  });

  final BlocStatus status;

  @override
  List<Object> get props => [
        status,
      ];

  MTradeCampaignState copyWith({
    BlocStatus? status,
  }) {
    return MTradeCampaignState(
      status: status ?? this.status,
    );
  }
}
