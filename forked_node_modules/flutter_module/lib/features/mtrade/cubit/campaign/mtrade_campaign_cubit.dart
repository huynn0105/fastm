import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/features/mtrade/repository/mtrade_product_repository.dart';

import '../../../../models/mtrade/campaign/campaign_model.dart';

part 'mtrade_campaign_state.dart';

class MTradeCampaignCubit extends Cubit<MTradeCampaignState> {
  MTradeCampaignCubit() : super(const MTradeCampaignState());

  final MTradeProductRepository _repository = MTradeProductRepository();

  Future<CampaignModel?> getCampaignEvent() async {
    final result = await _repository.getCampaignEvent();
    return result.data;
  }
}
