part of 'mtrade_searched_product_cubit.dart';

class MTradeSearchedProductState extends Equatable {
  const MTradeSearchedProductState({
    this.status = BlocStatus.initial,
    this.products = const [],
    this.hasMore = true,
  });

  final BlocStatus status;
  final List<MTradeProductModel> products;
  final bool hasMore;

  @override
  List<Object> get props => [
        status,
        products,
        hasMore,
      ];

  MTradeSearchedProductState copyWith({
    BlocStatus? status,
    List<MTradeProductModel>? products,
    bool? hasMore,
  }) {
    return MTradeSearchedProductState(
      status: status ?? this.status,
      products: products ?? this.products,
      hasMore: hasMore ?? this.hasMore,
    );
  }
}
