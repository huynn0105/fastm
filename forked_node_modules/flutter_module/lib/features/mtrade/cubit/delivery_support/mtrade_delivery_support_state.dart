part of 'mtrade_delivery_support_cubit.dart';

class MTradeDeliverySupportState extends Equatable {
  const MTradeDeliverySupportState({
    this.provinceStatus = BlocStatus.initial,
    this.districtStatus = BlocStatus.initial,
    this.wardStatus = BlocStatus.initial,
    this.provinces = const [],
    this.districts = const [],
    this.wards = const [],
    this.selectedProvince,
    this.selectedDistrict,
    this.selectedWard,
    this.errorMessages = const {},
  });

  final BlocStatus provinceStatus;
  final BlocStatus districtStatus;
  final BlocStatus wardStatus;
  final List<MasterDataModel> provinces;
  final List<MasterDataModel> districts;
  final List<MasterDataModel> wards;
  final MasterDataModel? selectedProvince;
  final MasterDataModel? selectedDistrict;
  final MasterDataModel? selectedWard;
  final Map<String, String> errorMessages;

  @override
  List<Object?> get props => [
        provinceStatus,
        districtStatus,
        wardStatus,
        provinces,
        districts,
        wards,
        selectedProvince,
        selectedDistrict,
        selectedWard,
        errorMessages,
      ];

  MTradeDeliverySupportState copyWith({
    BlocStatus? provinceStatus,
    BlocStatus? districtStatus,
    BlocStatus? wardStatus,
    List<MasterDataModel>? provinces,
    List<MasterDataModel>? districts,
    List<MasterDataModel>? wards,
    MasterDataModel? selectedProvince,
    MasterDataModel? selectedDistrict,
    MasterDataModel? selectedWard,
    bool clearDistrict = false,
    bool clearWard = false,
    Map<String, String>? errorMessages,
  }) {
    return MTradeDeliverySupportState(
      provinceStatus: provinceStatus ?? this.provinceStatus,
      districtStatus: districtStatus ?? this.districtStatus,
      wardStatus: wardStatus ?? this.wardStatus,
      provinces: provinces ?? this.provinces,
      districts: districts ?? this.districts,
      wards: wards ?? this.wards,
      selectedProvince: selectedProvince ?? this.selectedProvince,
      selectedDistrict: clearDistrict ? null : selectedDistrict ?? this.selectedDistrict,
      selectedWard: clearWard ? null : selectedWard ?? this.selectedWard,
      errorMessages: errorMessages ?? this.errorMessages,
    );
  }
}
