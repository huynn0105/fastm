import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';

import '../../../../models/master_data/master_data_model.dart';
import '../../repository/mtrade_product_repository.dart';

part 'express_delivery_support_state.dart';

class ExpressDeliverySupportCubit extends Cubit<ExpressDeliverySupportState> {
  ExpressDeliverySupportCubit() : super(const ExpressDeliverySupportState());

  final MTradeProductRepository _repository = MTradeProductRepository();

  getExpressProvinceSupport(String? productGroup) async {
    final result = await _repository.getFastDeliveryLocation(
      productGroup: productGroup == null ? null : (productGroup.isNotEmpty ? [productGroup] : []),
    );
    emit(state.copyWith(
      provinces: result.data,
    ));
  }
}
