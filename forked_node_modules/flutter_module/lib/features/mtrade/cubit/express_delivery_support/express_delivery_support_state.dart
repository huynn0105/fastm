part of 'express_delivery_support_cubit.dart';

class ExpressDeliverySupportState extends Equatable {
  const ExpressDeliverySupportState({
    this.provinces = const [],
  });

  final List<MasterDataModel> provinces;

  @override
  List<Object?> get props => [
        provinces,
      ];

  ExpressDeliverySupportState copyWith({
    List<MasterDataModel>? provinces,
  }) {
    return ExpressDeliverySupportState(
      provinces: provinces ?? [],
    );
  }
}
