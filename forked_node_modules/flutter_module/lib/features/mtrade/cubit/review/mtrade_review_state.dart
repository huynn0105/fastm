part of 'mtrade_review_cubit.dart';

class MTradeReviewState extends Equatable {
  const MTradeReviewState({
    this.status = BlocStatus.initial,
    this.usersReview = const [],
    this.statusReview,
    this.reviewStatus = BlocStatus.initial,
  });

  final BlocStatus status;
  final List<MTradeSelectUserReviewModel> usersReview;
  final MTradeStatusReviewModel? statusReview;
  final BlocStatus reviewStatus;

  @override
  List<Object?> get props => [
        status,
        usersReview,
        statusReview,
        reviewStatus,
      ];

  MTradeReviewState copyWith({
    BlocStatus? status,
    List<MTradeSelectUserReviewModel>? usersReview,
    MTradeStatusReviewModel? statusReview,
    BlocStatus? reviewStatus,
  }) {
    return MTradeReviewState(
      status: status ?? this.status,
      usersReview: usersReview ?? this.usersReview,
      statusReview: statusReview ?? this.statusReview,
      reviewStatus: reviewStatus ?? this.reviewStatus,
    );
  }
}
