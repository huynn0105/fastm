part of 'mtrade_my_review_cubit.dart';

class MyReviewState extends Equatable {
  const MyReviewState({
    this.status = BlocStatus.initial,
    this.reviews = const [],
    this.totalReview = 0,
    this.statusFilter = BlocStatus.initial,
    this.filters = const [],
    this.sumary,
    this.listSelected,
  });
  final BlocStatus status;
  final List<MyReviewData> reviews;
  final int totalReview;
  final BlocStatus statusFilter;
  final List<FilterMyReview> filters;
  final SumaryMyReview? sumary;
  final Map<String, int>? listSelected;

  @override
  List<Object?> get props => [
        status,
        reviews,
        totalReview,
        statusFilter,
        filters,
        sumary,
        listSelected,
      ];

  MyReviewState copyWith({
    BlocStatus? status,
    List<MyReviewData>? reviews,
    int? totalReview,
    BlocStatus? statusFilter,
    List<FilterMyReview>? filters,
    SumaryMyReview? sumary,
    Map<String, int>? listSelected,
  }) {
    return MyReviewState(
      status: status ?? this.status,
      reviews: reviews ?? this.reviews,
      totalReview: totalReview ?? this.totalReview,
      statusFilter: statusFilter ?? this.statusFilter,
      filters: filters ?? this.filters,
      sumary: sumary ?? this.sumary,
      listSelected: listSelected ?? this.listSelected,
    );
  }
}
