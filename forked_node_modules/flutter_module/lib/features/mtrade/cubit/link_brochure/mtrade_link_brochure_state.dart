part of 'mtrade_link_brochure_cubit.dart';

class MTradeLinkBrochureState extends Equatable {
  const MTradeLinkBrochureState({
    this.status = BlocStatus.initial,
    this.brochure,
    this.sendStatus = BlocStatus.initial,
  });

  final BlocStatus status;
  final MTradeLinkBrochureModel? brochure;
  final BlocStatus sendStatus;

  @override
  List<Object?> get props => [
        status,
        brochure,
        sendStatus,
      ];

  MTradeLinkBrochureState copyWith({
    BlocStatus? status,
    MTradeLinkBrochureModel? brochure,
    BlocStatus? sendStatus,
  }) {
    return MTradeLinkBrochureState(
      status: status ?? this.status,
      brochure: brochure ?? this.brochure,
      sendStatus: sendStatus ?? this.sendStatus,
    );
  }
}
