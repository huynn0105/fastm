part of 'mtrade_order_detail_cubit.dart';

class MTradeOrderDetailState extends Equatable {
  const MTradeOrderDetailState({
    this.status = BlocStatus.initial,
    this.orderDetail,
  });

  final BlocStatus status;
  final MTradeOrderDetailModel? orderDetail;

  @override
  // TODO: implement props
  List<Object?> get props => [status, orderDetail];

  MTradeOrderDetailState copyWith({
    BlocStatus? status,
    MTradeOrderDetailModel? orderDetail,
  }) {
    return MTradeOrderDetailState(
      status: status ?? this.status,
      orderDetail: orderDetail ?? this.orderDetail,
    );
  }
}
