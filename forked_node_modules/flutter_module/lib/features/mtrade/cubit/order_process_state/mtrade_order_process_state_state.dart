part of 'mtrade_order_process_state_cubit.dart';

class MTradeOrderProcessStateState extends Equatable {
  const MTradeOrderProcessStateState({
    this.status = BlocStatus.initial,
    this.orders = const [],
  });

  final BlocStatus status;
  final List<OrderModel> orders;

  @override
  List<Object?> get props => [status, orders];

  MTradeOrderProcessStateState copyWith({
    BlocStatus? status,
    List<OrderModel>? orders,
  }) {
    return MTradeOrderProcessStateState(
      status: status ?? this.status,
      orders: orders ?? this.orders,
    );
  }
}
