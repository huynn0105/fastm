part of 'request_otp_cubit.dart';

class RequestOtpState extends Equatable {
  const RequestOtpState({
    this.requestStatus = BlocStatus.initial,
    this.verifyStatus = BlocStatus.initial,
    this.errorFields = const {},
    this.countDown,
    this.logOtpID,
  });

  final BlocStatus requestStatus;
  final BlocStatus verifyStatus;
  final Map<String, String> errorFields;
  final int? countDown;
  final int? logOtpID;

  @override
  List<Object?> get props => [
        requestStatus,
        verifyStatus,
        errorFields,
        countDown,
        logOtpID,
      ];

  RequestOtpState copyWith({
    BlocStatus? requestStatus,
    BlocStatus? verifyStatus,
    Map<String, String>? errorFields,
    int? countDown,
    int? logOtpID,
  }) {
    return RequestOtpState(
      requestStatus: requestStatus ?? this.requestStatus,
      verifyStatus: verifyStatus ?? this.verifyStatus,
      errorFields: errorFields ?? this.errorFields,
      countDown: countDown ?? this.countDown,
      logOtpID: logOtpID ?? this.logOtpID,
    );
  }
}
