part of 'mtrade_consent_cubit.dart';

class MTradeConsentState extends Equatable {
  const MTradeConsentState({
    this.status = BlocStatus.initial,
    this.errorMessage = '',
  });
  final BlocStatus status;
  final String errorMessage;

  @override
  List<Object> get props => [
        status,
        errorMessage,
      ];

  MTradeConsentState copyWith({
    BlocStatus? status,
    String? errorMessage,
  }) {
    return MTradeConsentState(
      status: status ?? this.status,
      errorMessage: errorMessage ?? this.errorMessage,
    );
  }
}
