part of 'mtrade_indirect_bonus_cubit.dart';

class MTradeIndirectBonusState extends Equatable {
  MTradeIndirectBonusState({
    this.status = BlocStatus.initial,
    this.bonus = const [],
    this.selectedLevelIndex = 0,
  });

  final BlocStatus status;
  final List<MTradeIndirectBonusModel> bonus;
  final int selectedLevelIndex;

  @override
  List<Object?> get props => [
        status,
        bonus,
        selectedLevelIndex,
      ];

  MTradeIndirectBonusState copyWith({
    BlocStatus? status,
    List<MTradeIndirectBonusModel>? bonus,
    int? selectedLevelIndex,
  }) {
    return MTradeIndirectBonusState(
      status: status ?? this.status,
      bonus: bonus ?? this.bonus,
      selectedLevelIndex: selectedLevelIndex ?? this.selectedLevelIndex,
    );
  }
}
