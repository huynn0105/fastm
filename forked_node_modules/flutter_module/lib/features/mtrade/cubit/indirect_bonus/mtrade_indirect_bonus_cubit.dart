import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import '../../../../common/bloc_status.dart';
import '../../../../models/mtrade/bonus/mtrade_indirect_bonus_model.dart';

import '../../../../services/api/mtrade/payload/mtrade_bonus_payload.dart';
import '../../repository/mtrade_product_repository.dart';

part 'mtrade_indirect_bonus_state.dart';

class MTradeIndirectBonusCubit extends Cubit<MTradeIndirectBonusState> {
  MTradeIndirectBonusCubit() : super(MTradeIndirectBonusState());

  final MTradeProductRepository _repository = MTradeProductRepository();
  MTradeBonusPayload _payload = MTradeBonusPayload();

  fetchData({bool showLoading = true, bool loadmore = false}) async {
    if (showLoading) {
      emit(state.copyWith(
        status: BlocStatus.loading,
      ));
    }

    updatePayload(page: loadmore ? (_payload.page ?? 1) + 1 : 1);

    final result = await _repository.getIndirectBonus(
      payload: _payload,
    );

    if (result.status) {
      var data = result.data ?? [];
      if (loadmore) {
        data = [...state.bonus, ...data];
      }
      emit(state.copyWith(
        status: BlocStatus.success,
        bonus: data,
      ));
    } else {
      emit(state.copyWith(
        status: BlocStatus.failure,
      ));
    }
  }

  refreshData() async {
    await fetchData(showLoading: false);
  }

  loadmoreData() async {
    final length = state.bonus.length;
    await fetchData(showLoading: false, loadmore: true);
    return length != state.bonus.length;
  }

  updateDate(DateTime date) {
    updatePayload(month: date.month, year: date.year);
  }

  updateQuery(String value) {
    updatePayload(
      filterText: value,
    );
  }

  updateLevel(int index) {
    emit(state.copyWith(
      selectedLevelIndex: index,
    ));
    updatePayload(filterLevel: index > 0 ? "$index" : "");
  }

  updateOrderFilter(Map<String, dynamic> orderFilter) {
    updatePayload(
      orderFilter: orderFilter,
    );
  }

  updatePayload({
    int? month,
    int? year,
    String? filterText,
    String? filterLevel,
    int? page,
    Map<String, dynamic>? orderFilter,
  }) {
    _payload = _payload.copyWith(
      filterText: filterText,
      filterLevel: filterLevel,
      month: month,
      year: year,
      page: page,
      orderFilter: orderFilter,
    );
  }
}
