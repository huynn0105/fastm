part of 'mtrade_hot_product_cubit.dart';

class MTradeHotProductState extends Equatable {
  const MTradeHotProductState({
    this.status = BlocStatus.initial,
    this.products = const [],
  });

  final BlocStatus status;
  final List<MTradeProductModel> products;

  @override
  List<Object> get props => [
        status,
        products,
      ];

  MTradeHotProductState copyWith({
    BlocStatus? status,
    List<MTradeProductModel>? products,
  }) {
    return MTradeHotProductState(
      status: status ?? this.status,
      products: products ?? this.products,
    );
  }
}
