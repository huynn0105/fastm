import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter_module/features/mtrade/repository/mtrade_product_repository.dart';
import 'package:flutter_module/services/api/mtrade/payload/mtrade_tracking_agent_payload.dart';

part 'mtrade_tracking_agent_state.dart';

class MTradeTrackingAgentCubit extends Cubit<MTradeTrackingAgentState> {
  MTradeTrackingAgentCubit() : super(const MTradeTrackingAgentState());

  final MTradeProductRepository _repository = MTradeProductRepository();

  trackingAgent({required MTradeTrackingAgentPayload payload}) async {
    final result = await _repository.trackingAgent(payload: payload);
  }
}
