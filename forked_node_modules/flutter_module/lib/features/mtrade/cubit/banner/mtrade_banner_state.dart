part of 'mtrade_banner_cubit.dart';

class MTradeBannerState extends Equatable {
  const MTradeBannerState({
    this.status = BlocStatus.initial,
    this.banners = const [],
  });

  final BlocStatus status;
  final List<MTradeBannerModel> banners;

  @override
  List<Object> get props => [
        status,
        banners,
      ];

  MTradeBannerState copyWith({
    BlocStatus? status,
    List<MTradeBannerModel>? banners,
  }) {
    return MTradeBannerState(
      status: status ?? this.status,
      banners: banners ?? this.banners,
    );
  }
}
