part of 'mtrade_product_group_cubit.dart';

class MTradeProductGroupState extends Equatable {
  const MTradeProductGroupState({
    this.groupStatus = BlocStatus.initial,
    this.productStatus = BlocStatus.initial,
    this.groups = const [],
    this.specialGroups = const [],
    this.products = const [],
    this.selectedProductGroup = "",
    this.selectedSpecialGroup,
    this.hasMore = true,
  });

  final BlocStatus groupStatus;
  final BlocStatus productStatus;
  final List<ProductGroupsModel> groups;
  final List<MTradeSpecialGroupModel> specialGroups;
  final List<MTradeProductModel> products;
  final String selectedProductGroup;
  final MTradeSpecialGroupModel? selectedSpecialGroup;
  final bool hasMore;

  @override
  List<Object?> get props => [
        groupStatus,
        productStatus,
        groups,
        specialGroups,
        products,
        selectedProductGroup,
        selectedSpecialGroup,
        hasMore,
      ];

  MTradeProductGroupState copyWith({
    BlocStatus? groupStatus,
    BlocStatus? productStatus,
    List<ProductGroupsModel>? groups,
    List<MTradeSpecialGroupModel>? specialGroups,
    List<MTradeProductModel>? products,
    String? selectedProductGroup,
    MTradeSpecialGroupModel? selectedSpecialGroup,
    bool? hasMore,
  }) {
    return MTradeProductGroupState(
      groupStatus: groupStatus ?? this.groupStatus,
      productStatus: productStatus ?? this.productStatus,
      groups: groups ?? this.groups,
      specialGroups: specialGroups ?? this.specialGroups,
      products: products ?? this.products,
      selectedProductGroup: selectedProductGroup ?? this.selectedProductGroup,
      selectedSpecialGroup: selectedSpecialGroup ?? this.selectedSpecialGroup,
      hasMore: hasMore ?? this.hasMore,
    );
  }
}
