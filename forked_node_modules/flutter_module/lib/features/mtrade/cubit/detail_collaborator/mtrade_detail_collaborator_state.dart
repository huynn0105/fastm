part of 'mtrade_detail_collaborator_cubit.dart';

class MTradeDetailCollaboratorState extends Equatable {
  const MTradeDetailCollaboratorState({
    this.status = BlocStatus.initial,
    this.collaborator,
  });

  final BlocStatus status;
  final MTradeCollaboratorModel? collaborator;

  @override
  List<Object?> get props => [status, collaborator];

  MTradeDetailCollaboratorState copyWith({
    BlocStatus? status,
    MTradeCollaboratorModel? collaborator,
  }) {
    return MTradeDetailCollaboratorState(
      status: status ?? this.status,
      collaborator: collaborator ?? this.collaborator,
    );
  }
}
