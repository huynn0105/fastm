part of 'mtrade_check_project_code_cubit.dart';

class MTradeCheckProjectCodeState extends Equatable {
  const MTradeCheckProjectCodeState({
    this.checkProjectCodePayNow = BlocStatus.initial,
    this.checkProjectCodePayLater = BlocStatus.initial,
    this.data,
  });

  final BlocStatus checkProjectCodePayNow;
  final BlocStatus checkProjectCodePayLater;
  final MTradeCheckProjectCodeModel? data;

  @override
  List<Object?> get props => [
        checkProjectCodePayNow,
        checkProjectCodePayLater,
        data,
      ];

  MTradeCheckProjectCodeState copyWith({
    BlocStatus? checkProjectCodePayNow,
    BlocStatus? checkProjectCodePayLater,
    MTradeCheckProjectCodeModel? data,
  }) {
    return MTradeCheckProjectCodeState(
      checkProjectCodePayNow: checkProjectCodePayNow ?? this.checkProjectCodePayNow,
      checkProjectCodePayLater: checkProjectCodePayLater ?? this.checkProjectCodePayLater,
      data: data ?? this.data,
    );
  }
}
