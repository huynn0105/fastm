part of 'mtrade_suggested_product_cubit.dart';

class MTradeSuggestedProductState extends Equatable {
  const MTradeSuggestedProductState({
    this.status = BlocStatus.initial,
    this.products = const [],
  });

  final BlocStatus status;
  final List<MTradeProductModel> products;

  @override
  List<Object> get props => [status, products];

  MTradeSuggestedProductState copyWith({
    BlocStatus? status,
    List<MTradeProductModel>? products,
  }) {
    return MTradeSuggestedProductState(
      status: status ?? this.status,
      products: products ?? this.products,
    );
  }
}
