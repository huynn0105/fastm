part of 'mtrade_search_order_cubit.dart';

class MTradeSearchOrderState extends Equatable {
  const MTradeSearchOrderState({
    this.status = BlocStatus.initial,
    this.orderHistory,
    this.data,
  });

  final BlocStatus status;
  final List<OrderHistoryModel>? orderHistory;
  final MTradeSearchOrderModel? data;

  @override
  List<Object?> get props => [
        status,
        orderHistory,
        data,
      ];

  MTradeSearchOrderState copyWith({
    BlocStatus? status,
    List<OrderHistoryModel>? orderHistory,
    MTradeSearchOrderModel? data,
  }) {
    return MTradeSearchOrderState(
      status: status ?? this.status,
      orderHistory: orderHistory ?? this.orderHistory,
      data: data ?? this.data,
    );
  }
}
