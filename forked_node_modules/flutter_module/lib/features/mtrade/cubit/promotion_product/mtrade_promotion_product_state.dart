part of 'mtrade_promotion_product_cubit.dart';

class MTradePromotionProductState extends Equatable {
  const MTradePromotionProductState({
    this.status = BlocStatus.initial,
    this.promotions = const [],
  });

  final BlocStatus status;
  final List<MTradePromotionModel> promotions;

  @override
  List<Object> get props => [status, promotions];

  MTradePromotionProductState copyWith({
    BlocStatus? status,
    List<MTradePromotionModel>? promotions,
  }) {
    return MTradePromotionProductState(
      status: status ?? this.status,
      promotions: promotions ?? this.promotions,
    );
  }
}
