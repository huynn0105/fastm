part of 'buyer_request_cubit.dart';

class BuyerRequestState extends Equatable {
  final BlocStatus status;
  final BlocStatus completeStepStatus;
  final BlocStatus otpStatus;
  final Map<String, String> errorFields;
  final int countDown;
  final String? errMsg;
  final String? redirectLink;
  final int? logOtpID;

  BuyerRequestState({
    this.status = BlocStatus.initial,
    this.completeStepStatus = BlocStatus.initial,
    this.otpStatus = BlocStatus.initial,
    Map<String, String>? errorFields,
    this.countDown = AppConstants.otpCountdown,
    this.errMsg,
    this.redirectLink,
    this.logOtpID,
  }) : errorFields = errorFields ?? {}; //workaround to modify errorFields

  @override
  List<Object?> get props => [
        status,
        completeStepStatus,
        otpStatus,
        countDown,
        errMsg,
        errorFields,
        redirectLink,
        logOtpID,
      ];

  BuyerRequestState copyWith({
    BlocStatus? status,
    BlocStatus? completeStepStatus,
    BlocStatus? otpStatus,
    Map<String, String>? errorFields,
    int? countDown,
    String? errMsg,
    String? redirectLink,
    int? logOtpID,
  }) {
    return BuyerRequestState(
      status: status ?? this.status,
      completeStepStatus: completeStepStatus ?? this.completeStepStatus,
      otpStatus: otpStatus ?? this.otpStatus,
      errorFields: errorFields ?? this.errorFields,
      countDown: countDown ?? this.countDown,
      errMsg: errMsg,
      redirectLink: redirectLink,
      logOtpID: logOtpID ?? this.logOtpID,
    );
  }
}
