part of 'review_brochure_cubit.dart';

class ReviewBrochureState extends Equatable {
  const ReviewBrochureState({
    this.status = BlocStatus.initial,
    this.sendBrochureStatus = BlocStatus.initial,
    this.brochureTitle,
    this.document,
    this.pageTotal,
  });

  final BlocStatus status;
  final BlocStatus sendBrochureStatus;
  final String? brochureTitle;
  final Uint8List? document;
  final int? pageTotal;

  @override
  List<Object?> get props => [
        status,
        sendBrochureStatus,
        brochureTitle,
        document,
        pageTotal,
      ];

  ReviewBrochureState copyWith({
    BlocStatus? status,
    BlocStatus? sendBrochureStatus,
    String? brochureTitle,
    Uint8List? document,
    int? pageTotal,
  }) {
    return ReviewBrochureState(
      status: status ?? this.status,
      sendBrochureStatus: sendBrochureStatus ?? BlocStatus.initial,
      brochureTitle: brochureTitle ?? this.brochureTitle,
      document: document ?? this.document,
      pageTotal: pageTotal ?? this.pageTotal,
    );
  }
}
