import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';

part 'tabbar_state.dart';

class TabbarCubit extends Cubit<TabbarState> {
  TabbarCubit() : super(const TabbarState());

  setIndex(int index) {
    emit(state.copyWith(
      index: index,
      previousIndex: state.index,
    ));
  }
}
