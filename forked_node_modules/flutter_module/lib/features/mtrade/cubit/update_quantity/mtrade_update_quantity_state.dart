part of 'mtrade_update_quantity_cubit.dart';

class MTradeUpdateQuantityState extends Equatable {
  const MTradeUpdateQuantityState({
    this.status = BlocStatus.initial,
    this.quantity = 1,
    this.enabledDecrease = false,
    this.enabledIncrease = false,
    this.errorMessage,
  });

  final BlocStatus status;
  final int quantity;
  final bool enabledDecrease;
  final bool enabledIncrease;
  final String? errorMessage;

  @override
  List<Object?> get props => [
        status,
        quantity,
        enabledDecrease,
        enabledIncrease,
        errorMessage,
      ];

  MTradeUpdateQuantityState copyWith({
    BlocStatus? status,
    int? quantity,
    bool? enabledDecrease,
    bool? enabledIncrease,
    String? errorMessage,
  }) {
    return MTradeUpdateQuantityState(
      status: status ?? this.status,
      quantity: quantity ?? this.quantity,
      enabledDecrease: enabledDecrease ?? this.enabledDecrease,
      enabledIncrease: enabledIncrease ?? this.enabledIncrease,
      errorMessage: errorMessage,
    );
  }
}
