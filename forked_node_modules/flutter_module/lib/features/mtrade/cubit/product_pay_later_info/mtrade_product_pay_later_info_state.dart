part of 'mtrade_product_pay_later_info_cubit.dart';

class MTradeProductPayLaterInfoState extends Equatable {
  const MTradeProductPayLaterInfoState({
    this.status = BlocStatus.initial,
    this.data,
    this.errorMessage,
  });

  final BlocStatus status;
  final MTradePayLaterInfoModel? data;
  final String? errorMessage;

  @override
  List<Object?> get props => [
        status,
        data,
        errorMessage,
      ];

  MTradeProductPayLaterInfoState copyWith({
    BlocStatus? status,
    MTradePayLaterInfoModel? data,
    String? errorMessage,
    bool clear = false,
  }) {
    return MTradeProductPayLaterInfoState(
      status: status ?? this.status,
      data: clear && data == null ? null : data ?? this.data,
      errorMessage: errorMessage,
    );
  }
}
