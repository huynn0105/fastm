import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/features/mtrade/repository/mtrade_product_repository.dart';
import 'package:flutter_module/models/mtrade/pay_later/mtrade_pay_later_info_model.dart';
import 'package:flutter_module/services/api/mtrade/payload/mtrade_get_pay_later_info_payload.dart';

part 'mtrade_product_pay_later_info_state.dart';

class MTradeProductPayLaterInfoCubit extends Cubit<MTradeProductPayLaterInfoState> {
  MTradeProductPayLaterInfoCubit() : super(const MTradeProductPayLaterInfoState());

  final MTradeProductRepository _repository = MTradeProductRepository();

  fetchData({bool showLoading = true, required String? sku}) async {
    if (showLoading) {
      emit(state.copyWith(
        status: BlocStatus.loading,
        clear: true,
      ));
    }

    final result = await _repository.getPayLaterInfo(
      payload: MTradeGetPayLaterInfoPayload(
        sku: sku,
      ),
    );

    if (result.status) {
      emit(state.copyWith(
        status: BlocStatus.success,
        data: result.data,
      ));
    } else {
      emit(state.copyWith(
        status: BlocStatus.failure,
        errorMessage: result.errorMessage,
      ));
    }
  }
}