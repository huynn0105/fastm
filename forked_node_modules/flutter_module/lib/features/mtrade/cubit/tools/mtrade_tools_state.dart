part of 'mtrade_tools_cubit.dart';

class MTradeToolsState extends Equatable {
  const MTradeToolsState({
    this.status = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final MTradeToolsModel? data;

  @override
  List<Object?> get props => [
        status,
        data,
      ];

  MTradeToolsState copyWith({
    BlocStatus? status,
    MTradeToolsModel? data,
  }) {
    return MTradeToolsState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
