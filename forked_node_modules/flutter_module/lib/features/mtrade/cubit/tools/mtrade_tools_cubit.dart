import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter_module/common/bloc_status.dart';

import '../../../../models/mtrade/tools/mtrade_tools_model.dart';
import '../../repository/mtrade_product_repository.dart';

part 'mtrade_tools_state.dart';

class MTradeToolsCubit extends Cubit<MTradeToolsState> {
  MTradeToolsCubit() : super(const MTradeToolsState());

  final MTradeProductRepository _repository = MTradeProductRepository();

  fetchData() async {
    emit(state.copyWith(
      status: BlocStatus.loading,
    ));

    final result = await _repository.getTools();

    if (result.status) {
      emit(state.copyWith(
        status: BlocStatus.success,
        data: result.data,
      ));
    } else {
      emit(state.copyWith(
        status: BlocStatus.failure,
      ));
    }
  }
}
