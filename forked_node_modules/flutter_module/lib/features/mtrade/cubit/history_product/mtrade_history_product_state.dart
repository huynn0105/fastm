part of 'mtrade_history_product_cubit.dart';

class MTradeHistoryProductState extends Equatable {
  const MTradeHistoryProductState({
    this.status = BlocStatus.initial,
    this.products = const [],
  });

  final BlocStatus status;
  final List<MTradeProductModel> products;

  @override
  List<Object> get props => [
        status,
        products,
      ];

  MTradeHistoryProductState copyWith({
    BlocStatus? status,
    List<MTradeProductModel>? products,
  }) {
    return MTradeHistoryProductState(
      status: status ?? this.status,
      products: products ?? this.products,
    );
  }
}
