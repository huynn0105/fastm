part of 'mtrade_select_promotion_cubit.dart';

class MTradeSelectPromotionState extends Equatable {
  const MTradeSelectPromotionState({
    this.promotions = const [],
    this.checkStatus = BlocStatus.initial,
    this.errorMessage,
    this.errorPromotions,
  });

  final List<DataProductVariationsModel> promotions;
  final BlocStatus checkStatus;
  final String? errorMessage;
  final List<String>? errorPromotions;

  @override
  List<Object?> get props => [
        promotions,
        checkStatus,
        errorMessage,
        errorPromotions,
      ];

  MTradeSelectPromotionState copyWith({
    List<DataProductVariationsModel>? promotions,
    BlocStatus? checkStatus,
    String? errorMessage,
    List<String>? errorPromotions,
  }) {
    return MTradeSelectPromotionState(
      promotions: promotions ?? this.promotions,
      checkStatus: checkStatus ?? BlocStatus.initial,
      errorMessage: errorMessage,
      errorPromotions: errorPromotions ?? this.errorPromotions,
    );
  }
}
