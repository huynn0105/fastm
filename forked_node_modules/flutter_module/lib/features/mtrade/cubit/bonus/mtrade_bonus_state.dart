part of 'mtrade_bonus_cubit.dart';

class MTradeBonusState extends Equatable {
  MTradeBonusState({
    this.status = BlocStatus.initial,
    this.bonus,
    DateTime? date,
  }) : date = date ?? DateTime.now().toFirstDayInDateOnly();

  final BlocStatus status;
  final MTradeBonusModel? bonus;
  final DateTime date;

  @override
  List<Object?> get props => [
        status,
        bonus,
        date,
      ];

  MTradeBonusState copyWith({
    BlocStatus? status,
    MTradeBonusModel? bonus,
    DateTime? date,
  }) {
    return MTradeBonusState(
      status: status ?? this.status,
      bonus: bonus ?? this.bonus,
      date: date ?? this.date,
    );
  }
}
