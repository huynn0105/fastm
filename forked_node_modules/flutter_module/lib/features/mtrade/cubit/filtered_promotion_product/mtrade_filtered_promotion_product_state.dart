part of 'mtrade_filtered_promotion_product_cubit.dart';

class MTradeFilteredPromotionProductState extends Equatable {
  const MTradeFilteredPromotionProductState({
    this.status = BlocStatus.initial,
    this.products = const [],
  });

  final BlocStatus status;
  final List<MTradeProductModel> products;

  @override
  List<Object> get props => [
        status,
        products,
      ];

  MTradeFilteredPromotionProductState copyWith({
    BlocStatus? status,
    List<MTradeProductModel>? products,
  }) {
    return MTradeFilteredPromotionProductState(
      status: status ?? this.status,
      products: products ?? this.products,
    );
  }
}
