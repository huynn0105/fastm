part of 'mtrade_personal_website_setup_cubit.dart';

class MTradePersonalWebsiteSetupState extends Equatable {
  const MTradePersonalWebsiteSetupState({
    this.status = BlocStatus.initial,
    this.data,
    this.enabledProvince = false,
    this.enabledDistrict = false,
    this.enabledWard = false,
    this.selectedProvinceCode,
    this.selectedDistrictCode,
    this.selectedWardCode,
    this.provinces = const [],
    this.districts = const [],
    this.wards = const [],
    this.statusSave = BlocStatus.initial,
    this.enabledSave = true,
    this.selectedContactMethodCodes = const [],
    this.errorFields = const {},
    this.checkPageIDMessage,
    this.errorMessage,
    this.contactMethod,
  });

  final BlocStatus status;
  final MTradePersonalWebsiteSetupModel? data;
  final bool enabledProvince;
  final bool enabledDistrict;
  final bool enabledWard;
  final String? selectedProvinceCode;
  final String? selectedDistrictCode;
  final String? selectedWardCode;
  final List<MasterDataModel> provinces;
  final List<MasterDataModel> districts;
  final List<MasterDataModel> wards;
  final BlocStatus statusSave;
  final bool enabledSave;
  final List<String> selectedContactMethodCodes;
  final Map<String, String> errorFields;
  final String? checkPageIDMessage;
  final String? errorMessage;
  final MTradeContactMethodModel? contactMethod;

  @override
  List<Object?> get props => [
        status,
        data,
        enabledProvince,
        enabledDistrict,
        enabledWard,
        selectedProvinceCode,
        selectedDistrictCode,
        selectedWardCode,
        provinces,
        districts,
        wards,
        statusSave,
        enabledSave,
        selectedContactMethodCodes,
        errorFields,
        checkPageIDMessage,
        errorMessage,
        contactMethod,
      ];

  MTradePersonalWebsiteSetupState copyWith({
    BlocStatus? status,
    MTradePersonalWebsiteSetupModel? data,
    bool? enabledProvince,
    bool? enabledDistrict,
    bool? enabledWard,
    String? selectedProvinceCode,
    String? selectedDistrictCode,
    String? selectedWardCode,
    List<MasterDataModel>? provinces,
    List<MasterDataModel>? districts,
    List<MasterDataModel>? wards,
    BlocStatus? statusSave,
    bool? enabledSave,
    List<String>? selectedContactMethodCodes,
    Map<String, String>? errorFields,
    String? checkPageIDMessage,
    String? errorMessage,
    MTradeContactMethodModel? contactMethod,
  }) {
    return MTradePersonalWebsiteSetupState(
      status: status ?? this.status,
      data: data ?? this.data,
      enabledProvince: enabledProvince ?? this.enabledProvince,
      enabledDistrict: enabledDistrict ?? this.enabledDistrict,
      enabledWard: enabledWard ?? this.enabledWard,
      selectedProvinceCode: selectedProvinceCode ?? this.selectedProvinceCode,
      selectedDistrictCode: selectedDistrictCode ?? this.selectedDistrictCode,
      selectedWardCode: selectedWardCode ?? this.selectedWardCode,
      provinces: provinces ?? this.provinces,
      districts: districts ?? this.districts,
      wards: wards ?? this.wards,
      statusSave: statusSave ?? BlocStatus.initial,
      enabledSave: enabledSave ?? this.enabledSave,
      selectedContactMethodCodes: selectedContactMethodCodes ?? this.selectedContactMethodCodes,
      errorFields: errorFields ?? this.errorFields,
      checkPageIDMessage: checkPageIDMessage,
      errorMessage: errorMessage,
      contactMethod: contactMethod ?? this.contactMethod,
    );
  }
}
