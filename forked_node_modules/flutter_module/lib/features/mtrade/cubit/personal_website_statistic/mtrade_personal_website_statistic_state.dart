part of 'mtrade_personal_website_statistic_cubit.dart';

class MTradePersonalWebsiteStatisticState extends Equatable {
  const MTradePersonalWebsiteStatisticState({
    this.status = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final MTradePersonalWebsiteStatisticModel? data;

  @override
  List<Object?> get props => [status, data];

  MTradePersonalWebsiteStatisticState copyWith({
    BlocStatus? status,
    MTradePersonalWebsiteStatisticModel? data,
  }) {
    return MTradePersonalWebsiteStatisticState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
