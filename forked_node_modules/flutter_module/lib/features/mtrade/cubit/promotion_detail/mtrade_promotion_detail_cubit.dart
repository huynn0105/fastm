import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/models/mtrade/promotion/mtrade_promotion_model.dart';

part 'mtrade_promotion_detail_state.dart';

class MTradePromotionDetailCubit extends Cubit<MTradePromotionDetailState> {
  MTradePromotionDetailCubit() : super(const MTradePromotionDetailState());

  initData(MTradePromotionModel? data) {
    if (data == null) {
      return;
    }
    emit(state.copyWith(
      status: BlocStatus.success,
      promotion: data,
    ));
  }
}
