part of 'mtrade_promotion_detail_cubit.dart';

class MTradePromotionDetailState extends Equatable {
  const MTradePromotionDetailState({
    this.status = BlocStatus.initial,
    this.promotion,
  });

  final BlocStatus status;
  final MTradePromotionModel? promotion;

  @override
  List<Object?> get props => [
        status,
        promotion,
      ];

  MTradePromotionDetailState copyWith({
    BlocStatus? status,
    MTradePromotionModel? promotion,
  }) {
    return MTradePromotionDetailState(
      status: status ?? this.status,
      promotion: promotion ?? this.promotion,
    );
  }
}
