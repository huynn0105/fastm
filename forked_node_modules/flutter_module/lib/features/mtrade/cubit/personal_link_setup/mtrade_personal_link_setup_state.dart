part of 'mtrade_personal_link_setup_cubit.dart';

class MTradePersonalLinkSetupState extends Equatable {
  const MTradePersonalLinkSetupState({
    this.status = BlocStatus.initial,
    this.data,
    this.actionStatus = BlocStatus.initial,
    this.errors,
    this.actionMessage,
    this.removeStatus = BlocStatus.initial,
  });
  final BlocStatus status;
  final MTradePersonalLinkSetupModel? data;
  final BlocStatus actionStatus;
  final Map<String, String>? errors;
  final String? actionMessage;
  final BlocStatus removeStatus;

  @override
  List<Object?> get props => [
        status,
        data,
        actionStatus,
        errors,
        actionMessage,
        removeStatus,
      ];

  MTradePersonalLinkSetupState copyWith({
    BlocStatus? status,
    MTradePersonalLinkSetupModel? data,
    BlocStatus? actionStatus,
    Map<String, String>? errors,
    String? actionMessage,
    BlocStatus? removeStatus,
  }) {
    return MTradePersonalLinkSetupState(
      status: status ?? this.status,
      data: data ?? this.data,
      actionStatus: actionStatus ?? this.actionStatus,
      errors: errors ?? this.errors,
      actionMessage: actionMessage ?? this.actionMessage,
      removeStatus: removeStatus ?? this.removeStatus,
    );
  }
}
