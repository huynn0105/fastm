part of 'mtrade_search_promotion_cubit.dart';

class MTradeSearchPromotionState extends Equatable {
  const MTradeSearchPromotionState({
    this.searchStatus = BlocStatus.initial,
    this.status = BlocStatus.initial,
    this.promotions = const [],
    this.selectedProduct = const [],
    this.errorMessage = "",
    this.totalDiscount = 0,
    this.isApplyMulti,
  });

  final BlocStatus searchStatus;
  final BlocStatus status;
  final List<MTradePromotionModel> promotions;
  final List<DataProductVariationsModel> selectedProduct;
  final String errorMessage;
  final double totalDiscount;
  final bool? isApplyMulti;

  @override
  List<Object?> get props => [status, searchStatus, promotions, selectedProduct, errorMessage, isApplyMulti];

  MTradeSearchPromotionState copyWith({
    BlocStatus? status,
    BlocStatus? searchStatus,
    List<MTradePromotionModel>? promotions,
    List<DataProductVariationsModel>? selectedProduct,
    String? errorMessage,
    double? totalDiscount,
    bool? isApplyMulti,
  }) {
    return MTradeSearchPromotionState(
      status: status ?? this.status,
      searchStatus: searchStatus ?? this.searchStatus,
      promotions: promotions ?? this.promotions,
      selectedProduct: selectedProduct ?? this.selectedProduct,
      errorMessage: errorMessage ?? this.errorMessage,
      totalDiscount: totalDiscount ?? this.totalDiscount,
      isApplyMulti: isApplyMulti ?? this.isApplyMulti,
    );
  }
}
