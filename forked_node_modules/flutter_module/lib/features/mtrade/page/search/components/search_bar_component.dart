import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/utils/debounce_util.dart';
import 'package:flutter_module/features/mtrade/cubit/search/mtrade_search_cubit.dart';
import 'package:sliver_tools/sliver_tools.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/buttons.dart';
import '../../../../../common/widgets/images.dart';
import '../../mtrade/widgets/mtrade_text_field.dart';

class SearchBarComponent extends StatefulWidget {
  const SearchBarComponent({
    Key? key,
    required this.controller,
    required this.onSubmit,
  }) : super(key: key);

  final TextEditingController controller;
  final Function(String value) onSubmit;

  @override
  State<SearchBarComponent> createState() => _SearchBarComponentState();
}

class _SearchBarComponentState extends State<SearchBarComponent> {
  bool showClearButton = false;

  final DebounceUtil debounce = DebounceUtil(milliseconds: 200);

  @override
  void initState() {
    super.initState();
    widget.controller.addListener(onListen);
  }

  @override
  void dispose() {
    widget.controller.removeListener(onListen);
    debounce.cancel();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    return Container(
      height: 54,
      color: UIColors.background,
      padding: const EdgeInsets.symmetric(horizontal: 16),
      child: Row(
        children: [
          Expanded(
            child: MTradeSearchTextField(
              controller: widget.controller,
              hintText: "Tìm kiếm",
              autoFocus: true,
              showClearButton: showClearButton,
              onCleared: () {
                context.read<MTradeSearchCubit>().fetchData(query: "");
              },
              onChanged: (value) {
                debounce.run(() {
                  context.read<MTradeSearchCubit>().fetchData(query: value);
                });
              },
              onSubmitted: widget.onSubmit,
            ),
          ),
          AppSplashButton(
            onTap: context.popRoute,
            child: Padding(
              padding: const EdgeInsets.symmetric(horizontal: 16),
              child: Text(
                "Huỷ",
                style: UITextStyle.medium.copyWith(
                  fontSize: 16,
                  color: UIColors.grayText,
                ),
              ),
            ),
          ),
        ],
      ),
    );
  }

  onListen() {
    onShowClearButton(widget.controller.text);
  }

  onShowClearButton(String value) {
    if (value.isEmpty && showClearButton) {
      setState(() {
        showClearButton = false;
      });
      return;
    }
    if (value.isNotEmpty && !showClearButton) {
      setState(() {
        showClearButton = true;
      });
      return;
    }
  }
}
