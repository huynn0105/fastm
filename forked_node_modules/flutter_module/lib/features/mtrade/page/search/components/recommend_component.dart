import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/routes/routes.gr.dart';
import '../../../../../app_data.dart';
import '../../../../../common/bloc_status.dart';
import '../../../../../common/colors.dart';
import '../../../../../common/widgets/images.dart';
import '../../../cubit/hot_product/mtrade_hot_product_cubit.dart';
import 'package:flutter_staggered_grid_view/flutter_staggered_grid_view.dart';

import '../../../../../common/styles.dart';

class RecommendComponent extends StatelessWidget {
  const RecommendComponent({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<MTradeHotProductCubit, MTradeHotProductState>(
      builder: (context, state) {
        if (state.status.isInitial) {
          context.read<MTradeHotProductCubit>().fetchData();
        }

        final data = state.products.take(4).toList();
        if (data.isEmpty) {
          return const SizedBox();
        }
        return Column(
          mainAxisSize: MainAxisSize.min,
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Padding(
              padding: const EdgeInsets.all(16),
              child: Text(
                "Đề xuất cho bạn",
                style: UITextStyle.regular.copyWith(
                  fontSize: 16,
                  color: UIColors.grayText,
                ),
              ),
            ),
            AlignedGridView.count(
              shrinkWrap: true,
              crossAxisCount: 2,
              crossAxisSpacing: 2,
              mainAxisSpacing: 2,
              physics: const NeverScrollableScrollPhysics(),
              itemBuilder: (context, index) {
                final item = data[index];
                return SplashButton(
                  onTap: () {
                    context.pushRoute(MTradeProductDetailRoute(
                      productCode: item.code ?? "",
                      pageID: AppData.instance.landingPageID,
                      provinceCode: AppData.instance.landingPageProvinceCode,
                      districtCode: AppData.instance.landingPageDistrictCode,
                    ));
                  },
                  child: Container(
                    height: 76,
                    padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 8),
                    decoration: const BoxDecoration(
                      color: UIColors.white,
                    ),
                    child: Row(
                      children: [
                        AppImage.network(
                          url: item.image ?? "",
                          width: 60,
                          height: 60,
                          borderRadius: BorderRadius.circular(4),
                        ),
                        const SizedBox(
                          width: 12,
                        ),
                        Expanded(
                          child: Text(
                            item.name ?? "",
                            style: UITextStyle.regular.copyWith(
                              fontSize: 14,
                            ),
                            maxLines: 2,
                            overflow: TextOverflow.ellipsis,
                          ),
                        ),
                      ],
                    ),
                  ),
                );
              },
              itemCount: data.length,
            ),
          ],
        );
      },
    );
  }
}
