class HotProductCard extends StatelessWidget {
  const HotProductCard({
    Key? key,
    required this.data,
    this.width = 180,
    this.height = 345,
    this.isIntrinsicHeight = false,
  }) : super(key: key);

  final MTradeProductModel data;
  final double width;
  final double height;
  final bool isIntrinsicHeight;

  @override
  Widget build(BuildContext context) {
    final code = data.code ?? "";
    final title = data.name ?? "";
    final image = data.image ?? "";
    final subtitle = data.contest ?? "";
    final price = data.price ?? 0;
    final comparePrice = data.comparePrice ?? [];
    final bonus = data.comm ?? 0;
    final currency = data.currency ?? "";
    final paymentTypes = data.paymentMethod?.convertPaymentType() ?? [];
    final hideComm = data.hideComm ?? false;
    final hideMessage = data.hideMessage ?? "";
    final promotion = data.promotion;
    final showMTradeTag = data.mfastShipping ?? false;
    final showPromotionTag = promotion != null;
    final showPayLaterTag = paymentTypes.contains(MTradePaymentType.later);
    final showComparePriceTag = comparePrice.valueAt(1)?.value != null;

    ///
    return SplashButton(
      onTap: () {
        context.router.push(MTradeProductDetailRoute(
          productCode: code,
          product: MTradeProductDetailModel.fromProduct(data),
          pageID: AppData.instance.landingPageID,
          provinceCode: AppData.instance.landingPageProvinceCode,
          districtCode: AppData.instance.landingPageDistrictCode,
        ));
      },
      child: Container(
        width: width,
        height: isIntrinsicHeight ? null : height,
        clipBehavior: Clip.antiAlias,
        decoration: const BoxDecoration(
          color: UIColors.white,
        ),
        child: Stack(
          children: [
            Stack(
              children: [
                // Positioned(
                //   left: 0,
                //   right: 0,
                //   bottom: 0,
                //   child: Container(
                //     height: 60,
                //     width: double.infinity,
                //     alignment: Alignment.centerLeft,
                //     padding: const EdgeInsets.symmetric(vertical: 5, horizontal: 12),
                //     decoration: const BoxDecoration(
                //       gradient: LinearGradient(
                //         colors: UIColors.mtradeHighlightGradient,
                //         stops: [0, 1],
                //         begin: Alignment(-0.0, -1.0),
                //         end: Alignment(0.0, 1.0),
                //       ),
                //     ),
                //   ),
                // ),
                Padding(
                  padding: const EdgeInsets.symmetric(horizontal: 12),
                  child: Column(
                    crossAxisAlignment: CrossAxisAlignment.start,
                    children: [
                      Stack(
                        children: [
                          AppImage.network(
                            url: image,
                            width: width - 24,
                            height: width - 24,
                            borderRadius: BorderRadius.only(
                              topRight: showComparePriceTag ? const Radius.circular(8) : Radius.zero,
                            ),
                          ),
                          if (data.productIcon?.isNotEmpty == true)
                            Positioned(
                              left: 0,
                              right: 0,
                              bottom: 0,
                              child: SingleChildScrollView(
                                scrollDirection: Axis.horizontal,
                                physics: const NeverScrollableScrollPhysics(),
                                child: Row(
                                  children: data.productIcon!.map((e) {
                                    return AppImage.network(url: e, width: 38, height: 40, fit: BoxFit.contain);
                                  }).toList(),
                                  // children: [
                                  //   if (showPromotionTag) const ProductTag.promotion(),
                                  //   if (showPayLaterTag) const ProductTag.paylater(),
                                  //   if (showMTradeTag) const ProductTag.mtrade(),
                                  // ],
                                ),
                              ),
                            ),
                        ],
                      ),
                      const SizedBox(
                        height: 12,
                      ),
                      Column(
                        crossAxisAlignment: CrossAxisAlignment.start,
                        children: [
                          Text(
                            title,
                            style: UITextStyle.medium.copyWith(
                              fontSize: 16,
                              height: 1.25,
                            ),
                            maxLines: 1,
                            overflow: TextOverflow.ellipsis,
                          ),
                          const SizedBox(
                            height: 4,
                          ),
                          Row(
                            children: [
                              Expanded(
                                child: Wrap(
                                  spacing: 5,
                                  alignment: WrapAlignment.spaceBetween,
                                  crossAxisAlignment: WrapCrossAlignment.center,
                                  children: [
                                    Text(
                                      FormatUtil.currencyFormat(price),
                                      style: UITextStyle.semiBold.copyWith(
                                        color: UIColors.red,
                                        fontSize: 14,
                                        height: 1.25,
                                      ),
                                      maxLines: 1,
                                      overflow: TextOverflow.ellipsis,
                                    ),
                                    if (bonus > 0 && !hideComm && !kIsWeb)
                                      Row(
                                        mainAxisSize: MainAxisSize.min,
                                        children: [
                                          const AppImage.asset(
                                            asset: "ic_bonus",
                                            width: 16,
                                            height: 16,
                                          ),
                                          Flexible(
                                            child: Text(
                                              "${FormatUtil.currencyDoubleFormat(bonus, showUnit: false)}$currency",
                                              style: UITextStyle.semiBold.copyWith(
                                                color: UIColors.accentGreen,
                                                fontSize: 13,
                                                height: 1.25,
                                              ),
                                              maxLines: 1,
                                              overflow: TextOverflow.ellipsis,
                                            ),
                                          ),
                                        ],
                                      ),
                                    if (hideComm && !kIsWeb)
                                      Text(
                                        hideMessage,
                                        style: UITextStyle.semiBold.copyWith(
                                          color: UIColors.accentGreen,
                                          fontSize: 14,
                                          height: 1.25,
                                        ),
                                        maxLines: 1,
                                        overflow: TextOverflow.ellipsis,
                                      ),
                                  ],
                                ),
                              ),
                            ],
                          ),
                          if (promotion != null) ...[
                            const SizedBox(
                              height: 4,
                            ),
                            PromotionProductTag(
                              prefix: "Giáº£m",
                              data: data.getPromotionUnitCurrency(),
                            ),
                          ],
                        ],
                      ),
                      if (TextUtils.isNotEmpty(subtitle)) ...[
                        const SizedBox(
                          height: 12,
                        ),
                        Text(
                          subtitle,
                          style: UITextStyle.medium.copyWith(
                            fontSize: 14,
                            color: UIColors.green,
                            height: 1,
                          ),
                          maxLines: 2,
                          overflow: TextOverflow.ellipsis,
                        ),
                      ],
                      const SizedBox(
                        height: 12,
                      ),
                    ],
                  ),
                ),
              ],
            ),
            if (showComparePriceTag)
              Positioned(
                top: 0,
                right: 12,
                child: DiscountTag(
                  data: comparePrice.valueAt(1),
                ),
              ),
          ],
        ),
      ),
    );
  }
}
