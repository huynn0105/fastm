import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';

class ProductTag extends StatelessWidget {
  const ProductTag({
    super.key,
    required this.label,
    required this.width,
    required this.height,
    required this.backgroundColor,
  });
  const ProductTag.promotion({
    super.key,
    this.label = 'Ưu đãi',
    this.width = 45,
    this.height = 20,
    this.backgroundColor = UIColors.accentGreen,
  });

  const ProductTag.paylater({
    super.key,
    this.label = 'Trả chậm',
    this.width = 60,
    this.height = 20,
    this.backgroundColor = UIColors.secondaryColor,
  });

  const ProductTag.mtrade({
    super.key,
    this.label = 'MTrade',
    this.width = 50,
    this.height = 20,
    this.backgroundColor = UIColors.darkBlue,
  });

  final String label;
  final double width;
  final double height;
  final Color backgroundColor;

  @override
  Widget build(BuildContext context) {
    return Container(
      width: width,
      height: height,
      alignment: Alignment.center,
      decoration: BoxDecoration(
        color: backgroundColor,
      ),
      child: Text(
        label,
        style: UITextStyle.regular.copyWith(
          fontSize: 11,
          color: UIColors.white,
        ),
      ),
    );
  }
}
