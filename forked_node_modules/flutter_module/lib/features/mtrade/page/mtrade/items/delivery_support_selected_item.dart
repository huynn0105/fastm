import 'package:flutter/material.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/buttons.dart';
import '../../../../../common/widgets/images.dart';

class DeliverySupportSelectedItem extends StatelessWidget {
  const DeliverySupportSelectedItem({
    Key? key,
    required this.code,
    required this.value,
    required this.hintText,
    required this.onTap,
    this.isDisable = false,
  }) : super(key: key);

  final String code;
  final String value;
  final String hintText;
  final bool isDisable;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      isDisabled: isDisable,
      onTap: onTap,
      borderRadius: BorderRadius.circular(20),
      child: Container(
        height: 40,
        padding: const EdgeInsets.symmetric(horizontal: 16),
        decoration: BoxDecoration(
          color: isDisable ? UIColors.gray : UIColors.background,
          borderRadius: BorderRadius.circular(20),
        ),
        child: Row(
          children: [
            const SizedBox(
              width: 24,
              height: 24,
            ),
            Expanded(
              child: Center(
                child: Text(
                  code.isEmpty ? hintText : value,
                  style: UITextStyle.medium.copyWith(
                    fontSize: 16,
                    color: code.isEmpty ? UIColors.grayText : UIColors.defaultText,
                  ),
                ),
              ),
            ),
            const SizedBox(
              width: 24,
              height: 24,
              child: AppImage.asset(
                asset: "ic_mtrade_up_and_down",
                width: 24,
                height: 25,
              ),
            ),
          ],
        ),
      ),
    );
  }
}
