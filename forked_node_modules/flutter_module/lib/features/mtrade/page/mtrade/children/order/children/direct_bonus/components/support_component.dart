import 'dart:math';

import 'package:flutter/material.dart';
import 'package:flutter_module/common/global_function.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:url_launcher/url_launcher.dart';

import '../../../../../../../../../common/colors.dart';
import '../../../../../../../../../common/styles.dart';
import '../../../../../../../../../common/utils/validation_util.dart';
import '../../../../../../../../../common/widgets/images.dart';
import '../../../../../../../../../models/mtrade/bonus/support_model.dart';
import '../../../../../widgets/mtrade_block_widget.dart';

class SupportComponent extends StatelessWidget {
  const SupportComponent({
    Key? key,
    required this.data,
  }) : super(key: key);

  final List<SupportModel> data;

  @override
  Widget build(BuildContext context) {
    if (data.isEmpty) {
      return const SizedBox();
    }
    return MTradeBlockWidget(
      title: "Trung tâm hỗ trợ",
      titlePadding: EdgeInsets.zero,
      child: Container(
        padding: const EdgeInsets.symmetric(horizontal: 16),
        decoration: BoxDecoration(
          color: UIColors.white,
          borderRadius: BorderRadius.circular(12),
        ),
        child: Column(
          children: List.generate(
            max(0, data.length * 2 - 1),
            (index) {
              if (index.isOdd) {
                return const Divider(
                  height: 0,
                  indent: 36,
                  color: UIColors.lightGray,
                );
              }
              index = index ~/ 2;
              final item = data[index];
              return SplashButton(
                onTap: () {
                  final action = item.action ?? "";
                  if (TextUtils.isDeeplink(action)) {
                    GlobalFunction.launchScheme(action, mode: LaunchMode.externalApplication);
                  } else if (!PhoneInputValidation.dirty(value: action).isNotValid) {
                    GlobalFunction.launchPhone(action);
                  }
                },
                child: Padding(
                  padding: const EdgeInsets.symmetric(vertical: 12),
                  child: Row(
                    children: [
                      AppImage.network(
                        url: item.icon ?? "",
                        width: 24,
                        height: 24,
                      ),
                      const SizedBox(
                        width: 12,
                      ),
                      Expanded(
                        child: Row(
                          children: [
                            Text(
                              item.label ?? "",
                              style: UITextStyle.regular,
                              maxLines: 1,
                              overflow: TextOverflow.ellipsis,
                            ),
                            const SizedBox(
                              width: 12,
                            ),
                            Expanded(
                              child: Align(
                                alignment: Alignment.centerRight,
                                child: Text(
                                  item.rightLabel ?? "",
                                  style: UITextStyle.medium.copyWith(
                                    fontSize: 16,
                                    color: UIColors.primaryColor,
                                  ),
                                  maxLines: 1,
                                  overflow: TextOverflow.ellipsis,
                                ),
                              ),
                            ),
                          ],
                        ),
                      ),
                    ],
                  ),
                ),
              );
            },
          ),
        ),
      ),
    );
  }
}
