import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/features/mtrade/cubit/tools/mtrade_tools_cubit.dart';

import 'components/banner_component.dart';
import 'components/tools_component.dart';

@RoutePage()
class MTradeToolsPage extends StatelessWidget implements AutoRouteWrapper {
  const MTradeToolsPage({Key? key}) : super(key: key);

  @override
  Widget wrappedRoute(BuildContext context) {
    return BlocProvider(
      create: (_) => MTradeToolsCubit(),
      child: this,
    );
  }

  @override
  Widget build(BuildContext context) {
    final cubit = context.read<MTradeToolsCubit>();
    return Scaffold(
      body: BlocBuilder<MTradeToolsCubit, MTradeToolsState>(
        builder: (context, state) {
          if (state.status.isInitial) {
            cubit.fetchData();
          }

          return Stack(
            children: [
              ListView(
                padding: const EdgeInsets.only(bottom: 100),
                children: [
                  BannerComponent(
                    data: state.data?.banners ?? [],
                  ),
                  ToolsComponent(
                    data: state.data?.tools ?? [],
                  ),
                ],
              ),
            ],
          );
        },
      ),
    );
  }
}
