import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/common/widgets/empty_widget.dart';
import 'package:flutter_module/common/widgets/loading.dart';
import '../items/collaborator_item.dart';
import '../../../../../../../../../models/mtrade/bonus/mtrade_indirect_bonus_model.dart';

import '../../../../../../../../../common/widgets/loadmore_widget.dart';
import '../../../../../../../cubit/indirect_bonus/mtrade_indirect_bonus_cubit.dart';

class ListCollaboratorComponent extends StatelessWidget {
  const ListCollaboratorComponent({
    Key? key,
    required this.status,
    required this.data,
  }) : super(key: key);

  final BlocStatus status;
  final List<MTradeIndirectBonusModel> data;

  @override
  Widget build(BuildContext context) {
    if (status.showLoading) {
      return const LoadingWidget();
    }

    return LoadMoreWidget(
      onRefresh: () => context.read<MTradeIndirectBonusCubit>().refreshData(),
      onLoadMore: () => context.read<MTradeIndirectBonusCubit>().loadmoreData(),
      child: data.isEmpty && !status.showLoading
          ? const Align(
              alignment: Alignment.topCenter,
              child: EmptyWidget(
                message: "Hiện tại chưa phát sinh thu nhập\ntừ cộng tác viên của bạn",
              ),
            )
          : ListView.separated(
              padding: const EdgeInsets.fromLTRB(16, 0, 16, 100),
              itemBuilder: (context, index) {
                return CollaboratorItem(
                  item: data[index],
                );
              },
              separatorBuilder: (_, __) => const SizedBox(
                height: 10,
              ),
              itemCount: data.length,
            ),
    );
  }
}
