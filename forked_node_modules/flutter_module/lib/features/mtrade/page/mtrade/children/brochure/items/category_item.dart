import 'package:flutter/material.dart';
import 'package:flutter_module/common/bottom_sheet/wrapper/data_wrapper.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

import '../../../../../../../common/colors.dart';
import '../../../../../../../common/widgets/images.dart';

class CategoryItem extends StatelessWidget {
  const CategoryItem({
    Key? key,
    required this.data,
    required this.onDeleted,
  }) : super(key: key);

  final DataWrapper data;
  final Function(String? id) onDeleted;

  @override
  Widget build(BuildContext context) {
    return Container(
      decoration: BoxDecoration(
        color: UIColors.extraLightBlue,
        borderRadius: BorderRadius.circular(8),
      ),
      child: Row(
        children: [
          const SizedBox(
            width: 12,
          ),
          Text(
            data.value ?? "",
            style: UITextStyle.regular.copyWith(
              fontSize: 13,
            ),
          ),
          AppSplashButton(
            onTap: () => onDeleted(data.id),
            child: const Padding(
              padding: EdgeInsets.all(8.0),
              child: AppImage.asset(
                asset: "ic_mtrade_clear_x",
                width: 20,
                height: 20,
              ),
            ),
          ),
        ],
      ),
    );
  }
}
