import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/common/widgets/chart/donut_chart/animated_donut_chart.dart';
import 'package:flutter_module/common/widgets/chart/donut_chart/donut_chart_controller.dart';
import 'package:flutter_module/common/widgets/chart/donut_chart/donut_chart_legend.dart';
import 'package:flutter_module/common/widgets/html_widget.dart';
import 'package:flutter_module/models/mtrade/personal_website_statistic/mtrade_personal_website_statistic_model.dart';

class WebsiteDetailStatisticComponent extends StatelessWidget {
  const WebsiteDetailStatisticComponent({
    super.key,
    required this.chartController,
    this.data,
  });

  final DonutChartController chartController;
  final MTradePersonalWebsiteStatisticModel? data;

  @override
  Widget build(BuildContext context) {
    final total = data?.total;
    return Container(
      margin: const EdgeInsets.symmetric(vertical: 16),
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: UIColors.white,
        borderRadius: BorderRadius.circular(8),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Row(
            children: [
              AnimatedDonutChart(
                controller: chartController,
                onDisplayTitle: (value) {
                  return "${total?.count ?? 0}";
                },
                subtitle: total?.unit ?? "",
              ),
              const SizedBox(
                width: 16,
              ),
              Expanded(
                child: Column(
                  mainAxisAlignment: MainAxisAlignment.center,
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    if (TextUtils.isNotEmpty(total?.label))
                      HtmlWidget(
                        data: total?.label ?? "",
                        lineHeight: 1.2,
                      ),
                    if (TextUtils.isNotEmpty("${total?.count ?? ""}") && TextUtils.isNotEmpty(total?.unit ?? ""))
                      Text(
                        "${data?.total?.count ?? 0} ${total?.unit ?? ""}",
                        style: UITextStyle.semiBold.copyWith(
                          fontSize: 18,
                          color: UIColors.defaultText,
                        ),
                      ),
                  ],
                ),
              ),
            ],
          ),
          const SizedBox(
            height: 12,
          ),
          Text(
            "Phân loại khách hàng theo trạng thái tham gia",
            style: UITextStyle.regular.copyWith(
              fontSize: 14,
              color: UIColors.grayText,
            ),
          ),
          const SizedBox(
            height: 12,
          ),
          DonutChartLegend(
            controller: chartController,
            onDisplayLabel: (section) {
              return "${section.value.round()} ${section.unit}";
            },
          ),
        ],
      ),
    );
  }
}
