import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/enum/mtrade/website_setup_contact_method.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/mtrade/personal_website_statistic/statistic_model.dart';
import 'package:flutter_module/routes/routes.gr.dart';
import 'package:flutter_staggered_grid_view/flutter_staggered_grid_view.dart';

class WebsiteOverallStatisticComponent extends StatelessWidget {
  const WebsiteOverallStatisticComponent({
    super.key,
    required this.data,
    this.enabledOnTap = false,
    this.margin = const EdgeInsets.symmetric(horizontal: 16),
  });

  final List<StatisticModel> data;
  final EdgeInsets margin;
  final bool enabledOnTap;

  @override
  Widget build(BuildContext context) {
    return Container(
      margin: margin,
      alignment: Alignment.center,
      padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 6),
      constraints: const BoxConstraints(
        minHeight: 35,
      ),
      decoration: BoxDecoration(
        color: UIColors.white,
        borderRadius: BorderRadius.circular(8),
      ),
      child: SplashButton(
        isDisabled: !enabledOnTap,
        onTap: () {
          context.pushRoute(
              MTradePersonalWebsiteTabbarRoute(indexTab: WebsiteTabPage.statistic.index));
        },
        child: Row(
          children: [
            Expanded(
              child: AlignedGridView.count(
                shrinkWrap: true,
                padding: EdgeInsets.zero,
                physics: const NeverScrollableScrollPhysics(),
                crossAxisCount: 4,
                mainAxisSpacing: 6,
                crossAxisSpacing: 6,
                itemCount: data.length,
                itemBuilder: (context, index) {
                  final item = data[index];
                  return Row(
                    children: [
                      AppImage.network(
                        url: item.icon ?? "",
                        width: 24,
                        height: 24,
                      ),
                      const SizedBox(
                        width: 5,
                      ),
                      Expanded(
                        child: Text(
                          "${item.count ?? 0}",
                          style: UITextStyle.bold.copyWith(
                            fontSize: 14,
                            color: UIColors.grayText,
                          ),
                          maxLines: 1,
                          overflow: TextOverflow.ellipsis,
                        ),
                      ),
                    ],
                  );
                },
              ),
            ),
            if (enabledOnTap) ...[
              const SizedBox(
                width: 16,
              ),
              const AppImage.asset(
                asset: "ic_arrow_right",
                width: 24,
                height: 24,
              ),
            ],
          ],
        ),
      ),
    );
  }
}
