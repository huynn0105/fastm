import 'package:flutter/material.dart';
import 'package:flutter/src/widgets/framework.dart';
import 'package:flutter/src/widgets/placeholder.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

class ButtonInfoLinkComponent extends StatelessWidget {
  const ButtonInfoLinkComponent({
    super.key,
    this.title,
    this.value = 0,
    this.color = UIColors.lightPurple,
    this.flex = 1,
    this.onTap,
  });

  final String? title;
  final Color? color;
  final int? flex;
  final int? value;
  final Function()? onTap;

  @override
  Widget build(BuildContext context) {
    return Expanded(
      flex: flex ?? 1,
      child: SplashButton(
        isDisabled: onTap == null,
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(
              title ?? '',
              style: UITextStyle.regular.copyWith(
                fontSize: 13,
                color: UIColors.grayText,
              ),
            ),
            Row(
              children: [
                Flexible(
                  child: Text(
                    FormatUtil.doubleFormat(value?.toDouble()),
                    style: UITextStyle.semiBold.copyWith(
                      color: color,
                    ),
                  ),
                ),
                if (onTap != null) ...{
                  const SizedBox(
                    width: 4,
                  ),
                  AppImage.asset(
                    asset: 'ic_arrow_right',
                    width: 16,
                    height: 16,
                    color: color,
                  ),
                }
              ],
            ),
          ],
        ),
      ),
    );
  }
}
