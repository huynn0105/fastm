import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/size.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/html_widget.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:popover/popover.dart';

class TitleTooltipComponent extends StatelessWidget {
  const TitleTooltipComponent({
    super.key,
    this.title = '',
    this.note,
  });

  final String title;
  final String? note;

  @override
  Widget build(BuildContext context) {
    return RichText(
      text: TextSpan(
        text: title,
        style: UITextStyle.semiBold.copyWith(
          color: UIColors.boolText,
        ),
        children: [
          if (note?.isNotEmpty == true)
            WidgetSpan(
              alignment: PlaceholderAlignment.middle,
              child: Builder(builder: (context) {
                return SplashButton(
                  onTap: () {
                    showPopover(
                      context: context,
                      width: AppSize.instance.width * 0.75,
                      arrowWidth: 10,
                      arrowHeight: 10,
                      bodyBuilder: (context) {
                        return Container(
                          color: Colors.white,
                          margin: const EdgeInsets.all(16),
                          child: HtmlWidget(
                            data: note ?? "",
                          ),
                        );
                      },
                    );
                  },
                  child: const Padding(
                    padding: EdgeInsets.symmetric(horizontal: 8, vertical: 2),
                    child: AppImage.asset(
                      asset: 'ic_info_outline',
                      width: 20,
                      height: 20,
                    ),
                  ),
                );
              }),
            ),
        ],
      ),
    );
  }
}
