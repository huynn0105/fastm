import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/common/bottom_sheet/bottom_sheet_provider.dart';
import 'package:flutter_module/common/bottom_sheet/views/brochure_view.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/global_function.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/features/mtrade/cubit/link_brochure/mtrade_link_brochure_cubit.dart';
import 'package:flutter_module/features/mtrade/page/personal_link_setup/components/button_info_link_component.dart';
import 'package:flutter_module/models/mtrade/personal_link_setup/mtrade_personal_link_setup_model.dart';
import 'package:flutter_switch/flutter_switch.dart';

class InfoLinkComponent extends StatelessWidget {
  const InfoLinkComponent({
    super.key,
    this.link,
    this.detailLink,
    this.onChangeActiveLink,
  });

  final String? link;
  final DetailLinkSetup? detailLink;
  final Function(bool)? onChangeActiveLink;

  @override
  Widget build(BuildContext context) {
    return Container(
      color: UIColors.white,
      padding: const EdgeInsets.symmetric(horizontal: 16),
      child: Column(
        children: [
          const SizedBox(
            height: 12,
          ),
          Row(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              const AppImage.asset(
                asset: 'ic_mtrade_direct_bonus_primary',
                width: 40,
                height: 40,
              ),
              const SizedBox(
                width: 12,
              ),
              Expanded(
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    Text(
                      link ?? '',
                      style: UITextStyle.medium.copyWith(
                        fontSize: 16,
                        color: UIColors.boolText,
                      ),
                    ),
                    Row(
                      children: [
                        const AppImage.asset(
                          asset: 'ic_view',
                          width: 16,
                          height: 16,
                        ),
                        const SizedBox(
                          width: 4,
                        ),
                        Text(
                          '${detailLink?.views ?? 0} lượt xem',
                          style: UITextStyle.regular.copyWith(
                            fontSize: 13,
                            color: UIColors.grayText,
                          ),
                        ),
                      ],
                    ),
                    const SizedBox(
                      width: 8,
                    ),
                    Row(
                      children: [
                        ButtonInfoLinkComponent(
                          title: 'Quan tâm',
                          value: detailLink?.totalFollow,
                          flex: 3,
                        ),
                        ButtonInfoLinkComponent(
                          title: 'Tham gia',
                          value: detailLink?.totalAttended,
                          flex: 3,
                          color: UIColors.secondaryColor,
                        ),
                        ButtonInfoLinkComponent(
                          title: 'Thành công',
                          value: detailLink?.totalSuccess,
                          flex: 4,
                          color: UIColors.accentGreen,
                        ),
                      ],
                    )
                  ],
                ),
              ),
            ],
          ),
          const SizedBox(
            height: 8,
          ),
          const Divider(
            color: UIColors.extraLightGray,
            height: 0,
            thickness: 1,
          ),
          Row(
            children: [
              Expanded(
                child: BlocConsumer<MTradeLinkBrochureCubit, MTradeLinkBrochureState>(
                  listener: (context, state) {
                    if (state.status.isSuccess) {
                      BottomSheetProvider.instance.show(
                        context,
                        title: 'Bộ nhận diện',
                        child: BlocProvider.value(
                          value: context.read<MTradeLinkBrochureCubit>(),
                          child: BrochureView(
                            onSend: () {
                              _onSendBrochure(context, ID: detailLink?.id ?? '');
                            },
                          ),
                        ),
                      );
                    }
                  },
                  builder: (context, state) {
                    return SplashButton(
                      isDisabled: state.status.isLoading,
                      onTap: () {
                        
                        _onBrochure(context, ID: detailLink?.id ?? '');
                      },
                      child: Padding(
                        padding: const EdgeInsets.only(top: 14, bottom: 10),
                        child: Row(
                          children: [
                            const AppImage.asset(
                              asset: 'ic_mtrade_brochure',
                              width: 20,
                              height: 20,
                              color: UIColors.primaryColor,
                            ),
                            const SizedBox(
                              width: 4,
                            ),
                            if (state.status.isLoading) ...{
                              const SizedBox(
                                width: 8,
                              ),
                              const SizedBox(
                                width: 16,
                                height: 16,
                                child: CircularProgressIndicator(
                                  color: UIColors.primaryColor,
                                  strokeWidth: 1,
                                ),
                              ),
                            } else ...{
                              Text(
                                'Brochure',
                                style: UITextStyle.regular.copyWith(
                                  fontSize: 13,
                                  color: UIColors.grayText,
                                ),
                              ),
                            }
                          ],
                        ),
                      ),
                    );
                  },
                ),
              ),
              Expanded(
                child: SplashButton(
                  onTap: () {
                    _onShareLink();
                  },
                  child: Padding(
                    padding: const EdgeInsets.only(top: 14, bottom: 10),
                    child: Row(
                      children: [
                        Text(
                          'Gửi link',
                          style: UITextStyle.regular.copyWith(
                            fontSize: 13,
                            color: UIColors.grayText,
                          ),
                        ),
                        const SizedBox(
                          width: 4,
                        ),
                        const AppImage.asset(
                          asset: 'ic_mtrade_send_outline',
                          width: 20,
                          height: 20,
                        ),
                      ],
                    ),
                  ),
                ),
              ),
              Expanded(
                child: Padding(
                  padding: const EdgeInsets.symmetric(vertical: 8),
                  child: Row(
                    children: [
                      Text(
                        'Hoạt động',
                        style: UITextStyle.regular.copyWith(
                          fontSize: 13,
                          color: UIColors.grayText,
                        ),
                      ),
                      const SizedBox(
                        width: 4,
                      ),
                      FlutterSwitch(
                        width: 46,
                        height: 28,
                        padding: 2,
                        value: detailLink?.isActiveLink() ?? false,
                        onToggle: (value) {
                          onChangeActiveLink?.call(value);
                        },
                        activeColor: UIColors.primaryColor,
                        inactiveColor: UIColors.grayText,
                      )
                    ],
                  ),
                ),
              ),
            ],
          )
        ],
      ),
    );
  }

  _onShareLink() {
    GlobalFunction.shareText(link ?? '');
  }

  _onBrochure(BuildContext context, {required String ID}) {
    final cubit = context.read<MTradeLinkBrochureCubit>();
    cubit.fetchData(ID: ID);
  }

  _onSendBrochure(BuildContext context, {required String ID}) {
    final cubit = context.read<MTradeLinkBrochureCubit>();
    cubit.sendBrochureToEmail();
  }
}
