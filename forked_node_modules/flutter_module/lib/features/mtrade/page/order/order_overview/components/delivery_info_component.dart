import 'package:flutter/material.dart';
import 'package:flutter_module/common/utils/datetime_util.dart';

import '../../../../../../common/colors.dart';
import '../../../../../../common/styles.dart';
import '../../../../../../models/mtrade/order/payment_detai_model.dart';

class DeliveryInfoComponent extends StatelessWidget {
  const DeliveryInfoComponent({super.key, this.orders});

  final ListOrders? orders;

  @override
  Widget build(BuildContext context) {
    final fromDate = DateTimeUtil.convertDate(
      orders?.earliestDeliveryDate,
      fromFormat: DateTimeFormat.yyyy_MM_ddTHH_mm_ssSS,
      toFormat: DateTimeFormat.dd_MM,
    );
    final toDate = DateTimeUtil.convertDate(
      orders?.lastestDeliveryDate,
      fromFormat: DateTimeFormat.yyyy_MM_ddTHH_mm_ssSS,
      toFormat: DateTimeFormat.dd_MM,
    );
    final date = (fromDate == toDate) ? 'Ngày $fromDate' : 'Từ $fromDate đến $toDate';

    ///
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          '• Dự kiến giao: ',
          style: UITextStyle.regular.copyWith(
            color: UIColors.grayText,
          ),
        ),
        const SizedBox(height: 2),
        Text(
          date,
          style: UITextStyle.semiBold.copyWith(
            color: UIColors.red,
          ),
        ),
      ],
    );
  }
}
