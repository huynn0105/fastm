import '../../../../../../../../common/widgets/textfields.dart';
import '../../../../../../cubit/product_scheme_pay_later_order/buyer_info_cubit.dart';
class BuyerComponent extends StatelessWidget {
  const BuyerComponent({Key? key}) : super(key: key);

  @override
  Widget build(BuildContext context) {
    final cubit = context.read<BuyerInfoCubit>();
    return BlocBuilder<BuyerInfoCubit, BuyerInfoState>(
      builder: (context, state) {
        return Padding(
          padding: const EdgeInsets.fromLTRB(16, 24, 16, 0),
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                'Thông tin người trả chậm',
                style: UITextStyle.semiBold.copyWith(
                  color: UIColors.grayText,
                ),
              ),
              const SizedBox(
                height: 12,
              ),
              Container(
                padding: const EdgeInsets.all(16),
                decoration: BoxDecoration(
                  color: UIColors.white,
                  borderRadius: BorderRadius.circular(8),
                ),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    AutoScrollTag(
                      controller: cubit.scrollController,
                      key: ValueKey(state.errorIndexes[AppConstants.buyerNameKey] ?? 0),
                      index: state.errorIndexes[AppConstants.buyerNameKey] ?? 0,
                      child: CollectDataView(
                        errorMsg: state.errorFields[AppConstants.buyerNameKey],
                        child: UITextField(
                          controller: cubit.buyerNameController,
                          labelText: 'Họ và tên',
                          hasError: state.errorFields[AppConstants.buyerNameKey] != null,
                          onChanged: cubit.onBuyerNameChanged,
                        ),
                      ),
                    ),
                    const SizedBox(
                      height: 16,
                    ),
                    AutoScrollTag(
                      controller: cubit.scrollController,
                      key: ValueKey(state.errorIndexes[AppConstants.buyerPhoneKey] ?? 0),
                      index: state.errorIndexes[AppConstants.buyerPhoneKey] ?? 0,
                      child: CollectDataView(
                        errorMsg: state.errorFields[AppConstants.buyerPhoneKey],
                        child: UITextField(
                          controller: cubit.buyerPhoneController,
                          labelText: 'Số điện thoại',
                          hasError: state.errorFields[AppConstants.buyerPhoneKey] != null,
                          keyboardType: TextInputType.number,
                          inputFormatters: [
                            FilteringTextInputFormatter.allow(RegExp('[0-9]')),
                            LengthLimitingTextInputFormatter(10),
                          ],
                          onChanged: cubit.onBuyerPhoneChanged,
                        ),
                      ),
                    ),
                    if (AppData.instance.landingPageSource?.isDAA == true) ...[
                      const SizedBox(
                        height: 16,
                      ),
                      AutoScrollTag(
                        controller: cubit.scrollController,
                        key: ValueKey(state.errorIndexes[AppConstants.buyerIDNumberKey] ?? 0),
                        index: state.errorIndexes[AppConstants.buyerIDNumberKey] ?? 0,
                        child: CollectDataView(
                          errorMsg: state.errorFields[AppConstants.buyerIDNumberKey],
                          child: UITextField(
                            controller: cubit.buyerIDNumberController,
                            labelText: 'Số CCCD',
                            hasError: state.errorFields[AppConstants.buyerIDNumberKey] != null,
                            keyboardType: TextInputType.number,
                            inputFormatters: [
                              FilteringTextInputFormatter.allow(RegExp('[0-9]')),
                              LengthLimitingTextInputFormatter(12),
                            ],
                            onChanged: cubit.onBuyerIDNumberChanged,
                          ),
                        ),
                      ),
                    ],
                  ],
                ),
              ),
            ],
          ),
        );
      },
    );
  }
}
