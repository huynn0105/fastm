import 'package:flutter/material.dart';
import '../../../../../../../../common/extension/datetime_extension.dart';
import '../../../../../../../../common/styles.dart';
import '../../../../../../../../common/utils/format_util.dart';

class SchedulePaymentItem extends StatelessWidget {
  const SchedulePaymentItem({
    Key? key,
    required this.index,
    required this.length,
    required this.emi,
  }) : super(key: key);

  final int index;
  final int length;
  final double emi;

  @override
  Widget build(BuildContext context) {
    final month = DateTime.now().dateOnly().copyWith(day: 1).addMonth(index + 1).month;
    return Padding(
      padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 12),
      child: Row(
        mainAxisAlignment: MainAxisAlignment.spaceBetween,
        children: [
          Text(
            "Kì ${index + 1} - Tháng $month",
            textAlign: TextAlign.center,
            style: UITextStyle.medium.copyWith(
              fontSize: 16,
            ),
          ),
          Text(
            FormatUtil.currencyDoubleFormat(emi),
            textAlign: TextAlign.center,
            style: UITextStyle.medium.copyWith(
              fontSize: 16,
            ),
          ),
        ],
      ),
    );
  }
}
