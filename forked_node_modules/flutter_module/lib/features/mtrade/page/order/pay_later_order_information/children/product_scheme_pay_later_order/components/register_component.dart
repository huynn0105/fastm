import 'package:flutter/material.dart';
import 'package:flutter_module/app_data.dart';
import 'package:flutter_module/common/enum/mtrade/landing_page_source.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

class RegisterComponent extends StatelessWidget {
  const RegisterComponent({
    Key? key,
    required this.enabled,
    required this.onSubmitted,
    this.isLeaveRequest = false,
  }) : super(key: key);

  final bool enabled;
  final Function() onSubmitted;
  final bool isLeaveRequest;

  @override
  Widget build(BuildContext context) {
    final src = AppData.instance.landingPageSource;
    return Padding(
      padding: const EdgeInsets.fromLTRB(16, 16, 16, 0),
      child: PrimaryButton(
        enabled: enabled,
        onPressed: onSubmitted,
        radius: 8,
        title: isLeaveRequest ? (src.isDAA == true ? "Kiểm tra hồ sơ" : "Gửi yêu cầu trả chậm") : "Đăng ký trả chậm",
      ),
    );
  }
}
