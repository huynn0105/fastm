import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/features/mtrade/page/order/order_process_state/items/order_process_state_mixin.dart';
import 'package:flutter_module/models/mtrade/order/order_model.dart';
import 'package:flutter_module/routes/routes.gr.dart';
import '../../../../../../../common/colors.dart';
import '../../../../../../../common/styles.dart';
import '../../../../../../../common/utils/text_util.dart';
import '../../../../../../../common/widgets/buttons.dart';
import '../../../../../../../common/widgets/images.dart';

class OrderWaitingProcessStateItem extends StatelessWidget with OrderProcessStateMixin {
  const OrderWaitingProcessStateItem({
    Key? key,
    required this.data,
    this.isDisable = false,
  }) : super(key: key);

  final OrderModel data;
  final bool isDisable;

  @override
  Widget build(BuildContext context) {
    final title = data.processText ?? "";
    final image = data.icon ?? "";
    final date = convertDate(data.updatedDate);

    return AppSplashButton(
      isDisable: isDisable,
      onTap: () {
        context.pushRoute(
            MTradeOrderDetailRoute(orderId: data.id));
      },
      child: SizedBox(
        height: 44,
        child: Row(
          children: [
            Align(
              alignment: Alignment.topLeft,
              child: AppImage.network(
                url: image,
                width: 24,
                height: 24,
              ),
            ),
            const SizedBox(
              width: 10,
            ),
            Expanded(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    title,
                    style: UITextStyle.semiBold.copyWith(
                      fontSize: 14,
                      color: UIColors.secondaryColor,
                    ),
                  ),
                  Text(
                    date,
                    style: UITextStyle.regular.copyWith(
                      fontSize: 13,
                      color: UIColors.grayText,
                    ),
                  ),
                ],
              ),
            ),
            if(!isDisable)
              const Align(
                alignment: Alignment.centerRight,
                child: AppImage.asset(
                  asset: "ic_arrow_right",
                  width: 24,
                  height: 24,
                ),
              ),
          ],
        ),
      ),
    );
  }
}
