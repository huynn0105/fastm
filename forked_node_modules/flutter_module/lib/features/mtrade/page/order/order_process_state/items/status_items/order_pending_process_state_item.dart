import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/features/mtrade/page/order/order_process_state/items/order_process_state_mixin.dart';
import 'package:flutter_module/routes/routes.gr.dart';

import '../../../../../../../common/colors.dart';
import '../../../../../../../common/styles.dart';
import '../../../../../../../common/widgets/buttons.dart';
import '../../../../../../../common/widgets/images.dart';
import '../../../../../../../models/mtrade/order/order_model.dart';

class OrderPendingProcessStateItem extends StatelessWidget
    with OrderProcessStateMixin {
  const OrderPendingProcessStateItem({
    Key? key,
    required this.data,
    this.isDisabled = false,
  }) : super(key: key);

  final OrderModel data;
  final bool isDisabled;

  @override
  Widget build(BuildContext context) {
    final title = data.processText ?? "";
    final image = data.icon ?? "";
    final status = data.pendingStateOrder;

    return AppSplashButton(
      isDisable: isDisabled,
      onTap: () {
        context.pushRoute(MTradeOrderDetailRoute(orderId: data.id));
      },
      child: SizedBox(
        height: 44,
        child: Row(
          children: [
            Align(
              alignment: Alignment.topLeft,
              child: AppImage.network(
                url: image,
                width: 24,
                height: 24,
              ),
            ),
            const SizedBox(
              width: 10,
            ),
            Expanded(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    title,
                    style: UITextStyle.semiBold.copyWith(
                      fontSize: 14,
                      color: UIColors.darkBlue,
                    ),
                  ),
                  Row(
                    children: [
                      Text(
                        "${status?.name ?? ""}:",
                        style: UITextStyle.regular.copyWith(
                          fontSize: 13,
                          color: UIColors.grayText,
                        ),
                      ),
                      const SizedBox(
                        width: 10,
                      ),
                      Text(
                        convertDate(status?.expired),
                        style: UITextStyle.regular.copyWith(
                          fontSize: 13,
                          color: UIColors.grayText,
                        ),
                      ),
                    ],
                  ),
                ],
              ),
            ),
            if (!isDisabled)
              const Align(
                alignment: Alignment.centerRight,
                child: AppImage.asset(
                  asset: "ic_arrow_right",
                  width: 24,
                  height: 24,
                ),
              ),
          ],
        ),
      ),
    );
  }
}
