import 'package:flutter/material.dart';

import '../../../../../../common/global_function.dart';
import '../../../../../../common/utils/datetime_util.dart';
import '../../../../../../common/utils/text_util.dart';

mixin OrderProcessStateMixin {
  String convertDate(String? date) {
    return DateTimeUtil.convertDate(
      date,
      fromFormat: DateTimeFormat.yyyy_MM_dd_HH_mm_ss,
      toFormat: DateTimeFormat.HH_mm_dd_MM_yyyy,
      isFromUtc: false,
    );
  }

  void openWebView(BuildContext context, String? url) {
    GlobalFunction.pushWebView(
      context: context,
      url: TextUtils.convertPrivateUrl(url ?? ""),
    );
  }
}
