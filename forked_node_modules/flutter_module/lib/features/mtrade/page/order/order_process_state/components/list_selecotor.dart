import 'package:flutter/material.dart';
import 'package:flutter_module/common/bottom_sheet/wrapper/data_wrapper.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

class ListSelector extends StatelessWidget {
  const ListSelector({
    super.key,
    required this.data,
    this.onTabItem,
    this.indexSelected = 0,
    this.spacing = 8,
    this.keyTitle = 'title',
    this.fontSize = 13,
  });

  final List<DataWrapper>? data;
  final Function(dynamic, int)? onTabItem;
  final int indexSelected;
  final double spacing;
  final String keyTitle;
  final double fontSize;

  @override
  Widget build(BuildContext context) {
    return SizedBox(
      height: 32,
      width: double.infinity,
      child: ListView.builder(
        physics: AppConstants.physics,
        shrinkWrap: true,
        scrollDirection: Axis.horizontal,
        itemCount: data?.length ?? 0,
        itemBuilder: (context, index) {
          final item = data?[index];
          final isSelected = indexSelected != null && indexSelected == index;

          return Container(
            height: 32,
            margin: index == 0 ? EdgeInsets.zero : EdgeInsets.only(left: spacing),
            decoration: BoxDecoration(
              borderRadius: BorderRadius.circular(20),
              color: isSelected ? UIColors.primaryColor : UIColors.white,
            ),
            child: SplashButton(
              onTap: () {
                if (onTabItem != null) {
                  onTabItem!(item, index);
                }
              },
              child: Padding(
                padding: const EdgeInsets.symmetric(
                  horizontal: 12,
                ),
                child: Column(
                  mainAxisAlignment: MainAxisAlignment.center,
                  crossAxisAlignment: CrossAxisAlignment.center,
                  children: [
                    Text(
                      item?.value ?? '',
                      style: UITextStyle.medium.copyWith(
                        fontSize: fontSize,
                        color: isSelected ? UIColors.white : UIColors.grayText,
                      ),
                    ),
                  ],
                ),
              ),
            ),
          );
        },
      ),
    );
  }
}
