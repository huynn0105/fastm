import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/features/mtrade/cubit/filter/mtrade_filter_cubit.dart';

class FilterPathComponent extends StatelessWidget {
  const FilterPathComponent({
    Key? key,
    required this.query,
  }) : super(key: key);

  final String query;

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<MTradeFilterCubit, MTradeFilterState>(
      builder: (context, state) {
        final path = state.path;
        final data = [query, path].where((e) => e.isNotEmpty).join(' - ');
        if (data.isEmpty) {
          return const SizedBox();
        }
        return Container(
          alignment: Alignment.centerLeft,
          margin: const EdgeInsets.fromLTRB(16, 0, 16, 16),
          padding: const EdgeInsets.only(bottom: 16),
          decoration: const BoxDecoration(
            border: Border(
              bottom: BorderSide(
                color: UIColors.lightGray,
              ),
            ),
          ),
          child: Text(
            'Đang hiển thị với từ khóa “$data”',
            style: UITextStyle.semiBold.copyWith(
              fontSize: 14,
              color: UIColors.grayText,
            ),
          ),
        );
      },
    );
  }
}
