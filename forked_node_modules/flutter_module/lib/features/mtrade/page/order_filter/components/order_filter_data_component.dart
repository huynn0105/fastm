import 'package:flutter/material.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/features/mtrade/page/order_filter/items/order_filter_group_item.dart';
import 'package:flutter_module/features/mtrade/page/order_filter/utils/callback.dart';
import 'package:flutter_module/models/mtrade/filter/mtrade_filter_group_model.dart';
import 'package:flutter_module/models/mtrade/filter/selected_filter_model.dart';

class OrderFilterDataComponent extends StatelessWidget {
  const OrderFilterDataComponent({
    super.key,
    required this.status,
    required this.data,
    required this.selectedData,
    required this.onSelected,
  });

  final BlocStatus status;
  final List<MTradeFilterGroupModel> data;
  final Map<String, List<SelectedFilterModel>> selectedData;
  final OnFilterSelected onSelected;

  @override
  Widget build(BuildContext context) {
    return Expanded(
      child: ListView.separated(
        padding: const EdgeInsets.all(16),
        keyboardDismissBehavior: ScrollViewKeyboardDismissBehavior.onDrag,
        itemBuilder: (context, index) {
          return OrderFilterGroupItem(
            data: data[index],
            selectedData: selectedData[data[index].keyword ?? ''] ?? [],
            onSelected: onSelected,
          );
        },
        separatorBuilder: (_, __) => const Divider(height: 0),
        itemCount: data.length,
      ),
    );
  }
}
