import 'package:flutter/material.dart';
import 'package:flutter_module/features/mtrade/page/order_filter/components/order_filter_grid_view_component.dart';
import 'package:flutter_module/features/mtrade/page/order_filter/utils/callback.dart';
import 'package:flutter_module/features/mtrade/page/product_filter/widgets/filter_group_block.dart';
import 'package:flutter_module/models/mtrade/filter/mtrade_filter_group_model.dart';
import 'package:flutter_module/models/mtrade/filter/selected_filter_model.dart';

class OrderFilterGroupItem extends StatelessWidget {
  const OrderFilterGroupItem({
    super.key,
    required this.data,
    required this.selectedData,
    required this.onSelected,
  });

  final MTradeFilterGroupModel data;
  final List<SelectedFilterModel> selectedData;
  final OnFilterSelected onSelected;

  @override
  Widget build(BuildContext context) {
    final filters = data.list ?? [];
    final keyword = data.keyword ?? '';

    return FilterGroupBlock(
      title: data.title ?? '',
      child: OrderFilterGridViewComponent(
        data: filters,
        keyword: keyword,
        selectedData: selectedData,
        onSelected: onSelected,
      ),
    );
  }
}
