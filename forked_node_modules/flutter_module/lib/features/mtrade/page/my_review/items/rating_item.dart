import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/datetime_util.dart';
import 'package:flutter_module/models/mtrade/my_review/mtrade_my_review_model.dart';

import '../../../../../../common/widgets/images.dart';

class RatingItem extends StatelessWidget {
  const RatingItem({
    Key? key,
    required this.data,
  }) : super(key: key);

  final MyReviewData data;

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(
        color: UIColors.white,
        borderRadius: BorderRadius.circular(8),
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: UITextStyle.semiBold,
            maxLines: 1,
            overflow: TextOverflow.ellipsis,
          ),
          const SizedBox(
            height: 5,
          ),
          Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: [
              Padding(
                padding: const EdgeInsets.only(right: 10),
                child: Row(
                  mainAxisSize: MainAxisSize.min,
                  children: List.generate(rating, (index) {
                    return const AppImage.asset(
                      asset: "ic_star",
                      width: 16,
                      height: 16,
                      color: UIColors.green,
                    );
                  }),
                ),
              ),
              Flexible(
                child: Text(
                  "$name - $elapsedTime",
                  style: UITextStyle.regular.copyWith(
                    fontSize: 12,
                    color: UIColors.grayText,
                  ),
                  maxLines: 1,
                  overflow: TextOverflow.ellipsis,
                  textAlign: TextAlign.right,
                ),
              ),
            ],
          ),
          const SizedBox(
            height: 10,
          ),
          Text(
            comment,
            style: UITextStyle.regular.copyWith(
              fontSize: 13,
              color: UIColors.grayText,
            ),
            maxLines: 2,
            overflow: TextOverflow.ellipsis,
          ),
        ],
      ),
    );
  }

  int get rating => data.rate ?? 0;
  String get title => data.title ?? "";
  String get comment => data.note ?? "";
  String get name => data.fullName ?? "";
  String get elapsedTime {
    return DateTimeUtil.convertTimeAgo(
      data.createdDate ?? "",
      format: DateTimeFormat.yyyy_MM_dd,
      isFromUtc: false,
    );
  }
}
