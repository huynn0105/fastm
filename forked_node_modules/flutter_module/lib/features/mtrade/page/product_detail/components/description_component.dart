import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/html_widget.dart';

import '../../../../../models/mtrade/product_detail/description_model.dart';

class DescriptionComponent extends StatelessWidget {
  const DescriptionComponent({
    Key? key,
    required this.descriptions,
  }) : super(key: key);

  final List<DescriptionModel> descriptions;

  @override
  Widget build(BuildContext context) {
    if (descriptions.isEmpty) {
      return const SizedBox();
    }
    return ListView.separated(
      shrinkWrap: true,
      padding: const EdgeInsets.all(16),
      physics: const NeverScrollableScrollPhysics(),
      itemBuilder: (context, index) {
        final item = descriptions[index];
        return Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          mainAxisSize: MainAxisSize.min,
          children: [
            Text(
              item.title ?? "",
              style: UITextStyle.semiBold.copyWith(
                fontSize: 16,
                color: UIColors.grayText,
              ),
            ),
            const SizedBox(
              height: 16,
            ),
            HtmlWidget.raw(
              data: item.content ?? "",
              isSelectable: true,
            ),
          ],
        );
      },
      separatorBuilder: (_, __) => const SizedBox(
        height: 16,
      ),
      itemCount: descriptions.length,
    );
  }
}
