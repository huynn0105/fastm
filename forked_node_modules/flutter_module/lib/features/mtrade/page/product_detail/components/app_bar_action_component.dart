import 'package:flutter/foundation.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../cubit/cart/mtrade_cart_cubit.dart';

import 'package:badges/badges.dart' as badges;

import '../widgets/shopping_button.dart';

class AppBarActionComponent extends StatelessWidget {
  const AppBarActionComponent({
    Key? key,
    required this.onBack,
    required this.onHome,
    required this.onShare,
    required this.onCart,
    this.enabledShare = true,
  }) : super(key: key);

  final bool enabledShare;
  final Function() onBack;
  final Function() onCart;
  final Function() onShare;
  final Function() onHome;

  @override
  Widget build(BuildContext context) {
    return Row(
      children: [
        if (!kIsWeb)
          ActionButton(
            asset: "ic_arrow_left",
            onTap: onBack,
          ),
        const Spacer(),
        if (!kIsWeb)
          BlocBuilder<MTradeCartCubit, MTradeCartState>(
            builder: (context, state) {
              return badges.Badge(
                showBadge: state.products.isNotEmpty,
                badgeContent: Text(
                  "${state.products.length}",
                  style: UITextStyle.medium.copyWith(
                    color: UIColors.white,
                  ),
                ),
                position: badges.BadgePosition.topEnd(top: -5, end: -5),
                child: ActionButton(
                  asset: "ic_mtrade_cart_fill",
                  onTap: onCart,
                ),
              );
            },
          ),
        if (!kIsWeb)
          const SizedBox(
            width: 16,
          ),
        if (enabledShare) ...[
          ActionButton(
            asset: "ic_share_product",
            onTap: onShare,
          ),
          const SizedBox(
            width: 16,
          ),
        ],
        ActionButton(
          asset: "ic_go_home",
          onTap: onHome,
        ),
      ],
    );
  }
}
