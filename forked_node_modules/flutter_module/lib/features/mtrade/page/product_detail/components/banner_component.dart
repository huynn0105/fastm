import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';

import '../../../../../common/widgets/images.dart';
import '../../mtrade/widgets/mtrade_carousel_widget.dart';

class BannerComponent extends StatelessWidget {
  const BannerComponent({
    Key? key,
    required this.images,
    this.productIcon,
    this.enabledOpenPreview = true,
  }) : super(key: key);

  final List<String> images;
  final List<String>? productIcon;
  final bool enabledOpenPreview;

  @override
  Widget build(BuildContext context) {
    return Stack(
      children: [
        Column(
          children: [
            Container(
              color: UIColors.white,
              width: double.infinity,
              height: 375,
              child: MTradeCarouselWidget(
                enabledOpenPreview: enabledOpenPreview,
                images: images,
                aspectRatio: 1,
              ),
            ),
            if (productIcon?.isNotEmpty == true)
              const SizedBox(height: 14),
          ],
        ),
        if (productIcon?.isNotEmpty == true)
          Positioned(
            left: 0,
            right: 0,
            bottom: 0,
            child: SingleChildScrollView(
              scrollDirection: Axis.horizontal,
              physics: const NeverScrollableScrollPhysics(),
              child: Row(
                children: productIcon!.map((e) {
                  return AppImage.network(url: e, width: 57, height: 60, fit: BoxFit.contain);
                }).toList(),
                // children: [
                //   if (showPromotionTag) const ProductTag.promotion(),
                //   if (showPayLaterTag) const ProductTag.paylater(),
                //   if (showMTradeTag) const ProductTag.mtrade(),
                // ],
              ),
            ),
          ),
      ],
    );
  }
}
