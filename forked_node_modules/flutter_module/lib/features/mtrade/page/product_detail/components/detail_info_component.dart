import 'package:flutter/material.dart';
import 'package:flutter_module/common/utils/text_util.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../../../models/mtrade/product_detail/mtrade_product_detail_info_model.dart';
import '../items/detail_info_item.dart';

class DetailInfoComponent extends StatelessWidget {
  const DetailInfoComponent({
    Key? key,
    required this.data,
  }) : super(key: key);

  final DetailInfoModel? data;

  @override
  Widget build(BuildContext context) {
    return Container(
      color: UIColors.white,
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          const SizedBox(
            height: 16,
          ),
          Padding(
            padding: const EdgeInsets.only(left: 16),
            child: Text(
              "Thông tin chi tiết",
              style: UITextStyle.medium.copyWith(
                fontSize: 16,
                color: UIColors.grayText,
              ),
            ),
          ),
          const SizedBox(
            height: 6,
          ),
          ListView.separated(
            shrinkWrap: true,
            padding: EdgeInsets.zero,
            physics: const NeverScrollableScrollPhysics(),
            itemBuilder: (context, index) {
              return Container(
                decoration: BoxDecoration(
                  color: index.isEven ? UIColors.white : UIColors.background,
                ),
                child: list[index],
              );
            },
            separatorBuilder: (_, __) => const SizedBox(
              height: 4,
            ),
            itemCount: list.length,
          ),
          const SizedBox(
            height: 6,
          ),
        ],
      ),
    );
  }

  List<DetailInfoItem> get list {
    return [
      if (TextUtils.isNotEmpty(data?.insuranceDuration))
        DetailInfoItem(
          title: "Thời gian bảo hành",
          content: data?.insuranceDuration ?? "",
          highlight: true,
          backgroundColor: Colors.transparent,
        ),
      if (TextUtils.isNotEmpty(data?.size))
        DetailInfoItem(
          title: "Kích thước",
          content: data?.size ?? "",
          backgroundColor: Colors.transparent,
        ),
      if (TextUtils.isNotEmpty(data?.mass))
        DetailInfoItem(
          title: "Trọng lượng",
          content: data?.mass ?? "",
          backgroundColor: Colors.transparent,
        ),
    ];
  }
}
