class ShoppingButton extends StatelessWidget {
  const ShoppingButton({
    Key? key,
    required this.title,
    required this.onTap,
    required this.backgroundColor,
    required this.disableBackgroundColor,
    this.enabled = true,
    this.width = 90,
    this.height = 48,
    this.subtitle,
  }) : super(key: key);

  const ShoppingButton.payNow({
    Key? key,
    required this.onTap,
    this.enabled = true,
    this.width = 90,
    this.height = 48,
    this.subtitle,
  })  : title = "Trả ngay",
        backgroundColor = UIColors.primaryColor,
        disableBackgroundColor = UIColors.gray,
        super(key: key);

  const ShoppingButton.payLater({
    Key? key,
    required this.onTap,
    this.enabled = true,
    this.width = 90,
    this.height = 48,
    this.subtitle,
  })  : title = "Trả chậm",
        backgroundColor = UIColors.darkBlue,
        disableBackgroundColor = UIColors.darkGray,
        super(key: key);

  final double width;
  final double height;
  final String title;
  final VoidCallback onTap;
  final bool enabled;
  final Color backgroundColor;
  final Color disableBackgroundColor;
  final String? subtitle;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: onTap,
      isDisabled: !enabled,
      child: Container(
        constraints: BoxConstraints(
          minWidth: width,
          minHeight: height,
        ),
        alignment: Alignment.center,
        decoration: BoxDecoration(
          color: enabled ? backgroundColor : disableBackgroundColor,
        ),
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            Text(
              title,
              style: UITextStyle.medium.copyWith(
                fontSize: 16,
                color: UIColors.white,
                height: 1,
              ),
            ),
            AnimatedCrossFade(
              crossFadeState: TextUtils.isEmpty(subtitle) ? CrossFadeState.showFirst : CrossFadeState.showSecond,
              duration: const Duration(milliseconds: 500),
              firstChild: const SizedBox(),
              secondChild: Column(
                children: [
                  const SizedBox(
                    height: 3,
                  ),
                  Text(
                    subtitle ?? "",
                    style: UITextStyle.medium.copyWith(
                      fontSize: 12,
                      color: UIColors.white.withOpacity(0.8),
                      height: 1,
                    ),
                    maxLines: 1,
                    overflow: TextOverflow.ellipsis,
                  ),
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}

class ActionButton extends StatelessWidget {
  const ActionButton({
    Key? key,
    required this.asset,
    required this.onTap,
  }) : super(key: key);

  final String asset;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      onTap: onTap,
      child: Container(
        width: 44,
        height: 44,
        alignment: Alignment.center,
        decoration: const BoxDecoration(
          color: UIColors.blurBlackBackground,
          shape: BoxShape.circle,
        ),
        child: AppImage.asset(
          asset: asset,
          width: 24,
          height: 24,
          color: UIColors.white,
          fit: BoxFit.contain,
        ),
      ),
    );
  }
}
