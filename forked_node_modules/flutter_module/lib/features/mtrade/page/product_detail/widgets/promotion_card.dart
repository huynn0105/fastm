class PromotionCard extends StatelessWidget {
  const PromotionCard({
    super.key,
    required this.data,
    this.width = 275,
  });

  final double width;
  final MTradePromotionModel? data;

  @override
  Widget build(BuildContext context) {
    String remain = data?.formatRemain() ?? "";
    return Container(
      width: width,
      padding: const EdgeInsets.all(8),
      decoration: BoxDecoration(
        color: UIColors.background,
        borderRadius: BorderRadius.circular(8),
      ),
      child: Column(
        mainAxisSize: MainAxisSize.min,
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          OverflowText(
            data?.name ?? "",
            style: UITextStyle.bold.copyWith(
              fontSize: 16,
              color: UIColors.darkBlue,
            ),
          ),
          const SizedBox(
            height: 2,
          ),
          OverflowText(
            data?.omsPromotionConditions?.getFirst()?.masterPromotionCategory?.nameOut ?? "",
            style: UITextStyle.regular.copyWith(
              fontSize: 12,
              color: UIColors.grayText,
            ),
          ),
          const SizedBox(
            height: 8,
          ),
          // if (data?.getUsedPercent() == 0) ...{
          //   const SizedBox(
          //     height: 2,
          //   ),
          // } else ...{
          //   PromotionPercentBar(
          //     percent: data?.getUsedPercent() ?? 0,
          //   ),
          // },
          const SizedBox(
            height: 8,
          ),
          Row(
            children: [
              Expanded(
                child: Text(
                  data?.getUsedPercent() == 0
                      ? "Hết hạn sau $remain"
                      : "Đã dùng ${FormatUtil.doubleFormat((data?.getUsedPercent() ?? 0) * 100)}%, hết hạn sau $remain",
                  style: UITextStyle.semiBold.copyWith(
                    fontSize: 12,
                    color: UIColors.red,
                  ),
                  maxLines: 1,
                  overflow: TextOverflow.ellipsis,
                ),
              ),
              SplashButton(
                onTap: () {
                  context.pushRoute(MTradeDetailPromotionRoute(
                    showApplyButton: false,
                    promotion: data,
                  ));
                },
                child: Text(
                  "Điều kiện",
                  style: UITextStyle.semiBold.copyWith(
                    fontSize: 12,
                    color: UIColors.primaryColor,
                  ),
                ),
              ),
            ],
          ),
        ],
      ),
    );
  }
}
