import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/extension/list_extension.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/checkbox.dart';
import 'package:flutter_module/common/widgets/divider.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/features/mtrade/page/product_detail/widgets/promotion_percent_bar.dart';
import 'package:flutter_module/models/mtrade/promotion/mtrade_promotion_model.dart';
import 'package:flutter_module/routes/routes.gr.dart';

class PromotionItem extends StatelessWidget {
  const PromotionItem({
    super.key,
    required this.item,
    this.isSelected = false,
    this.isDisable = false,
    this.onSelect,
    this.onForceSelect,
  });

  final MTradePromotionModel item;
  final bool isSelected;
  final bool isDisable;
  final Function()? onSelect;
  final Function()? onForceSelect;

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      isDisable: isDisable,
      onTap: () {
        onSelect?.call();
      },
      child: Container(
        height: 90,
        margin: const EdgeInsets.only(
          bottom: 8,
        ),
        decoration: BoxDecoration(
          color: UIColors.white,
          borderRadius: BorderRadius.circular(8),
          border: Border.all(
            color: UIColors.white,
          ),
        ),
        child: Row(
          children: [
            const SizedBox(
              width: 64,
              height: double.infinity,
              child: Center(
                child: AppImage.network(
                  url: 'https://cdn.zeplin.io/647ef8fae68325260c3a642c/assets/698224de-baa0-4ccb-9b78-09f479a2cfa0.png',
                  width: 40,
                  height: 40,
                ),
              ),
            ),
            const DottedDivider(
              color: UIColors.lightGray,
              direction: Axis.vertical,
            ),
            Expanded(
              child: Container(
                padding: const EdgeInsets.only(left: 16, right: 12),
                decoration: BoxDecoration(
                  borderRadius: const BorderRadius.only(
                    topRight: Radius.circular(8),
                    bottomRight: Radius.circular(8),
                  ),
                  color: isDisable
                      ? UIColors.lightGray
                      : isSelected
                          ? UIColors.lightBlue
                          : UIColors.white,
                ),
                child: Stack(
                  children: [
                    Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      mainAxisAlignment: MainAxisAlignment.center,
                      children: [
                        Padding(
                          padding: const EdgeInsets.only(right: 24),
                          child: Text(
                            item.name ?? '',
                            style: isDisable
                                ? UITextStyle.medium.copyWith(
                                    fontSize: 16,
                                    color: UIColors.grayText,
                                  )
                                : UITextStyle.bold.copyWith(
                                    fontSize: 16,
                                    color: UIColors.darkBlue,
                                  ),
                            maxLines: 1,
                            overflow: TextOverflow.ellipsis,
                          ),
                        ),
                        Padding(
                          padding: const EdgeInsets.only(right: 24),
                          child: Text(
                            item.omsPromotionConditions?.getFirst()?.masterPromotionCategory?.nameOut ?? '',
                            style: UITextStyle.regular.copyWith(
                              fontSize: 12,
                              color: UIColors.grayText,
                            ),
                            maxLines: 1,
                            overflow: TextOverflow.ellipsis,
                          ),
                        ),
                        const SizedBox(
                          height: 6,
                        ),
                        // if (item.getUsedPercent() == 0) ...{
                        //   const SizedBox(
                        //     height: 2,
                        //   ),
                        // } else ...{
                        //   PromotionPercentBar(
                        //     percent: item.getUsedPercent(),
                        //   ),
                        // },
                        Row(
                          children: [
                            Expanded(
                              child: isDisable && item.errorMessage?.isNotEmpty == true
                                  ? Row(
                                      children: [
                                        const AppImage.asset(
                                          asset: 'ic_warning_outline',
                                          width: 16,
                                          height: 16,
                                        ),
                                        const SizedBox(
                                          width: 4,
                                        ),
                                        Text(
                                          item.errorMessage ?? '',
                                          style: UITextStyle.regular.copyWith(
                                            fontSize: 12,
                                            color: UIColors.red,
                                          ),
                                          maxLines: 1,
                                          overflow: TextOverflow.ellipsis,
                                        )
                                      ],
                                    )
                                  : Text(
                                      item.getUsedPercent() == 0
                                          ? "Hết hạn sau ${item.formatRemain()}"
                                          : "Đã dùng ${FormatUtil.doubleFormat(item.getUsedPercent() * 100)}%, hết hạn sau ${item.formatRemain()}",
                                      style: UITextStyle.semiBold.copyWith(
                                        fontSize: 12,
                                        color: UIColors.red,
                                      ),
                                      maxLines: 1,
                                      overflow: TextOverflow.ellipsis,
                                    ),
                            ),
                            SplashButton(
                              onTap: () async {
                                final isUse = await context.pushRoute(MTradeDetailPromotionRoute(
                                  showApplyButton: !isDisable,
                                  promotion: item,
                                ));
                                if (isUse is bool && isUse == true) {
                                  onForceSelect?.call();
                                }
                              },
                              child: Padding(
                                padding: const EdgeInsets.symmetric(
                                  vertical: 8,
                                ),
                                child: Text(
                                  'Điều kiện',
                                  style: UITextStyle.semiBold.copyWith(
                                    fontSize: 12,
                                    color: UIColors.primaryColor,
                                  ),
                                ),
                              ),
                            ),
                          ],
                        )
                      ],
                    ),
                    Positioned(
                      top: 12,
                      right: 0,
                      child: IgnorePointer(
                        ignoring: true,
                        child: AppCheckbox.circle(
                          value: isSelected,
                          isEnable: !isDisable,
                          onChanged: (value) {
                            onSelect?.call();
                          },
                        ),
                      ),
                    ),
                  ],
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }
}
