import 'package:flutter/material.dart';
import 'package:flutter_module/common/utils/text_util.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/divider.dart';
import '../../../../../common/widgets/images.dart';
import '../../../../../models/mtrade/order/order_model.dart';
import '../../mtrade/widgets/mtrade_block_widget.dart';
import '../../order/order_process_state/items/status_items/order_delivery_process_state_item.dart';

class OrderDetailComponent extends StatelessWidget {
  const OrderDetailComponent({
    Key? key,
    required this.orderID,
    required this.order,
  }) : super(key: key);

  final String orderID;
  final OrderModel? order;

  @override
  Widget build(BuildContext context) {
    final products = order?.items ?? [];
    //
    return MTradeBlockWidget(
      childPadding: const EdgeInsets.symmetric(horizontal: 16),
      titlePadding: const EdgeInsets.symmetric(horizontal: 16),
      spacing: 8,
      title: "Thông tin đơn hàng $orderID",
      child: Container(
        padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 12),
        decoration: BoxDecoration(
          borderRadius: BorderRadius.circular(8),
          color: UIColors.white,
        ),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            ListView.separated(
              shrinkWrap: true,
              padding: EdgeInsets.zero,
              physics: const NeverScrollableScrollPhysics(),
              itemBuilder: (context, index) {
                final item = products[index];
                return Row(
                  children: [
                    AppImage.network(
                      url: item.image ?? "",
                      width: 40,
                      height: 40,
                      borderRadius: BorderRadius.circular(8),
                    ),
                    const SizedBox(
                      width: 12,
                    ),
                    Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Text(
                          item.productName ?? "",
                          style: UITextStyle.regular.copyWith(
                            fontSize: 14,
                          ),
                        ),
                        const SizedBox(
                          height: 2,
                        ),
                        RichText(
                          text: TextSpan(
                            children: [
                              TextSpan(
                                text: "Số lượng:",
                                style: UITextStyle.regular.copyWith(
                                  fontSize: 13,
                                  color: UIColors.grayText,
                                ),
                              ),
                              const WidgetSpan(
                                child: SizedBox(
                                  width: 8,
                                ),
                              ),
                              TextSpan(
                                text: "${item.quantity ?? 0}",
                                style: UITextStyle.regular.copyWith(
                                  fontSize: 13,
                                  color: UIColors.grayText,
                                ),
                              ),
                            ],
                          ),
                        ),
                      ],
                    ),
                  ],
                );
              },
              separatorBuilder: (_, __) => const DottedDivider(
                height: 8,
                indent: 52,
              ),
              itemCount: products.length,
            ),
            const DottedDivider(
              height: 12,
            ),
            InfoItem(
              title: "Đơn vị vận chuyển: ",
              content: order?.trackingPartner?.partner ?? "",
              contentColor: UIColors.defaultText,
            ),
            const SizedBox(
              height: 4,
            ),
            InfoItem(
              title: "Dự kiến giao: ",
              content: order?.trackingPartner?.getEstimatedDeliveryDate() ?? "",
              contentColor: UIColors.red,
              contentStyle: UITextStyle.semiBold.copyWith(
                fontSize: 13,
              ),
            ),
          ],
        ),
      ),
    );
  }
}
