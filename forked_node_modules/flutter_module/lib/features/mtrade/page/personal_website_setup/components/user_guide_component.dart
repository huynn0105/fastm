import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/app_data.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

import '../../../../../routes/routes.gr.dart';

class UserGuideComponent extends StatelessWidget {
  const UserGuideComponent({
    Key? key,
    this.padding = const EdgeInsets.symmetric(horizontal: 16),
  }) : super(key: key);

  final EdgeInsets padding;

  @override
  Widget build(BuildContext context) {
    final data = AppData.instance.configs?.product?.landingPageUserGuides ?? [];
    if (data.isEmpty) {
      return const SizedBox();
    }
    return Padding(
      padding: padding,
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            "Hướng dẫn hữu ích dành cho bạn",
            style: UITextStyle.semiBold.copyWith(
              fontSize: 16,
              color: UIColors.grayText,
            ),
          ),
          const SizedBox(
            height: 8,
          ),
          Container(
            padding: const EdgeInsets.all(16),
            decoration: BoxDecoration(
              color: UIColors.white,
              borderRadius: BorderRadius.circular(8),
            ),
            child: ListView.separated(
              shrinkWrap: true,
              padding: EdgeInsets.zero,
              physics: const NeverScrollableScrollPhysics(),
              itemBuilder: (context, index) {
                final item = data[index];
                final content = item.content ?? "";
                final url = item.link ?? "";
                return SplashButton(
                  onTap: () {
                    context.pushRoute(
                      WebViewBottomSheetRoute(
                        url: url,
                        title: "Hướng dẫn",
                        showTitle: false,
                      ),
                    );
                  },
                  child: Row(
                    children: [
                      Expanded(
                        child: Text(
                          content,
                          style: UITextStyle.regular.copyWith(
                            fontSize: 14,
                            color: UIColors.primaryColor,
                          ),
                        ),
                      ),
                      const SizedBox(
                        width: 5,
                      ),
                      const AppImage.asset(
                        asset: "ic_arrow_right",
                        width: 24,
                        height: 24,
                        color: UIColors.primaryColor,
                      ),
                    ],
                  ),
                );
              },
              separatorBuilder: (_, __) => const Divider(
                height: 16,
              ),
              itemCount: data.length,
            ),
          ),
        ],
      ),
    );
  }
}
