import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/bottom_sheet/bottom_sheet_provider.dart';
import 'package:flutter_module/common/global_function.dart';
import 'package:flutter_module/common/toast/toast_provider.dart';
import 'package:flutter_module/models/mtrade/personal_website_setup/mtrade_personal_website_setup_model.dart';

import '../../../../../../../common/styles.dart';
import '../../../../../../../routes/routes.gr.dart';
import '../../../../../common/colors.dart';
import '../../../../../common/enum/mtrade/mtrade_tab_page.dart';
import '../../../../../common/event_bus/event_bus.dart';
import '../../../../../common/widgets/buttons.dart';
import '../../../../../common/widgets/images.dart';
import '../../../../../general_config.dart';
import 'qr_code_component.dart';

class WebsiteComponent extends StatelessWidget {
  const WebsiteComponent({
    Key? key,
    required this.title,
    this.titleStyle,
    required this.data,
    this.showSetup = true,
    this.forceCheckCode = false,
    this.showTopSizedBox = true,
    this.padding,
    this.onAction,
  }) : super(key: key);

  final String title;
  final TextStyle? titleStyle;
  final MTradePersonalWebsiteSetupModel? data;
  final bool showSetup;
  final bool forceCheckCode;
  final bool showTopSizedBox;
  final EdgeInsets? padding;
  final Function()? onAction;

  @override
  Widget build(BuildContext context) {
    final url = data?.sortLink ?? "";
    return Padding(
      padding: padding ?? const EdgeInsets.symmetric(horizontal: 16),
      child: Column(
        children: [
          if (showTopSizedBox)
            const SizedBox(
              height: 14,
            ),
          Row(
            mainAxisAlignment: MainAxisAlignment.spaceBetween,
            children: [
              Expanded(
                child: Text(
                  title,
                  style: titleStyle ??
                      UITextStyle.semiBold.copyWith(
                        fontSize: 16,
                        color: UIColors.grayText,
                      ),
                ),
              ),
              if (showSetup)
                AppSplashButton(
                  onTap: () {
                    _onSetUp(context);
                  },
                  child: Padding(
                    padding: const EdgeInsets.fromLTRB(10, 10, 0, 10),
                    child: Text(
                      "Thiết lập",
                      style: UITextStyle.semiBold.copyWith(
                        fontSize: 14,
                        color: UIColors.primaryColor,
                      ),
                    ),
                  ),
                ),
            ],
          ),
          if (!showSetup)
            const SizedBox(
              height: 10,
            ),
          Container(
            width: double.infinity,
            padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 8),
            decoration: BoxDecoration(
              color: UIColors.darkBlue,
              borderRadius: BorderRadius.circular(8),
            ),
            child: Column(
              children: [
                Row(
                  children: [
                    Flexible(
                      child: Text(
                        url,
                        style: UITextStyle.regular.copyWith(
                          color: UIColors.lightBlue,
                        ),
                        maxLines: 1,
                        overflow: TextOverflow.ellipsis,
                      ),
                    ),
                    const SizedBox(
                      width: 12,
                    ),
                    AppSplashButton(
                      onTap: () {
                        onAction?.call();
                        _onCopy(context, url);
                      },
                      child: Padding(
                        padding: const EdgeInsets.symmetric(vertical: 5),
                        child: Row(
                          children: [
                            const AppImage.asset(
                              asset: "ic_mtrade_copy",
                              width: 24,
                              height: 24,
                              color: UIColors.lightBlue,
                            ),
                            const SizedBox(
                              width: 4,
                            ),
                            Text(
                              "Copy",
                              style: UITextStyle.regular.copyWith(
                                color: UIColors.lightBlue.withOpacity(0.6),
                              ),
                            ),
                          ],
                        ),
                      ),
                    ),
                  ],
                ),
                Row(
                  mainAxisAlignment: MainAxisAlignment.spaceBetween,
                  children: [
                    AppSplashButton(
                      onTap: () {
                        onAction?.call();
                        _onQrGenerate(context, url);
                      },
                      child: Padding(
                        padding: const EdgeInsets.symmetric(vertical: 5),
                        child: Row(
                          children: [
                            const AppImage.asset(
                              asset: "ic_mtrade_qr_code",
                              width: 24,
                              height: 24,
                              color: UIColors.lightBlue,
                            ),
                            const SizedBox(
                              width: 4,
                            ),
                            Text(
                              "Quét mã QR",
                              style: UITextStyle.regular.copyWith(
                                color: UIColors.lightBlue.withOpacity(0.6),
                              ),
                            ),
                          ],
                        ),
                      ),
                    ),
                    AppSplashButton(
                      onTap: () {
                        onAction?.call();
                        _onShare(context, url);
                      },
                      child: Padding(
                        padding: const EdgeInsets.symmetric(vertical: 5),
                        child: Row(
                          children: [
                            Text(
                              "Gửi ngay",
                              style: UITextStyle.regular.copyWith(
                                color: UIColors.lightBlue.withOpacity(0.6),
                              ),
                            ),
                            const SizedBox(
                              width: 4,
                            ),
                            const AppImage.asset(
                              asset: "ic_mtrade_send",
                              width: 24,
                              height: 24,
                              color: UIColors.lightBlue,
                            ),
                          ],
                        ),
                      ),
                    ),
                  ],
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }

  _onCopy(BuildContext context, String url) {
    GlobalFunction.action(
      context: context,
      forceCheckPayNow: forceCheckCode,
      onTap: () {
        GlobalFunction.copyText(url).then(
          (_) {
            ToastProvider.instance.showCopy(context: context);
          },
        );
      },
    );
  }

  _onQrGenerate(BuildContext context, String url) {
    GlobalFunction.action(
      context: context,
      forceCheckPayNow: forceCheckCode,
      onTap: () {
        BottomSheetProvider.instance.show(
          context,
          title: "Quét mã QRCode",
          child: QrCodeComponent(
            data: url,
          ),
        );
      },
    );
  }

  _onShare(BuildContext context, String url) {
    GlobalFunction.action(
      context: context,
      forceCheckPayNow: forceCheckCode,
      onTap: () {
        GlobalFunction.shareText(
          url,
          subject: "Mua sắm thả ga cùng MTrade",
        );
      },
    );
  }

  _onSetUp(BuildContext context) {
    GlobalFunction.action(
      context: context,
      forceCheckPayNow: forceCheckCode,
      onTap: () {
        context.pushRoute(MTradePersonalWebsiteTabbarRoute());
        Future.delayed(const Duration(milliseconds: 200), () {
          eventBus.fire(MTradeMTradeTabPageIndexEventBus(MTradeTabPage.tools.index));
        });
      },
    );
  }
}
