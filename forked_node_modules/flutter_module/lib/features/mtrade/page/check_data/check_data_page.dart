import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/features/authentication/auth/auth_cubit.dart';

import '../../../../common/bloc_status.dart';
import '../../../../common/widgets/loading.dart';
import '../../../../common/widgets/loadmore_widget.dart';
import '../../../app_cubit.dart';
import '../../cubit/check_project_code/mtrade_check_project_code_cubit.dart';
import 'components/check_login_component.dart';
import 'components/check_project_code_component.dart';

class CheckDataPage extends StatelessWidget {
  const CheckDataPage({
    Key? key,
    required this.child,
  }) : super(key: key);

  final Widget child;

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<AuthCubit, AuthState>(
      builder: (context, authState) {
        if (!authState.hasLoggedIn) {
          return const Scaffold(
            body: Column(
              children: [
                CheckLoginComponent(),
              ],
            ),
          );
        }
        return BlocBuilder<MTradeCheckProjectCodeCubit, MTradeCheckProjectCodeState>(
          builder: (context, projectCodeState) {
            if (!projectCodeState.checkProjectCodePayNow.isSuccess) {
              return Scaffold(
                body: Stack(
                  children: [
                    LoadMoreWidget(
                      onRefresh: () async {
                        return context.read<MTradeCheckProjectCodeCubit>().refreshPayNow();
                      },
                      child: ListView(
                        children: [
                          if (!projectCodeState.checkProjectCodePayNow.isLoading)
                            CheckProjectCodeComponent(
                              message: projectCodeState.data?.payNow?.message ?? "",
                              url: projectCodeState.data?.payNow?.url ?? "",
                            ),
                        ],
                      ),
                    ),
                    Visibility(
                      visible: projectCodeState.checkProjectCodePayNow.isLoading,
                      child: const LoadingWidget.dark(),
                    ),
                  ],
                ),
              );
            }
            return child;
          },
        );
      },
    );
  }
}
