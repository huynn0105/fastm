import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/common/widgets/loading.dart';
import '../items/filter_item.dart';

import '../../../cubit/filter/mtrade_filter_cubit.dart';

class FilterDataComponent extends StatelessWidget {
  const FilterDataComponent({super.key});

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<MTradeFilterCubit, MTradeFilterState>(
      builder: (context, state) {
        if (state.status.showLoading) {
          return const Expanded(
            child: LoadingWidget(),
          );
        }

        return Expanded(
          child: ListView.separated(
            keyboardDismissBehavior: ScrollViewKeyboardDismissBehavior.onDrag,
            padding: const EdgeInsets.all(16),
            itemBuilder: (context, index) {
              return FilterItem(
                group: state.data[index],
                isTypingPrice: state.isInputtingPrice,
              );
            },
            separatorBuilder: (_, __) => const Divider(height: 0),
            itemCount: state.data.length,
          ),
        );
      },
    );
  }
}
