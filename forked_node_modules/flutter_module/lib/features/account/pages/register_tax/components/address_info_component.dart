import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/collect_data_view.dart';
import 'package:flutter_module/common/widgets/textfields.dart';

class AddressInfoComponent extends StatelessWidget {
  const AddressInfoComponent({
    super.key,
    this.title = "",
    this.onTapProvince,
    this.provinceController,
    this.onTapDistrict,
    this.districtController,
    this.onTapWard,
    this.wardController,
    this.onChangeAddress,
    this.addressController,
    this.provinceErrorMsg,
    this.districtErrorMsg,
    this.wardErrorMsg,
    this.addressErrorMsg,
  });

  final String title;
  final VoidCallback? onTapProvince;
  final TextEditingController? provinceController;
  final VoidCallback? onTapDistrict;
  final TextEditingController? districtController;
  final VoidCallback? onTapWard;
  final TextEditingController? wardController;
  final Function(String)? onChangeAddress;
  final TextEditingController? addressController;
  final String? provinceErrorMsg;
  final String? districtErrorMsg;
  final String? wardErrorMsg;
  final String? addressErrorMsg;

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(
        horizontal: 16,
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: UITextStyle.semiBold.copyWith(
              color: UIColors.orangeText,
              fontSize: 16,
            ),
          ),
          const SizedBox(
            height: 8,
          ),
          Container(
            width: double.infinity,
            padding: const EdgeInsets.all(16),
            decoration: BoxDecoration(
              color: UIColors.white,
              borderRadius: BorderRadius.circular(8),
            ),
            child: Column(
              children: [
                SplashButton(
                  onTap: onTapProvince,
                  child: CollectDataView(
                    errorMsg: provinceErrorMsg,
                    child: IgnorePointer(
                      ignoring: true,
                      child: UITextField.dropdown(
                        controller: provinceController,
                        labelText: 'Tỉnh / Thành phố',
                        labelTextStyle: UITextStyle.regular,
                      ),
                    ),
                  ),
                ),
                const SizedBox(
                  height: 16,
                ),
                SplashButton(
                  onTap: onTapDistrict,
                  child: CollectDataView(
                    errorMsg: districtErrorMsg,
                    child: IgnorePointer(
                      ignoring: true,
                      child: UITextField.dropdown(
                        controller: districtController,
                        labelText: 'Quận / Huyện',
                        labelTextStyle: UITextStyle.regular,
                      ),
                    ),
                  ),
                ),
                const SizedBox(
                  height: 16,
                ),
                SplashButton(
                  onTap: onTapWard,
                  child: CollectDataView(
                    errorMsg: wardErrorMsg,
                    child: IgnorePointer(
                      ignoring: true,
                      child: UITextField.dropdown(
                        controller: wardController,
                        labelText: 'Phường / Xã',
                        labelTextStyle: UITextStyle.regular,
                      ),
                    ),
                  ),
                ),
                const SizedBox(
                  height: 16,
                ),
                CollectDataView(
                  errorMsg: addressErrorMsg,
                  child: UITextField(
                    controller: addressController,
                    labelText: 'Số nhà / đường phố, thôn, xóm',
                    labelTextStyle: UITextStyle.regular,
                    onChanged: onChangeAddress,
                  ),
                ),
              ],
            ),
          )
        ],
      ),
    );
  }
}
