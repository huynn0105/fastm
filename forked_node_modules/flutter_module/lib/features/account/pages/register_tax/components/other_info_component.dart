import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/collect_data_view.dart';
import 'package:flutter_module/common/widgets/textfields.dart';

class OtherInfoComponent extends StatelessWidget {
  const OtherInfoComponent({
    super.key,
    this.title = "",
    this.onChangePhone,
    this.phoneController,
    this.onChangeEmail,
    this.emailController,
    this.phoneNumberErrorMsg,
    this.emailErrorMsg,
  });

  final String title;
  final Function(String)? onChangePhone;
  final TextEditingController? phoneController;
  final Function(String)? onChangeEmail;
  final TextEditingController? emailController;
  final String? phoneNumberErrorMsg;
  final String? emailErrorMsg;

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(
        horizontal: 16,
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Text(
            title,
            style: UITextStyle.semiBold.copyWith(
              color: UIColors.orangeText,
              fontSize: 16,
            ),
          ),
          const SizedBox(
            height: 8,
          ),
          Container(
            width: double.infinity,
            padding: const EdgeInsets.all(16),
            decoration: BoxDecoration(
              color: UIColors.white,
              borderRadius: BorderRadius.circular(8),
            ),
            child: Column(
              children: [
                CollectDataView(
                  errorMsg: phoneNumberErrorMsg,
                  child: UITextField(
                    controller: phoneController,
                    labelText: 'Số điện thoại liên hệ',
                    labelTextStyle: UITextStyle.regular,
                    onChanged: onChangeEmail,
                  ),
                ),
                const SizedBox(
                  height: 16,
                ),
                CollectDataView(
                  errorMsg: emailErrorMsg,
                  child: UITextField(
                    controller: emailController,
                    labelText: 'Email liên hệ',
                    labelTextStyle: UITextStyle.regular,
                    onChanged: onChangeEmail,
                  ),
                ),
              ],
            ),
          )
        ],
      ),
    );
  }
}
