import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/gender_util.dart';
import 'package:flutter_module/models/user/user_meta_data_model.dart';

class UserInfoComponent extends StatelessWidget {
  const UserInfoComponent({
    super.key,
    this.userMetaData,
  });

  final UserMetaDataModel? userMetaData;

  @override
  Widget build(BuildContext context) {
    Widget renderInfo(String title, String? description) {
      return Row(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          Expanded(
            flex: 3,
            child: Text(
              title,
              style: UITextStyle.regular.copyWith(
                color: UIColors.grayText,
              ),
            ),
          ),
          Expanded(
            flex: 7,
            child: Text(
              description ?? "",
              style: UITextStyle.regular,
            ),
          ),
        ],
      );
    }

    return Container(
      color: UIColors.white,
      padding: const EdgeInsets.symmetric(
        horizontal: 16,
        vertical: 12,
      ),
      child: Column(
        children: [
          renderInfo("Họ tên:", userMetaData?.countryIdName),
          const SizedBox(height: 4),
          renderInfo("Giới tính:", GenderUtil.instance.getNameGender(userMetaData?.gender ?? "")),
          const SizedBox(height: 4),
          renderInfo("Ngày sinh:", userMetaData?.countryIdDateOfBirth),
          const SizedBox(height: 4),
          renderInfo("CMND/CCCD:", userMetaData?.countryIdNumber),
          const SizedBox(height: 4),
          renderInfo("Ngày cấp:", userMetaData?.countryIdIssuedDate),
          const SizedBox(height: 4),
          renderInfo("Nơi cấp:", userMetaData?.countryIdIssuedBy),
        ],
      ),
    );
  }
}
