part of 'bank_account_detail_cubit.dart';

class BankAccountDetailState extends Equatable {
  const BankAccountDetailState({
    this.status = BlocStatus.initial,
    this.removeStatus = BlocStatus.initial,
  });

  final BlocStatus status;
  final BlocStatus removeStatus;

  @override
  List<Object> get props => [
        status,
        removeStatus,
      ];

  BankAccountDetailState copyWith({
    BlocStatus? status,
    BlocStatus? removeStatus,
  }) {
    return BankAccountDetailState(
      status: status ?? this.status,
      removeStatus: removeStatus ?? this.removeStatus,
    );
  }
}
