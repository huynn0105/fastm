import 'package:auto_route/auto_route.dart';
import 'package:flutter/foundation.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/dialogs/dialog_provider.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/toast/toast_provider.dart';
import 'package:flutter_module/common/widgets/appbar.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/checkbox.dart';
import 'package:flutter_module/common/widgets/divider.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/common/widgets/loading.dart';
import 'package:flutter_module/features/account/pages/bank_account_detail/components/dialog_remove_bank_account_component.dart';
import 'package:flutter_module/features/account/pages/bank_account_detail/cubit/bank_account_detail/bank_account_detail_cubit.dart';
import 'package:flutter_module/models/personal_setting/bank_account_model.dart';

@RoutePage()
class BankAccountDetailPage extends StatefulWidget implements AutoRouteWrapper {
  const BankAccountDetailPage({
    super.key,
    required this.bankAccount,
    this.isCanRemoveBankAccount = false,
  });

  final BankAccountModel bankAccount;
  final bool isCanRemoveBankAccount;

  @override
  State<BankAccountDetailPage> createState() => _BankAccountDetailPageState();
  @override
  Widget wrappedRoute(BuildContext context) {
    return BlocProvider(
      create: (context) {
        final cubit = BankAccountDetailCubit();
        return cubit;
      },
      child: this,
    );
  }
}

class _BankAccountDetailPageState extends State<BankAccountDetailPage> {
  bool isDefault = false;

  @override
  void initState() {
    super.initState();
    isDefault = widget.bankAccount.isDefault == true;
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: MFastSimpleAppBar(
        title: "Chi tiết tài khoản",
        context: context,
        onBack: () {
          context.popRoute(isDefault != widget.bankAccount.isDefault);
        },
      ),
      body: MultiBlocListener(
        listeners: [
          BlocListener<BankAccountDetailCubit, BankAccountDetailState>(
            listenWhen: (previous, current) => previous.status != current.status,
            listener: (context, state) {
              if (state.status.isSuccess) {
                setState(() {
                  isDefault = true;
                });
                ToastProvider.instance.show(
                  context: context,
                  message: "Đặt tài khoản mặc định thành công",
                );
              }
              if (state.status.isFailure) {
                ToastProvider.instance.show(
                  context: context,
                  message: "Đặt tài khoản mặc định thất bại",
                );
              }
            },
          ),
          BlocListener<BankAccountDetailCubit, BankAccountDetailState>(
            listenWhen: (previous, current) => previous.removeStatus != current.removeStatus,
            listener: (context, state) {
              if (state.removeStatus.isSuccess) {
                ToastProvider.instance.display("Xóa tài khoản thành công");
                context.popRoute(true);
              }
              if (state.removeStatus.isFailure) {
                ToastProvider.instance.show(
                  context: context,
                  message: "Xóa tài khoản thất bại",
                );
              }
            },
          ),
        ],
        child: BlocBuilder<BankAccountDetailCubit, BankAccountDetailState>(
          builder: (context, state) {
            return Stack(
              children: [
                Column(
                  children: [
                    Container(
                      padding: const EdgeInsets.all(16),
                      color: UIColors.white,
                      child: Column(
                        children: [
                          Row(
                            children: [
                              Expanded(
                                flex: 35,
                                child: Text(
                                  "Họ và tên:",
                                  style: UITextStyle.regular.copyWith(
                                    color: UIColors.grayText,
                                  ),
                                ),
                              ),
                              Expanded(
                                flex: 65,
                                child: Text(
                                  widget.bankAccount.bankFullName ?? "",
                                  style: UITextStyle.semiBold,
                                ),
                              )
                            ],
                          ),
                          const SizedBox(
                            height: 8,
                          ),
                          Row(
                            children: [
                              Expanded(
                                flex: 35,
                                child: Text(
                                  "Số CMND/CCCD:",
                                  style: UITextStyle.regular.copyWith(
                                    color: UIColors.grayText,
                                  ),
                                ),
                              ),
                              Expanded(
                                flex: 65,
                                child: Text(
                                  widget.bankAccount.bankIdNumber ?? "",
                                  style: UITextStyle.semiBold,
                                ),
                              )
                            ],
                          ),
                          const SizedBox(
                            height: 16,
                          ),
                          const DottedDivider(
                            height: 1,
                            color: UIColors.background,
                          ),
                          const SizedBox(
                            height: 12,
                          ),
                          Row(
                            children: [
                              Expanded(
                                child: Column(
                                  crossAxisAlignment: CrossAxisAlignment.start,
                                  children: [
                                    Text(
                                      "Loại tài khoản:",
                                      style: UITextStyle.regular.copyWith(
                                        color: UIColors.grayText,
                                      ),
                                    ),
                                    Text(
                                      widget.bankAccount.bankName ?? "",
                                      style: UITextStyle.semiBold,
                                    )
                                  ],
                                ),
                              ),
                              Expanded(
                                child: Column(
                                  crossAxisAlignment: CrossAxisAlignment.start,
                                  children: [
                                    Text(
                                      "Số tài khoản ngân hàng:",
                                      style: UITextStyle.regular.copyWith(
                                        color: UIColors.grayText,
                                      ),
                                    ),
                                    Text(
                                      widget.bankAccount.bankAccountNumber ?? "",
                                      style: UITextStyle.semiBold,
                                    )
                                  ],
                                ),
                              ),
                            ],
                          ),
                          const SizedBox(
                            height: 16,
                          ),
                          const DottedDivider(
                            height: 1,
                            color: UIColors.background,
                          ),
                          const SizedBox(
                            height: 12,
                          ),
                          Row(
                            children: [
                              Expanded(
                                flex: 3,
                                child: Text(
                                  "Trạng thái:",
                                  style: UITextStyle.regular.copyWith(
                                    color: UIColors.grayText,
                                  ),
                                ),
                              ),
                              const AppImage.asset(
                                asset: "ic_success",
                                width: 20,
                                height: 20,
                              ),
                              const SizedBox(
                                width: 8,
                              ),
                              Expanded(
                                flex: 7,
                                child: Text(
                                  widget.bankAccount.statusText ?? "",
                                  style: UITextStyle.semiBold.copyWith(
                                    color: UIColors.accentGreen,
                                  ),
                                ),
                              )
                            ],
                          ),
                        ],
                      ),
                    ),
                    const SizedBox(
                      height: 16,
                    ),
                    Padding(
                      padding: const EdgeInsets.symmetric(horizontal: 16),
                      child: AppCheckbox.rectangle(
                        value: isDefault,
                        isEnable: widget.isCanRemoveBankAccount && !isDefault,
                        title: "Đặt làm tài khoản ưu tiên",
                        onChanged: (p0) {
                          if (p0 == true) {
                            _onSetDefaultBankAccount(context);
                          }
                        },
                      ),
                    ),
                    const SizedBox(
                      height: 24,
                    ),
                    PrimaryButton(
                      onPressed: () {
                        context.popRoute();
                      },
                      title: "Quay lại",
                    ),
                    const SizedBox(
                      height: 16,
                    ),
                    if (widget.isCanRemoveBankAccount)
                      SplashButton(
                        onTap: () {
                          _onShowConfirmDialog(context);
                        },
                        child: Text(
                          "Hủy liên kết tài khoản",
                          style: UITextStyle.semiBold.copyWith(
                            color: UIColors.red,
                          ),
                        ),
                      )
                  ],
                ),
                Visibility(
                  visible: state.status.isLoading,
                  child: const LoadingWidget.dark(),
                )
              ],
            );
          },
        ),
      ),
    );
  }

  _onSetDefaultBankAccount(BuildContext context) {
    final cubit = context.read<BankAccountDetailCubit>();
    cubit.setDefaultBankAccount(widget.bankAccount.bankingId ?? "");
  }

  _onShowConfirmDialog(BuildContext context) {
    final cubit = context.read<BankAccountDetailCubit>();
    DialogProvider.instance.show(
      context: context,
      child: Builder(builder: (context) {
        return BlocProvider.value(
          value: cubit,
          child: DialogRemoveBankAccountComponent(
            bankAccount: widget.bankAccount,
            onConfirm: () {
              Navigator.of(context).pop();
              cubit.removeBankAccount(widget.bankAccount.bankingId ?? "");
            },
          ),
        );
      }),
    );
  }
}
