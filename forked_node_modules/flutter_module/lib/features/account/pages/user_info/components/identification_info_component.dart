import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/features/account/pages/user_info/items/user_info_item.dart';
import 'package:flutter_module/features/personal_setting/pages/personal_setting/widgets/personal_setting_block_widget.dart';

import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/user/user_meta_data_model.dart';
import 'package:flutter_module/routes/routes.gr.dart';

class IdentificationInfoComponent extends StatelessWidget {
  const IdentificationInfoComponent({
    Key? key,
    required this.userMetaData,
  }) : super(key: key);

  final UserMetaDataModel? userMetaData;

  @override
  Widget build(BuildContext context) {
    return PersonalSettingBlockWidget(
      title: "Thông tin định danh",
      margin: const EdgeInsets.all(16),
      padding: const EdgeInsets.all(12),
      trailing: AppSplashButton(
        onTap: () => _onShowEditSheet(context, userMetaData),
        child: const AppImage.asset(
          asset: "ic_edit",
          width: 24,
          height: 24,
        ),
      ),
      child: Column(
        children: [
          UserInfoItem(
            title: "Họ và tên",
            content: userMetaData?.countryIdName ?? '',
          ),
          const SizedBox(
            height: 10,
          ),
          UserInfoItem(
            title: "Số điện thoại",
            content: userMetaData?.mobilePhone ?? '',
          ),
          const SizedBox(
            height: 10,
          ),
          UserInfoItem(
            title: "Số CMND/ CCCD",
            content: userMetaData?.countryIdNumber ?? '',
          ),
          const SizedBox(
            height: 10,
          ),
          UserInfoItem(
            title: "Số CMND cũ",
            content: userMetaData?.countryOldIdNumber ?? '',
          ),
          const SizedBox(
            height: 10,
          ),
          UserInfoItem(
            title: "Ngày cấp",
            content: userMetaData?.countryIdIssuedDate ?? '',
          ),
          const SizedBox(
            height: 10,
          ),
          UserInfoItem(
            title: "Nơi cấp",
            content: userMetaData?.countryIdIssuedBy ?? '',
          ),
        ],
      ),
    );
  }

  _onShowEditSheet(BuildContext context, UserMetaDataModel? userMetaData) async {
    String phone = FormatUtil.phoneFormat(userMetaData?.mobilePhone ?? '');
    String idNumber = userMetaData?.countryIdNumber ?? '';
    BottomSheetProvider.instance.show(
      context,
      title: 'Thông tin cần thay đổi',
      child: Padding(
        padding: const EdgeInsets.all(16),
        child: Column(
          mainAxisSize: MainAxisSize.min,
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(
              'Chọn 1 trong 2 thông tin muốn thay đổi dưới đây',
              style: UITextStyle.regular.copyWith(
                fontSize: 13,
              ),
            ),
            const SizedBox(
              height: 16,
            ),
            ListTile(
              onTap: () {
                context.pushRoute(const MFastUpdatePhoneNumberRoute());
              },
              leading: const AppImage.asset(
                asset: 'ic_phone_circle',
                width: 48,
                height: 48,
              ),
              contentPadding: EdgeInsets.zero,
              title: const Text(
                'Số điện thoại đăng nhập',
              ),
              titleTextStyle: UITextStyle.regular.copyWith(
                fontSize: 16,
                color: UIColors.green,
              ),
              subtitle: Text(
                phone,
              ),
              subtitleTextStyle: UITextStyle.regular.copyWith(
                fontSize: 16,
              ),
              trailing: const AppImage.asset(
                asset: 'ic_arrow_right',
                width: 24,
                height: 24,
                color: UIColors.green,
              ),
            ),
            ListTile(
              leading: const AppImage.asset(
                asset: 'ic_cmnd_circle',
                width: 48,
                height: 48,
              ),
              contentPadding: EdgeInsets.zero,
              title: const Text(
                'CMND/CCCD đã định danh',
              ),
              titleTextStyle: UITextStyle.regular.copyWith(
                fontSize: 16,
                color: UIColors.primaryColor,
              ),
              subtitle: Text(
                idNumber,
              ),
              subtitleTextStyle: UITextStyle.regular.copyWith(
                fontSize: 16,
              ),
              trailing: const AppImage.asset(
                asset: 'ic_arrow_right',
                width: 24,
                height: 24,
                color: UIColors.primaryColor,
              ),
            )
          ],
        ),
      ),
    );
  }
}
