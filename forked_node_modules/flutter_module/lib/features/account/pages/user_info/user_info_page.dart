import 'package:flutter_module/features/account/cubit/user/user_cubit.dart';
import 'package:flutter_module/features/account/pages/user_info/components/basic_info_component.dart';
import 'package:flutter_module/features/account/pages/user_info/components/identification_info_component.dart';
import 'package:flutter_module/common/widgets/appbar.dart';
import 'package:flutter_module/routes/routes.dart';
import 'package:flutter_module/routes/routes.gr.dart';

@RoutePage()
class UserInfoPage extends StatelessWidget implements AutoRouteWrapper {
  const UserInfoPage({Key? key}) : super(key: key);

  @override
  Widget wrappedRoute(BuildContext context) {
    return this;
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: MFastAdvanceAppBar(
        context: context,
        title: "Thông tin cá nhân",
        onHome: () {
          getItInstance.get<AppRouter>().popUntilRoot();
        },
      ),
      body: BlocBuilder<UserCubit, UserState>(
        builder: (context, state) {
          final userMetaData = state.userMetaData;
          final timeDeleteAccount = userMetaData?.isCTVConfirmed == true ? '6' : '3';

          return Stack(
            children: [
              Column(
                children: [
                  BasicInfoComponent(
                    userMetaData: userMetaData,
                  ),
                  IdentificationInfoComponent(
                    userMetaData: userMetaData,
                  ),
                  Padding(
                    padding: const EdgeInsets.all(16),
                    child: RichText(
                      text: TextSpan(
                        text: "Tài khoản MFast sẽ tự động hủy, số dư tiền và các thông tin đi kèm cũng sẽ huỷ theo ",
                        style: UITextStyle.regular.copyWith(
                          fontSize: 13,
                        ),
                        children: [
                          TextSpan(
                            text: "khi không sử dụng trong $timeDeleteAccount tháng",
                            style: UITextStyle.semiBold.copyWith(
                              fontSize: 13,
                              color: UIColors.red,
                            ),
                          ),
                          const TextSpan(
                            text: ". Chi tiết tại ",
                          ),
                          TextSpan(
                            text: "điều khoản sử dụng",
                            style: UITextStyle.semiBold.copyWith(
                              fontSize: 13,
                              color: UIColors.primaryColor,
                            ),
                            recognizer: TapGestureRecognizer()
                              ..onTap = () {
                                context.router.push(WebViewRoute(
                                  title: 'Điều khoản sử dụng',
                                  url: AppData.instance.appInfo.termsOfUsageUrl ?? '',
                                ));
                              },
                          ),
                        ],
                      ),
                    ),
                  ),
                  const SizedBox(
                    height: 16,
                  ),
                  AppOutlinedButton(
                    onPressed: () {
                      context.popRoute();
                    },
                    backgroundColor: Colors.transparent,
                    borderRadius: BorderRadius.circular(50),
                    padding: const EdgeInsets.symmetric(
                      horizontal: 24,
                      vertical: 13,
                    ),
                    title: "Quay lại",
                  ),
                ],
              ),
              Visibility(
                visible: state.status.isLoading,
                child: const LoadingWidget.dark(),
              ),
            ],
          );
        },
      ),
    );
  }
}
