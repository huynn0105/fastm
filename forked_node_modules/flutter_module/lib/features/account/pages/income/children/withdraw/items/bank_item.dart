import 'package:flutter/material.dart';
import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/common/extension/string_extension.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/income/bank_model.dart';

import '../../../../../../../common/colors.dart';

class BankItem extends StatelessWidget {
  const BankItem({
    Key? key,
    required this.data,
    required this.isSelected,
    required this.isDisabled,
    required this.onTap,
    this.transactionFee,
    this.transactionMoney,
  }) : super(key: key);

  final BankModel? data;
  final double? transactionFee;
  final double? transactionMoney;
  final bool isSelected;
  final bool isDisabled;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    final bankIcon = data?.icon ?? '';
    final bankName = data?.bankName ?? '';
    final bankAccountName = data?.bankFullName ?? '';
    final bankAccountNumber = data?.bankAccountNumber ?? '';
    final bankIsDisabled = data?.disabled ?? false;

    final borderColor = isDisabled || bankIsDisabled
        ? UIColors.gray
        : isSelected
            ? UIColors.primaryColor
            : Colors.transparent;
    final avatarBorderColor = isDisabled || bankIsDisabled
        ? UIColors.gray
        : isSelected
            ? UIColors.primaryColor
            : UIColors.border;
    final backgroundColor = isDisabled || bankIsDisabled
        ? UIColors.lightGray
        : isSelected
            ? UIColors.lightBlue
            : UIColors.white;

    ///
    return AppSplashButton(
      onTap: onTap,
      child: AnimatedContainer(
        duration: AppConstants.duration,
        height: isSelected ? 120 : 60,
        width: double.infinity,
        alignment: Alignment.topCenter,
        child: Stack(
          children: [
            Positioned(
              left: 22,
              right: 0,
              top: 0,
              bottom: 0,
              child: AnimatedContainer(
                duration: AppConstants.duration,
                clipBehavior: Clip.antiAlias,
                padding: const EdgeInsets.only(left: 33, right: 12),
                decoration: BoxDecoration(
                  borderRadius: BorderRadius.circular(8),
                  color: backgroundColor,
                  border: Border.all(color: borderColor),
                ),
                child: OverflowBox(
                  minHeight: 60,
                  maxHeight: 120,
                  alignment: Alignment.topCenter,
                  child: Column(
                    children: [
                      SizedBox(
                        height: 58,
                        width: double.infinity,
                        child: Column(
                          crossAxisAlignment: CrossAxisAlignment.start,
                          mainAxisAlignment: MainAxisAlignment.center,
                          children: [
                            Row(
                              children: [
                                Expanded(
                                  child: Text(
                                    bankName,
                                    style: UITextStyle.regular,
                                    overflow: TextOverflow.ellipsis,
                                    maxLines: 1,
                                  ),
                                ),
                                Visibility(
                                  visible: isSelected,
                                  child: Text(
                                    'Đã chọn',
                                    style: UITextStyle.medium.copyWith(
                                      color: UIColors.primaryColor,
                                      fontSize: 13,
                                      height: 1,
                                    ),
                                  ),
                                ),
                                const SizedBox(
                                  width: 4,
                                ),
                                Container(
                                  width: 24,
                                  height: 24,
                                  alignment: Alignment.center,
                                  decoration: BoxDecoration(
                                    shape: BoxShape.circle,
                                    border: Border.all(
                                      color: isSelected ? UIColors.primaryColor : UIColors.border,
                                    ),
                                    color: isSelected ? UIColors.white : null,
                                  ),
                                  child: Visibility(
                                    visible: isSelected,
                                    child: Icon(
                                      Icons.circle,
                                      size: 18,
                                      color: isSelected ? UIColors.primaryColor : null,
                                    ),
                                  ),
                                ),
                              ],
                            ),
                            Visibility(
                              visible: isSelected,
                              child: Text(
                                "$bankAccountName - xxx${bankAccountNumber.slice(-4)}",
                                style: UITextStyle.medium.copyWith(
                                  color: UIColors.grayText,
                                ),
                                overflow: TextOverflow.ellipsis,
                                maxLines: 1,
                              ),
                            ),
                          ],
                        ),
                      ),
                      Visibility(
                        visible: isSelected,
                        child: Container(
                          height: 1,
                          width: double.infinity,
                          color: UIColors.white,
                        ),
                      ),
                      Visibility(
                        visible: isSelected,
                        child: SizedBox(
                          height: 58,
                          width: double.infinity,
                          child: Column(
                            crossAxisAlignment: CrossAxisAlignment.start,
                            mainAxisAlignment: MainAxisAlignment.center,
                            children: [
                              Row(
                                children: [
                                  Expanded(
                                    flex: 2,
                                    child: Text(
                                      "Phí giao dịch:",
                                      style: UITextStyle.regular.copyWith(
                                        color: UIColors.grayText,
                                        fontSize: 13,
                                      ),
                                      overflow: TextOverflow.ellipsis,
                                      maxLines: 1,
                                    ),
                                  ),
                                  const SizedBox(
                                    width: 10,
                                  ),
                                  Expanded(
                                    flex: 3,
                                    child: Text(
                                      FormatUtil.currencyDoubleFormat(transactionFee),
                                      style: UITextStyle.regular.copyWith(
                                        fontSize: 13,
                                      ),
                                      overflow: TextOverflow.ellipsis,
                                      maxLines: 1,
                                    ),
                                  ),
                                ],
                              ),
                              Row(
                                children: [
                                  Expanded(
                                    flex: 2,
                                    child: Text(
                                      "Số tiền giao dịch:",
                                      style: UITextStyle.regular.copyWith(
                                        color: UIColors.grayText,
                                        fontSize: 13,
                                      ),
                                      overflow: TextOverflow.ellipsis,
                                      maxLines: 1,
                                    ),
                                  ),
                                  const SizedBox(
                                    width: 10,
                                  ),
                                  Expanded(
                                    flex: 3,
                                    child: Text(
                                      FormatUtil.currencyDoubleFormat(transactionMoney),
                                      style: UITextStyle.bold.copyWith(
                                        fontSize: 13,
                                      ),
                                      overflow: TextOverflow.ellipsis,
                                      maxLines: 1,
                                    ),
                                  ),
                                ],
                              ),
                            ],
                          ),
                        ),
                      ),
                    ],
                  ),
                ),
              ),
            ),
            Positioned(
              left: 0,
              top: 8,
              child: AnimatedContainer(
                duration: AppConstants.duration,
                width: 44,
                height: 44,
                clipBehavior: Clip.antiAlias,
                alignment: Alignment.center,
                decoration: BoxDecoration(
                  color: UIColors.white,
                  shape: BoxShape.circle,
                  border: Border.all(color: avatarBorderColor),
                ),
                child: AppImage.network(
                  url: bankIcon,
                  width: 24,
                  height: 24,
                  errorBuilder: () {
                    return const AppImage.asset(
                      asset: "ic_bank",
                      width: 24,
                      height: 24,
                    );
                  },
                ),
              ),
            ),
          ],
        ),
      ),
    );
  }
}
