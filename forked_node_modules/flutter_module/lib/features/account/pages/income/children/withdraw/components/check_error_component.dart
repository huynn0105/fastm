import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_widget_from_html_core/flutter_widget_from_html_core.dart' as fwfh;

class CheckErrorComponent extends StatelessWidget {
  const CheckErrorComponent({
    super.key,
    required this.html,
  });

  final String html;

  @override
  Widget build(BuildContext context) {
    if (html.isEmpty) {
      return const SizedBox();
    }
    return Row(
      children: [
        Flexible(
          child: fwfh.HtmlWidget(
            html,
            textStyle: UITextStyle.regular.copyWith(
              fontSize: 16,
              height: 1.5,
              color: UIColors.defaultText,
            ),
            customWidgetBuilder: (element) {
              if (element.className == 'rowview') {
                return Row(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    const AppImage.asset(
                      asset: 'ic_bell_fill',
                      width: 28,
                      height: 28,
                      color: UIColors.accentRed,
                    ),
                    const SizedBox(
                      width: 12,
                    ),
                    Expanded(
                      child: Container(
                        constraints: const BoxConstraints(minHeight: 28),
                        child: Text(
                          element.text.trim(),
                          style: UITextStyle.bold.copyWith(
                            fontSize: 14,
                            color: UIColors.defaultText,
                          ),
                        ),
                      ),
                    ),
                  ],
                );
              }
              return null;
            },
          ),
        ),
      ],
    );
  }
}
