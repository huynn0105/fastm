import 'package:flutter/material.dart';
import 'package:flutter_module/common/widgets/collect_data_view.dart';
import 'package:flutter_module/common/widgets/empty_widget.dart';
import 'package:flutter_module/features/account/pages/income/children/withdraw/items/bank_item.dart';
import 'package:flutter_module/models/income/bank_model.dart';

import '../../../../../../../common/colors.dart';
import '../../../../../../../common/styles.dart';

class MethodWithdrawComponent extends StatelessWidget {
  const MethodWithdrawComponent({
    Key? key,
    required this.data,
    required this.selectedBankID,
    required this.transactoinFee,
    required this.transactionMoney,
    required this.onBankSelected,
    required this.isAllowWithdraw,
    this.errorMessage,
    required this.isLoading,
  }) : super(key: key);

  final List<BankModel> data;
  final String? selectedBankID;
  final double? transactoinFee;
  final double? transactionMoney;
  final Function(BankModel) onBankSelected;
  final String? errorMessage;
  final bool isAllowWithdraw;
  final bool isLoading;

  @override
  Widget build(BuildContext context) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          "2.",
          style: UITextStyle.semiBold.copyWith(
            color: UIColors.grayText,
          ),
        ),
        const SizedBox(
          width: 10,
        ),
        Expanded(
          child: Column(
            mainAxisSize: MainAxisSize.min,
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                "Chọn tài khoản ngân hàng",
                style: UITextStyle.semiBold.copyWith(
                  color: UIColors.grayText,
                ),
              ),
              const SizedBox(
                height: 6,
              ),
              if (data.isEmpty && !isLoading) ...[
                const SizedBox(
                  height: 10,
                ),
                const EmptyWidget(
                  message: 'Bạn chưa thêm tài khoản ngân hàng',
                ),
              ],
              if (data.isNotEmpty)
                CollectDataView(
                  errorMsg: errorMessage,
                  child: ListView.separated(
                    shrinkWrap: true,
                    padding: EdgeInsets.zero,
                    physics: const NeverScrollableScrollPhysics(),
                    itemBuilder: (context, index) {
                      final item = data[index];
                      bool isSelected = item.id == selectedBankID;

                      ///
                      return BankItem(
                        data: item,
                        transactionFee: transactoinFee,
                        transactionMoney: transactionMoney,
                        isSelected: isSelected,
                        isDisabled: !isAllowWithdraw,
                        onTap: () => onBankSelected(item),
                      );
                    },
                    separatorBuilder: (_, __) => const SizedBox(
                      height: 12,
                    ),
                    itemCount: data.length,
                  ),
                ),
            ],
          ),
        ),
      ],
    );
  }
}
