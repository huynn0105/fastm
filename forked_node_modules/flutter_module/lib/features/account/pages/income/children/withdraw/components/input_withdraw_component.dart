import 'package:flutter/material.dart';
import 'package:flutter/services.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/common/text_input_formatter/currency_text_input_formatter.dart';
import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';

import '../../../../../../../common/styles.dart';
import '../../../../../../../common/widgets/collect_data_view.dart';

class InputWithdrawComponent extends StatelessWidget {
  const InputWithdrawComponent({
    Key? key,
    required this.moneyController,
    required this.onMoneyChanged,
    required this.onMoneySelected,
    required this.availableBalance,
    required this.minWithdrawMoney,
    required this.defaulMoneyList,
    required this.errorMessage,
  }) : super(key: key);

  final TextEditingController moneyController;
  final Function(String) onMoneyChanged;
  final Function(double) onMoneySelected;
  final double availableBalance;
  final double minWithdrawMoney;
  final List<double> defaulMoneyList;
  final String? errorMessage;

  @override
  Widget build(BuildContext context) {
    return Row(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          "1.",
          style: UITextStyle.semiBold.copyWith(
            color: UIColors.grayText,
          ),
        ),
        const SizedBox(
          width: 10,
        ),
        Expanded(
          child: Column(
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(
                "Nhập số tiền cần rút",
                style: UITextStyle.semiBold.copyWith(
                  color: UIColors.grayText,
                ),
              ),
              const SizedBox(
                height: 6,
              ),
              Row(
                children: [
                  Text(
                    "Số dư khả dụng:",
                    style: UITextStyle.regular.copyWith(
                      color: UIColors.grayText,
                    ),
                  ),
                  const Padding(
                    padding: EdgeInsets.symmetric(horizontal: 5),
                    child: AppImage.asset(
                      asset: "ic_success",
                      width: 20,
                      height: 20,
                    ),
                  ),
                  Text(
                    FormatUtil.currencyDoubleFormat(availableBalance),
                    style: UITextStyle.semiBold,
                  ),
                ],
              ),
              const SizedBox(
                height: 6,
              ),
              CollectDataView(
                errorMsg: errorMessage,
                child: Container(
                  height: 48,
                  clipBehavior: Clip.hardEdge,
                  decoration: BoxDecoration(
                    borderRadius: BorderRadius.circular(8),
                  ),
                  child: TextField(
                    controller: moneyController,
                    cursorWidth: 2,
                    cursorHeight: 25,
                    cursorColor: UIColors.primaryColor,
                    cursorRadius: const Radius.circular(8),
                    style: UITextStyle.semiBold.copyWith(
                      fontSize: 16,
                    ),
                    textAlignVertical: TextAlignVertical.center,
                    keyboardType: TextInputType.number,
                    inputFormatters: [
                      FilteringTextInputFormatter.digitsOnly,
                      CurrencyTextInputFormatter.vi(),
                    ],
                    onChanged: (value) {
                      onMoneyChanged(value.replaceAll(".", ""));
                    },
                    decoration: InputDecoration(
                      filled: true,
                      fillColor: UIColors.white,
                      hintText: "Nhập số tiền",
                      hintStyle: UITextStyle.semiBold.copyWith(
                        fontSize: 16,
                        height: 1,
                        color: UIColors.grayText,
                      ),
                      border: InputBorder.none,
                      focusedBorder: OutlineInputBorder(
                        borderSide: BorderSide(
                          color: hasError ? UIColors.red : UIColors.primaryColor,
                        ),
                        borderRadius: BorderRadius.circular(8),
                      ),
                      contentPadding: const EdgeInsets.symmetric(
                        vertical: 8,
                        horizontal: 16,
                      ),
                      suffixIcon: Container(
                        width: 85,
                        alignment: Alignment.center,
                        margin: const EdgeInsets.only(right: 16),
                        child: Text(
                          "(>= ${FormatUtil.currencyDoubleFormat(minWithdrawMoney)})",
                          style: UITextStyle.regular.copyWith(
                            fontSize: 13,
                            height: 1,
                            color: UIColors.grayText,
                          ),
                        ),
                      ),
                    ),
                  ),
                ),
              ),
              const SizedBox(
                height: 14,
              ),
              SizedBox(
                height: 40,
                child: ListView.separated(
                  physics: AppConstants.physics,
                  scrollDirection: Axis.horizontal,
                  padding: EdgeInsets.zero,
                  itemBuilder: (context, index) {
                    final item = defaulMoneyList[index];
                    final format = FormatUtil.currencyDoubleFormat(item, showUnit: false);
                    return SplashButton(
                      borderRadius: BorderRadius.circular(20),
                      onTap: () {
                        moneyController.value = TextEditingValue(
                          text: format,
                          selection: TextSelection.collapsed(offset: format.length),
                        );
                        FocusManager.instance.primaryFocus?.unfocus();
                        onMoneySelected(item);
                      },
                      child: Container(
                        height: 40,
                        alignment: Alignment.center,
                        padding: const EdgeInsets.symmetric(horizontal: 16),
                        decoration: BoxDecoration(
                          color: UIColors.white,
                          borderRadius: BorderRadius.circular(20),
                        ),
                        child: Text(
                          format,
                          style: UITextStyle.medium.copyWith(
                            fontSize: 13,
                            color: UIColors.grayText,
                          ),
                        ),
                      ),
                    );
                  },
                  itemCount: defaulMoneyList.length,
                  separatorBuilder: (_, __) => const SizedBox(width: 8),
                ),
              ),
            ],
          ),
        ),
      ],
    );
  }

  bool get hasError => errorMessage != null;
}
