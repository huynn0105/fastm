part of 'bank_account_cubit.dart';

class BankAccountState extends Equatable {
  const BankAccountState({
    this.status = BlocStatus.initial,
    this.bidvMetaData,
    this.listBankAccount = const [],
  });

  final BlocStatus status;
  final MFastSuggestedBankModel? bidvMetaData;
  final List<BankAccountModel> listBankAccount;

  @override
  List<Object?> get props => [
        status,
        bidvMetaData,
        listBankAccount,
      ];

  BankAccountState copyWith({
    BlocStatus? status,
    MFastSuggestedBankModel? bidvMetaData,
    List<BankAccountModel>? listBankAccount,
  }) {
    return BankAccountState(
      status: status ?? this.status,
      bidvMetaData: bidvMetaData ?? this.bidvMetaData,
      listBankAccount: listBankAccount ?? this.listBankAccount,
    );
  }
}
