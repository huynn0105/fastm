import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/personal_setting/bank_account_model.dart';

class BankAccountItem extends StatelessWidget {
  const BankAccountItem({
    super.key,
    required this.item,
    this.onTap,
  });
  final BankAccountModel item;
  final VoidCallback? onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: onTap,
      child: Container(
        margin: const EdgeInsets.only(top: 10),
        padding: const EdgeInsets.all(12),
        decoration: BoxDecoration(
          color: item.bankLink == true
              ? UIColors.darkBlue
              : item.disabled == true
                  ? UIColors.gray
                  : UIColors.white,
          borderRadius: BorderRadius.circular(8),
        ),
        child: Row(
          children: [
            Container(
              width: 44,
              height: 44,
              decoration: const BoxDecoration(
                shape: BoxShape.circle,
                color: UIColors.background,
              ),
              child: Center(
                child: item.icon == null
                    ? const AppImage.asset(
                        asset: 'ic_bank',
                        width: 28,
                        height: 28,
                      )
                    : AppImage.network(
                        url: item.icon,
                        width: 28,
                        height: 28,
                      ),
              ),
            ),
            const SizedBox(
              width: 12,
            ),
            Expanded(
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Row(
                    children: [
                      Expanded(
                        child: Text(
                          item.bankName ?? "",
                          style: UITextStyle.medium.copyWith(
                            fontSize: 16,
                            color: item.bankLink == true ? UIColors.white : UIColors.boolText,
                          ),
                        ),
                      ),
                      if (item.bankLink == true) ...[
                        Text(
                          "Liên kết ngay",
                          style: UITextStyle.regular.copyWith(
                            fontSize: 13,
                            color: UIColors.white,
                          ),
                        ),
                        const SizedBox(
                          width: 4,
                        ),
                        const AppImage.asset(
                          asset: "ic_arrow_right",
                          width: 20,
                          height: 20,
                          color: UIColors.white,
                        ),
                      ] else if (item.isDefault == true) ...[
                        Text(
                          "Ưu tiên",
                          style: UITextStyle.regular.copyWith(
                            fontSize: 13,
                            color: UIColors.grayText,
                          ),
                        ),
                        const SizedBox(
                          width: 4,
                        ),
                        const AppImage.asset(
                          asset: "ic_success",
                          width: 20,
                          height: 20,
                        ),
                      ],
                    ],
                  ),
                  if (item.bankLink == true) ...[
                    Text(
                      "Tài khoản ${item.bankName} đã sẵn sàng để liên kết",
                      style: UITextStyle.regular.copyWith(
                        fontSize: 13,
                        color: UIColors.white.withOpacity(0.6),
                      ),
                    )
                  ] else if (item.disabled == true) ...[
                    Text(
                      item.disabledMessage ?? "",
                      style: UITextStyle.regular.copyWith(
                        fontSize: 13,
                        color: UIColors.grayText,
                      ),
                    )
                  ] else ...[
                    RichText(
                      text: TextSpan(
                        style: UITextStyle.regular.copyWith(
                          fontSize: 13,
                          color: UIColors.grayText,
                        ),
                        children: [
                          const TextSpan(
                            text: "Số tài khoản:    ",
                          ),
                          TextSpan(
                            text: item.bankAccountNumberCensor,
                            style: UITextStyle.semiBold,
                          ),
                        ],
                      ),
                    )
                  ]
                ],
              ),
            ),
          ],
        ),
      ),
    );
  }
}
