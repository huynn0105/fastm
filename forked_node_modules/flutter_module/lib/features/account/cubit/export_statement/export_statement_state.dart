part of 'export_statement_cubit.dart';

class ExportStatementState extends Equatable {
  const ExportStatementState({
    this.status = BlocStatus.initial,
    this.email,
    this.type,
    this.fromDate,
    this.toDate,
    this.enabledNextStep = true,
    this.errorFields = const {},
  });

  final BlocStatus status;
  final String? email;
  final ExportStatementType? type;
  final DateTime? fromDate;
  final DateTime? toDate;
  final bool enabledNextStep;
  final Map<String, String> errorFields;

  @override
  List<Object?> get props => [
        status,
        email,
        type,
        fromDate,
        toDate,
        enabledNextStep,
        errorFields,
      ];

  ExportStatementState copyWith({
    BlocStatus? status,
    String? email,
    ExportStatementType? type,
    DateTime? fromDate,
    DateTime? toDate,
    bool? enabledNextStep,
    Map<String, String>? errorFields,
  }) {
    return ExportStatementState(
      status: status ?? this.status,
      email: email ?? this.email,
      type: type ?? this.type,
      fromDate: fromDate ?? this.fromDate,
      toDate: toDate ?? this.toDate,
      enabledNextStep: enabledNextStep ?? this.enabledNextStep,
      errorFields: errorFields ?? this.errorFields,
    );
  }
}


enum ExportStatementFields {
  date,
  email,
}