part of 'statistic_income_cubit.dart';

class StatisticIncomeState extends Equatable {
  const StatisticIncomeState({
    this.status = BlocStatus.initial,
    this.data,
    this.statementStatus = BlocStatus.initial,
    this.errorMessage,
  });

  final BlocStatus status;
  final StatisticIncomeModel? data;
  final BlocStatus statementStatus;
  final String? errorMessage;

  @override
  List<Object?> get props => [
        status,
        data,
        statementStatus,
        errorMessage,
      ];

  StatisticIncomeState copyWith({
    BlocStatus? status,
    StatisticIncomeModel? data,
    BlocStatus? statementStatus,
    String? errorMessage,
  }) {
    return StatisticIncomeState(
      status: status ?? this.status,
      data: data ?? this.data,
      statementStatus: statementStatus ?? this.statementStatus,
      errorMessage: errorMessage,
    );
  }
}
