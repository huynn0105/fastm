part of 'history_income_cubit.dart';

class HistoryIncomeState extends Equatable {
  const HistoryIncomeState({
    this.status = BlocStatus.initial,
    this.type = HistoryIncomeType.all,
    this.data = const [],
    this.historyData,
  });

  final BlocStatus status;
  final HistoryIncomeType type;
  final List<HistoryIncomeItemModel> data;
  final HistoryIncomeModel? historyData;

  @override
  List<Object?> get props => [
        status,
        type,
        data,
        historyData,
      ];

  HistoryIncomeState copyWith({
    BlocStatus? status,
    HistoryIncomeType? type,
    List<HistoryIncomeItemModel>? data,
    HistoryIncomeModel? historyData,
  }) {
    return HistoryIncomeState(
      status: status ?? this.status,
      type: type ?? this.type,
      data: data ?? this.data,
      historyData: historyData ?? this.historyData,
    );
  }
}
