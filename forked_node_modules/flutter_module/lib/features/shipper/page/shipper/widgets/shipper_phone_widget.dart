import 'package:flutter/material.dart';
import 'package:flutter_module/common/global_function.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/buttons.dart';
import '../../../../../common/widgets/images.dart';

class ShipperPhoneWidget extends StatelessWidget {
  const ShipperPhoneWidget({
    Key? key,
    required this.value,
    this.enabled = true,
    this.hidePhone = false,
    this.isLTR = true,
  }) : super(key: key);

  final String value;
  final bool enabled;
  final bool hidePhone;
  final bool isLTR;

  @override
  Widget build(BuildContext context) {
    final Color color = enabled ? UIColors.primaryColor : UIColors.grayText;
    return SplashButton(
      onTap: () {
        GlobalFunction.launchPhone(value);
      },
      child: Row(
        mainAxisSize: MainAxisSize.min,
        children: [
          if (!isLTR) ...[
            AppImage.asset(
              asset: "ic_call_ringing",
              width: 20,
              height: 20,
              color: color,
            ),
            const SizedBox(
              width: 4,
            ),
          ],
          Flexible(
            child: Text(
              hidePhone ? "**** *** ***" : value,
              style: UITextStyle.semiBold.copyWith(
                fontSize: 13,
                color: color,
              ),
              maxLines: 1,
              overflow: TextOverflow.ellipsis,
            ),
          ),
          if (isLTR) ...[
            const SizedBox(
              width: 4,
            ),
            AppImage.asset(
              asset: "ic_call_ringing",
              width: 20,
              height: 20,
              color: color,
            ),
          ],
        ],
      ),
    );
  }
}
