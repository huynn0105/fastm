import 'package:flutter/material.dart';
import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/images.dart';

class ShipperLocationWidget extends StatelessWidget {
  const ShipperLocationWidget({
    Key? key,
    required this.distance,
    this.onTap,
  }) : super(key: key);

  final double? distance;
  final VoidCallback? onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: onTap,
      child: Column(
        mainAxisAlignment: MainAxisAlignment.start,
        children: [
          const AppImage.asset(
            asset: "ic_shipper_location",
            width: 28,
            height: 28,
          ),
          const SizedBox(
            height: 5,
          ),
          Text(
            " ${distance == null ? "---" : FormatUtil.currencyDoubleFormat(distance, showUnit: false)}Km",
            style: UITextStyle.regular.copyWith(
              fontSize: 12,
              color: UIColors.grayText,
            ),
          ),
        ],
      ),
    );
  }
}
