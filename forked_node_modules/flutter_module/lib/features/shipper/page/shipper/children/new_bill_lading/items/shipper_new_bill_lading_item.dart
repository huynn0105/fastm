class NewBillLadingItem extends StatelessWidget {
  const NewBillLadingItem({
    Key? key,
    required this.data,
  }) : super(key: key);

  final ShipperBillLadingModel data;

  @override
  Widget build(BuildContext context) {
    return Slidable(
      enabled: false,
      groupTag: "new_bill_lading_item",
      endActionPane: ActionPane(
        extentRatio: 80 / AppSize.instance.width,
        motion: const DrawerMotion(),
        children: [
          CustomSlidableAction(
            autoClose: true,
            backgroundColor: UIColors.primaryColor,
            foregroundColor: UIColors.white,
            padding: EdgeInsets.zero,
            onPressed: (context) {},
            child: Column(
              mainAxisAlignment: MainAxisAlignment.center,
              children: [
                const AppImage.asset(
                  asset: "ic_shipper_delivery_box",
                  width: 24,
                  height: 24,
                ),
                const SizedBox(
                  height: 4,
                ),
                Text(
                  'Nhận\nhàng',
                  style: UITextStyle.regular.copyWith(
                    fontSize: 14,
                    color: UIColors.white,
                    height: 1,
                  ),
                  textAlign: TextAlign.center,
                ),
              ],
            ),
          ),
        ],
      ),
      child: AppSplashButton(
        onTap: () {
          context.pushRoute(ShipperBillLadingDetailRoute(orderCode: data.orderCode ?? ""));
        },
        child: Container(
          height: 90,
          alignment: Alignment.center,
          decoration: const BoxDecoration(
            color: UIColors.white,
          ),
          child: Row(
            children: [
              const SizedBox(
                width: 16,
              ),
              Padding(
                padding: const EdgeInsets.symmetric(vertical: 10),
                child: ShipperLocationWidget(
                  distance: data.customer?.distance,
                  onTap: () {},
