import 'package:flutter/material.dart';

import '../../../../../../../common/colors.dart';
import '../../../../../../../common/styles.dart';
import '../../../../../../../common/widgets/images.dart';

class ShipperBillLadingStatusWidget extends StatelessWidget {
  const ShipperBillLadingStatusWidget({
    Key? key,
    required this.label,
    required this.asset,
    required this.color,
  }) : super(key: key);

  const ShipperBillLadingStatusWidget.success({
    Key? key,
    this.label = "Thành công",
    this.asset = "ic_success",
    this.color = UIColors.green,
  }) : super(key: key);

  const ShipperBillLadingStatusWidget.failure({
    Key? key,
    this.label = "Thất bại",
    this.asset = "ic_failure",
    this.color = UIColors.red,
  }) : super(key: key);

  final String label;
  final String asset;
  final Color color;

  @override
  Widget build(BuildContext context) {
    return Row(
      children: [
        AppImage.asset(
          asset: asset,
          width: 20,
          height: 20,
          color: color,
        ),
        const SizedBox(
          width: 5,
        ),
        Text(
          label,
          style: UITextStyle.semiBold.copyWith(
            fontSize: 13,
            color: color,
          ),
        ),
      ],
    );
  }
}
