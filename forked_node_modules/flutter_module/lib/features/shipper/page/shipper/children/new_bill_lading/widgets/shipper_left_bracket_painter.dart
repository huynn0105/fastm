import 'package:flutter/material.dart';

import '../../../../../../../common/colors.dart';

class ShipperLeftBracketPainter extends CustomPainter {
  @override
  void paint(Canvas canvas, Size size) {
    Path path = Path();
    path.moveTo(size.width * 0.6666667, size.height * 0.09101220);
    path.arcToPoint(
      Offset(size.width * 0.9506000, size.height * 0.004780488),
      radius: Radius.elliptical(size.width * 0.5333333, size.height * 0.09756098),
      rotation: 0,
      largeArc: false,
      clockwise: true,
    );
    path.lineTo(size.width, 0);
    path.lineTo(size.width, size.height);
    path.lineTo(size.width * 0.9506000, size.height * 0.9952195);
    path.arcToPoint(
      Offset(size.width * 0.6666667, size.height * 0.9089878),
      radius: Radius.elliptical(size.width * 0.5333333, size.height * 0.09756098),
      rotation: 0,
      largeArc: false,
      clockwise: true,
    );
    path.lineTo(size.width * 0.6666667, size.height * 0.7395122);
    path.cubicTo(
      size.width * 0.6666667,
      size.height * 0.6447561,
      size.width * 0.4188667,
      size.height * 0.5557317,
      0,
      size.height * 0.5000000,
    );
    path.cubicTo(
      size.width * 0.4188667,
      size.height * 0.4442683,
      size.width * 0.6666667,
      size.height * 0.3552439,
      size.width * 0.6666667,
      size.height * 0.2604878,
    );
    path.lineTo(size.width * 0.6666667, size.height * 0.09101220);
    path.close();

    Paint paint = Paint()..style = PaintingStyle.fill;
    paint.color = UIColors.primaryColor.withOpacity(1.0);
    canvas.drawPath(path, paint);
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) {
    return false;
  }
}
