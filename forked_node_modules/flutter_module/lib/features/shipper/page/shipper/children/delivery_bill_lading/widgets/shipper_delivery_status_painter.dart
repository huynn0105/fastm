import 'package:flutter/material.dart';

import '../../../../../../../common/colors.dart';

class ShipperDeliveryStatusPainter extends CustomPainter {
  const ShipperDeliveryStatusPainter({
    this.backgroundColor = UIColors.lightBlue,
  });

  final Color backgroundColor;

  @override
  void paint(Canvas canvas, Size size) {
    double w = size.width;
    double h = size.height;
    double space = h / 3;

    Paint paint = Paint()
      ..style = PaintingStyle.fill
      ..color = backgroundColor;

    Path path = Path()
      ..arcToPoint(
        Offset(space, 0),
        clockwise: true,
      )
      ..arcToPoint(
        Offset(0, h/2),
        clockwise: true,
      )
      ..arcToPoint(
        Offset(space, h),
        clockwise: true,
      )
      ..lineTo(w, h)
      ..lineTo(w, h / 2)
      ..lineTo(w, 0)
      ..lineTo(0, 0)
      ..close();

    canvas.drawPath(path, paint);
  }

  @override
  bool shouldRepaint(covariant CustomPainter oldDelegate) {
    return false;
  }
}
