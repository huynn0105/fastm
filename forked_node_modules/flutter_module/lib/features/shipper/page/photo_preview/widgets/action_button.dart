import 'package:flutter/material.dart';

import '../../../../../common/colors.dart';

class ActionButton extends StatelessWidget {
  const ActionButton.next({
    Key? key,
    this.enabled = false,
    required this.onTap,
  })  : icon = Icons.arrow_forward_rounded,
        super(key: key);

  const ActionButton.previous({
    Key? key,
    this.enabled = false,
    required this.onTap,
  })  : icon = Icons.arrow_back_rounded,
        super(key: key);

  final IconData icon;
  final bool enabled;
  final VoidCallback onTap;

  @override
  Widget build(BuildContext context) {
    return IconButton(
      onPressed: enabled ? onTap : null,
      icon: Icon(
        icon,
        size: 24,
        color: enabled ? UIColors.white : UIColors.gray.withOpacity(0.5),
      ),
    );
  }
}
