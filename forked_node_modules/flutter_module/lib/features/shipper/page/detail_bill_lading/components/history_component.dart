import 'package:flutter/material.dart';
import 'package:flutter_module/common/widgets/expandable_column.dart';
import '../../../../../models/shipper/order/oms_order_history.dart';
import '../items/history_item.dart';
import '../../../../../common/utils/datetime_util.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';

class HistoryComponent extends StatelessWidget {
  const HistoryComponent({Key? key, required this.data}) : super(key: key);
  final List<OmsOrderHistory> data;

  @override
  Widget build(BuildContext context) {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          "Lịch sử xử lý",
          style: UITextStyle.medium.copyWith(
            fontSize: 14,
            color: UIColors.grayText,
          ),
        ),
        const SizedBox(
          height: 14,
        ),
        ExpandableColumn(
          showExpand: data.length > 2,
          builder: (context, expanded) {
            int minSize = data.length > 2 ? 2 : data.length;
            return Column(
              children: List.generate(
                expanded ? data.length : minSize,
                (index) {
                  String? deliveryReason = data[index].masterOrderReasonByReasonDelivery?.messageOut;
                  String? deliveryStatus = data[index].masterDeliveryStatus?.name;
                  return HistoryItem(
                    date: DateTimeUtil.convertDate(
                      data[index].operationDate,
                      fromFormat: DateTimeFormat.yyyy_MM_ddTHH_mm_ssSS,
                      toFormat: DateTimeFormat.HH_mm_n_dd_MM_yyyy,
                    ),
                    label: deliveryReason ?? deliveryStatus ?? '',
                  );
                },
              ),
            );
          },
        ),
      ],
    );
  }
}
