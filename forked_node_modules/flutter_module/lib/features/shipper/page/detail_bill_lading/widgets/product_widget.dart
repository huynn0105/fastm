import 'dart:math';

import 'package:flutter/material.dart';
import '../../../../../common/colors.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/divider.dart';
import '../../../../../models/shipper/order/shipper_detail_order_model.dart';
import 'information_widget.dart';

class ProductWidget extends StatelessWidget {
  const ProductWidget({Key? key, this.data}) : super(key: key);
  final DeliveryModel? data;

  @override
  Widget build(BuildContext context) {
    final orders = data?.orderDetail?.omsOrderItems ?? [];
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        Text(
          "Danh sách hàng hóa:",
          style: UITextStyle.regular.copyWith(
            fontSize: 14,
            color: UIColors.grayText,
          ),
        ),
        Padding(
          padding: const EdgeInsets.only(left: 8, top: 8),
          child: ListView.separated(
            shrinkWrap: true,
            physics: const NeverScrollableScrollPhysics(),
            padding: EdgeInsets.zero,
            itemBuilder: (context, index) {
              return Row(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Text(
                    "${index + 1}.",
                    style: UITextStyle.regular.copyWith(
                      fontSize: 14,
                    ),
                  ),
                  const SizedBox(
                    width: 3,
                  ),
                  Flexible(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Text(
                          orders[index].omsProductVariation?.omsProduct?.name ?? '',
                          style: UITextStyle.regular.copyWith(
                            fontSize: 14,
                          ),
                          maxLines: 2,
                          overflow: TextOverflow.ellipsis,
                        ),
                        InformationWidget(
                          title: "Số lượng",
                          content: "${orders[index].quantity ?? 0}",
                          titleFlex: 1,
                          contentFlex: 3,
                        ),
                      ],
                    ),
                  ),
                ],
              );
            },
            separatorBuilder: (context, index) {
              return const DottedDivider(
                indent: 16,
                height: 8,
              );
            },
            itemCount: orders.length,
          ),
        ),
      ],
    );
  }
}
