import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter/cupertino.dart';
import 'package:flutter_module/common/utils/debounce_util.dart';

part 'shipper_bill_lading_tabbar_state.dart';

class ShipperBillLadingTabbarCubit extends Cubit<ShipperBillLadingTabbarState> {
  ShipperBillLadingTabbarCubit() : super(const ShipperBillLadingTabbarState());

  final TextEditingController searchController = TextEditingController();
  final DebounceUtil debounce = DebounceUtil(milliseconds: 200);

  updateNewCount(int count) {
    emit(state.copyWith(newCount: count));
  }

  updateDeliveryCount(int count) {
    emit(state.copyWith(deliveryCount: count));
  }

  updateCompletedCount(int count) {
    emit(state.copyWith(completedCount: count));
  }

  updateSearch(String search) {
    emit(state.copyWith(
      keySearch: search,
    ));
  }

  clearSearch() {
    searchController.clear();
    updateSearch("");
  }

  changeTabbar() {
    searchController.clear();
    emit(state.copyWith(
      keySearch: "",
      isChangeTabBar: true,
    ));
  }

  @override
  Future<void> close() {
    searchController.dispose();
    debounce.cancel();
    return super.close();
  }
}
