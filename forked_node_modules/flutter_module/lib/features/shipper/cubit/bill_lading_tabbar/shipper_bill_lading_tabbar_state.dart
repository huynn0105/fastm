part of 'shipper_bill_lading_tabbar_cubit.dart';

class ShipperBillLadingTabbarState extends Equatable {
  const ShipperBillLadingTabbarState({
    this.newCount = 0,
    this.deliveryCount = 0,
    this.completedCount = 0,
    this.keySearch = "",
    this.isChangeTabBar,
  });

  final int newCount;
  final int deliveryCount;
  final int completedCount;
  final String keySearch;
  final bool? isChangeTabBar;

  @override
  List<Object?> get props => [newCount, deliveryCount, completedCount, keySearch, isChangeTabBar];

  ShipperBillLadingTabbarState copyWith({
    int? newCount,
    int? deliveryCount,
    int? completedCount,
    String? keySearch,
    bool? isChangeTabBar,
  }) {
    return ShipperBillLadingTabbarState(
      newCount: newCount ?? this.newCount,
      deliveryCount: deliveryCount ?? this.deliveryCount,
      completedCount: completedCount ?? this.completedCount,
      keySearch: keySearch ?? this.keySearch,
      isChangeTabBar: isChangeTabBar,
    );
  }
}
