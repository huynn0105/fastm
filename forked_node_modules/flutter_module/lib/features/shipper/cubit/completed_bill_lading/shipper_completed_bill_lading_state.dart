part of 'shipper_completed_bill_lading_cubit.dart';

class ShipperCompletedBillLadingState extends Equatable {
  const ShipperCompletedBillLadingState({
    this.status = BlocStatus.initial,
    this.data = const [],
    this.count = 0,
  });

  final BlocStatus status;
  final List<ShipperBillLadingModel> data;
  final int count;

  @override
  List<Object> get props => [status, data, count];

  ShipperCompletedBillLadingState copyWith({
    BlocStatus? status,
    List<ShipperBillLadingModel>? data,
    int? count,
  }) {
    return ShipperCompletedBillLadingState(
      status: status ?? this.status,
      data: data ?? this.data,
      count: count ?? this.count,
    );
  }
}
