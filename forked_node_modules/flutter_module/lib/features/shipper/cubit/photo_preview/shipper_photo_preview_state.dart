part of 'shipper_photo_preview_cubit.dart';

class ShipperPhotoPreviewState extends Equatable {
  const ShipperPhotoPreviewState({
    this.photos = const [],
    this.currentIndex = 0,
    this.enabledNext = false,
    this.enabledPrevious = false,
  });

  final List<PhotoPreviewModel?> photos;
  final int currentIndex;
  final bool enabledNext;
  final bool enabledPrevious;

  @override
  List<Object> get props => [
        photos,
        currentIndex,
        enabledNext,
        enabledPrevious,
      ];

  ShipperPhotoPreviewState copyWith({
    List<PhotoPreviewModel?>? photos,
    int? currentIndex,
    bool? enabledNext,
    bool? enabledPrevious,
  }) {
    return ShipperPhotoPreviewState(
      photos: photos ?? this.photos,
      currentIndex: currentIndex ?? this.currentIndex,
      enabledNext: enabledNext ?? this.enabledNext,
      enabledPrevious: enabledPrevious ?? this.enabledPrevious,
    );
  }
}
