part of 'shipper_new_bill_lading_cubit.dart';

class ShipperNewBillLadingState extends Equatable {
  const ShipperNewBillLadingState({
    this.status = BlocStatus.initial,
    this.data = const [],
    this.count = 0,
  });

  final BlocStatus status;
  final List<ShipperBillLadingModel> data;
  final int count;

  @override
  List<Object> get props => [status, data, count];

  ShipperNewBillLadingState copyWith({
    BlocStatus? status,
    List<ShipperBillLadingModel>? data,
    int? count,
  }) {
    return ShipperNewBillLadingState(
      status: status ?? this.status,
      data: data ?? this.data,
      count: count ?? this.count,
    );
  }
}
