part of 'upload_image_cubit.dart';

class UploadImageState extends Equatable {
  const UploadImageState({
    this.status = BlocStatus.initial,
    this.checkPaymentStatus = BlocStatus.initial,
    this.countDown = 0,
    this.uploadInvoiceProcess = UploadInvoiceProcess.init,
    this.invoiceCheckBox = false,
    this.invoiceImages = const {},
    this.homeAddressImages = const {},
    this.portraitImages = const {},
    this.documentImages = const {},
    this.productImages = const {},
    this.setupImages = const {},
    this.otherImages = const {},
    this.errTitle,
    this.errMsg,
    Map<String, String>? errorFields,
  }) : errorFields = errorFields ?? const {}; //workaround to modify errorFields

  final BlocStatus status;
  final BlocStatus checkPaymentStatus;
  final int countDown;
  final UploadInvoiceProcess uploadInvoiceProcess;
  final bool invoiceCheckBox;
  final Map<String, Document?> invoiceImages;
  final Map<String, Document?> homeAddressImages;
  final Map<String, Document?> portraitImages;
  final Map<String, Document?> documentImages;
  final Map<String, Document?> productImages;
  final Map<String, Document?> setupImages;
  final Map<String, Document?> otherImages;
  final String? errTitle;
  final String? errMsg;
  final Map<String, String> errorFields;

  @override
  List<Object?> get props => [
        status,
        checkPaymentStatus,
        countDown,
        uploadInvoiceProcess,
        invoiceCheckBox,
        invoiceImages,
        homeAddressImages,
        portraitImages,
        documentImages,
        productImages,
        setupImages,
        otherImages,
        errTitle,
        errMsg,
        errorFields,
      ];

  UploadImageState copyWith({
    BlocStatus? status,
    BlocStatus? checkPaymentStatus,
    int? countDown,
    UploadInvoiceProcess? uploadInvoiceProcess,
    bool? invoiceCheckBox,
    Map<String, Document?>? invoiceImages,
    Map<String, Document?>? homeAddressImages,
    Map<String, Document?>? portraitImages,
    Map<String, Document?>? documentImages,
    Map<String, Document?>? productImages,
    Map<String, Document?>? setupImages,
    Map<String, Document?>? otherImages,
    String? errTitle,
    String? errMsg,
    Map<String, String>? errorFields,
  }) {
    return UploadImageState(
      status: status ?? this.status,
      checkPaymentStatus: checkPaymentStatus ?? BlocStatus.initial,
      countDown: countDown ?? this.countDown,
      uploadInvoiceProcess: uploadInvoiceProcess ?? this.uploadInvoiceProcess,
      invoiceCheckBox: invoiceCheckBox ?? this.invoiceCheckBox,
      invoiceImages: invoiceImages ?? this.invoiceImages,
      homeAddressImages: homeAddressImages ?? this.homeAddressImages,
      portraitImages: portraitImages ?? this.portraitImages,
      documentImages: documentImages ?? this.documentImages,
      productImages: productImages ?? this.productImages,
      setupImages: setupImages ?? this.setupImages,
      otherImages: otherImages ?? this.otherImages,
      errTitle: errTitle,
      errMsg: errMsg,
      errorFields: errorFields ?? this.errorFields,
    );
  }
}
