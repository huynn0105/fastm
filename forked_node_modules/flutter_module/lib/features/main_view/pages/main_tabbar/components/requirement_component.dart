class RequirementComponent extends StatelessWidget {
  const RequirementComponent({
    Key? key,
    required this.data,
    required this.onTap,
  }) : super(key: key);

  final List<CustomerRequirementModel> data;
  final Function(CustomerRequirementModel) onTap;

  @override
  Widget build(BuildContext context) {
    return Column(
      mainAxisSize: MainAxisSize.min,
      children: [
        Container(
          height: 50,
          width: double.infinity,
          color: UIColors.white,
          alignment: Alignment.center,
          child: Text(
            "Khách hàng của bạn cần gì?",
            style: UITextStyle.medium.copyWith(
              fontSize: 16,
              color: UIColors.grayText,
            ),
          ),
        ),
        SizedBox(
          height: 240,
          child: ListView.separated(
            shrinkWrap: true,
            padding: EdgeInsets.zero,
            physics: const ClampingScrollPhysics(),
            itemCount: data.length,
            itemBuilder: (context, index) {
              final item = data[index];
              return RequirementsItem(
                icon: item.requirementImage ?? '',
                title: item.requirementTitle ?? '',
                onTap: () => onTap(item),
              );
            },
            separatorBuilder: (_, __) => const Divider(
              height: 0,
              indent: 56,
              endIndent: 16,
              color: UIColors.white,
            ),
          ),
        ),
      ],
    );
  }
}
