part of 'app_cubit.dart';

class AppState extends Equatable {
  const AppState({
    this.status = BlocStatus.initial,
    this.webType,
    this.isAppRouterAttached = false,
  });

  final BlocStatus status;
  final WebType? webType;
  final bool isAppRouterAttached;

  @override
  List<Object?> get props => [
        status,
        webType,
        isAppRouterAttached,
      ];

  AppState copyWith({
    BlocStatus? status,
    WebType? webType,
    bool? isAppRouterAttached,
  }) {
    return AppState(
      status: status ?? this.status,
      webType: webType ?? this.webType,
      isAppRouterAttached: isAppRouterAttached ?? this.isAppRouterAttached,
    );
  }
}
