part of '../onboarding_page.dart';

class _LoginNow extends StatelessWidget {
  const _LoginNow({
    super.key,
  });

  @override
  Widget build(BuildContext context) {
    return Column(
      mainAxisSize: MainAxisSize.min,
      mainAxisAlignment: MainAxisAlignment.end,
      children: [
        PrimaryButton(
          onPressed: () {
            LocalDataHelper.instance.setIsOnboarding();
            context.router.replaceAll([const HomeRoute(), LoginRoute()]);
          },
          height: 60,
          width: 200,
          radius: 32,
          widget: Text(
            'Đăng nhập ngay',
            textAlign: TextAlign.center,
            style: UITextStyle.semiBold.copyWith(
              color: UIColors.white,
              fontSize: 18,
              height: 1,
            ),
          ),
        ),
        const SizedBox(height: 20),
        SplashButton(
          onTap: () {
            LocalDataHelper.instance.setIsOnboarding();
            context.router.replace(const HomeRoute());
          },
          child: Text(
            'Trải nghiệm trước',
            style: UITextStyle.semiBold.copyWith(
              fontSize: 18,
              height: 1,
              color: UIColors.blue,
            ),
          ),
        ),
      ],
    );
  }
}
