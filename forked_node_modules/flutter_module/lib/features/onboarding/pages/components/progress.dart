class _ProgressView extends StatelessWidget {
  const _ProgressView({
    super.key,
    required this.index,
  });
  final int index;
  @override
  Widget build(BuildContext context) {
    return SizedBox(
      height: 8,
      child: ListView.separated(
        scrollDirection: Axis.horizontal,
        shrinkWrap: true,
        itemBuilder: (_, index) {
          return GestureDetector(
            onTap: () {
              context.read<OnboardingCubit>().nextStep(step: index);
            },
            child: Container(
              width: 98,
              decoration: BoxDecoration(
                borderRadius: BorderRadius.circular(32),
                color: this.index >= index ? UIColors.blue : UIColors.gray,
              ),
            ),
          );
        },
        separatorBuilder: (_, __) => const SizedBox(width: 10),
        itemCount: 3,
      ),
    );
  }
}
