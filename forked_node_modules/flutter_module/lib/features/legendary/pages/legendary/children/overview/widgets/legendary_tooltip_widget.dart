import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/color_util.dart';
import 'package:flutter_module/features/legendary/pages/legendary/children/overview/widgets/legendary_rich_text.dart';
import 'package:flutter_module/models/mfast/legendary/legendary_experience_chart_model.dart';

class LegendaryTooltipWidget extends StatelessWidget {
  const LegendaryTooltipWidget({
    super.key,
    required this.data,
  });

  final List<List<HierNoteModel>> data;

  @override
  Widget build(BuildContext context) {
    final values = convertToTextSpan(data);
    return Container(
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(
        color: UIColors.white,
        borderRadius: BorderRadius.circular(12),
      ),
      child: Column(
        mainAxisSize: MainAxisSize.min,
        children: values,
      ),
    );
  }

  List<Widget> convertToTextSpan(List<List<HierNoteModel>> data) {
    return data.map(
      (e) {
        return LegendaryRichText(
          data: e,
          prefix: '-',
        );
      },
    ).toList();
  }
}
