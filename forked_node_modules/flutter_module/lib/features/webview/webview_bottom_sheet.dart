import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/features/webview/webview_page.dart';

@RoutePage(name: 'WebViewBottomSheetRoute')
class WebViewBottomSheet extends StatelessWidget{
  const WebViewBottomSheet({
    Key? key,
    @QueryParam("url") this.url,
    @QueryParam("title") this.title = "MTrade",
    @QueryParam("showTitle") this.showTitle = true,
  })  : assert(url != null),
        assert(title != null),
        super(key: key);

  final String? title;
  final String? url;
  final bool? showTitle;

  @override
  Widget build(BuildContext context) {
    return WebViewPage(
      url: url,
      title: title,
      showTitle: showTitle,
    );
  }
}
