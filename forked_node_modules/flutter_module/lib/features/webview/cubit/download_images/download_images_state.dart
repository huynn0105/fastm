part of 'download_images_cubit.dart';

class DownloadImagesState extends Equatable {
  const DownloadImagesState({
    this.status = BlocStatus.initial,
    this.paths = const [],
    this.shareStatus = BlocStatus.initial,
  });

  final BlocStatus status;
  final List<String> paths;
  final BlocStatus shareStatus;

  @override
  List<Object> get props => [
        status,
        paths,
        shareStatus,
      ];

  DownloadImagesState copyWith({
    BlocStatus? status,
    List<String>? paths,
    BlocStatus? shareStatus,
  }) {
    return DownloadImagesState(
      status: status ?? this.status,
      paths: paths ?? this.paths,
      shareStatus: shareStatus ?? this.shareStatus,
    );
  }
}
