part of 'webview_cubit.dart';

class WebViewState extends Equatable {
  const WebViewState({
    this.showLoading = false,
    this.iframeKey = "",
    this.errorType,
  });

  final bool showLoading;
  final String iframeKey;
  final WebViewErrorType? errorType;

  @override
  List<Object?> get props => [
        showLoading,
        iframeKey,
        errorType,
      ];

  WebViewState copyWith({
    bool? showLoading,
    String? iframeKey,
    WebViewErrorType? errorType,
    bool? clearErrorType,
  }) {
    return WebViewState(
      showLoading: showLoading ?? this.showLoading,
      iframeKey: iframeKey ?? this.iframeKey,
      errorType: (clearErrorType == true) ? null : errorType ?? this.errorType,
    );
  }
}
