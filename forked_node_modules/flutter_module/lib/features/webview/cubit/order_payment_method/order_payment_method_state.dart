part of 'order_payment_method_cubit.dart';

class MTradeOrderPaymentMethodState extends Equatable {
  const MTradeOrderPaymentMethodState({
    this.status = BlocStatus.initial,
    this.url = '',
  });

  final BlocStatus status;
  final String url;

  @override
  List<Object?> get props => [status, url];

  MTradeOrderPaymentMethodState copyWith({
    BlocStatus? status,
    String? url,
  }) {
    return MTradeOrderPaymentMethodState(
      status: status ?? this.status,
      url: url ?? this.url,
    );
  }
}
