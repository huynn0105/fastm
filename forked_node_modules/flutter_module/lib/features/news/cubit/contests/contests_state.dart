part of 'contests_cubit.dart';

class ContestsState extends Equatable {
  const ContestsState({
    this.status = BlocStatus.initial,
    this.deleteStatus = BlocStatus.initial,
    this.contests,
  });

  final BlocStatus status;
  final BlocStatus deleteStatus;
  final ContestsModel? contests;

  @override
  List<Object?> get props => [
        status,
        deleteStatus,
        contests,
      ];

  ContestsState copyWith({
    BlocStatus? status,
    BlocStatus? deleteStatus,
    ContestsModel? contests,
  }) {
    return ContestsState(
      status: status ?? this.status,
      deleteStatus: deleteStatus ?? BlocStatus.initial,
      contests: contests ?? this.contests,
    );
  }
}
