import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/features/news/repository/news_repository.dart';
import 'package:flutter_module/models/news/knowledges_model.dart';

part 'knowledges_state.dart';

class KnowledgesCubit extends Cubit<KnowledgesState> {
  KnowledgesCubit() : super(const KnowledgesState());

  final NewsRepository _repository = NewsRepository();

  fetchData({bool showLoading = true, bool loadmore = false}) async {
    if (showLoading) {
      emit(state.copyWith(
        status: BlocStatus.loading,
      ));
    }

    final result = await _repository.getListKnowledges();

    if (result.status) {
      emit(state.copyWith(
        status: BlocStatus.success,
        knowledges: result.data,
      ));
    } else {
      emit(state.copyWith(
        status: BlocStatus.failure,
      ));
    }
  }

  refreshData() async {
    await fetchData(showLoading: false);
  }
}
