part of 'knowledges_cubit.dart';

class KnowledgesState extends Equatable {
  const KnowledgesState({
    this.status = BlocStatus.initial,
    this.deleteStatus = BlocStatus.initial,
    this.knowledges = const [],
  });

  final BlocStatus status;
  final BlocStatus deleteStatus;
  final List<KnowledgesModel> knowledges;

  @override
  List<Object> get props => [
        status,
        deleteStatus,
        knowledges,
      ];

  KnowledgesState copyWith({
    BlocStatus? status,
    BlocStatus? deleteStatus,
    List<KnowledgesModel>? knowledges,
  }) {
    return KnowledgesState(
      status: status ?? this.status,
      deleteStatus: deleteStatus ?? BlocStatus.initial,
      knowledges: knowledges ?? this.knowledges,
    );
  }
}
