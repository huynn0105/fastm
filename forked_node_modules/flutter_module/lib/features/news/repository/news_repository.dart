import 'package:flutter_module/models/base_model.dart';
import 'package:flutter_module/models/news/contests_model.dart';
import 'package:flutter_module/models/news/knowledges_model.dart';
import 'package:flutter_module/models/news/news_model.dart';
import 'package:flutter_module/services/api/api_provider.dart';
import 'package:flutter_module/services/api/news/payload/get_news_payload.dart';

class NewsRepository {
  Future<BaseModel<List<KnowledgesModel>>> getListKnowledges() {
    return ApiProvider.instance.news.getListKnowledges();
  }

  Future<BaseModel<List<NewsModel>>> getListNews(GetListNewsPayload payload) {
    return ApiProvider.instance.news.getListNews(payload);
  }

  Future<BaseModel<ContestsModel>> getListContests() {
    return ApiProvider.instance.news.getListContests();
  }
}
