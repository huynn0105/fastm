import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:scroll_to_index/scroll_to_index.dart';

class ListSelector extends StatelessWidget {
  const ListSelector({
    super.key,
    required this.data,
    this.onTabItem,
    this.indexSelected,
    this.autoScrollController,
  });

  final List<dynamic>? data;
  final Function(dynamic, int)? onTabItem;
  final int? indexSelected;
  final AutoScrollController? autoScrollController;

  @override
  Widget build(BuildContext context) {
    return SizedBox(
      height: 32,
      width: double.infinity,
      child: ListView.builder(
        controller: autoScrollController,
        physics: AppConstants.physics,
        shrinkWrap: true,
        scrollDirection: Axis.horizontal,
        itemCount: data?.length ?? 0,
        itemBuilder: (context, index) {
          final item = data?[index];
          final isSelected = indexSelected != null && indexSelected == index;

          final child = Container(
            height: 32,
            margin: index == 0 ? EdgeInsets.zero : const EdgeInsets.only(left: 8),
            decoration: BoxDecoration(
              borderRadius: BorderRadius.circular(20),
              color: isSelected ? UIColors.primaryColor : UIColors.white,
            ),
            child: SplashButton(
              onTap: () {
                if (onTabItem != null) {
                  onTabItem!(item, index);
                }
              },
              child: Padding(
                padding: const EdgeInsets.symmetric(
                  horizontal: 12,
                ),
                child: Column(
                  mainAxisAlignment: MainAxisAlignment.center,
                  crossAxisAlignment: CrossAxisAlignment.center,
                  children: [
                    Text(
                      item?.title ?? '',
                      style: UITextStyle.medium.copyWith(
                        fontSize: 13,
                        color: isSelected ? UIColors.white : UIColors.boldText,
                      ),
                    ),
                  ],
                ),
              ),
            ),
          );

          if (autoScrollController == null) {
            return child;
          }

          return AutoScrollTag(
            index: index,
            key: ValueKey(index),
            controller: autoScrollController!,
            child: child,
          );
        },
      ),
    );
  }
}
