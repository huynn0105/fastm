import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/common/widgets/tabbar/tabbar.dart';

import '../../../../../routes/routes.gr.dart';

class TabbarComponent extends StatelessWidget {
  const TabbarComponent({
    super.key,
    this.focusedTabIndex,
    this.newsTabIndex,
  });

  final int? focusedTabIndex;
  final int? newsTabIndex;

  @override
  Widget build(BuildContext context) {
    return AutoTabsRouter.tabBar(
      physics: AppConstants.physics,
      routes: [
        const KnowledgesTabRoute(),
        NewsTabRoute(
          newsTabIndex: newsTabIndex,
        ),
        const ContestTabRoute(),
      ],
      builder: (context, child, tabController) {
        return Column(
          mainAxisSize: MainAxisSize.min,
          children: [
            UnderlineIndicatorTabbar(
              controller: tabController,
              titles: const [
                "Kiến thức",
                "Tin tức",
                "Thi đua",
              ],
              backgroundColor: UIColors.background,
              initIndex: focusedTabIndex,
              onTap: (index) {
                AutoTabsRouter.of(context).setActiveIndex(index);
              },
            ),
            const SizedBox(
              height: 16,
            ),
            Expanded(
              child: child,
            ),
          ],
        );
      },
    );
  }
}
