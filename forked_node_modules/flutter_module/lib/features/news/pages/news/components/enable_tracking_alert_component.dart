import 'package:app_settings/app_settings.dart';
import 'package:ekyc/common/styles.dart';
import 'package:flutter/foundation.dart';
import 'package:flutter/gestures.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:permission_handler/permission_handler.dart';

class EnableTrackingAlertComponent extends StatefulWidget {
  const EnableTrackingAlertComponent({super.key});

  @override
  State<EnableTrackingAlertComponent> createState() => _EnableTrackingAlertComponentState();
}

class _EnableTrackingAlertComponentState extends State<EnableTrackingAlertComponent> {
  bool isShowAlert = false;
  @override
  void initState() {
    super.initState();
    Permission.appTrackingTransparency.status.then((status) {
      setState(() {
        isShowAlert = status.isPermanentlyDenied;
      });
    });
  }

  @override
  Widget build(BuildContext context) {
    if (!isShowAlert) {
      return const SizedBox();
    }
    return Container(
      width: double.infinity,
      color: UIColors.lightOrange,
      margin: const EdgeInsets.only(bottom: 8),
      padding: const EdgeInsets.symmetric(
        horizontal: 16,
        vertical: 8,
      ),
      child: Row(
        children: [
          Expanded(
            child: RichText(
              text: TextSpan(
                style: UITextStyle.regular.copyWith(fontSize: 13),
                children: [
                  const TextSpan(
                    text: "Cho phép theo dõi ứng dụng đang tắt, bạn sẽ bỏ lỡ tin tức mới nhất và ưu đãi từ MFast. ",
                  ),
                  TextSpan(
                    recognizer: TapGestureRecognizer()
                      ..onTap = () {
                        AppSettings.openAppSettings();
                      },
                    text: "Mở lại",
                    style: UITextStyle.semiBold.copyWith(
                      fontSize: 13,
                      color: UIColors.primaryColor,
                    ),
                  ),
                ],
              ),
            ),
          ),
          const SizedBox(
            width: 16,
          ),
          SplashButton(
            onTap: () {
              setState(() {
                isShowAlert = false;
              });
            },
            child: const AppImage.asset(
              asset: 'ic_close',
              width: 24,
              height: 24,
            ),
          ),
        ],
      ),
    );
  }
}
