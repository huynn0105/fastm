import 'package:flutter/material.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/features/academy/page/academy/items/event_item.dart';
import 'package:flutter_module/models/academy/event/academy_event_model.dart';

class ListEvent extends StatelessWidget {
  const ListEvent({
    super.key,
    this.data = const [],
  });

  final List<AcademyEventModel>? data;

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(horizontal: 16),
      child: ListView.builder(
        shrinkWrap: true,
        physics: const NeverScrollableScrollPhysics(),
        itemCount: data?.length ?? 0,
        itemBuilder: (context, index) {
          final item = data![index];
          return EventItem(item: item, index: index);
        },
      ),
    );
  }
}
