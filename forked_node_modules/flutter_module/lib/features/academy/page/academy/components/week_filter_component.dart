import 'package:flutter/foundation.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/models/academy/event/academy_filter_event_model.dart';

class WeekFilterComponent extends StatelessWidget {
  const WeekFilterComponent({
    super.key,
    required this.data,
    this.onSelected,
    this.selectedData = const [],
  });

  final List<AcademyFilterEventModel> data;
  final List<AcademyFilterEventModel> selectedData;
  final Function(AcademyFilterEventModel item, int index)? onSelected;

  @override
  Widget build(BuildContext context) {
    return SizedBox(
      height: 56,
      child: ListView.separated(
        scrollDirection: Axis.horizontal,
        separatorBuilder: (context, index) {
          return const SizedBox(
            width: 8,
          );
        },
        itemBuilder: (BuildContext context, int index) {
          final item = data[index];
          final isSelected = selectedData.indexWhere((e) => listEquals(e.value, item.value)) >= 0;

          ///
          return Container(
            padding: const EdgeInsets.symmetric(horizontal: 8),
            decoration: BoxDecoration(
              borderRadius: BorderRadius.circular(8),
              color: isSelected ? UIColors.primaryColor : UIColors.white,
            ),
            child: SplashButton(
              onTap: () {
                onSelected!(item, index);
              },
              child: Column(
                crossAxisAlignment: CrossAxisAlignment.center,
                mainAxisAlignment: MainAxisAlignment.center,
                children: [
                  Text(
                    item.text ?? '',
                    style: isSelected
                        ? UITextStyle.semiBold.copyWith(
                            color: UIColors.white,
                          )
                        : UITextStyle.regular.copyWith(
                            color: UIColors.grayText,
                          ),
                  ),
                  const SizedBox(
                    height: 2,
                  ),
                  Row(
                    crossAxisAlignment: CrossAxisAlignment.center,
                    mainAxisAlignment: MainAxisAlignment.center,
                    children: [
                      if ((item.count ?? 0) > 0)
                        Container(
                          width: 8,
                          height: 8,
                          decoration: BoxDecoration(
                            color: isSelected ? UIColors.white : UIColors.darkBlue,
                            borderRadius: BorderRadius.circular(8),
                          ),
                        ),
                      const SizedBox(
                        width: 6,
                      ),
                      Text(
                        (item.count ?? 0) > 0 ? "${item.count.toString()} lá»›p" : '',
                        style: UITextStyle.semiBold.copyWith(
                          fontSize: 13,
                          color: isSelected ? UIColors.white : UIColors.darkBlue,
                        ),
                      ),
                    ],
                  )
                ],
              ),
            ),
          );
        },
        itemCount: data.length,
      ),
    );
  }
}
