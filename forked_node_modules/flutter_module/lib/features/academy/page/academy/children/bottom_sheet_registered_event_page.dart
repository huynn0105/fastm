import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/common/enum/academy/registered_event_status.dart';
import 'package:flutter_module/common/widgets/tabbar/tabbar.dart';
import 'package:flutter_module/features/academy/cubit/academy_count_registered_event/academy_count_registered_event_cubit.dart';
import 'package:flutter_module/routes/routes.gr.dart';

@RoutePage()
class BottomSheetRegisteredEventPage extends StatefulWidget implements AutoRouteWrapper {
  const BottomSheetRegisteredEventPage({
    super.key,
    this.title = 'Danh sách sự kiện đã đăng ký',
  });

  final String title;

  @override
  State<BottomSheetRegisteredEventPage> createState() => _BottomSheetRegisteredEventPageState();

  @override
  Widget wrappedRoute(BuildContext context) {
    return BlocProvider(
      create: (context) => AcademyCountRegisteredEventCubit(),
      child: this,
    );
  }
}

class _BottomSheetRegisteredEventPageState extends State<BottomSheetRegisteredEventPage> {
  @override
  void initState() {
    super.initState();
    context.read<AcademyCountRegisteredEventCubit>().fetchData();
  }

  @override
  Widget build(BuildContext context) {
    return AutoTabsRouter.tabBar(
      physics: AppConstants.physics,
      routes: [
        AcademyRegisteredEventRoute(
          status: RegisteredEventStatus.startSoon.name,
        ),
        AcademyRegisteredEventRoute(
          status: RegisteredEventStatus.end.name,
        ),
      ],
      builder: (context, child, tabController) {
        return Column(
          children: [
            BlocBuilder<AcademyCountRegisteredEventCubit, AcademyCountRegisteredEventState>(
              builder: (context, state) {
                return UnderlineIndicatorTabbar(
                  controller: tabController,
                  backgroundColor: UIColors.white,
                  titles: [
                    "Sắp diễn ra (${state.listCountRegisteredEvent?[0].total ?? 0})",
                    "Đã tham gia (${state.listCountRegisteredEvent?[1].total ?? 0})",
                  ],
                  onTap: (index) {
                    AutoTabsRouter.of(context).setActiveIndex(index);
                  },
                );
              },
            ),
            Expanded(
              child: child,
            ),
          ],
        );
      },
    );
  }
}
