import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/common/widgets/radio.dart';

class QuizAnswerItem extends StatelessWidget {
  final String? answerValue;
  final Function()? onTapAnswer;
  final bool? isChecked;
  final bool? isWrong;
  final bool? isRight;

  const QuizAnswerItem({
    super.key,
    required this.answerValue,
    this.onTapAnswer,
    this.isChecked,
    this.isWrong,
    this.isRight,
  });

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: () {
        onTapAnswer!();
      },
      child: Container(
        width: double.infinity,
        padding: const EdgeInsets.all(12),
        margin: EdgeInsets.only(top: isWrong == true || isRight == true ? 8 : 12),
        decoration: BoxDecoration(
          border: Border.all(
            width: 1,
            color: isWrong == true
                ? UIColors.red
                : isRight == true
                    ? UIColors.accentGreen
                    : UIColors.gray,
          ),
          color: isWrong == true
              ? UIColors.lightRed
              : isRight == true
                  ? UIColors.lightGreen
                  : null,
          borderRadius: BorderRadius.circular(8),
        ),
        child: Row(
          children: [
            AppRadio(
              icon: isWrong == true || isRight == true
                  ? Container(
                      margin: const EdgeInsets.only(right: 4),
                      child: AppImage.asset(
                        asset: isWrong == true ? 'ic_remove' : 'ic_status_success',
                        width: 24,
                        height: 24,
                      ),
                    )
                  : null,
              title: answerValue ?? '',
              value: isChecked ?? false,
              fontSize: 16,
            ),
          ],
        ),
      ),
    );
  }
}
