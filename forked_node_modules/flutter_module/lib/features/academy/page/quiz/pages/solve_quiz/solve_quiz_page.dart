class SolveQuizPage extends StatefulWidget {
  final Function()? onNext;
  final AcademyEventDetailModel? event;
  final bool readOnly;

  const SolveQuizPage({
    super.key,
    this.onNext,
    this.event,
    this.readOnly = false,
  });

  @override
  State<SolveQuizPage> createState() => _SolveQuizPageState();
}

class _SolveQuizPageState extends State<SolveQuizPage> {
  late int indexQuiz;

  List<String> listIdAnswer = [];

  @override
  void initState() {
    super.initState();
    indexQuiz = 0;
  }

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<AcademyQuizCubit, AcademyQuizState>(
      builder: (context, state) {
        final isLastQuiz = indexQuiz == state.listQuiz.length - 1;
        return SingleChildScrollView(
          physics: AppConstants.physics,
          padding: const EdgeInsets.symmetric(horizontal: 20, vertical: 16),
          child: Column(
            children: [
              Text(
                'Bảng câu hỏi tổng hợp kiến thức sự kiện',
                style: UITextStyle.medium.copyWith(fontSize: 16),
              ),
              Text(
                widget.event?.eventName ?? '',
                style: UITextStyle.medium.copyWith(fontSize: 16, color: UIColors.darkBlue),
              ),
              const SizedBox(
                height: 12,
              ),
              ProcessBarComponent(
                width: AppSize.instance.width - 40,
                maxQuiz: state.listQuiz.length,
                currentQuiz: indexQuiz + 1,
              ),
              const SizedBox(
                height: 16,
              ),
              QuizItem(
                quiz: state.listQuiz.valueAt(indexQuiz),
                listIdAnswer: listIdAnswer,
                listSubmitQuizAnswer: state.listSubmitQuizAnswer,
                userAnswers: state.userAnswers,
                readOnly: widget.readOnly,
                isReview: state.questionType?.isReview == true,
              ),
              const SizedBox(
                height: 26,
              ),
              Opacity(
                opacity: isLastQuiz && widget.readOnly ? 0 : 1,
                child: Align(
                  alignment: Alignment.center,
                  child: PrimaryButton(
                    title: "Tiếp tục",
                    onPressed: () {
                      if (isLastQuiz) {
                        widget.onNext!();
                        return;
                      }
                      setState(() {
                        indexQuiz = indexQuiz + 1;
                      });
                    },
                  ),
                ),
              ),
              const SizedBox(
                height: 6,
              ),
              if (indexQuiz > 0)
                Align(
                  alignment: Alignment.center,
                  child: PrimaryButton(
                    buttonColor: UIColors.background,
                    widget: Text(
                      "Quay lại",
                      style: UITextStyle.regular.copyWith(
                        color: UIColors.lightBlack,
                      ),
                    ),
                    onPressed: () {
                      setState(() {
                        indexQuiz = indexQuiz - 1;
                      });
                    },
                  ),
                ),
            ],
          ),
        );
      },
    );
  }
}
