import 'package:auto_route/auto_route.dart';
import 'package:collection/collection.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/global_function.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/features/academy/cubit/academy_quiz/academy_quiz_cubit.dart';
import 'package:flutter_module/routes/routes.gr.dart';

final listSupport = [
  {"title": 'Fanpage Zalo MFast', "url": ''},
  {"title": 'Nhóm hỗ trợ tất cả dự án tài chính', "url": ''},
];

class ContactSupportComponent extends StatelessWidget {
  const ContactSupportComponent({super.key});

  @override
  Widget build(BuildContext context) {
    return BlocBuilder<AcademyQuizCubit, AcademyQuizState>(
      builder: (context, state) {
        return Container(
          padding: const EdgeInsets.symmetric(horizontal: 12),
          decoration: BoxDecoration(
            color: UIColors.primaryColor,
            borderRadius: BorderRadius.circular(8),
          ),
          child: Column(
            children: state.listSupport
                .mapIndexed(
                  (index, item) => Opacity(
                    opacity: 0.9,
                    child: SplashButton(
                      onTap: () {
                        if (TextUtils.isDeeplink(item.link)) {
                          GlobalFunction.launchScheme(item.link ?? '');
                        } else {
                          context.pushRoute(WebViewRoute(url: item.link ?? ''));
                        }
                      },
                      child: Column(
                        children: [
                          if (index > 0)
                            const Divider(
                              color: UIColors.white,
                              height: 0,
                            ),
                          const SizedBox(
                            height: 10,
                          ),
                          Row(
                            children: [
                              Expanded(
                                child: Text(
                                  item.name ?? '',
                                  style: UITextStyle.semiBold.copyWith(
                                    color: UIColors.white,
                                  ),
                                ),
                              ),
                              const AppImage.asset(
                                asset: 'ic_arrow_right',
                                color: UIColors.white,
                                width: 20,
                                height: 20,
                              )
                            ],
                          ),
                          const SizedBox(
                            height: 10,
                          ),
                        ],
                      ),
                    ),
                  ),
                )
                .toList(),
          ),
        );
      },
    );
  }
}
