import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import '../../../../../models/academy/event_detail/academy_event_detail_model.dart';

import '../items/info_item.dart';
import 'speaker_component.dart';

class EventContentComponent extends StatelessWidget {
  const EventContentComponent({
    Key? key,
    required this.data,
  }) : super(key: key);

  final AcademyEventDetailModel? data;

  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.symmetric(horizontal: 16),
      child: Column(
        children: [
          if (TextUtils.isNotEmpty(data?.eventDesc)) ...[
            InfoItem(
              asset: "ic_academy_note",
              title: "Nội dung sự kiện",
              content: data?.eventDesc ?? "",
              color: UIColors.secondaryColor,
            ),
            const SizedBox(
              height: 16,
            ),
          ],
          if (data?.speaker != null) ...[
            SpeakerComponent(
              data: data?.speaker,
            ),
          ],
          if (data?.speakers != null) ...[
            ...(data?.speakers ?? [])
                .map(
                  (e) => SpeakerComponent(
                    data: e,
                  ),
                )
                .toList()
          ],
          if (TextUtils.isNotEmpty(data?.eventReason)) ...[
            const SizedBox(
              height: 24,
            ),
            InfoItem(
              asset: "ic_academy_like",
              title: "Tại sao bạn nên tham gia?",
              content: data?.eventReason ?? "",
              color: UIColors.green,
            ),
          ],
        ],
      ),
    );
  }
}
