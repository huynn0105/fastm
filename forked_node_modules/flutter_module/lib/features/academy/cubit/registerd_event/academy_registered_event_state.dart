part of 'academy_registered_event_cubit.dart';

class AcademyRegisteredEventState extends Equatable {
  const AcademyRegisteredEventState({
    this.status = BlocStatus.initial,
    this.registeredEvents = const [],
  });

  final BlocStatus status;
  final List<AcademyRegisteredEventModel> registeredEvents;

  @override
  List<Object> get props => [
        status,
        registeredEvents,
      ];

  AcademyRegisteredEventState copyWith({
    BlocStatus? status,
    List<AcademyRegisteredEventModel>? registeredEvents,
  }) {
    return AcademyRegisteredEventState(
      status: status ?? this.status,
      registeredEvents: registeredEvents ?? this.registeredEvents,
    );
  }
}
