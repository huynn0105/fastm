part of 'academy_register_event_otp_cubit.dart';

class AcademyRegisterEventOTPState extends Equatable {
  final BlocStatus status;
  final BlocStatus completeStepStatus;
  final BlocStatus otpStatus;
  final Map<String, String> errorFields;
  final int countDown;
  final String? errMsg;

  AcademyRegisterEventOTPState({
    this.status = BlocStatus.initial,
    this.completeStepStatus = BlocStatus.initial,
    this.otpStatus = BlocStatus.initial,
    Map<String, String>? errorFields,
    this.countDown = AppConstants.otpCountdown,
    this.errMsg,
  }) : errorFields = errorFields ?? {}; //workaround to modify errorFields

  @override
  List<Object?> get props => [
        status,
        completeStepStatus,
        otpStatus,
        countDown,
        errMsg,
        errorFields,
      ];

  AcademyRegisterEventOTPState copyWith({
    BlocStatus? status,
    BlocStatus? completeStepStatus,
    BlocStatus? otpStatus,
    Map<String, String>? errorFields,
    int? countDown,
    String? errMsg,
    AcademyEventCheckOutModel? checkOutEvent,
  }) {
    return AcademyRegisterEventOTPState(
      status: status ?? this.status,
      completeStepStatus: completeStepStatus ?? BlocStatus.initial,
      otpStatus: otpStatus ?? this.otpStatus,
      errorFields: errorFields ?? this.errorFields,
      countDown: countDown ?? this.countDown,
      errMsg: errMsg,
    );
  }
}
