part of 'academy_event_cubit.dart';

class AcademyEventState extends Equatable {
  const AcademyEventState({
    this.status = BlocStatus.initial,
    this.events = const [],
  });

  final BlocStatus status;
  final List<AcademyEventModel> events;

  @override
  List<Object> get props => [status, events];

  AcademyEventState copyWith({
    BlocStatus? status,
    List<AcademyEventModel>? events,
  }) {
    return AcademyEventState(
      status: status ?? this.status,
      events: events ?? this.events,
    );
  }
}
