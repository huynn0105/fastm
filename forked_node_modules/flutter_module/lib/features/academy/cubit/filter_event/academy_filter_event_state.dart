part of 'academy_filter_event_cubit.dart';

class AcademyFilterEventState extends Equatable {
  const AcademyFilterEventState({
    this.status = BlocStatus.initial,
    this.filterEvents = const [],
    this.indexSelected = 0,
  });

  final BlocStatus status;
  final List<AcademyFilterEventModel> filterEvents;
  final int indexSelected;

  @override
  List<Object> get props => [status, filterEvents, indexSelected];

  AcademyFilterEventState copyWith({
    BlocStatus? status,
    List<AcademyFilterEventModel>? filterEvents,
    int? indexSelected,
  }) {
    return AcademyFilterEventState(
      status: status ?? this.status,
      filterEvents: filterEvents ?? this.filterEvents,
      indexSelected: indexSelected ?? this.indexSelected,
    );
  }
}
