part of 'academy_event_detail_cubit.dart';

class AcademyEventDetailState extends Equatable {
  const AcademyEventDetailState({
    this.status = BlocStatus.initial,
    this.event,
    this.hasError,
    this.errorMessage,
  });

  final BlocStatus status;
  final AcademyEventDetailModel? event;
  final bool? hasError;
  final String? errorMessage;

  @override
  List<Object?> get props => [
        status,
        event,
        hasError,
        errorMessage,
      ];

  AcademyEventDetailState copyWith({
    BlocStatus? status,
    AcademyEventDetailModel? event,
    EventDetailStatus? eventStatus,
    bool? hasError,
    String? errorMessage,
  }) {
    return AcademyEventDetailState(
      status: status ?? this.status,
      event: event ?? this.event,
      errorMessage: errorMessage,
      hasError: hasError,
    );
  }
}
