import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/enum/statistic_working/statistic_working_level.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/statistic_working/statistic_checking_model.dart';

class StatisticCheckingValueWidget extends StatelessWidget {
  const StatisticCheckingValueWidget({
    super.key,
    required this.level,
    this.data,
    this.isHeader = false,
  });

  final String level;
  final CheckingDetailModel? data;
  final bool isHeader;

  @override
  Widget build(BuildContext context) {
    final total = data?.checkingTotal ?? 0;
    final avg = FormatUtil.doubleFormat(data?.avg ?? 0);
    final unknown = data?.notCheckingAgent ?? 0;
    final checkingAgent = data?.checkingAgent ?? 0;

    if (StatisticWorkingLevel.getLevel(level)?.isAgent == true && !isHeader) {
      return Row(
        children: [
          const AppImage.asset(
            asset: 'ic_checking_success',
            width: 24,
            height: 24,
          ),
          const SizedBox(
            width: 4,
          ),
          Text(
            '$total',
            style: UITextStyle.regular.copyWith(
              fontSize: 14,
              color: UIColors.defaultText,
            ),
          ),
          const SizedBox(
            width: 2,
          ),
          Expanded(
            child: Text(
              'lượt điểm danh trong ngày',
              style: UITextStyle.regular.copyWith(
                fontSize: 14,
                color: UIColors.grayText,
              ),
            ),
          ),
        ],
      );
    }
    return Row(
      children: [
        Expanded(
          child: Row(
            children: [
              const AppImage.asset(
                asset: 'ic_checking_success',
                width: 24,
                height: 24,
              ),
              const SizedBox(
                width: 4,
              ),
              Expanded(
                child: Text(
                  '$total',
                  style: UITextStyle.regular.copyWith(
                    fontSize: 14,
                    color: UIColors.defaultText,
                  ),
                ),
              ),
            ],
          ),
        ),
        Expanded(
          child: Row(
            children: [
              const AppImage.asset(
                asset: 'ic_checking_avg',
                width: 24,
                height: 24,
              ),
              const SizedBox(
                width: 4,
              ),
              Expanded(
                child: Text(
                  '$avg',
                  style: UITextStyle.regular.copyWith(
                    fontSize: 14,
                    color: UIColors.defaultText,
                  ),
                ),
              ),
            ],
          ),
        ),
        Expanded(
          child: Row(
            children: [
              const AppImage.asset(
                asset: 'ic_report_agentReportSuccess',
                width: 24,
                height: 24,
              ),
              const SizedBox(
                width: 4,
              ),
              Expanded(
                child: Text(
                  '$checkingAgent',
                  style: UITextStyle.regular.copyWith(
                    fontSize: 14,
                    color: UIColors.defaultText,
                  ),
                ),
              ),
            ],
          ),
        ),
        Expanded(
          child: Row(
            children: [
              const AppImage.asset(
                asset: 'ic_checking_unknown',
                width: 24,
                height: 24,
              ),
              const SizedBox(
                width: 4,
              ),
              Expanded(
                child: Text(
                  '$unknown',
                  style: UITextStyle.regular.copyWith(
                    fontSize: 14,
                    color: UIColors.defaultText,
                  ),
                ),
              ),
            ],
          ),
        ),
      ],
    );
  }
}
