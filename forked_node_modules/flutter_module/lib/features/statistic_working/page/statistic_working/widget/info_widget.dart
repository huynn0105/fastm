class ContactInfoWidget extends StatelessWidget {
  const ContactInfoWidget({
    super.key,
    required this.phoneNumber,
    required this.userID,
    required this.lineLabel,
    this.phoneColor = UIColors.white,
    this.chatColor = UIColors.white,
    this.lineLabelColor = UIColors.white,
    this.isHidePhoneNumber = false,
  });

  final String phoneNumber;
  final String userID;
  final String lineLabel;
  final Color phoneColor;
  final Color chatColor;
  final Color lineLabelColor;
  final bool isHidePhoneNumber;

  @override
  Widget build(BuildContext context) {
    return Row(
      mainAxisSize: MainAxisSize.min,
      children: [
        InfoWidget(
          asset: 'ic_call_ringing_outline',
          title: FormatUtil.phoneFormat(phoneNumber),
          color: phoneColor,
          onTap: () {
            GlobalFunction.launchPhone(phoneNumber);
          },
        ),
        Container(
          height: 4,
          width: 4,
          margin: const EdgeInsets.symmetric(horizontal: 8),
          decoration: const BoxDecoration(
            shape: BoxShape.circle,
            color: UIColors.gray,
          ),
        ),
        InfoWidget(
          asset: 'ic_message',
          title: 'Chat',
          color: chatColor,
          onTap: () {
            context.router.push(ChatRoute(chatUserID: userID));
          },
        ),
        Container(
          height: 4,
          width: 4,
          margin: const EdgeInsets.symmetric(horizontal: 8),
          decoration: const BoxDecoration(
            shape: BoxShape.circle,
            color: UIColors.gray,
          ),
        ),
        InfoWidget(
          asset: 'ic_stair_down',
          title: lineLabel,
          color: lineLabelColor,
        ),
      ],
    );
  }
}

class InfoWidget extends StatelessWidget {
  const InfoWidget({
    Key? key,
    required this.asset,
    required this.title,
    this.color = UIColors.white,
    this.onTap,
  }) : super(key: key);

  final String asset;
  final String title;
  final Color color;
  final Function()? onTap;

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      onTap: onTap,
      child: Row(
        mainAxisSize: MainAxisSize.min,
        children: [
          AppImage.asset(
            asset: asset,
            width: 16,
            height: 16,
            color: color,
            fit: BoxFit.contain,
          ),
          const SizedBox(
            width: 4,
          ),
          Text(
            title,
            style: UITextStyle.regular.copyWith(
              color: color,
              fontSize: 13,
            ),
          ),
        ],
      ),
    );
  }
}

