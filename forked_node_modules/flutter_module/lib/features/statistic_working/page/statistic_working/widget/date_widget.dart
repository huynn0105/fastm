import 'package:flutter/material.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/datetime_util.dart';
import 'package:flutter_module/features/mtrade/page/mtrade/children/order/widgets/date_action_button.dart';

class DateWidget extends StatelessWidget {
  const DateWidget({
    super.key,
    required this.date,
    required this.enabledNext,
    required this.enabledPrevious,
    required this.onNext,
    required this.onPrevious,
    this.format = DateTimeFormat.EEEE_dd_MM,
  });

  final DateTime date;
  final bool enabledNext;
  final bool enabledPrevious;
  final Function() onNext;
  final Function() onPrevious;
  final DateTimeFormat format;

  @override
  Widget build(BuildContext context) {
    return Row(
      mainAxisAlignment: MainAxisAlignment.center,
      children: [
        DateActionButton.left(
          onTap: onPrevious,
          enabled: enabledPrevious,
        ),
        Container(
          constraints: const BoxConstraints(minWidth: 120),
          alignment: Alignment.center,
          child: Text(
            DateTimeUtil.getString(date, format: format),
            style: UITextStyle.medium.copyWith(
              fontSize: 16,
            ),
          ),
        ),
        DateActionButton.right(
          onTap: onNext,
          enabled: enabledNext,
        ),
      ],
    );
  }
}
