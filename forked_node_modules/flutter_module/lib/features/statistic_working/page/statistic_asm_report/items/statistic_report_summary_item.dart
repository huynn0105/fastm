import 'package:flutter_module/common/enum/mfast/report_item_type.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/format_util.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter/material.dart';

import '../../../../../models/statistic_working/statistic_report_model.dart';

class StatisticReportSummaryItem extends StatelessWidget {
  const StatisticReportSummaryItem({super.key, this.item});

  final ReportDetailItemModel? item;

  @override
  Widget build(BuildContext context) {
    return Row(
      children: [
        AppImage.asset(asset: 'ic_report_${item?.code}', width: 20, height: 20),
        const SizedBox(width: 4),
        Text(
          item?.type == ReportItemType.money.name
              ? FormatUtil.numberFormat(item?.value, thousandSymbol: 'k', millionSymbol: 'tr')
              : '${item?.value?.toInt() ?? ''}',
          style: UITextStyle.regular,
        )
      ],
    );
  }
}
