import 'package:flutter/material.dart';
import 'package:info_popup/info_popup.dart';

import '../../../../../common/colors.dart';
import '../../../../../common/size.dart';
import '../../../../../common/styles.dart';
import '../../../../../common/widgets/images.dart';

class ReportInfoPopup extends StatelessWidget {
  const ReportInfoPopup({
    super.key,
    required this.child,
    this.onControllerCreated,
    this.isAgent = false,
    this.onDismissed,
  });

  final Widget child;
  final OnControllerCreated? onControllerCreated;
  final bool isAgent;
  final Function()? onDismissed;

  @override
  Widget build(BuildContext context) {
    final helpContents = [
      {
        'icon': 'ic_report_mtrade',
        'content': 'Tổng doanh số đơn hàng MTrade (Gồm trả ngay và trả chậm)',
      },
      {
        'icon': 'ic_report_pl',
        'content': 'Tổng số tiền đã giải ngân (triệu)',
      },
      {
        'icon': 'ic_report_creditCard',
        'content': 'Tổng số thẻ tín dụng mở thành công (đơn)',
      },
      {
        'icon': 'ic_report_daa',
        'content': 'Tổng số tài khoản ngân hàng, ví điện tử mở thành công (đơn)',
      },
      {
        'icon': 'ic_report_insurance',
        'content': 'Tổng doanh số bảo hiểm (triệu)',
      },
      {
        'icon': 'ic_report_agentReport',
        'content': 'Tổng số báo cáo tự khai từ Agent',
      },
    ];
    if (!isAgent) {
      helpContents.addAll([
        {
          'icon': 'ic_report_agentReportSuccess',
          'content': 'Số Agent đã báo cáo trong ngày',
        },
        {
          'icon': 'ic_report_agentNotReport',
          'content': 'Số Agent chưa báo cáo trong ngày',
        },
      ]);
    }
    return InfoPopupWidget(
      onControllerCreated: onControllerCreated,
      enableHighlight: true,
      dismissTriggerBehavior: PopupDismissTriggerBehavior.anyWhere,
      popupClickTriggerBehavior: PopupClickTriggerBehavior.none,
      infoPopupDismissed: onDismissed,
      contentMaxWidth: AppSize.instance.width - 32,
      arrowTheme: const InfoPopupArrowTheme(
        color: Colors.white,
        arrowDirection: ArrowDirection.up,
      ),
      highLightTheme: HighLightTheme(
        backgroundColor: UIColors.blurBackground,
        radius: BorderRadius.circular(8),
      ),
      indicatorOffset: const Offset(0, 0),
      contentOffset: const Offset(0, 0),
      customContent: () => Container(
        decoration: BoxDecoration(
          color: UIColors.white,
          borderRadius: BorderRadius.circular(12),
        ),
        padding: const EdgeInsets.all(12),
        width: 290,
        child: ListView.separated(
          padding: EdgeInsets.zero,
          shrinkWrap: true,
          physics: const NeverScrollableScrollPhysics(),
          itemBuilder: (context, index) {
            return Row(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                AppImage.asset(
                  asset: helpContents[index]['icon'],
                  width: 20,
                  height: 20,
                ),
                const SizedBox(width: 8),
                Expanded(
                  child: Text(
                    helpContents[index]['content'] ?? '',
                    style: UITextStyle.regular.copyWith(
                      height: 1.4,
                    ),
                  ),
                )
              ],
            );
          },
          separatorBuilder: (context, index) {
            return const SizedBox(height: 8);
          },
          itemCount: helpContents.length,
        ),
      ),
      child: child,
    );
  }
}
