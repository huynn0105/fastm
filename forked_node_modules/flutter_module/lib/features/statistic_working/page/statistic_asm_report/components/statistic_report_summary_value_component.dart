import 'package:flutter/material.dart';
import 'package:flutter_module/common/size.dart';
import 'package:flutter_module/models/statistic_working/statistic_report_model.dart';

import '../items/statistic_report_summary_item.dart';

class StatisticReportSummaryValueComponent extends StatelessWidget {
  const StatisticReportSummaryValueComponent({
    super.key,
    this.details,
  });

  final List<ReportDetailItemModel>? details;

  @override
  Widget build(BuildContext context) {
    final itemWidth = (AppSize.instance.width - 32) / 4;
    return GridView.builder(
      padding: EdgeInsets.zero,
      shrinkWrap: true,
      physics: const NeverScrollableScrollPhysics(),
      gridDelegate: SliverGridDelegateWithFixedCrossAxisCount(
        crossAxisCount: 4,
        childAspectRatio: itemWidth / 22,
        mainAxisSpacing: 12,
        crossAxisSpacing: 6,
      ),
      itemBuilder: (BuildContext context, int index) {
        return StatisticReportSummaryItem(
          item: details?[index],
        );
      },
      itemCount: details?.length ?? 0,
    );
  }
}
