part of 'statistic_asm_checking_cubit.dart';

class StatisticAsmCheckingState extends Equatable {
  const StatisticAsmCheckingState({
    this.status = BlocStatus.initial,
    this.data = const [],
  });

  final BlocStatus status;
  final List<StatisticCheckingModel> data;

  @override
  List<Object?> get props => [
        status,
        data,
      ];

  StatisticAsmCheckingState copyWith({
    BlocStatus? status,
    List<StatisticCheckingModel>? data,
  }) {
    return StatisticAsmCheckingState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
