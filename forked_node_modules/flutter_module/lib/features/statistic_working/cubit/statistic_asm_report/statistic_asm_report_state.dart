part of 'statistic_asm_report_cubit.dart';

class StatisticAsmReportState extends Equatable {
  const StatisticAsmReportState({
    this.status = BlocStatus.initial,
    this.data = const [],
  });

  final BlocStatus status;
  final List<StatisticReportModel> data;

  @override
  List<Object?> get props => [
        status,
        data,
      ];

  StatisticAsmReportState copyWith({
    BlocStatus? status,
    List<StatisticReportModel>? data,
  }) {
    return StatisticAsmReportState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
