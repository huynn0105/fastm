part of 'statistic_agent_cubit.dart';

class StatisticAgentState extends Equatable {
  const StatisticAgentState({
    this.status = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final TimeCheckingWrapperModel? data;

  @override
  List<Object?> get props => [
        status,
        data,
      ];

  StatisticAgentState copyWith({
    BlocStatus? status,
    TimeCheckingWrapperModel? data,
  }) {
    return StatisticAgentState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
