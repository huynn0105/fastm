part of 'statistic_path_cubit.dart';

class StatisticPathState extends Equatable {
  const StatisticPathState({
    this.status = BlocStatus.initial,
    this.data = const [],
    this.isPush,
  });

  final BlocStatus status;
  final List<SaleInfoModel> data;
  final bool? isPush;

  @override
  List<Object?> get props => [
        status,
        data,
        isPush,
      ];

  StatisticPathState copyWith({
    BlocStatus? status,
    List<SaleInfoModel>? data,
    bool? isPush,
  }) {
    return StatisticPathState(
      status: status ?? this.status,
      data: data ?? this.data,
      isPush: isPush,
    );
  }
}
