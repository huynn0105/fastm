part of 'statistic_summary_checking_cubit.dart';

class StatisticSummaryCheckingState extends Equatable {
  const StatisticSummaryCheckingState({
    this.status = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final StatisticCheckingModel? data;

  @override
  List<Object?> get props => [
        status,
        data,
      ];

  StatisticSummaryCheckingState copyWith({
    BlocStatus? status,
    StatisticCheckingModel? data,
  }) {
    return StatisticSummaryCheckingState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
