part of 'statistic_date_selection_cubit.dart';

class StatisticDateSelectionState extends Equatable {
  StatisticDateSelectionState({
    DateTime? date,
    this.enabledNext = false,
    this.enabledPrevious = true,
  }) : date = date ?? DateTime.now().dateOnly();

  final DateTime date;
  final bool enabledNext;
  final bool enabledPrevious;

  @override
  List<Object> get props => [
        date,
        enabledNext,
        enabledPrevious,
      ];

  StatisticDateSelectionState copyWith({
    DateTime? date,
    bool? enabledNext,
    bool? enabledPrevious,
  }) {
    return StatisticDateSelectionState(
      date: date ?? this.date,
      enabledNext: enabledNext ?? this.enabledNext,
      enabledPrevious: enabledPrevious ?? this.enabledPrevious,
    );
  }
}
