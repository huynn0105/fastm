part of 'statistic_summary_report_cubit.dart';

class StatisticSummaryReportState extends Equatable {
  const StatisticSummaryReportState({
    this.status = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final StatisticReportModel? data;

  @override
  List<Object?> get props => [
        status,
        data,
      ];

  StatisticSummaryReportState copyWith({
    BlocStatus? status,
    StatisticReportModel? data,
  }) {
    return StatisticSummaryReportState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
