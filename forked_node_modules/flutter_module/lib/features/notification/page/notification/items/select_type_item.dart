import 'package:flutter/material.dart';
import 'package:flutter_module/common/bottom_sheet/wrapper/data_wrapper.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';

class SelectTypeItem extends StatelessWidget {
  const SelectTypeItem({
    super.key,
    required this.item,
    this.index,
    this.isSelected = false,
    this.onTap,
  });

  final DataWrapper item;
  final int? index;
  final bool isSelected;
  final VoidCallback? onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: onTap,
      child: Container(
        padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 6),
        margin: const EdgeInsets.only(right: 8),
        decoration: BoxDecoration(
          color: isSelected ? UIColors.primaryColor : UIColors.background,
          borderRadius: BorderRadius.circular(18),
        ),
        child: Text(
          item.value ?? '',
          style: isSelected
              ? UITextStyle.medium.copyWith(
                  color: UIColors.white,
                )
              : UITextStyle.regular.copyWith(
                  color: UIColors.grayText,
                ),
        ),
      ),
    );
  }
}
