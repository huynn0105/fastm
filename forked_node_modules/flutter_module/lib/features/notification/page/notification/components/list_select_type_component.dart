import 'package:collection/collection.dart';
import 'package:flutter/material.dart';
import 'package:flutter_module/common/bottom_sheet/wrapper/data_wrapper.dart';
import 'package:flutter_module/features/notification/page/notification/items/select_type_item.dart';

class ListSelectTypeComponent extends StatelessWidget {
  const ListSelectTypeComponent({
    super.key,
    this.data = const [],
    this.idSelected,
    this.onTapItem,
  });

  final List<DataWrapper> data;
  final String? idSelected;
  final Function(DataWrapper item)? onTapItem;

  @override
  Widget build(BuildContext context) {
    return SizedBox(
      height: 32,
      child: ListView(
        scrollDirection: Axis.horizontal,
        padding: const EdgeInsets.only(
          left: 16,
          right: 8,
        ),
        children: [
          ...data.mapIndexed(
            (i, e) {
              final bool isSelected = idSelected == e.id;

              return SelectTypeItem(
                item: e,
                index: i,
                isSelected: isSelected,
                onTap: () {
                  _onTapItem(e);
                },
              );
            },
          )
        ],
      ),
    );
  }

  _onTapItem(DataWrapper item) {
    onTapItem?.call(item);
  }
}
