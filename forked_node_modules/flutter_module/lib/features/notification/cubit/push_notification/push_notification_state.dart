part of 'push_notification_cubit.dart';

class PushNotificationState extends Equatable {
  const PushNotificationState({
    this.chatUnreadCount = 0,
    this.notificationUnreadCount = 0,
  });

  final int chatUnreadCount;
  final int notificationUnreadCount;

  @override
  List<Object> get props => [
        chatUnreadCount,
        notificationUnreadCount,
      ];

  PushNotificationState copyWith({
    int? chatUnreadCount,
    int? notificationUnreadCount,
  }) {
    return PushNotificationState(
      chatUnreadCount: chatUnreadCount ?? this.chatUnreadCount,
      notificationUnreadCount: notificationUnreadCount ?? this.notificationUnreadCount,
    );
  }
}
