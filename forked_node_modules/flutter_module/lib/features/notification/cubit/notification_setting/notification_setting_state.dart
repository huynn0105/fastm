part of 'notification_setting_cubit.dart';

class NotificationSettingState extends Equatable {
  const NotificationSettingState({
    this.status = BlocStatus.initial,
    this.settings = const [],
    this.updateStatus = BlocStatus.initial,
  });

  final BlocStatus status;
  final List<MFastNotificationSettingModel> settings;
  final BlocStatus updateStatus;

  @override
  List<Object> get props => [
        status,
        settings,
        updateStatus,
      ];

  NotificationSettingState copyWith({
    BlocStatus? status,
    List<MFastNotificationSettingModel>? settings,
    BlocStatus? updateStatus,
  }) {
    return NotificationSettingState(
      status: status ?? this.status,
      settings: settings ?? this.settings,
      updateStatus: updateStatus ?? this.updateStatus,
    );
  }
}
