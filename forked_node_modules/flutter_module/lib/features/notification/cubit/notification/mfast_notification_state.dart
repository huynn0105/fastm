part of 'mfast_notification_cubit.dart';

class MFastNotificationState extends Equatable {
  const MFastNotificationState({
    this.status = BlocStatus.initial,
    this.notifications = const [],
    this.readAllStatus = BlocStatus.initial,
  });

  final BlocStatus status;
  final List<MFastNotificationModel> notifications;
  final BlocStatus readAllStatus;

  @override
  List<Object?> get props => [
        status,
        notifications,
        readAllStatus,
      ];

  MFastNotificationState copyWith({
    BlocStatus? status,
    List<MFastNotificationModel>? notifications,
    BlocStatus? readAllStatus,
  }) {
    return MFastNotificationState(
      status: status ?? this.status,
      notifications: notifications ?? this.notifications,
      readAllStatus: readAllStatus ?? this.readAllStatus,
    );
  }
}
