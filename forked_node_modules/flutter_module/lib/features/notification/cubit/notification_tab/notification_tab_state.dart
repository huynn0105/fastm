class NotificationTabState {
  const NotificationTabState({
    this.status = BlocStatus.initial,
    this.isTriggerShowAll = false,
    this.isTriggerShowUnread = false,
    this.isTriggerReadAll = false,
    this.totalSystemUnread = 0,
    this.totalAdminUnread = 0,
  });

  final BlocStatus status;
  final bool isTriggerShowAll;
  final bool isTriggerShowUnread;
  final bool isTriggerReadAll;
  final int totalSystemUnread;
  final int totalAdminUnread;

  // @override
  // List<Object> get props => [
  //       status,
  //       isTriggerShowAll,
  //       isTriggerShowUnread,
  //       isTriggerReadAll,
  //     ];

  NotificationTabState copyWith({
    BlocStatus? status,
    bool? isTriggerShowAll,
    bool? isTriggerShowUnread,
    bool? isTriggerReadAll,
    int? totalSystemUnread,
    int? totalAdminUnread,
  }) {
    return NotificationTabState(
      status: status ?? this.status,
      isTriggerShowAll: isTriggerShowAll ?? false,
      isTriggerShowUnread: isTriggerShowUnread ?? false,
      isTriggerReadAll: isTriggerReadAll ?? false,
      totalSystemUnread: totalSystemUnread ?? this.totalSystemUnread,
      totalAdminUnread: totalAdminUnread ?? this.totalAdminUnread,
    );
  }
}
