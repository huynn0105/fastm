import 'package:flutter_module/models/chat/icon/sticker_model.dart';

class StickerDataSource {
  StickerDataSource._();

  static Map<String, List<StickerModel>> _generate(String key, int length) {
    return {
      key: List.generate(
        length,
        (index) {
          String code = '${key}_${index + 1}';
          return StickerModel(
            code: code,
            asset: getAsset(code),
          );
        },
      ),
    };
  }

  static String getAsset(String code) => 'assets/stickers/$code.png';

  static Map<String, List<StickerModel>> get cat => _generate('Cat', 16);

  static Map<String, List<StickerModel>> get dog => _generate('Dog', 24);

  static Map<String, List<StickerModel>> get mascot => _generate('Mascot', 16);

  static Map<String, List<StickerModel>> get shiba => _generate('Shiba', 28);

  static Map<String, List<StickerModel>> get catFace => _generate('Squirrel', 18);

  static Map<String, List<StickerModel>> get data => {...mascot, ...cat, ...catFace, ...dog, ...shiba};
}
