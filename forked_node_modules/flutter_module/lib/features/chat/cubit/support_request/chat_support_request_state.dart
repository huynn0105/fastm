part of 'chat_support_request_cubit.dart';

class ChatSupportRequestState extends Equatable {
  const ChatSupportRequestState({
    this.status = BlocStatus.initial,
    this.supportRequests,
  });
  final BlocStatus status;
  final ChatSupportRequestModel? supportRequests;

  @override
  List<Object?> get props => [
        status,
        supportRequests,
      ];

  ChatSupportRequestState copyWith({
    BlocStatus? status,
    ChatSupportRequestModel? supportRequests,
  }) {
    return ChatSupportRequestState(
      status: status ?? this.status,
      supportRequests: supportRequests ?? this.supportRequests,
    );
  }
}
