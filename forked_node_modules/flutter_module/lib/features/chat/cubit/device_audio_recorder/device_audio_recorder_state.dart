part of 'device_audio_recorder_cubit.dart';

class DeviceAudioRecorderState extends Equatable {
  const DeviceAudioRecorderState({
    this.status = BlocStatus.initial,
  });

  final BlocStatus status;

  @override
  List<Object?> get props => [
        status,
      ];

  DeviceAudioRecorderState copyWith({
    BlocStatus? status,
  }) {
    return DeviceAudioRecorderState(
      status: status ?? this.status,
    );
  }
}