part of 'device_audio_player_cubit.dart';

class DeviceAudioPlayerState extends Equatable {
  const DeviceAudioPlayerState({
    this.status = BlocStatus.initial,
    this.data,
  });

  final BlocStatus status;
  final AudioPlayerData? data;

  @override
  List<Object?> get props => [status, data];

  DeviceAudioPlayerState copyWith({
    BlocStatus? status,
    AudioPlayerData? data,
  }) {
    return DeviceAudioPlayerState(
      status: status ?? this.status,
      data: data ?? this.data,
    );
  }
}
