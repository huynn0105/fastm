part of 'file_handler_cubit.dart';

class FileHandlerState extends Equatable {
  const FileHandlerState({
    this.data = const {},
  });

  final Map<String, FileHandlerData> data;

  @override
  List<Object> get props => [data];

  FileHandlerState copyWith({
    Map<String, FileHandlerData>? data,
  }) {
    return FileHandlerState(
      data: data ?? this.data,
    );
  }
}

class FileHandlerData extends Equatable {
  const FileHandlerData({
    this.status = BlocStatus.initial,
    this.id,
    this.progress,
  });

  final BlocStatus status;
  final String? id;
  final double? progress;

  @override
  List<Object?> get props => [id, progress, status];

  FileHandlerData copyWith({
    String? id,
    double? progress,
    BlocStatus? status,
  }) {
    return FileHandlerData(
      id: id ?? this.id,
      progress: progress ?? this.progress,
      status: status ?? this.status,
    );
  }
}
