part of 'thread_action_cubit.dart';

class ThreadActionState extends Equatable {
  const ThreadActionState({
    this.action = ThreadActionType.none,
    this.showScrollToTopButton = false,
    this.showKeyboard = false,
    this.keyboardHeight = 0,
    this.minEdge = 0,
    this.maxEdge = 0,
    this.highlightMessageID,
  });

  final ThreadActionType action;
  final bool showScrollToTopButton;
  final bool showKeyboard;
  final double keyboardHeight;
  final int minEdge;
  final int maxEdge;
  final String? highlightMessageID;

  @override
  List<Object?> get props => [
        action,
        showScrollToTopButton,
        showKeyboard,
        keyboardHeight,
        minEdge,
        maxEdge,
        highlightMessageID,
      ];

  ThreadActionState copyWith({
    ThreadActionType? action,
    bool? showScrollToTopButton,
    bool? showKeyboard,
    double? keyboardHeight,
    int? minEdge,
    int? maxEdge,
    String? highlightMessageID,
  }) {
    return ThreadActionState(
      action: action ?? this.action,
      showScrollToTopButton: showScrollToTopButton ?? this.showScrollToTopButton,
      showKeyboard: showKeyboard ?? this.showKeyboard,
      keyboardHeight: keyboardHeight ?? this.keyboardHeight,
      minEdge: minEdge ?? this.minEdge,
      maxEdge: maxEdge ?? this.maxEdge,
      highlightMessageID: highlightMessageID,
    );
  }
}
