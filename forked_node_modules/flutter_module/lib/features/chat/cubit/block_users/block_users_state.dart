part of 'block_users_cubit.dart';

class BlockUsersState extends Equatable {
  const BlockUsersState({
    this.status = BlocStatus.initial,
    this.unblockStatus = BlocStatus.initial,
    this.users = const [],
    this.errorMessage,
  });

  final BlocStatus status;
  final BlocStatus unblockStatus;
  final List<BlockedUserModel> users;
  final String? errorMessage;

  @override
  List<Object?> get props => [
        status,
        unblockStatus,
        users,
        errorMessage,
      ];

  BlockUsersState copyWith({
    BlocStatus? status,
    BlocStatus? unblockStatus,
    List<BlockedUserModel>? users,
    String? errorMessage,
  }) {
    return BlockUsersState(
      status: status ?? this.status,
      unblockStatus: unblockStatus ?? this.unblockStatus,
      users: users ?? this.users,
      errorMessage: errorMessage,
    );
  }
}
