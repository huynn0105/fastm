part of 'device_media_cubit.dart';

class DeviceMediaState extends Equatable {
  const DeviceMediaState({
    this.status = BlocStatus.initial,
    this.convertStatus = BlocStatus.initial,
    this.total = 0,
    this.assets = const [],
    this.selectedAssets = const [],
    this.files = const [],
    this.hasPermission = false,
    this.hasIOSLimitedPermission = false,
    this.convertErrorMessage,
  });

  final BlocStatus status;
  final BlocStatus convertStatus;
  final int total;
  final List<AssetEntity> assets;
  final List<AssetEntity> selectedAssets;
  final List<MediaModel> files;
  final bool hasPermission;
  final bool hasIOSLimitedPermission;
  final String? convertErrorMessage;

  @override
  List<Object?> get props => [
        status,
        convertStatus,
        total,
        assets,
        selectedAssets,
        files,
        hasPermission,
        hasIOSLimitedPermission,
        convertErrorMessage,
      ];

  DeviceMediaState copyWith({
    BlocStatus? status,
    BlocStatus? convertStatus,
    int? total,
    List<AssetEntity>? assets,
    List<AssetEntity>? selectedAssets,
    List<MediaModel>? files,
    bool? hasPermission,
    bool? hasIOSLimitedPermission,
    String? convertErrorMessage,
  }) {
    return DeviceMediaState(
      status: status ?? this.status,
      convertStatus: convertStatus ?? this.convertStatus,
      total: total ?? this.total,
      assets: assets ?? this.assets,
      selectedAssets: selectedAssets ?? this.selectedAssets,
      files: files ?? this.files,
      hasPermission: hasPermission ?? this.hasPermission,
      hasIOSLimitedPermission: hasIOSLimitedPermission ?? this.hasIOSLimitedPermission,
      convertErrorMessage: convertErrorMessage,
    );
  }
}
