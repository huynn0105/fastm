part of 'chat_setting_cubit.dart';

class ChatSettingState extends Equatable {
  const ChatSettingState({
    this.backgroundImageStatus = BlocStatus.initial,
    this.notificationStatus = BlocStatus.initial,
    this.favoriteStatus = BlocStatus.initial,
    this.deleteStatus = BlocStatus.initial,
    this.leaveStatus = BlocStatus.initial,
    this.blockStatus = BlocStatus.initial,
    this.avatarStatus = BlocStatus.initial,
    this.isNotificationOn = false,
    this.isFavorite = false,
    this.isBlock = false,
  });

  final BlocStatus backgroundImageStatus;
  final BlocStatus notificationStatus;
  final BlocStatus favoriteStatus;
  final BlocStatus deleteStatus;
  final BlocStatus leaveStatus;
  final BlocStatus blockStatus;
  final BlocStatus avatarStatus;
  final bool isNotificationOn;
  final bool isFavorite;
  final bool isBlock;

  @override
  List<Object> get props => [
        backgroundImageStatus,
        notificationStatus,
        favoriteStatus,
        deleteStatus,
        leaveStatus,
        blockStatus,
        avatarStatus,
        isNotificationOn,
        isFavorite,
        isBlock,
      ];

  ChatSettingState copyWith({
    BlocStatus? backgroundImageStatus,
    BlocStatus? notificationStatus,
    BlocStatus? favoriteStatus,
    BlocStatus? deleteStatus,
    BlocStatus? leaveStatus,
    BlocStatus? blockStatus,
    BlocStatus? avatarStatus,
    bool? isNotificationOn,
    bool? isFavorite,
    bool? isBlock,
  }) {
    return ChatSettingState(
      backgroundImageStatus: backgroundImageStatus ?? this.backgroundImageStatus,
      notificationStatus: notificationStatus ?? this.notificationStatus,
      favoriteStatus: favoriteStatus ?? this.favoriteStatus,
      deleteStatus: deleteStatus ?? this.deleteStatus,
      leaveStatus: leaveStatus ?? this.leaveStatus,
      blockStatus: blockStatus ?? this.blockStatus,
      avatarStatus: avatarStatus ?? BlocStatus.initial,
      isNotificationOn: isNotificationOn ?? this.isNotificationOn,
      isFavorite: isFavorite ?? this.isFavorite,
      isBlock: isBlock ?? this.isBlock,
    );
  }
}
