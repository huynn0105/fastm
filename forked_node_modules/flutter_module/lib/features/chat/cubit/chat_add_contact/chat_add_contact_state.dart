part of 'chat_add_contact_cubit.dart';

class ChatAddContactState extends Equatable {
  const ChatAddContactState({
    this.status = BlocStatus.initial,
    this.contacts = const [],
    this.requestStatus = BlocStatus.initial,
  });

  final BlocStatus status;
  final List<ChatSearchContactModel> contacts;
  final BlocStatus requestStatus;

  @override
  List<Object?> get props => [status, contacts, requestStatus];

  ChatAddContactState copyWith({
    BlocStatus? status,
    List<ChatSearchContactModel>? contacts,
    BlocStatus? requestStatus,
  }) {
    return ChatAddContactState(
      status: status ?? this.status,
      contacts: contacts ?? this.contacts,
      requestStatus: requestStatus ?? BlocStatus.initial,
    );
  }
}
