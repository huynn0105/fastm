part of 'device_icon_cubit.dart';

class DeviceIconState extends Equatable {
  const DeviceIconState({
    this.status = BlocStatus.initial,
    this.data = const {},
    this.selectedGroup,
    this.selectedEmojis = const [],
    this.selectedStickers = const [],
  });

  final BlocStatus status;
  final Map<String, List<dynamic>> data;
  final String? selectedGroup;
  final List<String> selectedEmojis;
  final List<String> selectedStickers;

  @override
  List<Object?> get props => [
        status,
        data,
        selectedGroup,
        selectedEmojis,
        selectedStickers,
      ];

  DeviceIconState copyWith({
    BlocStatus? status,
    Map<String, List<dynamic>>? data,
    String? selectedGroup,
    List<String>? selectedEmojis,
    List<String>? selectedStickers,
  }) {
    return DeviceIconState(
      status: status ?? this.status,
      data: data ?? this.data,
      selectedGroup: selectedGroup ?? this.selectedGroup,
      selectedEmojis: selectedEmojis ?? this.selectedEmojis,
      selectedStickers: selectedStickers ?? this.selectedStickers,
    );
  }
}
