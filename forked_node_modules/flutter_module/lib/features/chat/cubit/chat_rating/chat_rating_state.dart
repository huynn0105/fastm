part of 'chat_rating_cubit.dart';

class ChatRatingState extends Equatable {
  const ChatRatingState({
    this.status = BlocStatus.initial,
    this.reviews = const [],
    this.totalAmount = 0,
    this.totalAvg = 0,
    this.totalPercent,
    this.level,
    this.user,
  });

  final BlocStatus status;
  final List<MentorRatingUserModel> reviews;
  final int? totalAmount;
  final double? totalAvg;
  final RatingPercentModel? totalPercent;
  final UserRatingLevel? level;
  final UserRating? user;

  @override
  List<Object?> get props => [
        status,
        reviews,
        totalAmount,
        totalAvg,
        totalPercent,
        level,
        user,
      ];

  ChatRatingState copyWith({
    BlocStatus? status,
    List<MentorRatingUserModel>? reviews,
    int? totalAmount,
    double? totalAvg,
    RatingPercentModel? totalPercent,
    UserRatingLevel? level,
    UserRating? user,
  }) {
    return ChatRatingState(
      status: status ?? this.status,
      reviews: reviews ?? this.reviews,
      totalAmount: totalAmount ?? this.totalAmount,
      totalAvg: totalAvg ?? this.totalAvg,
      totalPercent: totalPercent ?? this.totalPercent,
      level: level ?? this.level,
      user: user ?? this.user,
    );
  }
}
