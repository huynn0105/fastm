part of 'chat_requested_contact_cubit.dart';

class ChatRequestedContactState extends Equatable {
  const ChatRequestedContactState({
    this.status = BlocStatus.initial,
    this.invitations = const [],
    this.sentRequests = const [],
    this.actionStatus = BlocStatus.initial,
  });

  final BlocStatus status;
  final List<ChatRequestContactModel> invitations;
  final List<ChatRequestContactModel> sentRequests;
  final BlocStatus actionStatus;

  @override
  List<Object> get props => [
        status,
        invitations,
        sentRequests,
        actionStatus,
      ];

  ChatRequestedContactState copyWith({
    BlocStatus? status,
    List<ChatRequestContactModel>? invitations,
    List<ChatRequestContactModel>? sentRequests,
    BlocStatus? actionStatus,
  }) {
    return ChatRequestedContactState(
      status: status ?? this.status,
      invitations: invitations ?? this.invitations,
      sentRequests: sentRequests ?? this.sentRequests,
      actionStatus: actionStatus ?? BlocStatus.initial,
    );
  }
}
