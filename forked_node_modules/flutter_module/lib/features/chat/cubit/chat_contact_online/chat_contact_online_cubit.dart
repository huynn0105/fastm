import 'package:bloc/bloc.dart';
import 'package:equatable/equatable.dart';
import 'package:flutter_module/common/bloc_status.dart';
import 'package:flutter_module/features/chat/repository/chat_repository.dart';
import 'package:flutter_module/models/chat/contact/chat_contact_model.dart';

part 'chat_contact_online_state.dart';

class ChatContactOnlineCubit extends Cubit<ChatContactOnlineState> {
  ChatContactOnlineCubit() : super(const ChatContactOnlineState());

  final ChatRepository _repository = ChatRepository();

  fetchContactsOnline() async {
    emit(state.copyWith(
      status: BlocStatus.loading,
    ));

    final results = await _repository.getContactsOnline();

    if (results.status) {
      emit(state.copyWith(
        status: BlocStatus.success,
        contactsOnline: results.data,
      ));
    } else {
      emit(state.copyWith(
        status: BlocStatus.failure,
      ));
    }
  }
}
