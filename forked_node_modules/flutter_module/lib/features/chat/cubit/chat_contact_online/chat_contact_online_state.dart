part of 'chat_contact_online_cubit.dart';

class ChatContactOnlineState extends Equatable {
  const ChatContactOnlineState({
    this.status = BlocStatus.initial,
    this.contactsOnline = const [],
  });

  final BlocStatus status;
  final List<ChatContactModel> contactsOnline;

  @override
  List<Object> get props => [
        status,
        contactsOnline,
      ];

  ChatContactOnlineState copyWith({
    BlocStatus? status,
    List<ChatContactModel>? contactsOnline,
  }) {
    return ChatContactOnlineState(
      status: status ?? this.status,
      contactsOnline: contactsOnline ?? this.contactsOnline,
    );
  }
}
