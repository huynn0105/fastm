part of 'create_thread_group_cubit.dart';

class CreateThreadGroupState extends Equatable {
  const CreateThreadGroupState({
    this.status = BlocStatus.initial,
    this.uploadStatus = BlocStatus.initial,
    this.usersSelected = const [],
    this.image,
    this.thread,
  });

  final BlocStatus status;
  final BlocStatus uploadStatus;
  final List<ChatContactModel> usersSelected;
  final String? image;
  final ChatThreadModel? thread;

  @override
  List<Object?> get props => [
        status,
        uploadStatus,
        usersSelected,
        image,
        thread,
      ];

  CreateThreadGroupState copyWith({
    BlocStatus? status,
    BlocStatus? uploadStatus,
    List<ChatContactModel>? usersSelected,
    String? image,
    ChatThreadModel? thread,
  }) {
    return CreateThreadGroupState(
      status: status ?? this.status,
      uploadStatus: uploadStatus ?? this.uploadStatus,
      usersSelected: usersSelected ?? this.usersSelected,
      image: image ?? this.image,
      thread: thread ?? this.thread,
    );
  }
}

class CreateThreadGroupCubitInitial extends CreateThreadGroupState {}
