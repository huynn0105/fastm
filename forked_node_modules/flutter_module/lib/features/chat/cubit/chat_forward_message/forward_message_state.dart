part of 'forward_message_cubit.dart';

class ChatForwardMessageState extends Equatable {
  const ChatForwardMessageState({
    this.status = BlocStatus.initial,
    this.submitStatus = BlocStatus.initial,
    this.recentContacts = const [],
    this.groupContacts = const [],
    this.friendContacts = const [],
    this.selectedRecentContacts = const [],
    this.selectedGroupContacts = const [],
    this.selectedFriendContacts = const [],
    this.description = '',
  });

  final BlocStatus status;
  final BlocStatus submitStatus;
  final List<ForwardContactModel> recentContacts;
  final List<ForwardContactModel> groupContacts;
  final List<ForwardContactModel> friendContacts;
  final List<ForwardContactModel> selectedRecentContacts;
  final List<ForwardContactModel> selectedGroupContacts;
  final List<ForwardContactModel> selectedFriendContacts;
  final String description;

  @override
  List<Object> get props => [
        status,
    submitStatus,
        recentContacts,
        groupContacts,
        friendContacts,
        selectedRecentContacts,
        selectedGroupContacts,
        selectedFriendContacts,
        description,
      ];

  ChatForwardMessageState copyWith({
    BlocStatus? status,
    BlocStatus? submitStatus,
    List<ForwardContactModel>? recentContacts,
    List<ForwardContactModel>? groupContacts,
    List<ForwardContactModel>? friendContacts,
    List<ForwardContactModel>? selectedRecentContacts,
    List<ForwardContactModel>? selectedGroupContacts,
    List<ForwardContactModel>? selectedFriendContacts,
    String? description,
  }) {
    return ChatForwardMessageState(
      status: status ?? this.status,
      submitStatus: submitStatus ?? BlocStatus.initial,
      recentContacts: recentContacts ?? this.recentContacts,
      groupContacts: groupContacts ?? this.groupContacts,
      friendContacts: friendContacts ?? this.friendContacts,
      selectedRecentContacts: selectedRecentContacts ?? this.selectedRecentContacts,
      selectedGroupContacts: selectedGroupContacts ?? this.selectedGroupContacts,
      selectedFriendContacts: selectedFriendContacts ?? this.selectedFriendContacts,
      description: description ?? this.description,
    );
  }
}
