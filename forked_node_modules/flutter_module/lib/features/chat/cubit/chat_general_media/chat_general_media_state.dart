part of 'chat_general_media_cubit.dart';

class ChatGeneralMediaState extends Equatable {
  const ChatGeneralMediaState({
    this.status = BlocStatus.initial,
    this.videosStatus = BlocStatus.initial,
    this.images = const [],
    this.videos = const [],
  });

  final BlocStatus status;
  final BlocStatus videosStatus;
  final List<MessageModel> images;
  final List<MessageModel> videos;

  @override
  List<Object> get props => [
        status,
        videosStatus,
        images,
        videos,
      ];

  ChatGeneralMediaState copyWith({
    BlocStatus? status,
    BlocStatus? videosStatus,
    List<MessageModel>? images,
    List<MessageModel>? videos,
  }) {
    return ChatGeneralMediaState(
      status: status ?? this.status,
      videosStatus: videosStatus ?? this.videosStatus,
      images: images ?? this.images,
      videos: videos ?? this.videos,
    );
  }
}
