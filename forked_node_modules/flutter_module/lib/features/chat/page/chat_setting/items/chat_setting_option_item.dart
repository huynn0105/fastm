import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_switch/flutter_switch.dart';

class ChatSettingOptionItem extends StatelessWidget {
  const ChatSettingOptionItem({
    super.key,
    this.icon,
    this.title,
    this.isSwitch,
    this.iconColor,
    this.onSwitchChange,
    this.switchValue,
    this.onTap,
  });

  final String? icon;
  final String? title;
  final bool? isSwitch;
  final Color? iconColor;
  final Function(bool)? onSwitchChange;
  final bool? switchValue;
  final Function()? onTap;

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      onTap: onTap,
      child: Padding(
        padding: const EdgeInsets.only(top: 12, bottom: 8),
        child: Row(
          children: [
            AppImage.asset(
              asset: icon,
              width: 24,
              height: 24,
              color: iconColor,
            ),
            const SizedBox(
              width: 12,
            ),
            Expanded(
              child: Text(
                title ?? '',
                style: UITextStyle.medium.copyWith(
                  fontSize: 16,
                  color: UIColors.boolText,
                ),
              ),
            ),
            if (isSwitch is bool) ...{
              FlutterSwitch(
                width: 46,
                height: 28,
                value: switchValue ?? false,
                borderRadius: 23,
                padding: 2,
                activeColor: UIColors.primaryColor,
                inactiveColor: UIColors.gray,
                onToggle: (val) {
                  onSwitchChange?.call(val);
                },
              ),
            } else ...{
              const AppImage.asset(
                asset: 'ic_arrow_right',
                width: 24,
                height: 24,
              ),
            }
          ],
        ),
      ),
    );
  }
}
