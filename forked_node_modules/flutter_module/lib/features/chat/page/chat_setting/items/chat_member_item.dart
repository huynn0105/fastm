                      style: UITextStyle.medium.copyWith(
                        fontSize: 16,
                        color: UIColors.boolText,
                      ),
                    ),
                    if (isMe || isMe)
                      TextSpan(
                        text: "${isMe ? " (TÃ´i)" : ""}${isAdmin ? " (Admin)" : ""}",
                        style: UITextStyle.medium.copyWith(
                          fontSize: 16,
                          color: UIColors.grayText,
                        ),
                      ),
                  ],
                ),
              ),
            ],
          ),
          if (showDivider)
            const Divider(
              thickness: 1,
              color: UIColors.lightGray,
              height: 1,
              indent: 60,
            ),
        ],
      ),
    );
  }
}
