import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/enum/chat/media_type.dart';
import 'package:flutter_module/common/extension/duration_extension.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/chat/file/media_model.dart';

class ChatGeneralMediaItem extends StatelessWidget {
  const ChatGeneralMediaItem({
    super.key,
    required this.item,
    this.onTap,
  });

  final MediaModel item;
  final Function? onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: () {
        onTap?.call();
      },
      child: Stack(
        fit: StackFit.expand,
        children: [
          Container(
            margin: const EdgeInsets.only(top: 2),
            decoration: BoxDecoration(
              borderRadius: BorderRadius.circular(6),
              color: UIColors.background,
            ),
            clipBehavior: Clip.hardEdge,
            child: AppImage.network(
              url: item.type == MediaType.video.name ? item.thumbnail?.url : item.url,
            ),
          ),
          if (item.type == MediaType.video.name) ...{
            Center(
              child: Opacity(
                opacity: 0.6,
                child: Container(
                  width: 24,
                  height: 24,
                  decoration: BoxDecoration(
                    borderRadius: BorderRadius.circular(24 / 2),
                    color: UIColors.darkGray,
                  ),
                  child: const AppImage.asset(
                    asset: 'ic_play',
                    width: 24,
                    height: 24,
                  ),
                ),
              ),
            ),
            Positioned(
              bottom: 4,
              right: 4,
              child: Container(
                padding: const EdgeInsets.symmetric(horizontal: 3),
                decoration: BoxDecoration(
                  color: UIColors.shadow.withOpacity(0.6),
                  borderRadius: BorderRadius.circular(12),
                ),
                child: Text(
                  Duration(milliseconds: item.duration ?? 0).getMMss(),
                  style: UITextStyle.semiBold.copyWith(
                    fontSize: 6,
                    color: UIColors.white,
                  ),
                ),
              ),
            )
          }
        ],
      ),
    );
  }
}
