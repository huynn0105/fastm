import 'package:auto_route/auto_route.dart';
import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/features/chat/cubit/chat_thread/chat_thread_cubit.dart';
import 'package:flutter_module/features/chat/page/chat/components/create_chat_component.dart';
import 'package:flutter_module/models/chat/thread/chat_thread_model.dart';

class CreateChatItem extends StatelessWidget {
  const CreateChatItem({
    super.key,
    required this.item,
  });

  final CreateChatOption item;
  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: () async {
        if (item.route != null) {
          context.pushRoute(item.route);
          if (context.mounted) {
            context.popRoute();
          }
        }
      },
      child: Container(
        margin: const EdgeInsets.only(top: 16),
        padding: const EdgeInsets.symmetric(horizontal: 16),
        child: Row(
          children: [
            AppImage.asset(
              asset: item.icon,
              width: 32,
              height: 32,
            ),
            const SizedBox(
              width: 16,
            ),
            Expanded(
              child: Text(
                item.title ?? '',
                style: UITextStyle.semiBold.copyWith(
                  fontSize: 18,
                  color: UIColors.boolText,
                ),
              ),
            ),
            const AppImage.asset(
              asset: 'ic_arrow_right',
              width: 24,
              height: 24,
            )
          ],
        ),
      ),
    );
  }
}
