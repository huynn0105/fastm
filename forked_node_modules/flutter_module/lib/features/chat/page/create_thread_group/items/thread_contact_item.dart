import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/avatar/chat_avatar.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/checkbox.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/chat/contact/chat_contact_model.dart';
import 'package:flutter_module/services/firebase/firebase_database/firebase_database_util.dart';

class ThreadContactItem extends StatelessWidget {
  const ThreadContactItem({
    super.key,
    required this.item,
    this.isSelected = false,
    this.isDisabled = false,
    this.onTap,
  });

  final ChatContactModel item;
  final bool isSelected;
  final bool isDisabled;
  final VoidCallback? onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      isDisabled: isDisabled,
      onTap: () {
        onTap?.call();
      },
      child: Column(
        children: [
          Row(
            children: [
              ChatAvatar(
                url: item.avatar ?? '',
                name: item.fullName ?? '',
                size: 48,
              ),
              const SizedBox(
                width: 12,
              ),
              Expanded(
                child: Text(
                  item.fullName ?? '',
                  style: UITextStyle.medium.copyWith(fontSize: 16),
                ),
              ),
              AppCheckbox.circle(
                value: isSelected,
                isEnable: !isDisabled,
              ),
            ],
          ),
          const SizedBox(
            height: 4,
          ),
          const Divider(
            height: 1,
            color: UIColors.lightGray,
            thickness: 1,
            indent: 60,
          )
        ],
      ),
    );
  }
}
