import 'package:flutter/material.dart';
import 'package:flutter_bloc/flutter_bloc.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/constants.dart';
import 'package:flutter_module/common/toast/toast_provider.dart';
import 'package:flutter_module/common/widgets/loadmore_widget.dart';
import 'package:flutter_module/features/chat/cubit/chat_requested_contact/chat_requested_contact_cubit.dart';
import 'package:flutter_module/features/chat/page/contact_request/items/requested_contact_item.dart';
import 'package:flutter_module/models/chat/contact/chat_request_contact_model.dart';

class ListRequestedContactsComponent extends StatelessWidget {
  const ListRequestedContactsComponent({
    super.key,
    required this.data,
  });

  final List<ChatRequestContactModel> data;

  @override
  Widget build(BuildContext context) {
    return LoadMoreWidget(
      onRefresh: () {
        final cubit = context.read<ChatRequestedContactCubit>();
        return cubit.fetchInvitationAndSendingRequest();
      },
      child: ListView.builder(
        physics: AppConstants.physics,
        // shrinkWrap: true,
        itemCount: data.length,
        itemBuilder: (context, index) {
          final item = data[index];
          return RequestedContactItem(
            item: item,
            index: index,
            onCancelRequest: () {
              _onCancelRequestContact(context, item.phoneNumber ?? '');
            },
          );
        },
      ),
    );
  }

  _onCancelRequestContact(BuildContext context, String phoneNumber) async {
    final cubit = context.read<ChatRequestedContactCubit>();
    final isSuccess = await cubit.cancelRequestContact(phoneNumber);

    if (!isSuccess && context.mounted) {
      ToastProvider.instance.show(
        context: context,
        message: "Huỷ lời mời lỗi, vui lòng thử lại",
        backgroundColor: UIColors.red,
        duration: const Duration(seconds: 1),
      );
    }
  }
}
