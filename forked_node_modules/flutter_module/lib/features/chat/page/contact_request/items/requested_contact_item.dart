import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/avatar/chat_avatar.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/models/chat/contact/chat_request_contact_model.dart';

class RequestedContactItem extends StatelessWidget {
  const RequestedContactItem({
    super.key,
    required this.item,
    this.index,
    this.onCancelRequest,
  });

  final ChatRequestContactModel item;
  final int? index;
  final Function()? onCancelRequest;

  @override
  Widget build(BuildContext context) {
    return Container(
      margin: const EdgeInsets.only(top: 12),
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(
        color: UIColors.white,
        borderRadius: BorderRadius.circular(8),
      ),
      child: Row(
        children: [
          ChatAvatar(
            uid: item.uid,
            url: item.avatar ?? '',
            size: 48,
            name: item.fullName ?? '',
          ),
          const SizedBox(
            width: 12,
          ),
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  (item.fullName ?? ''),
                  style: UITextStyle.medium.copyWith(
                    fontSize: 16,
                    color: UIColors.boolText,
                  ),
                  maxLines: 1,
                  overflow: TextOverflow.ellipsis,
                ),
              ],
            ),
          ),
          AppOutlinedButton(
            backgroundColor: UIColors.red,
            borderRadius: BorderRadius.circular(24),
            borderWidth: 0,
            padding: const EdgeInsets.symmetric(
              horizontal: 24,
            ),
            height: 28,
            child: Text(
              'Hủy lời mời',
              style: UITextStyle.regular.copyWith(
                color: UIColors.white,
              ),
            ),
            onPressed: () {
              onCancelRequest?.call();
            },
          )
        ],
      ),
    );
  }
}
