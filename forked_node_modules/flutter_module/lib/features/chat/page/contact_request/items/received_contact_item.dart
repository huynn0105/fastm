import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/avatar/chat_avatar.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_module/models/chat/contact/chat_request_contact_model.dart';
import 'package:flutter_module/services/firebase/firebase_database/firebase_database_util.dart';

class ReceivedContactItem extends StatelessWidget {
  const ReceivedContactItem({
    super.key,
    required this.item,
    this.index,
    this.onAcceptContact,
    this.onRejectContact,
  });

  final ChatRequestContactModel item;
  final int? index;
  final Function()? onAcceptContact;
  final Function()? onRejectContact;

  @override
  Widget build(BuildContext context) {
    return Container(
      margin: const EdgeInsets.only(top: 12),
      padding: const EdgeInsets.symmetric(horizontal: 12, vertical: 8),
      decoration: BoxDecoration(
        color: UIColors.white,
        borderRadius: BorderRadius.circular(8),
      ),
      child: Row(
        children: [
          ChatAvatar(
            uid: item.uid,
            url: item.avatar ?? '',
            size: 72,
            name: item.fullName ?? '',
          ),
          const SizedBox(
            width: 12,
          ),
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  (item.fullName ?? ''),
                  style: UITextStyle.medium.copyWith(
                    fontSize: 16,
                    color: UIColors.boolText,
                  ),
                  maxLines: 1,
                  overflow: TextOverflow.ellipsis,
                ),
                Text(
                  item.message ?? '',
                  style: UITextStyle.regular.copyWith(
                    fontSize: 12,
                    color: UIColors.grayText,
                  ),
                ),
                Row(
                  mainAxisAlignment: MainAxisAlignment.spaceBetween,
                  children: [
                    Expanded(
                      child: AppOutlinedButton(
                        backgroundColor: UIColors.primaryColor,
                        borderRadius: BorderRadius.circular(24),
                        borderWidth: 0,
                        height: 28,
                        child: Text(
                          'Chấp nhận',
                          style: UITextStyle.regular.copyWith(
                            color: UIColors.white,
                          ),
                        ),
                        onPressed: () {
                          onAcceptContact?.call();
                        },
                      ),
                    ),
                    const SizedBox(
                      width: 14,
                    ),
                    Expanded(
                      child: AppOutlinedButton(
                        backgroundColor: UIColors.lightGray,
                        borderRadius: BorderRadius.circular(24),
                        borderWidth: 0,
                        height: 28,
                        child: Text(
                          'Từ chối',
                          style: UITextStyle.regular.copyWith(
                            color: UIColors.boolText,
                          ),
                        ),
                        onPressed: () {
                          onRejectContact?.call();
                        },
                      ),
                    ),
                  ],
                ),
              ],
            ),
          ),
        ],
      ),
    );
  }
}
