import 'dart:ui';

import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/size.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/utils/datetime_util.dart';
import 'package:flutter_module/common/utils/text_util.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/images.dart';
import 'package:flutter_xlider/flutter_xlider.dart';
import 'package:video_player/video_player.dart';

class VideoControllerComponent extends StatelessWidget {
  const VideoControllerComponent({
    super.key,
    required this.controller,
  });

  final VideoPlayerController controller;

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: EdgeInsets.only(
        top: 12,
        bottom: 27 + AppSize.instance.safeBottomBarHeight,
      ),
      child: Row(
        children: [
          const SizedBox(
            width: 4,
          ),
          SplashButton(
            onTap: () {
              controller.value.isPlaying ? controller.pause() : controller.play();
            },
            child: Padding(
              padding: const EdgeInsets.all(12),
              child: AppImage.asset(
                asset: controller.value.isPlaying ? 'ic_pause_video' : 'ic_play_video',
                width: 24,
                height: 24,
              ),
            ),
          ),
          Expanded(
            child: FlutterSlider(
              values: [controller.value.position.inSeconds.toDouble()],
              max: getMax,
              min: 0,
              onDragCompleted: (_, value, __) {
                controller.seekTo(Duration(seconds: TextUtils.parseInt(value) ?? 0));
              },
              handler: FlutterSliderHandler(
                child: const SizedBox(),
              ),
              trackBar: FlutterSliderTrackBar(
                activeTrackBar: BoxDecoration(
                  borderRadius: BorderRadius.circular(4),
                  color: UIColors.primaryColor,
                ),
                inactiveTrackBar: BoxDecoration(
                  borderRadius: BorderRadius.circular(4),
                  color: UIColors.background,
                ),
                activeTrackBarHeight: 4,
                inactiveTrackBarHeight: 4,
              ),
              tooltip: FlutterSliderTooltip(
                disabled: true,
              ),
              handlerHeight: 10,
            ),
          ),
          const SizedBox(
            width: 16,
          ),
          Text(
            DateTimeUtil.formatDuration(controller.value.position),
            style: UITextStyle.semiBold.copyWith(
              fontSize: 12,
              color: UIColors.white,
              fontFeatures: [
                const FontFeature.tabularFigures(),
              ],
            ),
          ),
          const SizedBox(
            width: 16,
          ),
        ],
      ),
    );
  }

  double get getMax => controller.value.duration.inSeconds.toDouble() + 0.0000001;
}
