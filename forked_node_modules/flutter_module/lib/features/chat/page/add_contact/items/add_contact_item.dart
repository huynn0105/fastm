import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/avatar/chat_avatar.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/models/chat/contact/chat_search_contact_model.dart';

class AddContactItem extends StatelessWidget {
  const AddContactItem({
    super.key,
    required this.item,
    this.index,
    this.onSendAddContact,
    this.isExist,
  });

  final ChatSearchContactModel item;
  final int? index;
  final Function()? onSendAddContact;
  final bool? isExist;

  @override
  Widget build(BuildContext context) {
    return Container(
      margin: const EdgeInsets.only(top: 12),
      padding: const EdgeInsets.all(12),
      decoration: BoxDecoration(
        color: UIColors.white,
        borderRadius: BorderRadius.circular(8),
      ),
      child: Row(
        children: [
          ChatAvatar(
            url: item.avatar ?? '',
            name: item.nickname ?? '',
            size: 48,
          ),
          const SizedBox(
            width: 12,
          ),
          Expanded(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                Text(
                  item.nickname ?? '',
                  style: UITextStyle.medium.copyWith(
                    fontSize: 16,
                    color: UIColors.boolText,
                  ),
                  maxLines: 1,
                  overflow: TextOverflow.ellipsis,
                ),
                Text(
                  'Mã MFast: ${item.referralCode}',
                  style: UITextStyle.regular.copyWith(
                    fontSize: 13,
                    color: UIColors.grayText,
                  ),
                )
              ],
            ),
          ),
          const SizedBox(
            width: 12,
          ),
          if (isExist == true) ...{
            Text('Đã kết bạn',
                style: UITextStyle.regular.copyWith(
                  color: UIColors.grayText,
                ))
          } else ...{
            AppOutlinedButton(
              backgroundColor: UIColors.primaryColor, //item.isSent == true ? UIColors.red : UIColors.primaryColor,
              borderRadius: BorderRadius.circular(24),
              borderWidth: 0,
              padding: const EdgeInsets.symmetric(
                horizontal: 24,
              ),
              height: 40,
              child: Text(
                'Kết bạn',
                // item.isReceived == true
                //     ? 'Chấp nhận'
                //     : item.isSent == true
                //         ? 'Huỷ lời mời'
                //         : 'Kết bạn',
                style: UITextStyle.regular.copyWith(
                  color: UIColors.white,
                ),
              ),
              onPressed: () {
                onSendAddContact?.call();
              },
            ),
          },
        ],
      ),
    );
  }
}
