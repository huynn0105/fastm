import 'package:flutter/material.dart';
import 'package:flutter_module/features/chat/page/forward_message/items/contact_item.dart';
import 'package:flutter_module/features/chat/page/forward_message/widgets/contact_sticky_header.dart';
import 'package:flutter_module/models/chat/contact/forward_contact_model.dart';

class FriendContactComponent extends StatelessWidget {
  const FriendContactComponent({
    super.key,
    required this.data,
    required this.selectedData,
    this.onSelected,
  });

  final List<ForwardContactModel> data;
  final List<ForwardContactModel> selectedData;
  final Function(ForwardContactModel)? onSelected;

  @override
  Widget build(BuildContext context) {
    if (data.isEmpty) {
      return const SliverToBoxAdapter();
    }
    return ContactStickyHeader(
      title: 'Bạn bè',
      itemBuilder: (context, index) {
        final item = data[index];
        final isSelected = selectedData.indexWhere((e) => e.uid == item.uid) >= 0;
        return ChatContactItem(
          name: item.title ?? '',
          avatar: item.avatar ?? '',
          isSelected: isSelected,
          isSingleThread: true,
          onTap: () => onSelected?.call(item),
        );
      },
      itemCount: data.length,
    );
  }
}
