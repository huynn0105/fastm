import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/textfields.dart';

class InputComponent extends StatelessWidget {
  const InputComponent({
    super.key,
    this.readOnly = true,
    this.autofocus = false,
    this.controller,
    this.focusNode,
    this.onTap,
    this.onChanged,
  });

  final TextEditingController? controller;
  final FocusNode? focusNode;
  final bool readOnly;
  final bool autofocus;
  final Function()? onTap;
  final Function(String)? onChanged;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: onTap,
      isDisabled: !readOnly,
      borderRadius: BorderRadius.circular(8),
      child: SizedBox(
        height: 40,
        child: UISearchTextField(
          controller: controller,
          focusNode: focusNode,
          inputBorder: OutlineInputBorder(
            borderRadius: BorderRadius.circular(8),
            borderSide: const BorderSide(color: UIColors.white),
          ),
          hintText: 'Tìm kiếm',
          hintTextStyle: UITextStyle.medium.copyWith(
            fontSize: 16,
            color: UIColors.grayText,
            height: 1,
          ),
          textStyle: UITextStyle.medium.copyWith(
            fontSize: 16,
            height: 1.4,
          ),
          readOnly: readOnly,
          autofocus: autofocus,
          onChanged: onChanged,
        ),
      ),
    );
  }
}
