import 'package:flutter/material.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/avatar/chat_avatar.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/common/widgets/checkbox.dart';
import 'package:flutter_module/common/widgets/images.dart';

class ChatContactItem extends StatelessWidget {
  const ChatContactItem({
    super.key,
    required this.name,
    required this.avatar,
    required this.isSelected,
    required this.isSingleThread,
    required this.onTap,
  });

  final String name;
  final String avatar;
  final bool isSelected;
  final bool isSingleThread;
  final Function() onTap;

  @override
  Widget build(BuildContext context) {
    return AppSplashButton(
      onTap: onTap,
      child: Row(
        children: [
          ChatAvatar(
            url: avatar,
            size: 48,
            name: name,
            isSingleThread: isSingleThread,
          ),
          const SizedBox(
            width: 10,
          ),
          Expanded(
            child: Text(
              name,
              style: UITextStyle.medium.copyWith(
                fontSize: 16,
              ),
            ),
          ),
          const SizedBox(
            width: 10,
          ),
          IgnorePointer(
            ignoring: true,
            child: AppCheckbox.circle(
              value: isSelected,
            ),
          ),
        ],
      ),
    );
  }
}
