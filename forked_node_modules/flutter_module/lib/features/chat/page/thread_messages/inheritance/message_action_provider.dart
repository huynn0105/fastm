import 'package:flutter/material.dart';
import 'package:flutter_module/features/chat/common/typedef.dart';

class MessageActionProvider extends InheritedWidget {
  const MessageActionProvider({
    super.key,
    required super.child,
    required this.onMessageReplied,
    required this.onReactionMessageTap,
    required this.onReactionMessageSelected,
    required this.onQuotedMessageTap,
    required this.onMessageRecalled,
    required this.onMessageEdited,
    required this.onMessageDeleted,
  });

  final OnMessageCallback onMessageReplied;
  final OnMessageReactionCallback onReactionMessageTap;
  final OnMessageReactionSelectedCallback onReactionMessageSelected;
  final OnMessageCallback onQuotedMessageTap;
  final OnMessageCallback onMessageRecalled;
  final OnMessageCallback onMessageEdited;
  final OnMessageCallback onMessageDeleted;

  @override
  bool updateShouldNotify(MessageActionProvider oldWidget) {
    return false;
  }

  static MessageActionProvider of(BuildContext context) {
    return context.dependOnInheritedWidgetOfExactType<MessageActionProvider>()!;
  }
}
