import 'package:flutter/material.dart';

class MessageWidgetProvider extends InheritedWidget {
  const MessageWidgetProvider({
    super.key,
    required super.child,
    required this.minRadius,
    required this.maxRadius,
    required this.borderRadius,
    required this.columnMainAxis,
    required this.columnCrossAxis,
    required this.rowMainAxis,
    required this.rowCrossAxis,
    this.showBorder = false,
  });

  final double minRadius;
  final double maxRadius;
  final BorderRadius borderRadius;
  final bool showBorder;

  /// Column
  final MainAxisAlignment columnMainAxis;
  final CrossAxisAlignment columnCrossAxis;

  /// Row
  final MainAxisAlignment rowMainAxis;
  final CrossAxisAlignment rowCrossAxis;

  @override
  bool updateShouldNotify(MessageWidgetProvider oldWidget) {
    return oldWidget.borderRadius != borderRadius ||
        oldWidget.minRadius != minRadius ||
        oldWidget.maxRadius != maxRadius ||
        oldWidget.columnMainAxis != columnMainAxis ||
        oldWidget.columnCrossAxis != columnCrossAxis ||
        oldWidget.rowMainAxis != rowMainAxis ||
        oldWidget.rowCrossAxis != rowCrossAxis ||
        oldWidget.showBorder != showBorder;
  }

  static MessageWidgetProvider of(BuildContext context) {
    return context.dependOnInheritedWidgetOfExactType<MessageWidgetProvider>()!;
  }
}
