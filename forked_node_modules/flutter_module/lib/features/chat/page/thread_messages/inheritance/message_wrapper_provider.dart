import 'package:flutter/material.dart';
import 'package:flutter_module/models/chat/message/message_wrapper_model.dart';

class MessageWrapperProvider extends InheritedWidget {
  const MessageWrapperProvider({
    super.key,
    required this.data,
    required super.child,
  });

  final MessageWrapperModel data;

  @override
  bool updateShouldNotify(MessageWrapperProvider oldWidget) {
    return data != oldWidget.data;
  }

  static MessageWrapperProvider of(BuildContext context) {
    return context.dependOnInheritedWidgetOfExactType<MessageWrapperProvider>()!;
  }
}
