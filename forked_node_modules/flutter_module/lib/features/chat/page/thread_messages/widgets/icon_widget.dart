import 'dart:io';

import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/models/chat/icon/emoji_model.dart';
import 'package:flutter_module/models/chat/icon/icon_model.dart';
import 'package:flutter_module/models/chat/icon/sticker_model.dart';

class IconWidget extends StatelessWidget {
  const IconWidget({
    super.key,
    required this.icon,
    this.size = 25,
    this.width,
    this.height,
    this.onEmojiSelected,
    this.onStickerSelected,
  });

  final IconModel icon;
  final double size;
  final double? width;
  final double? height;
  final Function(String)? onEmojiSelected;
  final Function(String)? onStickerSelected;

  @override
  Widget build(BuildContext context) {
    if (icon is EmojiModel) {
      final data = icon as EmojiModel;
      return SplashButton(
        onTap: () => onEmojiSelected?.call(data.code ?? ''),
        child: Container(
          width: width,
          height: height,
          alignment: Alignment.center,
          child: Text(
            data.code ?? '',
            style: UITextStyle.regular.copyWith(
              fontSize: size + (Platform.isAndroid ? 0 : 10),
              height: 1,
            ),
          ),
        ),
      );
    }
    if (icon is StickerModel) {
      final data = icon as StickerModel;
      return SplashButton(
        onTap: () => onStickerSelected?.call(data.code ?? ''),
        child: Container(
          alignment: Alignment.center,
          child: Image.asset(
            data.asset ?? '',
            width: width,
            height: height,
            fit: BoxFit.contain,
            errorBuilder: (_, __, ___) {
              return Container(
                height: height,
                width: width,
                decoration: BoxDecoration(
                  color: UIColors.gray,
                  borderRadius: BorderRadius.circular(8),
                ),
              );
            },
          ),
        ),
      );
    }
    return const SizedBox();
  }
}
