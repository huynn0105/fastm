class MarkupDataModel {
  MarkupDataModel({
    required this.id,
    required this.display,
    this.label,
    this.imageURL,
    this.assetURI,
  });

  factory MarkupDataModel.fromJson(Map<String, dynamic> map) {
    return MarkupDataModel(
      id: map['id'] as String,
      display: map['display'] as String,
      imageURL: map['imageURL'] as String?,
      label: map['fullName'] as String?,
      assetURI: map['assetURI'] as String?,
    );
  }

  String id;
  String display;
  String? imageURL;
  String? label;
  String? assetURI;

  Map<String, dynamic> toJson() {
    final map = <String, dynamic>{};
    map['id'] = id;
    map['display'] = display;
    map['fullName'] = label;
    map['imageURL'] = imageURL;
    map['assetURI'] = assetURI;
    return map;
  }
}
