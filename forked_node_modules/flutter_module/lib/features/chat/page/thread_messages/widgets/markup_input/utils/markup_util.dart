class MarkupUtil {
  MarkupUtil._();

  static String kMentionTrigger = '@';
  static String kHashTagTrigger = '#';
  static String kCommandTrigger = '/';

  static String get validTrigger => '$kMentionTrigger|$kHashTagTrigger|$kCommandTrigger';

  static String get validPattern => '(($validTrigger)\\[([^[]*)]\\(([^(^)]*)\\))';

  static String getMarkupFormatText({
    required String? trigger,
    required String? display,
    required String? id,
  }) {
    return '$trigger[$display]($id)';
  }

  static String getMarkupText({
    required String? trigger,
    required String? display,
    required String? id,
  }) {
    return '$trigger$display';
  }

  static String getHTMLMarkup(String value) {
    return '<span>$value</span>';
  }
}
