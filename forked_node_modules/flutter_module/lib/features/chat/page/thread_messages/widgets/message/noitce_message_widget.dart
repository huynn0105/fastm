import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/widgets/html_widget.dart';
import 'package:flutter_module/models/chat/message/message_wrapper_model.dart';

class NoticeMessageWidget extends StatelessWidget {
  const NoticeMessageWidget({super.key, required this.data});

  final MessageWrapperModel data;

  @override
  Widget build(BuildContext context) {
    return Container(
      alignment: Alignment.center,
      padding: const EdgeInsets.symmetric(horizontal: 16, vertical: 4),
      decoration: const BoxDecoration(
        color: UIColors.lightGray,
      ),
      child: HtmlWidget(
        data: data.message?.htmlText ?? data.message?.text ?? '',
        fontSize: 12,
        lineHeight: 1.1,
        shrinkWrap: true,
        alignment: Alignment.center,
        textAlign: TextAlign.center,
      ),
    );
  }
}
