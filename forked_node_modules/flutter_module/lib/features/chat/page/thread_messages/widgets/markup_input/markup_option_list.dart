import 'package:flutter/material.dart';

import 'models/markup_data_model.dart';

class MarkupSuggestionOptionList extends StatelessWidget {
  const MarkupSuggestionOptionList({
    super.key,
    required this.data,
    required this.onTap,
    required this.suggestionListHeight,
    this.suggestionBuilder,
    this.suggestionListDecoration,
  });

  final Widget Function(MarkupDataModel)? suggestionBuilder;
  final List<MarkupDataModel> data;
  final Function(MarkupDataModel) onTap;
  final double suggestionListHeight;
  final BoxDecoration? suggestionListDecoration;

  @override
  Widget build(BuildContext context) {
    if (data.isEmpty) {
      return const SizedBox.shrink();
    }
    return Container(
      height: suggestionListHeight,
      decoration: suggestionListDecoration ?? const BoxDecoration(color: Colors.white),
      constraints: BoxConstraints(
        maxHeight: suggestionListHeight,
        minHeight: 45,
      ),
      child: ListView.separated(
        shrinkWrap: true,
        itemCount: data.length,
        padding: EdgeInsets.zero,
        separatorBuilder: (_, __) => const SizedBox(),
        itemBuilder: (context, index) {
          return InkWell(
            onTap: () {
              onTap(data[index]);
            },
            child: suggestionBuilder != null
                ? SizedBox(
                    height: 45,
                    child: suggestionBuilder!(data[index]),
                  )
                : Container(
                    height: 45,
                    color: Colors.blue,
                    padding: const EdgeInsets.all(12),
                    child: Text(
                      data[index].display,
                      style: const TextStyle(fontSize: 12),
                    ),
                  ),
          );
        },
      ),
    );
  }
}
