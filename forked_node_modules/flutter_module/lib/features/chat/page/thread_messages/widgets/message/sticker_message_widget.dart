import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/features/chat/common/data_source/sticker_data_source.dart';
import 'package:flutter_module/models/chat/message/message_wrapper_model.dart';

class StickerWidgetMessage extends StatelessWidget {
  const StickerWidgetMessage({super.key, required this.data});

  final MessageWrapperModel data;

  @override
  Widget build(BuildContext context) {
    final message = data.message;
    final code = message?.htmlText ?? '';
    return Container(
      width: 100,
      height: 100,
      alignment: Alignment.center,
      child: Image.asset(
        StickerDataSource.getAsset(code),
        width: 100,
        height: 100,
        fit: BoxFit.fill,
        errorBuilder: (_, __, ___) {
          return Container(
            width: 100,
            height: 100,
            decoration: const BoxDecoration(
              color: UIColors.gray,
            ),
          );
        },
      ),
    );
  }
}
