import 'package:flutter/material.dart';
import 'package:flutter_module/common/colors.dart';
import 'package:flutter_module/common/styles.dart';
import 'package:flutter_module/common/widgets/buttons.dart';
import 'package:flutter_module/features/chat/common/data_source/reaction_data_source.dart';
import 'package:flutter_module/models/chat/message/reaction_model.dart';

class ReactionWidget extends StatelessWidget {
  const ReactionWidget({
    super.key,
    required this.data,
    this.onTap,
  });

  final ReactionModel data;
  final Function()? onTap;

  @override
  Widget build(BuildContext context) {
    return SplashButton(
      onTap: onTap,
      borderRadius: BorderRadius.circular(12),
      child: Card(
        margin: EdgeInsets.zero,
        shape: RoundedRectangleBorder(borderRadius: BorderRadius.circular(12)),
        child: Container(
          height: 24,
          alignment: Alignment.center,
          padding: const EdgeInsets.all(2),
          decoration: BoxDecoration(
            color: UIColors.white,
            borderRadius: BorderRadius.circular(12),
          ),
          child: Row(
            mainAxisSize: MainAxisSize.min,
            children: [
              Image.asset(
                ReactionDataSource.getAsset(data.code ?? ''),
                width: 20,
                height: 20,
                fit: BoxFit.contain,
              ),
              Visibility(
                visible: data.count > 1,
                child: Container(
                  alignment: Alignment.center,
                  constraints: const BoxConstraints(
                    minWidth: 15,
                  ),
                  child: Text(
                    data.count.toString(),
                    style: UITextStyle.bold.copyWith(
                      fontSize: 14,
                      color: UIColors.grayText,
                    ),
                  ),
                ),
              )
            ],
          ),
        ),
      ),
    );
  }
}
