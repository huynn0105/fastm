import 'package:flutter/material.dart';

import '../utils/markup_typedef.dart';
import '../utils/markup_util.dart';

class MarkupAnnotationModel {
  MarkupAnnotationModel({
    required this.trigger,
    this.id,
    this.display,
    this.style,
    this.disableMarkup = false,
    this.markupConverter,
  });

  String trigger;
  String? id;
  String? display;
  TextStyle? style;

  bool disableMarkup;

  final MarkupConverter? markupConverter;

  String getMarkupFormatText() {
    return MarkupUtil.getMarkupFormatText(
      trigger: trigger,
      display: display,
      id: id,
    );
  }
}
