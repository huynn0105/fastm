import 'package:flutter/material.dart';
import 'package:flutter_module/features/chat/page/thread_messages/widgets/welcome/single_welcome_widget.dart';
import 'package:flutter_module/models/chat/thread/chat_thread_model.dart';

class WelcomeComponent extends StatelessWidget {
  const WelcomeComponent({
    super.key,
    required this.thread,
  });

  final ChatThreadModel? thread;

  @override
  Widget build(BuildContext context) {
    return Column(
      children: [
        const SizedBox(
          height: 16,
        ),
        Visibility(
          visible: thread?.isSingleThread() == true,
          child: Padding(
            padding: const EdgeInsets.symmetric(horizontal: 16),
            child: SingleWelcomeWidget(thread: thread),
          ),
        ),
      ],
    );
  }
}
