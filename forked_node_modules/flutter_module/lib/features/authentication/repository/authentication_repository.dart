import 'package:flutter_module/models/base_model.dart';
import 'package:flutter_module/services/api/api_provider.dart';
import 'package:flutter_module/services/api/authentication/payload/logout_payload.dart';
import 'package:flutter_module/services/api/authentication/payload/register_payload.dart';
import 'package:flutter_module/services/api/authentication/payload/request_otp_payload.dart';
import 'package:flutter_module/services/api/authentication/payload/verify_password_payload.dart';

import '../../../models/authentication/otp_model.dart';
import '../../../models/user/user_info_model.dart';
import '../../../services/api/authentication/payload/verify_otp_payload.dart';

class AuthenticationRepository {
  Future<BaseModel<OtpModel>> requestOTP(RequestOtpPayload payload) {
    return ApiProvider.instance.authentication.requestOtp(payload);
  }

  Future<BaseModel<UserInfoModel>> verifyOtp(VerifyOtpPayload payload) {
    return ApiProvider.instance.authentication.verifyOtp(payload);
  }

  Future<BaseModel<UserInfoModel>> verifyPassword(VerifyPasswordPayload payload) {
    return ApiProvider.instance.authentication.verifyPassword(payload);
  }

  Future<BaseModel<bool>> logout(LogoutPayload payload) {
    return ApiProvider.instance.authentication.logout(payload);
  }

  Future<BaseModel<bool>> checkNickname(String nickname) {
    return ApiProvider.instance.authentication.checkNickname(nickname);
  }

  Future<BaseModel<UserInfoModel>> register(RegisterPayload payload) {
    return ApiProvider.instance.authentication.register(payload);
  }
}
