
part of 'login_cubit.dart';

class LoginState extends Equatable {
  final BlocStatus status;
  final BlocStatus verifyPasswordStatus;
  final bool acceptedPolicy;
  final bool enableBiometric;
  final String phoneNumber;
  final String? errMsg;
  final Map<String, String> errorFields;

  LoginState({
    this.status = BlocStatus.initial,
    this.verifyPasswordStatus = BlocStatus.initial,
    this.acceptedPolicy = false,
    this.enableBiometric = false,
    this.phoneNumber = '',
    this.errMsg = '',
    Map<String, String>? errorFields,
  }) : errorFields = errorFields ?? {}; //workaround to modify errorFields

  @override
  List<Object?> get props => [
    status,
    verifyPasswordStatus,
    acceptedPolicy,
    enableBiometric,
    phoneNumber,
    errMsg,
    errorFields,
  ];

  LoginState copyWith({
    BlocStatus? status,
    BlocStatus? verifyPasswordStatus,
    bool? acceptedPolicy,
    bool? enableBiometric,
    String? phoneNumber,
    String? errMsg,
    Map<String, String>? errorFields,
  }) {
    return LoginState(
      status: status ?? this.status,
      verifyPasswordStatus: verifyPasswordStatus ?? BlocStatus.initial,
      acceptedPolicy: acceptedPolicy ?? this.acceptedPolicy,
      enableBiometric: enableBiometric ?? this.enableBiometric,
      phoneNumber: phoneNumber ?? this.phoneNumber,
      errorFields: errorFields ?? this.errorFields,
      errMsg: errMsg,
    );
  }
}
