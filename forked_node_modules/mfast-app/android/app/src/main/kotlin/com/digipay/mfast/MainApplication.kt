package com.digipay.mfast

import com.moengage.core.DataCenter
import com.moengage.core.LogLevel
import com.moengage.core.MoEngage
import com.moengage.core.config.LogConfig
import com.moengage.core.config.NotificationConfig
import io.flutter.app.FlutterApplication
import com.moengage.flutter.MoEInitializer

class MainApplication : FlutterApplication() {
    override fun onCreate() {
        super.onCreate()
        val moEngage: MoEngage.Builder = MoEngage.Builder(this, "9QBXCRQ6ZYJBYYXZQMZAG3UM", DataCenter.DATA_CENTER_1)
            .configureLogs(LogConfig(LogLevel.VERBOSE, false))
            .configureNotificationMetaData(
                NotificationConfig(
                    R.drawable.ic_notification,
                    R.drawable.ic_notification,
                    R.color.noti_color,
                    true,
                    isBuildingBackStackEnabled = true,
                    isLargeIconDisplayEnabled = true
                )
            )
        MoEInitializer.initialiseDefaultInstance(context = this, builder = moEngage)
    }
}